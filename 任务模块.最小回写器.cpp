template<class T节点>
std::uint64_t 私有_运行包主键_按节点(const T节点* 节点) noexcept
{
    return 节点 ? static_cast<std::uint64_t>(reinterpret_cast<std::uintptr_t>(节点)) : 0;
}

template<class T节点>
void 私有_写入引用(可解析引用<T节点>& 输出, T节点* 节点) noexcept
{
    输出.指针 = 节点;
    输出.主键 = 节点 ? 节点->获取主键() : std::string{};
}

const char* 私有_动作类型文本(const 任务运行包::枚举_动作类型 动作类型) noexcept
{
    switch (动作类型) {
    case 任务运行包::枚举_动作类型::观察:
        return "观察";
    case 任务运行包::枚举_动作类型::解释:
        return "解释";
    case 任务运行包::枚举_动作类型::预警:
        return "预警";
    case 任务运行包::枚举_动作类型::建议:
        return "建议";
    case 任务运行包::枚举_动作类型::执行:
        return "执行";
    case 任务运行包::枚举_动作类型::回滚:
        return "回滚";
    case 任务运行包::枚举_动作类型::收束:
        return "收束";
    default:
        return "治理";
    }
}

方法类::节点类* 私有_取来源方法(const 结构_任务管理结果& 治理结果) noexcept
{
    if (治理结果.当前一步结果.当前方法节点) {
        return 治理结果.当前一步结果.当前方法节点;
    }
    if (治理结果.当前请求.上下文.当前方法) {
        return 治理结果.当前请求.上下文.当前方法;
    }
    return 治理结果.上下文.当前方法;
}

状态节点类* 私有_取条件状态(const 结构_任务管理结果& 治理结果) noexcept
{
    if (治理结果.宿主目标状态) return 治理结果.宿主目标状态;
    if (治理结果.步骤目标状态) return 治理结果.步骤目标状态;
    if (治理结果.结果目标状态) return 治理结果.结果目标状态;
    return 治理结果.最新目标状态;
}

状态节点类* 私有_取结果状态(const 结构_任务管理结果& 治理结果) noexcept
{
    if (治理结果.宿主结果状态) return 治理结果.宿主结果状态;
    if (治理结果.步骤结果状态) return 治理结果.步骤结果状态;
    if (治理结果.结果结果状态) return 治理结果.结果结果状态;
    return 治理结果.最新状态;
}

bool 私有_推导执行成功(
    const 结构_任务管理结果& 治理结果,
    const 任务包* 一步任务包) noexcept
{
    if (治理结果.最新结果节点
        || 治理结果.最新动态
        || 治理结果.最新因果
        || 治理结果.已创建结果节点
        || 治理结果.已生成治理证据
        || 治理结果.已写回目标状态
        || 治理结果.已推进任务状态) {
        return true;
    }
    if (一步任务包 && !一步任务包->任务语义键.empty()) {
        return true;
    }
    return false;
}

void 私有_写差额(差额值& 输出, const 任务运行包::I64 目标值, const 任务运行包::I64 当前值) noexcept
{
    输出.目标值 = 目标值;
    输出.当前值 = 当前值;
    输出.差额 = 目标值 - 当前值;
    输出.趋势 = 0;
}

std::uint64_t 私有_推导任务包主键(
    const 任务包* 一步任务包,
    const 结构_任务管理结果& 治理结果,
    const 时间戳 now) noexcept
{
    if (一步任务包 && 一步任务包->包头.包主键 != 0) {
        return 一步任务包->包头.包主键;
    }
    if (治理结果.当前请求.请求ID != 0) {
        return 治理结果.当前请求.请求ID;
    }
    if (治理结果.当前步骤节点) {
        return 私有_运行包主键_按节点(治理结果.当前步骤节点);
    }
    if (治理结果.最新结果节点) {
        return 私有_运行包主键_按节点(治理结果.最新结果节点);
    }
    if (治理结果.当前请求.宿主任务) {
        return 私有_运行包主键_按节点(治理结果.当前请求.宿主任务)
            ^ (static_cast<std::uint64_t>(治理结果.当前请求.提交序号) << 1);
    }
    return (static_cast<std::uint64_t>(now) << 8)
        ^ static_cast<std::uint64_t>(治理结果.当前请求.提交序号);
}

任务运行包::枚举_结算结论 私有_推导结算结论(
    const 结构_任务管理结果& 治理结果,
    const 任务包* 一步任务包,
    const bool 执行成功) noexcept
{
    if (治理结果.最近执行前门控结果 == 枚举_任务管理执行前门控结果::禁止继续) {
        return 任务运行包::枚举_结算结论::越界;
    }
    if (治理结果.当前下一步去向 == 枚举_任务管理下一步去向::进入收束) {
        return 任务运行包::枚举_结算结论::持平;
    }
    if (!执行成功) {
        return 任务运行包::枚举_结算结论::无效推进;
    }
    if (一步任务包
        && (一步任务包->动作类型 == 任务运行包::枚举_动作类型::解释
            || 一步任务包->动作类型 == 任务运行包::枚举_动作类型::预警
            || 一步任务包->动作类型 == 任务运行包::枚举_动作类型::建议
            || 一步任务包->动作类型 == 任务运行包::枚举_动作类型::观察)) {
        return 任务运行包::枚举_结算结论::持平;
    }
    if (治理结果.已写回目标状态 || 治理结果.已创建结果节点) {
        return 任务运行包::枚举_结算结论::缩差;
    }
    return 任务运行包::枚举_结算结论::持平;
}

动态节点类* 私有_创建最小动态(
    自我类& 自我对象,
    const 时间戳 now,
    const 任务包* 一步任务包,
    const 结算包& 结算,
    const 结构_任务管理结果& 治理结果,
    const std::string& 调用点) noexcept
{
    auto* 自我内部世界 = 自我对象.确保自我内部世界(调用点 + "/自我内部世界");
    auto* 自我存在 = 自我对象.确保自我存在(调用点 + "/自我存在");
    if (!自我内部世界 || !自我存在) {
        return nullptr;
    }

    auto* 条件状态 = 私有_取条件状态(治理结果);
    auto* 结果状态 = 私有_取结果状态(治理结果);
    动态节点类* 动态节点 = nullptr;
    if (条件状态 || 结果状态) {
        动态节点 = 世界树.动态().创建动态(自我内部世界, 条件状态, 结果状态, now, now);
    } else {
        auto* 动态主信息 = new 动态节点主信息类{};
        动态主信息->时间 = 结构_时间段{ now, now };
        动态节点 = 世界树.创建动态(自我内部世界, 动态主信息);
    }
    if (!动态节点) {
        return nullptr;
    }

    auto* 动态主信息 = 世界树.动态().取动态主信息(动态节点);
    if (动态主信息) {
        const auto* 动作词 = 语素集.添加词性词(
            一步任务包 && !一步任务包->任务语义键.empty()
                ? 一步任务包->任务语义键
                : std::string{ 私有_动作类型文本(
                    一步任务包 ? 一步任务包->动作类型 : 任务运行包::枚举_动作类型::建议) },
            "动词");
        动态主信息->名称 = 动作词;
        动态主信息->类型 = 动作词;
        动态主信息->来源类型 = 枚举_动态来源类型::方法调用;
        动态主信息->来源动作名 = 动作词;
        动态主信息->来源动作相位 = 结算.执行成功 ? 枚举_动作事件相位::完成 : 枚举_动作事件相位::失败;
        动态主信息->来源执行成功 = 结算.执行成功;
        动态主信息->来源错误码 = 结算.错误码;
        动态主信息->动态路径签名 = 结算.动态路径签名;
        动态主信息->时间 = 结构_时间段{ now, now };
    }

    (void)世界树.动态().设置主体特征(动态节点, 自我存在, nullptr);
    if (auto* 来源方法 = 私有_取来源方法(治理结果)) {
        (void)世界树.动态().绑定来源方法(
            动态节点,
            reinterpret_cast<方法节点类*>(来源方法),
            来源方法->主信息.动作名,
            结算.执行成功 ? 枚举_动作事件相位::完成 : 枚举_动作事件相位::失败);
    }
    (void)世界树.动态().绑定来源场景(动态节点, 自我内部世界, 自我内部世界);
    return 动态节点;
}

因果实例节点类* 私有_创建最小因果候选(
    自我类& 自我对象,
    const 时间戳 now,
    const 任务包* 一步任务包,
    const 结算包& 结算,
    const 结构_任务管理结果& 治理结果,
    动态节点类* 动态节点,
    const std::string& 调用点) noexcept
{
    auto* 自我内部世界 = 自我对象.确保自我内部世界(调用点 + "/自我内部世界");
    auto* 自我存在 = 自我对象.确保自我存在(调用点 + "/自我存在");
    if (!自我内部世界 || !自我存在) {
        return nullptr;
    }

    const std::string 动作语义键 =
        一步任务包 && !一步任务包->任务语义键.empty()
        ? 一步任务包->任务语义键
        : std::string{ "任务管理_一步治理" };
    auto* 动作词 = 语素集.添加词性词(动作语义键, "名词");

    auto* 因果主信息 = new 因果实例主信息类{};
    因果主信息->名称 = 动作词;
    因果主信息->类型 = 动作词;
    因果主信息->锚点类型 = 枚举_因果锚点类型::存在;
    私有_写入引用(因果主信息->锚点信息, 自我存在);
    因果主信息->条件时间段 = 结构_时间段{ now, now };
    因果主信息->动作时间 = now;
    因果主信息->结果时间 = now;
    因果主信息->来源类型 = 枚举_因果来源类型::执行生成;
    因果主信息->来源任务主键 = 结算.任务包主键;
    因果主信息->来源方法主键 = 结算.选定方法主键;
    因果主信息->置信度 = 一步任务包 && 一步任务包->选定方法.has_value()
        ? 一步任务包->选定方法->置信度
        : (结算.执行成功 ? 6000 : 3000);
    因果主信息->已验证 = false;
    因果主信息->动作语义键 = 动作语义键;

    auto* 因果节点 = 世界树.创建因果实例(自我内部世界, 因果主信息);
    if (!因果节点) {
        return nullptr;
    }

    auto* 来源方法 = 私有_取来源方法(治理结果);
    (void)世界树.因果().绑定动作主体语义(
        因果节点,
        自我存在,
        reinterpret_cast<基础信息节点类*>(来源方法),
        动作语义键);
    if (动态节点) {
        (void)世界树.因果().按动态桥接结果状态(因果节点, 动态节点, true);
    }

    auto 追加条件 = [&](状态节点类* 节点) {
        if (节点) {
            (void)世界树.因果().追加条件状态(因果节点, 节点);
        }
    };
    auto 追加结果 = [&](状态节点类* 节点, const bool 设为主结果) {
        if (节点) {
            (void)世界树.因果().追加结果状态(因果节点, 节点, 设为主结果);
        }
    };

    追加条件(治理结果.宿主目标状态);
    if (治理结果.步骤目标状态 && 治理结果.步骤目标状态 != 治理结果.宿主目标状态) {
        追加条件(治理结果.步骤目标状态);
    }
    if (治理结果.结果目标状态
        && 治理结果.结果目标状态 != 治理结果.宿主目标状态
        && 治理结果.结果目标状态 != 治理结果.步骤目标状态) {
        追加条件(治理结果.结果目标状态);
    }
    追加结果(治理结果.宿主结果状态, true);
    if (治理结果.步骤结果状态 && 治理结果.步骤结果状态 != 治理结果.宿主结果状态) {
        追加结果(治理结果.步骤结果状态, false);
    }
    if (治理结果.结果结果状态
        && 治理结果.结果结果状态 != 治理结果.宿主结果状态
        && 治理结果.结果结果状态 != 治理结果.步骤结果状态) {
        追加结果(治理结果.结果结果状态, false);
    }
    return 因果节点;
}

