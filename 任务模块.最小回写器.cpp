bool 执行最小回流结算与回写(
    自我类& 自我对象,
    const 时间戳 now,
    const 任务包* 一步任务包,
    const 结构_任务管理结果& 治理结果,
    结构_最小回写结果* 输出) noexcept
{
    if (!输出) {
        return false;
    }

    auto& 回写结果 = *输出;
    回写结果 = {};

    auto& 结算 = 回写结果.结算包;
    结算 = {};

    const auto 任务包主键 = 私有_推导任务包主键(一步任务包, 治理结果, now);
    const auto 轮次主键 =
        一步任务包 && 一步任务包->包头.轮次主键 != 0
        ? 一步任务包->包头.轮次主键
        : (治理结果.当前请求.请求ID != 0 ? 治理结果.当前请求.请求ID : 任务包主键);
    const auto 追踪签名 =
        一步任务包 && 一步任务包->包头.追踪签名 != 0
        ? 一步任务包->包头.追踪签名
        : (轮次主键 ^ (static_cast<std::uint64_t>(now) << 1));
    const bool 执行成功 = 私有_推导执行成功(治理结果, 一步任务包);

    结算.包头.包主键 = 任务包主键 ^ 0x5A17E0F1ULL;
    if (结算.包头.包主键 == 0) {
        结算.包头.包主键 = static_cast<std::uint64_t>(now) ^ 0x5A17E0F1ULL;
    }
    结算.包头.线程主键 =
        一步任务包 && 一步任务包->包头.线程主键 != 0
        ? 一步任务包->包头.线程主键
        : 治理结果.当前分身ID;
    结算.包头.轮次主键 = 轮次主键;
    结算.包头.宿主存在主键 =
        一步任务包 && 一步任务包->包头.宿主存在主键 != 0
        ? 一步任务包->包头.宿主存在主键
        : 治理结果.当前主体ID;
    结算.包头.父包主键 = 任务包主键;
    结算.包头.创建时间 = now;
    结算.包头.追踪签名 = 追踪签名 ^ 0xC35A17ULL;

    结算.任务包主键 = 任务包主键;
    结算.选定方法主键 =
        一步任务包 && 一步任务包->选定方法.has_value()
        ? 一步任务包->选定方法->方法主键
        : 私有_运行包主键_按节点(私有_取来源方法(治理结果));
    结算.执行成功 = 执行成功;
    结算.被中断 = 治理结果.最近执行前门控结果 == 枚举_任务管理执行前门控结果::禁止继续;
    结算.已超时 = false;
    结算.错误码 = 执行成功 ? 0 : (结算.被中断 ? 2 : 1);
    结算.动态路径签名 = 追踪签名 ^ 任务包主键;

    私有_写差额(结算.服务差额结算前, 自我对象.根目标服务值(), 自我对象.获取服务值());
    私有_写差额(结算.服务差额结算后, 自我对象.根目标服务值(), 自我对象.获取服务值());
    私有_写差额(结算.物理安全差额结算前, 自我对象.根目标安全值(), 自我对象.获取物理安全值());
    私有_写差额(结算.物理安全差额结算后, 自我对象.根目标安全值(), 自我对象.获取物理安全值());
    私有_写差额(结算.风险安全差额结算前, 自我对象.根目标安全值(), 自我对象.获取风险安全值());
    私有_写差额(结算.风险安全差额结算后, 自我对象.根目标安全值(), 自我对象.获取风险安全值());

    if (治理结果.当前下一步去向 == 枚举_任务管理下一步去向::进入收束) {
        结算.触发收束 = true;
        结算.建议进入收束 = true;
        结算.止损原因 = "任务管理请求进入收束";
    }
    if (治理结果.最近执行前门控结果 == 枚举_任务管理执行前门控结果::仅允许等待) {
        结算.触发降级 = true;
        if (结算.止损原因.empty()) {
            结算.止损原因 = "执行前门控仅允许等待";
        }
    } else if (治理结果.最近执行前门控结果 == 枚举_任务管理执行前门控结果::仅允许收束) {
        结算.触发收束 = true;
        结算.建议进入收束 = true;
        if (结算.止损原因.empty()) {
            结算.止损原因 = "执行前门控仅允许收束";
        }
    } else if (治理结果.最近执行前门控结果 == 枚举_任务管理执行前门控结果::禁止继续) {
        结算.触发降级 = true;
        结算.建议重试 = true;
        if (结算.止损原因.empty()) {
            结算.止损原因 = "执行前门控禁止继续";
        }
    }

    结算.结算结论 = 私有_推导结算结论(治理结果, 一步任务包, 执行成功);
    结算.是否有效推进 =
        结算.结算结论 == 任务运行包::枚举_结算结论::缩差
        || 结算.结算结论 == 任务运行包::枚举_结算结论::持平;
    if (!执行成功 && !结算.建议进入收束) {
        结算.建议重试 = true;
    }

    回写结果.动态节点 = 治理结果.最新动态;
    回写结果.因果节点 = 治理结果.最新因果;
    回写结果.已复用现有动态 = 回写结果.动态节点 != nullptr;
    回写结果.已复用现有因果 = 回写结果.因果节点 != nullptr;

    if (!回写结果.动态节点) {
        回写结果.动态节点 = 私有_创建最小动态(
            自我对象,
            now,
            一步任务包,
            结算,
            治理结果,
            "任务最小回写器::执行最小回流结算与回写/动态");
    }
    if (回写结果.动态节点) {
        回写结果.已写入动态 = true;
        if (const auto* 动态主信息 = 世界树.动态().取动态主信息(回写结果.动态节点)) {
            if (动态主信息->来源输入场景.指针) {
                结算.输入场景主键 = 私有_运行包主键_按节点(动态主信息->来源输入场景.指针);
            }
            if (动态主信息->来源输出场景.指针) {
                结算.输出场景主键 = 私有_运行包主键_按节点(动态主信息->来源输出场景.指针);
            }
            结算.执行成功 = 动态主信息->来源执行成功;
            结算.错误码 = 动态主信息->来源错误码;
            if (动态主信息->动态路径签名 != 0) {
                结算.动态路径签名 = 动态主信息->动态路径签名;
            }
        }
    }

    if (!回写结果.因果节点) {
        回写结果.因果节点 = 私有_创建最小因果候选(
            自我对象,
            now,
            一步任务包,
            结算,
            治理结果,
            回写结果.动态节点,
            "任务最小回写器::执行最小回流结算与回写/因果");
    }
    if (回写结果.因果节点) {
        回写结果.已写入因果候选 = true;
    }

    学习候选项 候选{};
    候选.锚点主键 = 回写结果.因果节点
        ? 私有_运行包主键_按节点(回写结果.因果节点)
        : 私有_运行包主键_按节点(回写结果.动态节点);
    候选.来源任务主键 = 结算.任务包主键;
    候选.来源方法主键 = 结算.选定方法主键;
    候选.动作语义键 =
        一步任务包 && !一步任务包->任务语义键.empty()
        ? 一步任务包->任务语义键
        : "任务管理_一步治理";
    候选.置信度 =
        一步任务包 && 一步任务包->选定方法.has_value()
        ? 一步任务包->选定方法->置信度
        : (结算.执行成功 ? 6000 : 3000);
    候选.样本类别 = 结算.执行成功
        ? 任务运行包::枚举_学习样本类别::正样本
        : 任务运行包::枚举_学习样本类别::负样本;
    候选.已验证 = false;
    结算.学习候选集.push_back(候选);

    auto* 默认场景 = 自我对象.确保自我内部世界(
        "任务最小回写器::执行最小回流结算与回写/默认场景");
    if (结算.输入场景主键 == 0) {
        结算.输入场景主键 = 私有_运行包主键_按节点(默认场景);
    }
    if (结算.输出场景主键 == 0) {
        结算.输出场景主键 = 私有_运行包主键_按节点(默认场景);
    }

    回写结果.已生成结算包 = 任务运行包::封口结算包(结算, now, nullptr);

    std::ostringstream 摘要;
    摘要 << "结算包=" << 结算.包头.包主键
        << " | 结论=" << 结算结论文本(结算.结算结论)
        << " | 动态=" << (回写结果.已写入动态 ? "有" : "无")
        << " | 因果候选=" << (回写结果.已写入因果候选 ? "有" : "无")
        << " | 已封口=" << (回写结果.已生成结算包 ? "是" : "否");
    if (!结算.止损原因.empty()) {
        摘要 << " | 止损=" << 结算.止损原因;
    }
    回写结果.摘要 = 摘要.str();
    return 回写结果.已生成结算包;
}

