{
    return 语素_选择默认信息入口类型(词性);
}

inline 枚举_主信息类型 语素_推断人类词性默认基础信息类型(枚举_词性 词性) noexcept
{
    const auto 默认入口 = 语素_选择默认信息入口类型(词性);
    const auto 入口类型 = 语素_取信息入口主信息类型(默认入口);
    if (入口类型 != 枚举_主信息类型::未定义) {
        return 入口类型;
    }

    switch (词性) {
    // 名词族：对象、概念、组织、地点、时间等都先进入“存在”。
    case 枚举_词性::名词:
    case 枚举_词性::专有名词:
    case 枚举_词性::抽象名词:
    case 枚举_词性::地名:
    case 枚举_词性::时间词:
    case 枚举_词性::方位词:
    case 枚举_词性::n:
    case 枚举_词性::f:
    case 枚举_词性::s:
    case 枚举_词性::t:
    case 枚举_词性::nr:
    case 枚举_词性::ns:
    case 枚举_词性::nt:
    case 枚举_词性::nw:
    case 枚举_词性::nz:
    case 枚举_词性::PER:
    case 枚举_词性::LOC:
    case 枚举_词性::ORG:
    case 枚举_词性::TIME:
        return 枚举_主信息类型::存在;

    // 代词是语言入口，不是稳定本体。
    case 枚举_词性::代词:
    case 枚举_词性::r:
        return 枚举_主信息类型::指代;

    // 形容词和状态词先成为抽象特征模板；具体值再由特征/状态实例承载。
    case 枚举_词性::形容词:
    case 枚举_词性::状态词:
    case 枚举_词性::a:
    case 枚举_词性::an:
        return 枚举_主信息类型::抽象特征;

    // 数词、量词通常提供度量或数量特征。
    case 枚举_词性::数词:
    case 枚举_词性::量词:
    case 枚举_词性::m:
    case 枚举_词性::q:
        return 枚举_主信息类型::特征;

    // 副词、介词多用于程度、方向、比较、关系、条件等二次限定。
    case 枚举_词性::副词:
    case 枚举_词性::介词:
    case 枚举_词性::ad:
    case 枚举_词性::d:
    case 枚举_词性::p:
        return 枚举_主信息类型::二次特征_修饰;

    // 动词族都先进入动态。及物/不及物放入动态模板的角色约束，不拆语素结构。
    case 枚举_词性::动词:
    case 枚举_词性::v:
    case 枚举_词性::vd:
    case 枚举_词性::vn:
        return 枚举_主信息类型::动态;

    // 功能词与标点保留语言事实，但不强行伪装成存在/特征/动态。
    case 枚举_词性::连词:
    case 枚举_词性::助词:
    case 枚举_词性::叹词:
    case 枚举_词性::拟声词:
    case 枚举_词性::标点符号:
    case 枚举_词性::习用语:
    case 枚举_词性::简称略语:
    case 枚举_词性::非语素字:
    case 枚举_词性::语气词:
    case 枚举_词性::外文字符:
    case 枚举_词性::短语:
    case 枚举_词性::c:
    case 枚举_词性::u:
    case 枚举_词性::xc:
    case 枚举_词性::w:
        return 枚举_主信息类型::语言信息_仅记录;

    default:
        return 枚举_主信息类型::未定义;
    }
}

inline bool 语素_基础信息类型可由语素入口模板创建(枚举_主信息类型 类型) noexcept
{
    switch (类型) {
    case 枚举_主信息类型::指代:
    case 枚举_主信息类型::存在:
    case 枚举_主信息类型::抽象特征:
    case 枚举_主信息类型::特征:
    case 枚举_主信息类型::动态:
    case 枚举_主信息类型::状态:
    case 枚举_主信息类型::场景:
    case 枚举_主信息类型::二次特征_修饰:
    case 枚举_主信息类型::因果:
    case 枚举_主信息类型::语言信息_仅记录:
        return true;
    default:
        return false;
    }
}

inline bool 语素_人类词性是否兼容基础信息类型(枚举_词性 词性, 枚举_主信息类型 类型) noexcept
{
    const auto 默认类型 = 语素_推断人类词性默认基础信息类型(词性);
    if (默认类型 == 类型) return true;

    // 抽象特征模板与具体特征实例共享同一个语素入口。
    if (默认类型 == 枚举_主信息类型::抽象特征 && 类型 == 枚举_主信息类型::特征) return true;
    if (默认类型 == 枚举_主信息类型::特征 && 类型 == 枚举_主信息类型::抽象特征) return true;

    // 当前系统中“关系”仍由二次特征承载。
    if (默认类型 == 枚举_主信息类型::二次特征_修饰
        && (类型 == 枚举_主信息类型::关系 || 类型 == 枚举_主信息类型::关系信息)) {
        return true;
    }
    return false;
}

