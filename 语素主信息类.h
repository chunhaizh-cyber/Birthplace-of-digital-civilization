inline const 结构_信息入口定义* 语素_查找信息入口定义(枚举_信息入口类型 信息入口类型) noexcept
{
    for (const auto& 定义 : 语素_信息入口定义表) {
        if (定义.信息入口类型 == 信息入口类型) {
            return &定义;
        }
    }
    return nullptr;
}

inline 枚举_主信息类型 语素_取信息入口主信息类型(枚举_信息入口类型 信息入口类型) noexcept
{
    const auto* 定义 = 语素_查找信息入口定义(信息入口类型);
    return 定义 ? 定义->主信息类型 : 枚举_主信息类型::未定义;
}

