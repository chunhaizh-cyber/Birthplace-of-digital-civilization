inline std::vector<枚举_信息入口类型> 语素_取人类词性信息入口候选(枚举_词性 词性)
{
    std::vector<枚举_信息入口类型> 候选集;
    for (const auto& 候选 : 语素_人类词性到信息入口候选表) {
        if (候选.人类词性 == 词性 && 候选.信息入口类型 != 枚举_信息入口类型::未定义) {
            候选集.push_back(候选.信息入口类型);
        }
    }
    return 候选集;
}

inline 枚举_信息入口类型 语素_选择默认信息入口类型(枚举_词性 词性) noexcept
{
    const 结构_人类词性到信息入口候选* 最佳 = nullptr;
    for (const auto& 候选 : 语素_人类词性到信息入口候选表) {
        if (候选.人类词性 != 词性 || 候选.信息入口类型 == 枚举_信息入口类型::未定义) {
            continue;
        }
        if (!最佳 || 候选.优先级 > 最佳->优先级) {
            最佳 = &候选;
        }
    }
    return 最佳 ? 最佳->信息入口类型 : 枚举_信息入口类型::未定义;
}

