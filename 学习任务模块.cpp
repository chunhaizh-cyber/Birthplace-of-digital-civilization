    void 私有_设定阶段状态(
        结构_学习任务实体& 实体,
        const 结构_学习任务同步输入& 输入) noexcept
    {
        const bool 补原语或治理本能 = 私有_学习目标为补原语或治理本能(输入);
        if (私有_根层禁止学习(输入.最近根层重判结果)) {
            实体.当前阶段 = 枚举_学习阶段::待适配;
            实体.当前状态 = 枚举_学习状态::挂起;
            实体.当前等待原因 = 枚举_学习等待原因::根层禁止学习;
            return;
        }

        if (输入.可直接进入学习) {
            实体.当前阶段 = 枚举_学习阶段::待调度;
            实体.当前状态 = 枚举_学习状态::可调度;
            实体.当前等待原因 = 枚举_学习等待原因::无;
            return;
        }

        实体.当前阶段 = 枚举_学习阶段::待适配;
        实体.当前状态 = 枚举_学习状态::挂起;
        实体.当前等待原因 = (!补原语或治理本能 && 输入.仅需求位方法头)
            ? 枚举_学习等待原因::仅需求位方法头
            : 枚举_学习等待原因::等待对象补齐;
    }

