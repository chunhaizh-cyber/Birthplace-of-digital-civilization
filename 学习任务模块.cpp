    命中->来源宿主任务 = 输入.来源宿主任务;
    命中->来源步骤 = 输入.来源步骤;
    命中->来源结果 = 输入.来源结果;
    命中->来源方法 = 输入.来源方法;
    命中->最近根层重判结果 = 输入.最近根层重判结果;
    命中->当前缺口类型 = 输入.当前缺口类型;
