#include "学习任务模块.h"

#include <mutex>
#include <sstream>
#include <utility>

#include "语素类.h"

namespace {
    using 任务节点 = 任务类::节点类;
    using 方法节点 = 方法类::节点类;

    std::mutex 私有_学习账本锁{};
    std::uint64_t 私有_下一个学习账本ID = 1;
    std::vector<结构_学习任务实体> 私有_学习任务表{};

    std::string 私有_安全词(const 词性节点类* 词) noexcept
    {
        if (!词) {
            return {};
        }

        try {
            return 语素集.获取词(词);
        }
        catch (...) {
            return {};
        }
    }

    std::string 私有_任务标题(const 任务节点* 节点) noexcept
    {
        if (!节点) {
            return "空任务";
        }
        const auto 标题 = 私有_安全词(节点->主信息.名称);
        return 标题.empty() ? std::string("任务#") + 节点->获取主键() : 标题;
    }

    std::string 私有_方法标题(const 方法节点* 节点) noexcept
    {
        if (!节点) {
            return "空方法";
        }
        const auto 标题 = 私有_安全词(节点->主信息.动作名);
        return 标题.empty() ? std::string("方法#") + 节点->获取主键() : 标题;
    }

    std::string 私有_默认去重键(const 结构_学习任务同步输入& 输入) noexcept
    {
        if (!输入.去重键.empty()) {
            return 输入.去重键;
        }
        if (输入.学习子任务) {
            return 输入.学习子任务->获取主键();
        }

        std::ostringstream 输出;
        输出 << 私有_任务标题(输入.来源宿主任务)
            << "|" << static_cast<int>(输入.当前缺口类型)
            << "|" << static_cast<int>(输入.当前下一步去向)
            << "|" << 私有_方法标题(输入.来源方法);
        return 输出.str();
    }

    bool 私有_根层禁止学习(枚举_任务管理根层重判结果 结果) noexcept
    {
        return 结果 == 枚举_任务管理根层重判结果::待机保持
            || 结果 == 枚举_任务管理根层重判结果::收束优先
            || 结果 == 枚举_任务管理根层重判结果::停止退出边界;
    }

    void 私有_设定阶段状态(
        结构_学习任务实体& 实体,
        const 结构_学习任务同步输入& 输入) noexcept
    {
        if (私有_根层禁止学习(输入.最近根层重判结果)) {
            实体.当前阶段 = 枚举_学习阶段::待适配;
            实体.当前状态 = 枚举_学习状态::挂起;
            实体.当前等待原因 = 枚举_学习等待原因::根层禁止学习;
            return;
        }

        if (输入.可直接进入学习) {
            实体.当前阶段 = 枚举_学习阶段::待调度;
            实体.当前状态 = 枚举_学习状态::可调度;
            实体.当前等待原因 = 枚举_学习等待原因::无;
            return;
        }

        实体.当前阶段 = 枚举_学习阶段::待适配;
        实体.当前状态 = 枚举_学习状态::挂起;
        实体.当前等待原因 = 输入.仅需求位方法头
            ? 枚举_学习等待原因::仅需求位方法头
            : 枚举_学习等待原因::原料不足;
    }

    std::string 私有_阶段文本(枚举_学习阶段 阶段) noexcept
    {
        switch (阶段) {
        case 枚举_学习阶段::待适配: return "待适配";
        case 枚举_学习阶段::待调度: return "待调度";
        case 枚举_学习阶段::采样: return "采样";
        case 枚举_学习阶段::更新: return "更新";
        case 枚举_学习阶段::验证: return "验证";
        case 枚举_学习阶段::提交: return "提交";
        case 枚举_学习阶段::回滚: return "回滚";
        default: return "未定义";
        }
    }

    std::string 私有_状态文本(枚举_学习状态 状态) noexcept
    {
        switch (状态) {
        case 枚举_学习状态::待创建: return "待创建";
        case 枚举_学习状态::挂起: return "挂起";
        case 枚举_学习状态::可调度: return "可调度";
        case 枚举_学习状态::执行中: return "执行中";
        case 枚举_学习状态::已提交: return "已提交";
        case 枚举_学习状态::已回滚: return "已回滚";
        case 枚举_学习状态::已失败: return "已失败";
        default: return "未定义";
        }
    }

    std::string 私有_等待原因文本(枚举_学习等待原因 原因) noexcept
    {
        switch (原因) {
        case 枚举_学习等待原因::无: return "无";
        case 枚举_学习等待原因::原料不足: return "原料不足";
        case 枚举_学习等待原因::仅需求位方法头: return "仅需求位方法头";
        case 枚举_学习等待原因::等待对象补齐: return "等待对象补齐";
        case 枚举_学习等待原因::等待再次命中: return "等待再次命中";
        case 枚举_学习等待原因::根层禁止学习: return "根层禁止学习";
        default: return "未定义";
        }
    }

    bool 私有_学习状态终结(枚举_学习状态 状态) noexcept
    {
        return 状态 == 枚举_学习状态::已提交
            || 状态 == 枚举_学习状态::已回滚
            || 状态 == 枚举_学习状态::已失败;
    }

    void 私有_刷新实体摘要(结构_学习任务实体& 实体) noexcept
    {
        std::ostringstream 输出;
        输出 << "子任务=" << 实体.学习子任务标题
            << " | 学习方法=" << 实体.学习方法标题
            << " | 阶段=" << 私有_阶段文本(实体.当前阶段)
            << " | 状态=" << 私有_状态文本(实体.当前状态)
            << " | 等待=" << 私有_等待原因文本(实体.当前等待原因)
            << " | 优先级=" << 实体.当前优先级
            << " | 重试=" << 实体.重试次数
            << " | 缺口=" << static_cast<int>(实体.当前缺口类型)
            << " | 去向=" << static_cast<int>(实体.当前下一步去向)
            << " | 来源宿主=" << 实体.来源宿主任务标题
            << " | 来源方法=" << 实体.来源方法标题;
        if (!实体.对象来源摘要.empty()) {
            输出 << " | 对象=" << 实体.对象来源摘要;
        }
        if (!实体.待学习方法骨架摘要.empty()) {
            输出 << " | 骨架=" << 实体.待学习方法骨架摘要;
        }
        if (!实体.最近执行摘要.empty()) {
            输出 << " | 执行=" << 实体.最近执行摘要;
        }
        if (!实体.最近失败摘要.empty()) {
            输出 << " | 失败=" << 实体.最近失败摘要;
        }
        if (!实体.最近回流摘要.empty()) {
            输出 << " | 回流=" << 实体.最近回流摘要;
        }
        if (!实体.最近反馈摘要.empty()) {
            输出 << " | 最近反馈=" << 实体.最近反馈摘要;
        }
        实体.摘要 = 输出.str();
    }

    bool 私有_学习状态活跃(枚举_学习状态 状态) noexcept
    {
        return 状态 == 枚举_学习状态::待创建
            || 状态 == 枚举_学习状态::挂起
            || 状态 == 枚举_学习状态::可调度
            || 状态 == 枚举_学习状态::执行中;
    }

    bool 私有_允许同步输入重算阶段状态(const 结构_学习任务实体& 实体) noexcept
    {
        return !私有_学习状态终结(实体.当前状态)
            && 实体.当前状态 != 枚举_学习状态::执行中;
    }

    结构_学习任务实体* 私有_查找学习任务_按ID(std::uint64_t 账本ID) noexcept
    {
        if (账本ID == 0) {
            return nullptr;
        }

        for (auto& 实体 : 私有_学习任务表) {
            if (实体.账本ID == 账本ID) {
                return &实体;
            }
        }
        return nullptr;
    }
}

bool 学习任务模块::创建或更新学习任务(
    const 结构_学习任务同步输入& 输入,
    结构_学习任务实体* 输出) noexcept
{
    if (!输入.学习子任务 && !输入.学习方法 && !输入.来源宿主任务) {
        return false;
    }

    std::lock_guard<std::mutex> 锁(私有_学习账本锁);

    const auto 去重键 = 私有_默认去重键(输入);
    auto* 命中 = static_cast<结构_学习任务实体*>(nullptr);
    for (auto& 实体 : 私有_学习任务表) {
        if (实体.去重键 == 去重键) {
            命中 = &实体;
            break;
        }
    }

    if (!命中) {
        结构_学习任务实体 新实体{};
        新实体.账本ID = 私有_下一个学习账本ID++;
        新实体.去重键 = 去重键;
        新实体.本轮新建 = true;
        私有_学习任务表.push_back(std::move(新实体));
        命中 = &私有_学习任务表.back();
    }
    else {
        命中->本轮更新 = true;
        命中->本轮新建 = false;
    }

    命中->最近更新时间 = 输入.当前时间;
    命中->学习子任务 = 输入.学习子任务;
    命中->学习方法 = 输入.学习方法;
    命中->来源宿主任务 = 输入.来源宿主任务;
    命中->来源步骤 = 输入.来源步骤;
    命中->来源结果 = 输入.来源结果;
    命中->来源方法 = 输入.来源方法;
    命中->最近根层重判结果 = 输入.最近根层重判结果;
    命中->当前缺口类型 = 输入.当前缺口类型;
    命中->当前下一步去向 = 输入.当前下一步去向;
    命中->当前方法来源 = 输入.当前方法来源;
    命中->仅需求位方法头 = 输入.仅需求位方法头;
    命中->可直接进入学习 = 输入.可直接进入学习;
    命中->学习子任务标题 = 私有_任务标题(输入.学习子任务);
    命中->学习方法标题 = 私有_方法标题(输入.学习方法);
    命中->来源宿主任务标题 = 私有_任务标题(输入.来源宿主任务);
    命中->来源步骤标题 = 私有_任务标题(输入.来源步骤);
    命中->来源结果标题 = 私有_任务标题(输入.来源结果);
    命中->来源方法标题 = 私有_方法标题(输入.来源方法);
    命中->最近反馈摘要 = 输入.最近反馈摘要;

    if (私有_允许同步输入重算阶段状态(*命中)) {
        私有_设定阶段状态(*命中, 输入);
    }
    私有_刷新实体摘要(*命中);

    if (输出) {
        *输出 = *命中;
    }
    return true;
}

bool 学习任务模块::读取学习任务实体(
    std::uint64_t 账本ID,
    结构_学习任务实体* 输出) noexcept
{
    if (!输出 || 账本ID == 0) {
        return false;
    }

    std::lock_guard<std::mutex> 锁(私有_学习账本锁);
    const auto* 命中 = 私有_查找学习任务_按ID(账本ID);
    if (!命中) {
        return false;
    }

    *输出 = *命中;
    return true;
}

bool 学习任务模块::更新学习任务实体(
    const 结构_学习任务更新输入& 输入,
    结构_学习任务实体* 输出) noexcept
{
    if (输入.账本ID == 0) {
        return false;
    }

    std::lock_guard<std::mutex> 锁(私有_学习账本锁);
    auto* 命中 = 私有_查找学习任务_按ID(输入.账本ID);
    if (!命中) {
        return false;
    }

    if (输入.当前时间 != 0) {
        命中->最近更新时间 = 输入.当前时间;
    }
    if (输入.覆写阶段) {
        命中->当前阶段 = 输入.当前阶段;
    }
    if (输入.覆写状态) {
        命中->当前状态 = 输入.当前状态;
    }
    if (输入.覆写等待原因) {
        命中->当前等待原因 = 输入.当前等待原因;
    }
    if (输入.覆写可合并键) {
        命中->可合并键 = 输入.可合并键;
    }
    if (输入.覆写当前优先级) {
        命中->当前优先级 = 输入.当前优先级;
    }
    if (输入.覆写仅需求位方法头) {
        命中->仅需求位方法头 = 输入.仅需求位方法头;
    }
    if (输入.覆写可直接进入学习) {
        命中->可直接进入学习 = 输入.可直接进入学习;
    }
    if (输入.累加重试次数) {
        命中->重试次数 += 输入.重试次数增量;
        if (命中->重试次数 < 0) {
            命中->重试次数 = 0;
        }
    }
    if (输入.覆写对象来源摘要) {
        命中->对象来源摘要 = 输入.对象来源摘要;
    }
    if (输入.覆写待学习方法骨架摘要) {
        命中->待学习方法骨架摘要 = 输入.待学习方法骨架摘要;
    }
    if (输入.覆写最近执行摘要) {
        命中->最近执行摘要 = 输入.最近执行摘要;
    }
    if (输入.覆写最近失败摘要) {
        命中->最近失败摘要 = 输入.最近失败摘要;
    }
    if (输入.覆写最近回流摘要) {
        命中->最近回流摘要 = 输入.最近回流摘要;
    }

    私有_刷新实体摘要(*命中);
    if (输出) {
        *输出 = *命中;
    }
    return true;
}

bool 学习任务模块::读取学习账本快照(
    结构_学习账本快照* 输出) noexcept
{
    if (!输出) {
        return false;
    }

    std::lock_guard<std::mutex> 锁(私有_学习账本锁);
    输出->总数 = 私有_学习任务表.size();
    输出->活跃数 = 0;
    输出->挂起数 = 0;
    输出->可调度数 = 0;
    输出->执行中数 = 0;
    输出->已完成数 = 0;
    输出->任务列表 = 私有_学习任务表;
    输出->最近摘要.clear();

    for (const auto& 实体 : 私有_学习任务表) {
        if (私有_学习状态活跃(实体.当前状态)) {
            ++输出->活跃数;
        }
        if (实体.当前状态 == 枚举_学习状态::挂起) {
            ++输出->挂起数;
        }
        else if (实体.当前状态 == 枚举_学习状态::可调度) {
            ++输出->可调度数;
        }
        else if (实体.当前状态 == 枚举_学习状态::执行中) {
            ++输出->执行中数;
        }
        else if (实体.当前状态 == 枚举_学习状态::已提交) {
            ++输出->已完成数;
        }
    }

    if (!私有_学习任务表.empty()) {
        输出->最近摘要 = 私有_学习任务表.back().摘要;
    }
    return true;
}

std::vector<结构_学习任务实体> 学习任务模块::枚举学习任务实体()
{
    std::lock_guard<std::mutex> 锁(私有_学习账本锁);
    return 私有_学习任务表;
}

I64 学习任务模块::统计活跃学习任务数量() noexcept
{
    std::lock_guard<std::mutex> 锁(私有_学习账本锁);
    I64 数量 = 0;
    for (const auto& 实体 : 私有_学习任务表) {
        if (私有_学习状态活跃(实体.当前状态)) {
            ++数量;
        }
    }
    return 数量;
}
