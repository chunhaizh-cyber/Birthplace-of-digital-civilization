    枚举_方法类型 私有_推导动作句柄方法类型(const 结构体_动作句柄& 动作句柄) noexcept
    {
        switch (动作句柄.类型) {
        case 枚举_动作句柄类型::外部实现主键:
            return 动作句柄.外部实现主键.empty()
                ? 枚举_方法类型::未定义
                : 枚举_方法类型::外部方法;
        case 枚举_动作句柄类型::动作序列主键:
            return 动作句柄.动作序列主键.empty()
                ? 枚举_方法类型::未定义
                : 枚举_方法类型::内部方法;
        case 枚举_动作句柄类型::本能函数ID: {
            if (动作句柄.本能ID == 0) {
                return 枚举_方法类型::未定义;
            }
            const auto* 元信息 = 本能方法类::查询元信息(
                static_cast<枚举_本能方法ID>(动作句柄.本能ID));
            if (元信息 && 元信息->分类 == 枚举_本能方法分类::外部动作) {
                return 枚举_方法类型::外部方法;
            }
            return 枚举_方法类型::内部方法;
        }
        default:
            return 枚举_方法类型::未定义;
        }
    }

    枚举_方法类型 私有_读取方法类型特征(const 方法节点* 方法头) noexcept
    {
        auto* 方法虚拟存在 = 方法头 ? 方法头->主信息.方法虚拟存在.指针 : nullptr;
        auto* 宿主基础节点 = reinterpret_cast<const 基础信息节点类*>(方法虚拟存在);
        auto* 特征节点 = 宿主基础节点
            ? 世界树.存在().查找子特征_按类型(宿主基础节点, 私有_方法特征_方法类型())
            : nullptr;
        I64 当前值 = 0;
        if (!特征节点 || !世界树.读取特征_I64(特征节点, 当前值)) {
            return 枚举_方法类型::未定义;
        }
        return static_cast<枚举_方法类型>(当前值);
    }

    枚举_方法类型 私有_推导方法类型(
        const 方法节点* 学习方法,
        const 方法节点* 来源方法) noexcept
    {
        if (const auto 类型 = 私有_读取方法类型特征(学习方法);
            类型 != 枚举_方法类型::未定义) {
            return 类型;
        }

        if (学习方法) {
            if (const auto 类型 = 私有_推导动作句柄方法类型(学习方法->主信息.动作句柄);
                类型 != 枚举_方法类型::未定义) {
                return 类型;
            }
        }

        if (const auto 类型 = 私有_读取方法类型特征(来源方法);
            类型 != 枚举_方法类型::未定义) {
            return 类型;
        }

        return 来源方法
            ? 私有_推导动作句柄方法类型(来源方法->主信息.动作句柄)
            : 枚举_方法类型::未定义;
    }

