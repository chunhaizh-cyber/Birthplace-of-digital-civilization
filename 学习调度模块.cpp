    bool 私有_本能缺口属于原语或治理补齐(
        枚举_任务管理本能能力缺口类型 缺口类型) noexcept
    {
        switch (缺口类型) {
        case 枚举_任务管理本能能力缺口类型::缺观察原语:
        case 枚举_任务管理本能能力缺口类型::缺写回原语:
        case 枚举_任务管理本能能力缺口类型::缺挂点原语:
        case 枚举_任务管理本能能力缺口类型::缺动作边界:
        case 枚举_任务管理本能能力缺口类型::缺状态差分取证:
        case 枚举_任务管理本能能力缺口类型::缺实例因果取证:
        case 枚举_任务管理本能能力缺口类型::缺抽象因果归纳:
            return true;
        default:
            return false;
        }
    }

    bool 私有_保持正式提交闸门等待(const 结构_学习任务实体& 实体) noexcept
    {
        return 实体.当前阶段 == 枚举_学习阶段::提交
            && 实体.当前等待原因 == 枚举_学习等待原因::等待正式提交放行
            && !实体.允许正式资产提交
            && 实体.当前状态 != 枚举_学习状态::已提交
            && 实体.当前状态 != 枚举_学习状态::已回滚
            && 实体.当前状态 != 枚举_学习状态::已失败;
    }

    const char* 私有_学习通道文本(const 结构_学习任务实体& 实体) noexcept
    {
        return 私有_本能缺口属于原语或治理补齐(实体.来源本能能力缺口类型)
            ? "补原语/治理本能"
            : "补方法";
    }

