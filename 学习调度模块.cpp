#include "学习调度模块.h"

#include <algorithm>
#include <sstream>
#include <vector>

namespace {
    const char* 私有_学习状态文本(枚举_学习状态 状态) noexcept
    {
        switch (状态) {
        case 枚举_学习状态::待创建: return "待创建";
        case 枚举_学习状态::挂起: return "挂起";
        case 枚举_学习状态::可调度: return "可调度";
        case 枚举_学习状态::执行中: return "执行中";
        case 枚举_学习状态::已提交: return "已提交";
        case 枚举_学习状态::已回滚: return "已回滚";
        case 枚举_学习状态::已失败: return "已失败";
        default: return "未定义";
        }
    }

    const char* 私有_等待原因文本(枚举_学习等待原因 原因) noexcept
    {
        switch (原因) {
        case 枚举_学习等待原因::无: return "无";
        case 枚举_学习等待原因::原料不足: return "原料不足";
        case 枚举_学习等待原因::仅需求位方法头: return "仅需求位方法头";
        case 枚举_学习等待原因::等待对象补齐: return "等待对象补齐";
        case 枚举_学习等待原因::等待再次命中: return "等待再次命中";
        case 枚举_学习等待原因::根层禁止学习: return "根层禁止学习";
        default: return "未定义";
        }
    }

    I64 私有_计算优先级(
        const 结构_学习任务实体& 实体,
        const 结构_学习对象适配结果& 适配结果) noexcept
    {
        I64 优先级 = 10;
        if (适配结果.可直接进入学习) {
            优先级 += 100;
        }
        if (实体.当前缺口类型 != 枚举_任务管理缺口类型::无缺口
            && 实体.当前缺口类型 != 枚举_任务管理缺口类型::未定义) {
            优先级 += 30;
        }
        if (实体.当前方法来源 == 枚举_任务管理方法来源::默认本能兜底) {
            优先级 += 20;
        }
        if (实体.当前下一步去向 == 枚举_任务管理下一步去向::回到重筹办) {
            优先级 += 10;
        }
        优先级 -= 实体.重试次数 * 5;
        return 优先级 < 1 ? 1 : 优先级;
    }

    std::string 私有_构造可合并键(
        const 结构_学习任务实体& 实体,
        const 结构_学习对象适配结果& 适配结果)
    {
        std::ostringstream 输出;
        输出 << 实体.来源宿主任务标题
            << "|" << 实体.学习方法标题
            << "|" << static_cast<int>(实体.当前缺口类型)
            << "|" << (适配结果.仅需求位方法头 ? "需求位" : "骨架位");
        return 输出.str();
    }

    std::string 私有_构造调度摘要(
        const 结构_学习任务实体& 实体,
        const 结构_学习对象适配结果& 适配结果,
        I64 优先级)
    {
        std::ostringstream 输出;
        输出 << "状态=" << 私有_学习状态文本(实体.当前状态)
            << " | 优先级=" << 优先级
            << " | 等待=" << 私有_等待原因文本(实体.当前等待原因)
            << " | 适配=" << 适配结果.摘要;
        return 输出.str();
    }
}

结构_学习调度快照 学习调度模块::构建学习调度快照() noexcept
{
    结构_学习调度快照 输出{};
    const auto 任务列表 = 学习任务模块::枚举学习任务实体();
    输出.任务表总数 = 任务列表.size();

    for (const auto& 实体 : 任务列表) {
        const auto 适配结果 = 学习对象适配器::适配学习对象(实体);
        const auto 优先级 = 私有_计算优先级(实体, 适配结果);
        const auto 可合并键 = 私有_构造可合并键(实体, 适配结果);

        结构_学习任务更新输入 更新{};
        更新.账本ID = 实体.账本ID;
        更新.当前时间 = 实体.最近更新时间;
        更新.覆写当前优先级 = true;
        更新.当前优先级 = 优先级;
        更新.覆写可合并键 = true;
        更新.可合并键 = 可合并键;
        更新.覆写对象来源摘要 = true;
        更新.对象来源摘要 = 适配结果.原料.来源摘要;
        更新.覆写待学习方法骨架摘要 = true;
        更新.待学习方法骨架摘要 = 适配结果.摘要;
        if (实体.当前状态 != 枚举_学习状态::执行中
            && 实体.当前状态 != 枚举_学习状态::已提交
            && 实体.当前状态 != 枚举_学习状态::已回滚
            && 实体.当前状态 != 枚举_学习状态::已失败) {
            更新.覆写状态 = true;
            更新.当前状态 = 适配结果.可直接进入学习
                ? 枚举_学习状态::可调度
                : 枚举_学习状态::挂起;
            更新.覆写阶段 = true;
            更新.当前阶段 = 适配结果.可直接进入学习
                ? 枚举_学习阶段::待调度
                : 枚举_学习阶段::待适配;
            更新.覆写等待原因 = true;
            更新.当前等待原因 = 适配结果.可直接进入学习
                ? 枚举_学习等待原因::无
                : (适配结果.仅需求位方法头
                    ? 枚举_学习等待原因::仅需求位方法头
                    : 枚举_学习等待原因::等待对象补齐);
        }
        学习任务模块::更新学习任务实体(更新, nullptr);

        结构_学习调度项 项{};
        项.实体 = 实体;
        if (实体.当前状态 != 枚举_学习状态::执行中
            && 实体.当前状态 != 枚举_学习状态::已提交
            && 实体.当前状态 != 枚举_学习状态::已回滚
            && 实体.当前状态 != 枚举_学习状态::已失败) {
            项.实体.当前状态 = 适配结果.可直接进入学习
                ? 枚举_学习状态::可调度
                : 枚举_学习状态::挂起;
            项.实体.当前阶段 = 适配结果.可直接进入学习
                ? 枚举_学习阶段::待调度
                : 枚举_学习阶段::待适配;
            项.实体.当前等待原因 = 适配结果.可直接进入学习
                ? 枚举_学习等待原因::无
                : (适配结果.仅需求位方法头
                    ? 枚举_学习等待原因::仅需求位方法头
                    : 枚举_学习等待原因::等待对象补齐);
        }
        项.实体.当前优先级 = 优先级;
        项.实体.可合并键 = 可合并键;
        项.实体.对象来源摘要 = 适配结果.原料.来源摘要;
        项.实体.待学习方法骨架摘要 = 适配结果.摘要;
        项.适配结果 = 适配结果;
        项.当前优先级 = 优先级;
        项.去重键 = 实体.去重键;
        项.可合并键 = 可合并键;
        项.摘要 = 私有_构造调度摘要(项.实体, 项.适配结果, 优先级);

        if (实体.当前状态 == 枚举_学习状态::执行中) {
            输出.执行中列表.push_back(std::move(项));
        }
        else if (适配结果.可直接进入学习
            && (实体.当前状态 == 枚举_学习状态::可调度
                || 实体.当前状态 == 枚举_学习状态::待创建
                || 实体.当前状态 == 枚举_学习状态::挂起)) {
            输出.就绪队列.push_back(std::move(项));
        }
        else if (实体.当前状态 != 枚举_学习状态::已提交
            && 实体.当前状态 != 枚举_学习状态::已回滚
            && 实体.当前状态 != 枚举_学习状态::已失败) {
            输出.等待表.push_back(std::move(项));
        }
    }

    const auto 排序器 = [](const 结构_学习调度项& 左, const 结构_学习调度项& 右) {
        if (左.当前优先级 != 右.当前优先级) {
            return 左.当前优先级 > 右.当前优先级;
        }
        return 左.实体.账本ID < 右.实体.账本ID;
    };
    std::sort(输出.就绪队列.begin(), 输出.就绪队列.end(), 排序器);
    std::sort(输出.等待表.begin(), 输出.等待表.end(), 排序器);
    std::sort(输出.执行中列表.begin(), 输出.执行中列表.end(), 排序器);

    输出.就绪队列数 = 输出.就绪队列.size();
    输出.等待表数 = 输出.等待表.size();
    输出.执行中数 = 输出.执行中列表.size();

    std::ostringstream 摘要;
    摘要 << "学习任务表=" << 输出.任务表总数
        << " | 就绪=" << 输出.就绪队列数
        << " | 等待=" << 输出.等待表数
        << " | 执行中=" << 输出.执行中数;
    if (!输出.就绪队列.empty()) {
        摘要 << " | 当前候选=" << 输出.就绪队列.front().实体.学习子任务标题;
    }
    else if (!输出.执行中列表.empty()) {
        摘要 << " | 当前执行=" << 输出.执行中列表.front().实体.学习子任务标题;
    }
    输出.摘要 = 摘要.str();
    return 输出;
}

bool 学习调度模块::选择一条可执行学习任务(
    结构_学习调度项* 输出,
    结构_学习调度快照* 快照输出) noexcept
{
    auto 快照 = 构建学习调度快照();
    if (快照输出) {
        *快照输出 = 快照;
    }
    if (!输出 || 快照.就绪队列.empty()) {
        return false;
    }

    *输出 = 快照.就绪队列.front();
    return true;
}
