#include "自我线程轮次执行器.h"

#include "自我类.h"

namespace 自我线程轮次执行器 {

bool 执行一轮最小任务治理(
    自我类& 自我对象,
    时间戳 now,
    const 枚举_任务管理根层重判结果 根层重判结果,
    const 枚举_任务管理执行前门控结果 执行前门控结果,
    const std::string& 上层反馈摘要,
    const std::string& 调用点,
    结构_任务治理桥接结果* 输出) noexcept
{
    if (!输出) {
        return false;
    }

    auto& 结果 = *输出;
    结果 = {};
    结果.根层重判结果 = 根层重判结果;
    结果.执行前门控结果 = 执行前门控结果;
    结果.上层反馈摘要 = 上层反馈摘要;

    const auto 上位输入调用点 = 调用点 + "/上位输入";
    结果.任务管理上位输入已刷新 = 任务管理任务模块::写入上位输入镜像(
        自我对象,
        根层重判结果,
        执行前门控结果,
        上层反馈摘要,
        now,
        上位输入调用点);

    const auto 任务管理调用点 = 调用点 + "/任务管理";
    结果.任务管理治理触发 = 任务管理任务模块::执行一步治理(
        自我对象,
        now,
        任务管理调用点,
        &结果.治理结果);

    if (结果.任务管理治理触发) {
        结果.已生成一步治理任务包 = 任务管理任务模块::生成一步治理任务包(
            结果.治理结果,
            &结果.一步治理任务包);

        const auto* 任务包指针 =
            结果.已生成一步治理任务包
            ? &结果.一步治理任务包
            : nullptr;
        结果.已生成回流结算包 = 自我线程最小回写器::执行最小回流结算与回写(
            自我对象,
            now,
            任务包指针,
            结果.治理结果,
            &结果.最小回写结果);
        结果.已写入动态节点 = 结果.最小回写结果.已写入动态;
        结果.已写入因果候选 = 结果.最小回写结果.已写入因果候选;
    }

    return true;
}

} // namespace 自我线程轮次执行器
