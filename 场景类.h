#pragma once

#include "基础信息类.h"

class 场景类 {
public:
    explicit 场景类(基础信息类* 基础信息 = nullptr) noexcept;

    void 绑定基础信息(基础信息类* 基础信息) noexcept;

    场景节点主信息类* 取场景主信息(场景节点类* 节点) const noexcept;
    const 场景节点主信息类* 取场景主信息(const 场景节点类* 节点) const noexcept;

    场景节点类* 创建场景(基础信息节点类* 父节点, 场景节点主信息类* 主信息);
    场景节点类* 创建场景_按名称(
        基础信息节点类* 父节点,
        const 语素入口节点类* 名称,
        枚举_世界类型 世界类型 = 枚举_世界类型::其它
    );
    场景节点类* 取或创建子场景_按名称(
        基础信息节点类* 父节点,
        const 语素入口节点类* 名称,
        枚举_世界类型 世界类型 = 枚举_世界类型::其它
    );
    bool 删除场景(场景节点类* 节点);

    std::vector<场景节点类*> 枚举全部场景() const;
    std::vector<场景节点类*> 获取子场景(const 基础信息节点类* 父节点) const;
    std::vector<存在节点类*> 获取子存在(const 基础信息节点类* 父节点) const;
    std::vector<特征节点类*> 获取子特征(const 基础信息节点类* 父节点) const;
    场景节点类* 查找子场景_按名称(const 基础信息节点类* 父节点, const 语素入口节点类* 名称) const;

    bool 绑定宿主(场景节点类* 节点, 存在节点类* 宿主存在);
    bool 绑定来源文章(场景节点类* 节点, 文章节点类* 来源文章);
    bool 追加状态(场景节点类* 节点, 状态节点类* 状态节点);
    bool 追加动态(场景节点类* 节点, 动态节点类* 动态节点);
    bool 追加关系(场景节点类* 节点, 二次特征节点类* 二次特征节点);
    bool 追加二次特征(场景节点类* 节点, 二次特征节点类* 二次特征节点);
    bool 追加实例因果(场景节点类* 节点, 因果实例节点类* 因果实例节点);

private:
    基础信息类* 基础信息_ = nullptr;
};
