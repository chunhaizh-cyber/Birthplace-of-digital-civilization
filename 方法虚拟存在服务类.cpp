bool 方法虚拟存在服务类::同步方法首节点结构特征(
    方法类::节点类* 方法首节点,
    存在节点类* 宿主存在,
    时间戳 now)
{
    auto* 首节点 = 私有_归一到方法首节点(方法首节点);
    if (!首节点) {
        return false;
    }
    if (!确保方法虚拟存在(首节点, 宿主存在, now)) {
        return false;
    }

    const auto& 首信息 = 首节点->主信息.首节点信息();
    bool 已写入 = false;
    if (auto* 父方法存在 = 私有_解析方法虚拟存在引用(
            私有_解析方法引用(首信息.父方法),
            宿主存在,
            now)) {
        已写入 = 私有_写入指针特征(首节点, "父方法", 父方法存在, 宿主存在, now) || 已写入;
    }
    if (auto* 前置方法存在 = 私有_解析方法虚拟存在引用(
            私有_解析方法引用(首信息.前置方法),
            宿主存在,
            now)) {
        已写入 = 私有_写入指针特征(首节点, "前置方法", 前置方法存在, 宿主存在, now) || 已写入;
    }
    if (auto* 后续方法存在 = 私有_解析方法虚拟存在引用(
            私有_解析方法引用(首信息.后续方法),
            宿主存在,
            now)) {
        已写入 = 私有_写入指针特征(首节点, "后续方法", 后续方法存在, 宿主存在, now) || 已写入;
    }
    if (auto* 条件场景 = 私有_取首个条件场景(首节点)) {
        已写入 = 私有_写入指针特征(首节点, "方法条件场景", 条件场景, 宿主存在, now) || 已写入;
    }
    if (auto* 结果场景 = 私有_取首个结果场景(首节点)) {
        已写入 = 私有_写入指针特征(首节点, "方法结果场景", 结果场景, 宿主存在, now) || 已写入;
    }
    if (!首信息.能力.结果包.结果项集.empty()) {
        const auto 结果数量 = 首信息.能力.结果包.结果项集.size();
        const auto 可查找结果数量 =
            首信息.能力.可被方法查找命中 && !首信息.能力.是否根写入原语
                ? 结果数量
                : 0;

        已写入 = 写入方法I64特征(
            首节点,
            私有_方法语义特征词("方法能力结果数量"),
            static_cast<I64>(结果数量),
            宿主存在,
            now) || 已写入;
        已写入 = 写入方法I64特征(
            首节点,
            私有_方法语义特征词("方法可查找结果数量"),
            static_cast<I64>(可查找结果数量),
            宿主存在,
            now) || 已写入;
    }
    return 已写入;
}

