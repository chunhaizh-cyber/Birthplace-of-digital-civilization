bool 方法虚拟存在服务类::写入方法指针特征(
    方法类::节点类* 方法首节点,
    const 语素入口节点类* 特征类型,
    const void* 指针,
    存在节点类* 宿主存在,
    时间戳 now)
{
    if (!指针) {
        return false;
    }
    return 记录方法虚拟存在特征(
        方法首节点,
        特征类型,
        特征值{ 指针句柄{ reinterpret_cast<std::uintptr_t>(指针) } },
        宿主存在,
        now);
}

bool 方法虚拟存在服务类::记录方法运行统计(
    方法类::节点类* 方法首节点,
    const char* 统计名,
    I64 增量,
    存在节点类* 宿主存在,
    时间戳 now)
{
    auto* 首节点 = 私有_归一到方法首节点(方法首节点);
    if (!首节点 || !统计名 || !*统计名) {
        return false;
    }
    auto* 方法虚拟存在 = 确保方法虚拟存在(首节点, 宿主存在, now);
    if (!方法虚拟存在) {
        return false;
    }

    auto* 特征类型 = 私有_方法统计特征词(统计名);
    auto* 特征节点 = 世界树.确保特征(
        reinterpret_cast<基础信息节点类*>(方法虚拟存在),
        特征类型);
    if (!特征节点) {
        return false;
    }

    I64 当前值 = 0;
    (void)世界树.读取特征_I64(特征节点, 当前值);
    return 世界树.写入特征_I64(特征节点, 当前值 + 增量, now);
}

