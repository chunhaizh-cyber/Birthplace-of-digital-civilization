    bool 私有_同步方法身份与默认特征(
        方法类::节点类* 方法首节点,
        存在节点类* 宿主存在,
        时间戳 now) noexcept
    {
        auto* 首节点 = 私有_归一到方法首节点(方法首节点);
        if (!首节点) {
            return false;
        }

        const auto& 首信息 = 首节点->主信息.首节点信息();
        const auto 方法类型 = 私有_推导方法类型(首节点->主信息);
        const auto 默认成熟度 = 私有_默认成熟度阶段(首节点->主信息);

        bool 已写入 = false;
        // 方法虚拟存在的身份镜像由服务层统一同步，方法类只保留底层写特征能力。
        已写入 = 方法虚拟存在服务类::写入方法I64特征(
            首节点,
            私有_方法语义特征词("方法类型"),
            static_cast<I64>(方法类型),
            宿主存在,
            now) || 已写入;
        已写入 = 方法虚拟存在服务类::写入方法I64特征(
            首节点,
            私有_方法语义特征词("方法来源"),
            static_cast<I64>(首信息.来源),
            宿主存在,
            now) || 已写入;
        已写入 = 方法虚拟存在服务类::写入方法I64特征(
            首节点,
            私有_方法语义特征词("动作句柄类型"),
            static_cast<I64>(首信息.动作句柄.类型),
            宿主存在,
            now) || 已写入;
        已写入 = 方法虚拟存在服务类::写入方法I64特征(
            首节点,
            私有_方法语义特征词("成熟度阶段"),
            默认成熟度,
            宿主存在,
            now) || 已写入;
        已写入 = 方法虚拟存在服务类::写入方法I64特征(
            首节点,
            私有_方法语义特征词("允许自动召回"),
            私有_默认允许自动召回(默认成熟度),
            宿主存在,
            now) || 已写入;
        已写入 = 方法虚拟存在服务类::写入方法I64特征(
            首节点,
            私有_方法语义特征词("允许默认执行"),
            私有_默认允许默认执行(默认成熟度),
            宿主存在,
            now) || 已写入;
        已写入 = 方法虚拟存在服务类::写入方法I64特征(
            首节点,
            私有_方法语义特征词("特征账本生命周期"),
            static_cast<I64>(枚举_特征生命周期::候选),
            宿主存在,
            now) || 已写入;

        if (首信息.动作句柄.类型 == 枚举_动作句柄类型::本能函数ID
            && 首信息.动作句柄.本能ID != 0) {
            已写入 = 方法虚拟存在服务类::写入方法I64特征(
                首节点,
                私有_方法语义特征词("本能动作ID"),
                首信息.动作句柄.本能ID,
                宿主存在,
                now) || 已写入;
            已写入 = 方法虚拟存在服务类::写入方法I64特征(
                首节点,
                自我特征定义类::类型_自我_自我能力(),
                首信息.动作句柄.本能ID,
                宿主存在,
                now) || 已写入;
        }
        return 已写入;
    }

    // 方法域动作动态生成点：
    // 方法虚拟存在服务层在同步“方法状态”特征时调用本函数，
    // 把目标方法虚拟存在上的方法状态旧值 -> 新值落成动作动态。
    // 该函数只记录方法域记忆变化，不写任务状态，不修改需求树，
    // 也不提交世界事实；动作动态引用仅回写到方法虚拟存在。
    动态节点类* 私有_输出方法状态动作动态(
        方法类::节点类* 方法首节点,
        存在节点类* 方法虚拟存在,
        特征节点类* 方法状态特征,
        I64 旧状态,
        I64 新状态,
        bool 有旧状态,
        时间戳 now) noexcept
    {
        auto* 首节点 = 私有_归一到方法首节点(方法首节点);
        if (!首节点 || !方法虚拟存在 || !方法状态特征 || (有旧状态 && 旧状态 == 新状态)) {
            return nullptr;
        }

        auto* 场景 = 世界树.取存在内部世界(方法虚拟存在);
        if (!场景) {
            return nullptr;
        }

        auto* 宿主 = reinterpret_cast<基础信息节点类*>(方法虚拟存在);
        auto* 旧状态节点 = 世界树.状态().创建内部状态(
            场景,
            宿主,
            方法状态特征,
            特征值{ 有旧状态 ? 旧状态 : static_cast<I64>(0) },
            枚举_存在状态事件::变化,
            false,
            now);
        auto* 新状态节点 = 世界树.状态().创建内部状态(
            场景,
            宿主,
            方法状态特征,
            特征值{ 新状态 },
            枚举_存在状态事件::变化,
            true,
            now);
        if (!旧状态节点 || !新状态节点) {
            return nullptr;
        }

        auto* 动态 = 世界树.动态().创建动态(
            场景,
            旧状态节点,
            新状态节点,
            now,
            now);
        if (!动态) {
            return nullptr;
        }

        const auto* 动作词 = 私有_方法语义特征词("方法状态提交");
        if (auto* 动态主信息 = 世界树.动态().取动态主信息(动态)) {
            动态主信息->名称 = 动作词;
            动态主信息->类型 = 动作词;
            动态主信息->来源类型 = 枚举_动态来源类型::内部机制动作;
            动态主信息->来源动作名 = 动作词;
            动态主信息->来源动作相位 = 枚举_动作事件相位::完成;
            动态主信息->来源执行成功 = true;
            动态主信息->时间 = 结构_时间段{ now, now };
        }

        (void)世界树.动态().设置业务语义类型(
            动态,
            枚举_动态业务语义类型::治理过程动态);
        (void)世界树.动态().设置主体特征(动态, 宿主, 方法状态特征);
        (void)世界树.动态().绑定来源场景(动态, 场景, 场景);
        (void)世界树.写入特征_指针(
            宿主,
            私有_方法语义特征词("方法状态动作动态"),
            动态,
            now);
        (void)世界树.写入特征_指针(
            宿主,
            私有_方法语义特征词("最近动作动态"),
            动态,
            now);
        return 动态;
    }

    bool 私有_同步方法状态特征(
        方法类::节点类* 方法首节点,
        存在节点类* 宿主存在,
        时间戳 now) noexcept
    {
        auto* 首节点 = 私有_归一到方法首节点(方法首节点);
        if (!首节点) {
            return false;
        }

        I64 方法状态 = 0;
        if (!首节点->主信息.有动作骨架()) {
            方法状态 = 私有_方法状态_待动作骨架值();
        } else if (!首节点->主信息.首节点信息().动作句柄.有效()) {
            方法状态 = 私有_方法状态_待可执行入口值();
        } else if (私有_方法直接子节点数量(首节点, 枚举_方法节点种类::方法条件节点) <= 0) {
            方法状态 = 私有_方法状态_待条件节点值();
        } else if (私有_方法直接子节点数量(首节点, 枚举_方法节点种类::方法结果节点) <= 0
            && 私有_方法条件结果对数量(首节点) <= 0) {
            方法状态 = 私有_方法状态_待结果节点值();
        } else if (私有_方法条件结果对数量(首节点) <= 0) {
            方法状态 = 私有_方法状态_待条件结果对值();
        } else {
            // “方法状态=可用”的最低结构前提是已经存在至少一组条件结果对。
            方法状态 = 私有_方法状态_可用值();
        }

        auto* 方法虚拟存在 = 首节点->主信息.首节点信息().方法虚拟存在.指针;
        auto* 方法状态特征 = 方法虚拟存在
            ? 世界树.确保特征(
                reinterpret_cast<基础信息节点类*>(方法虚拟存在),
                私有_方法语义特征词("方法状态"))
            : nullptr;
        I64 旧状态 = 0;
        const bool 有旧状态 = 方法状态特征
            && 世界树.读取特征_I64(方法状态特征, 旧状态);

        const bool 已写入 = 方法虚拟存在服务类::写入方法I64特征(
            首节点,
            私有_方法语义特征词("方法状态"),
            方法状态,
            宿主存在,
            now);
        if (已写入 && 方法状态特征 && (!有旧状态 || 旧状态 != 方法状态)) {
            (void)私有_输出方法状态动作动态(
                首节点,
                方法虚拟存在,
                方法状态特征,
                旧状态,
                方法状态,
                有旧状态,
                now);
        }
        return 已写入;
    }

