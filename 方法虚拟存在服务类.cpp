    I64 私有_方法直接子节点数量(方法类::节点类* 方法, 枚举_方法节点种类 节点种类) noexcept
    {
        if (!方法 || !方法->子) {
            return 0;
        }
        I64 数量 = 0;
        auto* 首子 = static_cast<方法类::节点类*>(方法->子);
        auto* 当前 = 首子;
        std::size_t 保护计数 = 0;
        do {
            if (当前 && 当前->主信息.节点种类() == 节点种类) {
                ++数量;
            }
            当前 = 当前 ? static_cast<方法类::节点类*>(当前->下) : nullptr;
            ++保护计数;
        } while (当前 && 当前 != 首子 && 保护计数 < 4096);
        return 数量;
    }

    I64 私有_方法条件结果对数量(方法类::节点类* 方法) noexcept
    {
        if (!方法 || !方法->子) {
            return 0;
        }
        I64 数量 = 0;
        auto* 首子 = static_cast<方法类::节点类*>(方法->子);
        auto* 当前 = 首子;
        std::size_t 保护计数 = 0;
        do {
            if (当前
                && 当前->主信息.节点种类() == 枚举_方法节点种类::方法条件节点
                && 当前->子) {
                auto* 首结果 = static_cast<方法类::节点类*>(当前->子);
                auto* 结果 = 首结果;
                std::size_t 结果保护 = 0;
                do {
                    if (结果 && 结果->主信息.节点种类() == 枚举_方法节点种类::方法结果节点) {
                        ++数量;
                    }
                    结果 = 结果 ? static_cast<方法类::节点类*>(结果->下) : nullptr;
                    ++结果保护;
                } while (结果 && 结果 != 首结果 && 结果保护 < 1024);
            }
            当前 = 当前 ? static_cast<方法类::节点类*>(当前->下) : nullptr;
            ++保护计数;
        } while (当前 && 当前 != 首子 && 保护计数 < 4096);
        return 数量;
    }

    std::vector<存在节点类*> 私有_方法虚拟存在概念集(方法类::节点类* 方法首节点) noexcept
    {
        std::vector<存在节点类*> 概念集{};
        const auto 追加概念 = [&概念集](存在节点类* 概念) noexcept {
            if (!概念) {
                return;
            }
            for (const auto* 已有 : 概念集) {
                if (已有 == 概念) {
                    return;
                }
            }
            概念集.push_back(概念);
        };

        auto* 方法概念 = 世界树.确保核心概念_方法();
        追加概念(世界树.确保根概念_存在());
        追加概念(世界树.确保根概念_虚拟存在());
        追加概念(方法概念);

        if (方法首节点) {
            const auto& 首信息 = 方法首节点->主信息.首节点信息();
            const auto 方法类型 = 私有_推导方法类型(方法首节点->主信息);
            if (const auto* 类型概念 = 私有_方法类型概念名(方法类型)) {
                追加概念(世界树.确保存在概念(
                    语素集.添加信息入口词(类型概念, 枚举_信息入口类型::方法概念入口),
                    方法概念));
            }
            if (const auto* 来源概念 = 私有_方法来源概念名(首信息.来源)) {
                追加概念(世界树.确保存在概念(
                    语素集.添加信息入口词(来源概念, 枚举_信息入口类型::方法概念入口),
                    方法概念));
            }
            if (auto* 动作方法概念词 = 私有_动作名转方法概念词(首信息.动作名)) {
                追加概念(世界树.确保存在概念(动作方法概念词, 方法概念));
            }
        }
        return 概念集;
    }

    bool 私有_确保I64特征已初始化(
        基础信息节点类* 宿主,
        const 语素入口节点类* 特征类型,
        I64 默认值,
        时间戳 now) noexcept
    {
        if (!宿主 || !特征类型) {
            return false;
        }
        auto* 特征节点 = 世界树.确保特征(宿主, 特征类型);
        if (!特征节点) {
            return false;
        }

        I64 当前值 = 0;
        if (世界树.读取特征_I64(特征节点, 当前值)) {
            return true;
        }
        return 世界树.写入特征_I64(特征节点, 默认值, now);
    }

