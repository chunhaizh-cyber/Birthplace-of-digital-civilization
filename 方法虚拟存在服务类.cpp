    方法类::节点类* 私有_归一到方法首节点(方法类::节点类* 节点) noexcept
    {
        auto* 当前 = 节点;
        while (当前) {
            if (当前->主信息.节点种类() == 枚举_方法节点种类::方法首节点) {
                return 当前;
            }
            当前 = static_cast<方法类::节点类*>(当前->父);
        }
        return nullptr;
    }

    const 语素入口节点类* 私有_方法语义特征词(const char* 名称) noexcept
    {
        return 语素集.添加信息入口词(
            名称 ? 名称 : "方法语义特征",
            枚举_信息入口类型::特征模板入口);
    }

    const 语素入口节点类* 私有_方法统计特征词(const char* 名称) noexcept
    {
        if (!名称 || !*名称) {
            return 语素集.添加信息入口词("方法统计", 枚举_信息入口类型::特征模板入口);
        }
        return 语素集.添加信息入口词(std::string("方法") + 名称, 枚举_信息入口类型::特征模板入口);
    }

    const 语素入口节点类* 私有_动作名转方法概念词(const 语素入口节点类* 动作名) noexcept
    {
        if (!动作名) {
            return nullptr;
        }
        try {
            const auto 词面 = 语素集.获取词(动作名);
            if (词面.empty()) {
                return nullptr;
            }
            // 动作名本身是动态模板入口，只能表示“做什么”；方法概念必须使用方法概念入口。
            // 若直接把动态入口绑定到存在概念，会触发“动态入口 -> 存在节点”的类型冲突。
            return 语素集.添加信息入口词(词面, 枚举_信息入口类型::方法概念入口);
        }
        catch (...) {
            return nullptr;
        }
    }

    const char* 私有_方法类型概念名(枚举_方法类型 类型) noexcept
    {
        switch (类型) {
        case 枚举_方法类型::内部方法: return "内部方法";
        case 枚举_方法类型::外部方法: return "外部方法";
        default: return nullptr;
        }
    }

    const char* 私有_方法来源概念名(枚举_方法来源 来源) noexcept
    {
        switch (来源) {
        case 枚举_方法来源::本能: return "本能方法";
        case 枚举_方法来源::外部导入: return "外部导入方法";
        case 枚举_方法来源::因果抽象: return "因果抽象方法";
        case 枚举_方法来源::观察抽象: return "观察抽象方法";
        case 枚举_方法来源::路径提炼: return "路径提炼方法";
        case 枚举_方法来源::运行时方法沉淀: return "运行时沉淀方法";
        default: return nullptr;
        }
    }

    枚举_方法类型 私有_推导方法类型(const 方法主信息类& 主信息) noexcept
    {
        switch (主信息.首节点信息().动作句柄.类型) {
        case 枚举_动作句柄类型::外部实现主键:
            return 枚举_方法类型::外部方法;
        case 枚举_动作句柄类型::本能函数ID:
        case 枚举_动作句柄类型::动作序列主键:
            return 枚举_方法类型::内部方法;
        default:
            return 枚举_方法类型::未定义;
        }
    }

    I64 私有_默认成熟度阶段(const 方法主信息类& 主信息) noexcept
    {
        return 主信息.首节点信息().动作句柄.类型 == 枚举_动作句柄类型::本能函数ID
            && 主信息.首节点信息().动作句柄.本能ID != 0
            ? 3
            : 1;
    }

    I64 私有_默认允许自动召回(I64 成熟度阶段) noexcept
    {
        return 成熟度阶段 >= 2 ? 1 : 0;
    }

    I64 私有_默认允许默认执行(I64 成熟度阶段) noexcept
    {
        return 成熟度阶段 >= 3 ? 1 : 0;
    }

