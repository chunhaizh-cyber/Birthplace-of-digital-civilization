#include "二次特征类.h"

#include <algorithm>

namespace {
    template<class T节点>
    可解析引用<T节点> 私有_生成引用(T节点* 节点)
    {
        可解析引用<T节点> out{};
        out.指针 = 节点;
        if (节点) out.主键 = 节点->获取主键();
        return out;
    }

    template<class T节点>
    bool 私有_引用已存在(const std::vector<可解析引用<T节点>>& 列表, T节点* 节点)
    {
        for (const auto& 项 : 列表) {
            if (项.指针 == 节点) return true;
            if (节点 && !项.主键.empty() && 项.主键 == 节点->获取主键()) return true;
        }
        return false;
    }
}

二次特征类::二次特征类(基础信息类* 基础信息) noexcept
    : 基础信息_(基础信息)
{
}

void 二次特征类::绑定基础信息(基础信息类* 基础信息) noexcept
{
    基础信息_ = 基础信息;
}

二次特征主信息类* 二次特征类::取二次特征主信息(二次特征节点类* 节点) const noexcept
{
    return 基础信息_ ? 基础信息_->取主信息<二次特征主信息类>(节点) : nullptr;
}

const 二次特征主信息类* 二次特征类::取二次特征主信息(const 二次特征节点类* 节点) const noexcept
{
    return 基础信息_ ? 基础信息_->取主信息<二次特征主信息类>(节点) : nullptr;
}

二次特征节点类* 二次特征类::创建二次特征(基础信息节点类* 父节点, 二次特征主信息类* 主信息)
{
    if (!基础信息_ || !主信息) return nullptr;
    return static_cast<二次特征节点类*>(基础信息_->添加子节点(父节点, 主信息));
}

二次特征节点类* 二次特征类::创建二次特征_按度量(
    基础信息节点类* 父节点,
    枚举_二次特征种类 种类,
    const std::string& 度量签名_链键)
{
    auto* 主信息 = new 二次特征主信息类{};
    主信息->种类 = 种类;
    主信息->度量签名_链键 = 度量签名_链键;
    return 创建二次特征(父节点, 主信息);
}

bool 二次特征类::删除二次特征(二次特征节点类* 节点)
{
    if (!基础信息_ || !节点) return false;

    auto* 父节点 = static_cast<基础信息节点类*>(节点->父);
    auto* 场景主信息 = 基础信息_->取主信息<场景节点主信息类>(父节点);
    if (场景主信息) {
        场景主信息->关系索引.erase(
            std::remove_if(
                场景主信息->关系索引.begin(),
                场景主信息->关系索引.end(),
                [&](const auto& 项) { return 项.指针 == 节点 || 项.主键 == 节点->获取主键(); }),
            场景主信息->关系索引.end());
    }

    return 基础信息_->删除节点(节点);
}

std::vector<二次特征节点类*> 二次特征类::枚举全部二次特征() const
{
    std::vector<二次特征节点类*> out;
    if (!基础信息_) return out;

    for (auto* 节点 : 基础信息_->枚举节点_按类型<二次特征主信息类>()) {
        out.push_back(static_cast<二次特征节点类*>(节点));
    }
    return out;
}

std::vector<二次特征节点类*> 二次特征类::获取子二次特征(const 基础信息节点类* 父节点) const
{
    std::vector<二次特征节点类*> out;
    if (!基础信息_) return out;

    for (auto* 节点 : 基础信息_->枚举子节点_按类型<二次特征主信息类>(父节点)) {
        out.push_back(static_cast<二次特征节点类*>(节点));
    }
    return out;
}

bool 二次特征类::绑定场景(二次特征节点类* 节点, 场景节点类* 所属场景)
{
    auto* 主信息 = 取二次特征主信息(节点);
    auto* 场景主信息 = 基础信息_ ? 基础信息_->取主信息<场景节点主信息类>(所属场景) : nullptr;
    if (!主信息) return false;

    主信息->所属场景 = 私有_生成引用(所属场景);
    if (场景主信息 && !私有_引用已存在(场景主信息->关系索引, 节点)) {
        场景主信息->关系索引.push_back(私有_生成引用(节点));
    }
    return true;
}

bool 二次特征类::设置主体客体(二次特征节点类* 节点, 基础信息节点类* 主体, 基础信息节点类* 客体)
{
    auto* 主信息 = 取二次特征主信息(节点);
    if (!主信息) return false;

    主信息->主体 = 私有_生成引用(主体);
    主信息->客体 = 私有_生成引用(客体);
    return true;
}

bool 二次特征类::设置左右对象(二次特征节点类* 节点, 基础信息节点类* 左对象, 基础信息节点类* 右对象)
{
    auto* 主信息 = 取二次特征主信息(节点);
    if (!主信息) return false;

    主信息->左对象 = 私有_生成引用(左对象);
    主信息->右对象 = 私有_生成引用(右对象);
    return true;
}

bool 二次特征类::写入标量值(二次特征节点类* 节点, I64 标量值, bool 是否满足, I64 置信度)
{
    auto* 主信息 = 取二次特征主信息(节点);
    if (!主信息) return false;

    主信息->标量值 = 标量值;
    主信息->是否满足 = 是否满足;
    主信息->置信度 = 置信度;
    主信息->统计.记录观测(结构体_时间戳::当前_微秒());
    return true;
}
