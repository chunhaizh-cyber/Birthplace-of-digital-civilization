二次特征节点类* 二次特征类::创建二次特征(基础信息节点类* 父节点, 二次特征主信息类* 主信息)
{
    if (!基础信息_ || !主信息) return nullptr;
    if (主信息->度量签名_链键.empty()) {
        主信息->刷新度量签名_链键();
    }
    return static_cast<二次特征节点类*>(基础信息_->添加子节点(父节点, 主信息));
}

二次特征节点类* 二次特征类::创建二次特征_按度量(
    基础信息节点类* 父节点,
    枚举_二次特征种类 种类,
    const std::string& 度量签名_链键)
{
    auto* 主信息 = new 二次特征主信息类{};
    主信息->种类 = 种类;
    主信息->度量签名_链键 = 度量签名_链键;
    if (主信息->度量签名_链键.empty()) {
        主信息->刷新度量签名_链键();
    }
    return 创建二次特征(父节点, 主信息);
}

二次特征节点类* 二次特征类::获取或创建子二次特征_按签名(
    基础信息节点类* 父节点,
    const 二次特征主信息类& 模板)
{
    if (!基础信息_) return nullptr;

    二次特征主信息类 标准模板(模板);
    if (标准模板.度量签名_链键.empty()) {
        标准模板.刷新度量签名_链键();
    }
    if (标准模板.度量签名_链键.empty()) return nullptr;

    for (auto* 现有节点 : 获取子二次特征(父节点)) {
        const auto* 现有主信息 = 取二次特征主信息(现有节点);
        if (现有主信息 && 现有主信息->度量签名_链键 == 标准模板.度量签名_链键) {
            return 现有节点;
        }
    }

    return 创建二次特征(父节点, new 二次特征主信息类(标准模板));
}

二次特征节点类* 二次特征类::获取或创建概念树根()
{
    if (!基础信息_) return nullptr;

    for (auto* 节点 : 基础信息_->枚举子节点_按类型<二次特征主信息类>(nullptr)) {
        auto* 二次节点 = static_cast<二次特征节点类*>(节点);
        const auto* 主信息 = 取二次特征主信息(二次节点);
        if (!主信息) continue;
        if (主信息->形态 != 二次特征主信息类::枚举_形态::概念模板) continue;
        if (主信息->度量签名_链键 == k二次特征概念树根链键) {
            return 二次节点;
        }
    }

    auto* 主信息 = new 二次特征主信息类{};
    主信息->形态 = 二次特征主信息类::枚举_形态::概念模板;
    主信息->度量签名_链键 = k二次特征概念树根链键;
    主信息->标量区间 = I64区间{ I64_MIN, I64_MAX };
    主信息->区间层级 = 二次特征主信息类::枚举_区间层级::未定义;
    return 创建二次特征(nullptr, 主信息);
}

二次特征节点类* 二次特征类::获取或创建度量链根(
    const std::string& 度量签名_链键,
    枚举_二次特征种类 种类)
{
    if (!基础信息_ || 度量签名_链键.empty()) return nullptr;

    auto* 概念树根 = 获取或创建概念树根();
    if (!概念树根) return nullptr;

    for (auto* 节点 : 获取子二次特征(概念树根)) {
        auto* 主信息 = 取二次特征主信息(节点);
        if (!主信息) continue;
        if (主信息->形态 != 二次特征主信息类::枚举_形态::概念模板) continue;
        if (主信息->区间层级 != 二次特征主信息类::枚举_区间层级::未定义) continue;
        if (主信息->度量签名_链键 != 度量签名_链键) continue;
        私有_确保默认概念刻度(节点);
        return 节点;
    }

    auto* 主信息 = new 二次特征主信息类{};
    主信息->形态 = 二次特征主信息类::枚举_形态::概念模板;
    主信息->种类 = 种类;
    主信息->度量签名_链键 = 度量签名_链键;
    主信息->标量区间 = I64区间{ I64_MIN, I64_MAX };
    主信息->区间层级 = 二次特征主信息类::枚举_区间层级::未定义;

    auto* 链根 = 创建二次特征(概念树根, 主信息);
    私有_确保默认概念刻度(链根);
    return 链根;
}

二次特征节点类* 二次特征类::注册概念小区间(
    二次特征节点类* 任意域节点或链根,
    const I64区间& 区间,
    const 词性节点类* 概念名称)
{
    if (!基础信息_ || !任意域节点或链根 || !区间.有效()) return nullptr;

    auto* 链根 = 任意域节点或链根;
    while (链根) {
        const auto* 主信息 = 取二次特征主信息(链根);
        if (!主信息) return nullptr;
        if (主信息->形态 == 二次特征主信息类::枚举_形态::概念模板
            && 主信息->区间层级 == 二次特征主信息类::枚举_区间层级::未定义
            && 主信息->度量签名_链键 != k二次特征概念树根链键) {
            break;
        }
        链根 = static_cast<二次特征节点类*>(链根->父);
    }
    if (!链根) return nullptr;

    私有_确保默认概念刻度(链根);

    for (auto* 节点 : 获取子二次特征(链根)) {
        const auto* 主信息 = 取二次特征主信息(节点);
        if (!主信息 || !主信息->标量区间.has_value()) continue;
        if (主信息->区间层级 != 二次特征主信息类::枚举_区间层级::小区间) continue;
        if (主信息->标量区间->低值 != 区间.低值 || 主信息->标量区间->高值 != 区间.高值) continue;
        if (!概念名称) {
            if (!主信息->概念名称) return 节点;
            continue;
        }
        if (私有_词性主键相等(主信息->概念名称, 概念名称)) {
            return 节点;
        }
    }

    const auto* 链根主信息 = 取二次特征主信息(链根);
    auto* 主信息 = new 二次特征主信息类{};
    主信息->形态 = 二次特征主信息类::枚举_形态::概念模板;
    主信息->种类 = 链根主信息 ? 链根主信息->种类 : 枚举_二次特征种类::未定义;
    主信息->度量签名_链键 = 链根主信息 ? 链根主信息->度量签名_链键 : std::string{};
    主信息->标量区间 = 区间;
    主信息->区间层级 = 二次特征主信息类::枚举_区间层级::小区间;
    主信息->概念名称 = 概念名称;
    return 创建二次特征(链根, 主信息);
}

二次特征节点类* 二次特征类::按差值命中概念节点(二次特征节点类* 链根, I64 差值)
{
    if (!基础信息_ || !链根) return nullptr;
    私有_确保默认概念刻度(链根);

    二次特征节点类* 最佳节点 = nullptr;
    I64 最佳宽度 = I64_MAX;
    bool 最佳是小区间 = false;

    for (auto* 节点 : 获取子二次特征(链根)) {
        const auto* 主信息 = 取二次特征主信息(节点);
        if (!主信息 || !主信息->标量区间.has_value() || !主信息->标量区间->有效()) continue;
        if (主信息->区间层级 != 二次特征主信息类::枚举_区间层级::大区间
            && 主信息->区间层级 != 二次特征主信息类::枚举_区间层级::小区间) {
            continue;
        }
        if (!主信息->标量区间->包含(差值)) continue;

        const I64 当前宽度 = 主信息->标量区间->高值 == I64_MAX
            ? I64_MAX
            : (主信息->标量区间->高值 - 主信息->标量区间->低值);
        const bool 当前是小区间 = 主信息->区间层级 == 二次特征主信息类::枚举_区间层级::小区间;

        if (!最佳节点
            || 当前宽度 < 最佳宽度
            || (当前宽度 == 最佳宽度 && 当前是小区间 && !最佳是小区间)) {
            最佳节点 = 节点;
            最佳宽度 = 当前宽度;
            最佳是小区间 = 当前是小区间;
        }
    }

    return 最佳节点;
}

二次特征节点类* 二次特征类::用概念名称反查节点(二次特征节点类* 链根, const 词性节点类* 概念名称)
{
    if (!基础信息_ || !链根 || !概念名称) return nullptr;
    私有_确保默认概念刻度(链根);

    for (auto* 节点 : 获取子二次特征(链根)) {
        const auto* 主信息 = 取二次特征主信息(节点);
        if (!主信息) continue;
        if (主信息->区间层级 != 二次特征主信息类::枚举_区间层级::小区间) continue;
        if (私有_词性主键相等(主信息->概念名称, 概念名称)) {
            return 节点;
        }
    }

    for (auto* 节点 : 获取子二次特征(链根)) {
        const auto* 主信息 = 取二次特征主信息(节点);
        if (!主信息) continue;
        if (主信息->区间层级 != 二次特征主信息类::枚举_区间层级::大区间) continue;
        if (私有_词性主键相等(主信息->概念名称, 概念名称)) {
            return 节点;
        }
    }

    return nullptr;
}

bool 二次特征类::删除二次特征(二次特征节点类* 节点)
{
    if (!基础信息_ || !节点) return false;

    auto* 父节点 = static_cast<基础信息节点类*>(节点->父);
    auto* 场景主信息 = 基础信息_->取主信息<场景节点主信息类>(父节点);
    if (场景主信息) {
        私有_移除二次特征索引(&场景主信息->关系索引, 节点);
        私有_移除二次特征索引(&场景主信息->二次特征索引, 节点);
    }

    return 基础信息_->删除节点(节点);
}

std::vector<二次特征节点类*> 二次特征类::枚举全部二次特征() const
{
    std::vector<二次特征节点类*> out;
    if (!基础信息_) return out;

    for (auto* 节点 : 基础信息_->枚举节点_按类型<二次特征主信息类>()) {
        out.push_back(static_cast<二次特征节点类*>(节点));
    }
    return out;
}

