#include "二次特征类.h"

#include <algorithm>

namespace {
    template<class T节点>
    可解析引用<T节点> 私有_生成引用(T节点* 节点)
    {
        可解析引用<T节点> out{};
        out.指针 = 节点;
        if (节点) out.主键 = 节点->获取主键();
        return out;
    }

    template<class T节点>
    bool 私有_引用已存在(const std::vector<可解析引用<T节点>>& 列表, T节点* 节点)
    {
        for (const auto& 项 : 列表) {
            if (项.指针 == 节点) return true;
            if (节点 && !项.主键.empty() && 项.主键 == 节点->获取主键()) return true;
        }
        return false;
    }

    void 私有_追加二次特征索引(场景节点主信息类* 主信息, 二次特征节点类* 节点)
    {
        if (!主信息 || !节点) return;
        const auto 引用 = 私有_生成引用(节点);
        if (!私有_引用已存在(主信息->关系索引, 节点)) {
            主信息->关系索引.push_back(引用);
        }
        if (!私有_引用已存在(主信息->二次特征索引, 节点)) {
            主信息->二次特征索引.push_back(引用);
        }
    }

    void 私有_移除二次特征索引(std::vector<可解析引用<二次特征节点类>>* 列表, 二次特征节点类* 节点)
    {
        if (!列表 || !节点) return;
        列表->erase(
            std::remove_if(
                列表->begin(),
                列表->end(),
                [&](const auto& 项) { return 项.指针 == 节点 || 项.主键 == 节点->获取主键(); }),
            列表->end());
    }
}

二次特征类::二次特征类(基础信息类* 基础信息) noexcept
    : 基础信息_(基础信息)
{
}

void 二次特征类::绑定基础信息(基础信息类* 基础信息) noexcept
{
    基础信息_ = 基础信息;
}

二次特征主信息类* 二次特征类::取二次特征主信息(二次特征节点类* 节点) const noexcept
{
    return 基础信息_ ? 基础信息_->取主信息<二次特征主信息类>(节点) : nullptr;
}

const 二次特征主信息类* 二次特征类::取二次特征主信息(const 二次特征节点类* 节点) const noexcept
{
    return 基础信息_ ? 基础信息_->取主信息<二次特征主信息类>(节点) : nullptr;
}

二次特征节点类* 二次特征类::创建二次特征(基础信息节点类* 父节点, 二次特征主信息类* 主信息)
{
    if (!基础信息_ || !主信息) return nullptr;
    if (主信息->度量签名_链键.empty()) {
        主信息->刷新度量签名_链键();
    }
    return static_cast<二次特征节点类*>(基础信息_->添加子节点(父节点, 主信息));
}

二次特征节点类* 二次特征类::创建二次特征_按度量(
    基础信息节点类* 父节点,
    枚举_二次特征种类 种类,
    const std::string& 度量签名_链键)
{
    auto* 主信息 = new 二次特征主信息类{};
    主信息->种类 = 种类;
    主信息->度量签名_链键 = 度量签名_链键;
    if (主信息->度量签名_链键.empty()) {
        主信息->刷新度量签名_链键();
    }
    return 创建二次特征(父节点, 主信息);
}

bool 二次特征类::删除二次特征(二次特征节点类* 节点)
{
    if (!基础信息_ || !节点) return false;

    auto* 父节点 = static_cast<基础信息节点类*>(节点->父);
    auto* 场景主信息 = 基础信息_->取主信息<场景节点主信息类>(父节点);
    if (场景主信息) {
        私有_移除二次特征索引(&场景主信息->关系索引, 节点);
        私有_移除二次特征索引(&场景主信息->二次特征索引, 节点);
    }

    return 基础信息_->删除节点(节点);
}

std::vector<二次特征节点类*> 二次特征类::枚举全部二次特征() const
{
    std::vector<二次特征节点类*> out;
    if (!基础信息_) return out;

    for (auto* 节点 : 基础信息_->枚举节点_按类型<二次特征主信息类>()) {
        out.push_back(static_cast<二次特征节点类*>(节点));
    }
    return out;
}

std::vector<二次特征节点类*> 二次特征类::获取子二次特征(const 基础信息节点类* 父节点) const
{
    std::vector<二次特征节点类*> out;
    if (!基础信息_) return out;

    for (auto* 节点 : 基础信息_->枚举子节点_按类型<二次特征主信息类>(父节点)) {
        out.push_back(static_cast<二次特征节点类*>(节点));
    }
    return out;
}

bool 二次特征类::绑定场景(二次特征节点类* 节点, 场景节点类* 所属场景)
{
    auto* 主信息 = 取二次特征主信息(节点);
    auto* 场景主信息 = 基础信息_ ? 基础信息_->取主信息<场景节点主信息类>(所属场景) : nullptr;
    if (!主信息) return false;

    主信息->所属场景 = 私有_生成引用(所属场景);
    if (场景主信息) {
        私有_追加二次特征索引(场景主信息, 节点);
    }
    return true;
}

bool 二次特征类::设置生成语义(
    二次特征节点类* 节点,
    枚举_二次特征域 域,
    枚举_二次特征主母式 主母式,
    枚举_二次特征粒度 粒度,
    枚举_二次特征基准类型 基准类型,
    枚举_时间归一方式 时间归一方式,
    枚举_二次特征值形态 值形态)
{
    auto* 主信息 = 取二次特征主信息(节点);
    if (!主信息) return false;

    主信息->域 = 域;
    主信息->主母式 = 主母式;
    主信息->粒度 = 粒度;
    主信息->基准类型 = 基准类型;
    主信息->时间归一方式 = 时间归一方式;
    if (值形态 != 枚举_二次特征值形态::未定义) {
        主信息->值形态 = 值形态;
    }
    主信息->刷新度量签名_链键();
    return true;
}

bool 二次特征类::绑定来源(
    二次特征节点类* 节点,
    状态节点类* 来源起始状态,
    状态节点类* 来源结果状态,
    动态节点类* 来源动态,
    场景节点类* 来源场景,
    std::uint32_t 来源动态层级,
    std::uint64_t 来源动态路径签名)
{
    auto* 主信息 = 取二次特征主信息(节点);
    if (!主信息) return false;

    主信息->来源起始状态 = 私有_生成引用(来源起始状态);
    主信息->来源结果状态 = 私有_生成引用(来源结果状态);
    主信息->来源动态 = 私有_生成引用(来源动态);
    主信息->来源场景 = 私有_生成引用(来源场景);

    if (来源动态 && 基础信息_) {
        if (const auto* 动态主信息 = 基础信息_->取主信息<动态节点主信息类>(来源动态)) {
            if (来源动态层级 == 0) {
                来源动态层级 = 动态主信息->动态层级;
            }
            if (来源动态路径签名 == 0) {
                来源动态路径签名 = 动态主信息->动态路径签名;
            }
            if (!来源场景 && 来源动态->父) {
                主信息->来源场景 = 私有_生成引用(static_cast<场景节点类*>(来源动态->父));
            }
        }
    }

    主信息->来源动态层级 = 来源动态层级;
    主信息->来源动态路径签名 = 来源动态路径签名;
    主信息->刷新度量签名_链键();
    return true;
}

