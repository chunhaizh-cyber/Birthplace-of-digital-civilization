    template<class T节点>
    可解析引用<T节点> 私有_生成引用(T节点* 节点)
    {
        可解析引用<T节点> out{};
        out.指针 = 节点;
        if (节点) out.主键 = 节点->获取主键();
        return out;
    }

    template<class T节点>
    bool 私有_引用已存在(const std::vector<可解析引用<T节点>>& 列表, T节点* 节点)
    {
        for (const auto& 项 : 列表) {
            if (项.指针 == 节点) return true;
            if (节点 && !项.主键.empty() && 项.主键 == 节点->获取主键()) return true;
        }
        return false;
    }

    void 私有_追加二次特征索引(场景节点主信息类* 主信息, 二次特征节点类* 节点)
    {
        if (!主信息 || !节点) return;
        const auto 引用 = 私有_生成引用(节点);
        if (!私有_引用已存在(主信息->关系索引, 节点)) {
            主信息->关系索引.push_back(引用);
        }
        if (!私有_引用已存在(主信息->二次特征索引, 节点)) {
            主信息->二次特征索引.push_back(引用);
        }
    }

    void 私有_移除二次特征索引(std::vector<可解析引用<二次特征节点类>>* 列表, 二次特征节点类* 节点)
    {
        if (!列表 || !节点) return;
        列表->erase(
            std::remove_if(
                列表->begin(),
                列表->end(),
                [&](const auto& 项) { return 项.指针 == 节点 || 项.主键 == 节点->获取主键(); }),
            列表->end());
    }

    bool 私有_词性主键相等(const 词性节点类* 左, const 词性节点类* 右) noexcept
    {
        if (左 == 右) return true;
        if (!左 || !右) return false;
        const auto 左主键 = 左->获取主键();
        const auto 右主键 = 右->获取主键();
        return !左主键.empty() && 左主键 == 右主键;
    }

    std::string 私有_转小写ASCII(const std::string& 文本)
    {
        std::string 输出 = 文本;
        std::transform(输出.begin(), 输出.end(), 输出.begin(), [](unsigned char c) {
            return static_cast<char>(std::tolower(c));
        });
        return 输出;
    }

    bool 私有_包含片段_忽略大小写(const std::string& 文本, const char* 片段)
    {
        if (!片段 || !*片段) return false;
        return 私有_转小写ASCII(文本).find(私有_转小写ASCII(片段)) != std::string::npos;
    }

