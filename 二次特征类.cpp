bool 二次特征类::刷新概念模板(二次特征节点类* 节点)
{
    auto* 主信息 = 取二次特征主信息(节点);
    if (!主信息) return false;
    if (主信息->形态 == 二次特征主信息类::枚举_形态::概念模板) return false;
    if (主信息->度量签名_链键.empty()) {
        主信息->刷新度量签名_链键();
    }
    if (主信息->度量签名_链键.empty()) return false;

    auto* 链根 = 获取或创建度量链根(主信息->度量签名_链键, 主信息->种类);
    if (!链根) return false;

    二次特征节点类* 命中节点 = nullptr;
    if (主信息->概念名称) {
        命中节点 = 用概念名称反查节点(链根, 主信息->概念名称);
    }
    if (!命中节点) {
        I64 点值 = 0;
        switch (主信息->值形态) {
        case 枚举_二次特征值形态::布尔:
            点值 = 主信息->是否满足 ? 1 : 0;
            break;
        case 枚举_二次特征值形态::枚举:
        case 枚举_二次特征值形态::签名:
            点值 = static_cast<I64>(主信息->离散编码);
            break;
        default:
            点值 = 主信息->标量值;
            break;
        }
        命中节点 = 按差值命中概念节点(链根, 点值);
    }

    if (!命中节点) {
        主信息->概念模板.清空();
        return false;
    }

    主信息->概念模板 = 私有_生成引用(命中节点);
    const auto* 概念主信息 = 取二次特征主信息(命中节点);
    if (概念主信息 && !主信息->概念名称 && 概念主信息->概念名称) {
        主信息->概念名称 = 概念主信息->概念名称;
    }
    return true;
}

void 二次特征类::私有_确保默认概念刻度(二次特征节点类* 链根)
{
    if (!基础信息_ || !链根) return;

    const auto* 链根主信息 = 取二次特征主信息(链根);
    if (!链根主信息) return;

    auto 刻度列表 = 获取度量默认刻度_由链键(链根主信息->度量签名_链键);
    if (刻度列表.empty()) {
        刻度列表 = 私有_推断默认刻度(链根主信息->度量签名_链键);
    }
    if (刻度列表.empty()) return;

    for (const auto& 刻度 : 刻度列表) {
        bool 已存在 = false;
        for (auto* 节点 : 获取子二次特征(链根)) {
            const auto* 主信息 = 取二次特征主信息(节点);
            if (!主信息 || !主信息->标量区间.has_value()) continue;
            if (主信息->标量区间->低值 != 刻度.低值 || 主信息->标量区间->高值 != 刻度.高值) continue;
            const auto 期望层级 = 刻度.是小区间
                ? 二次特征主信息类::枚举_区间层级::小区间
                : 二次特征主信息类::枚举_区间层级::大区间;
            if (主信息->区间层级 != 期望层级) continue;
            已存在 = true;
            break;
        }
        if (已存在) continue;

        auto* 主信息 = new 二次特征主信息类{};
        主信息->形态 = 二次特征主信息类::枚举_形态::概念模板;
        主信息->种类 = 链根主信息->种类;
        主信息->度量签名_链键 = 链根主信息->度量签名_链键;
        主信息->标量区间 = I64区间{ 刻度.低值, 刻度.高值 };
        主信息->区间层级 = 刻度.是小区间
            ? 二次特征主信息类::枚举_区间层级::小区间
            : 二次特征主信息类::枚举_区间层级::大区间;
        if (!刻度.名称.empty()) {
            主信息->概念名称 = 语素集.添加词性词(刻度.名称, "名词");
        }
        (void)创建二次特征(链根, 主信息);
    }
}
