bool 二次特征类::写入标量值(二次特征节点类* 节点, I64 标量值, bool 是否满足, I64 置信度)
{
    auto* 主信息 = 取二次特征主信息(节点);
    if (!主信息) return false;

    主信息->标量值 = 标量值;
    if (主信息->值形态 == 枚举_二次特征值形态::未定义) {
        主信息->值形态 = 枚举_二次特征值形态::标量;
    }
    主信息->是否满足 = 是否满足;
    主信息->置信度 = 置信度;
    主信息->统计.记录观测(结构体_时间戳::当前_微秒());
    return true;
}

