#pragma once

#include <cstddef>
#include <cstdint>
#include <string>

#include "需求主信息类.h"

enum class 枚举_需求树更新指令类型 : std::uint8_t {
    未定义 = 0,
    新增需求 = 1,
    更新需求 = 2,
    满足需求 = 3,
    删除需求 = 4,
};

struct 结构_需求树更新指令 {
    枚举_需求树更新指令类型 指令类型 = 枚举_需求树更新指令类型::未定义;
    std::uintptr_t 父需求指针 = 0;
    std::string 父需求主键{};
    std::uintptr_t 目标需求指针 = 0;
    std::string 目标需求主键{};
    const 词性节点类* 需求类型 = nullptr;
    const 词性节点类* 需求名称 = nullptr;
    std::int64_t 新安全权重 = 0;
    bool 使用新安全权重 = false;
    std::int64_t 新服务权重 = 0;
    bool 使用新服务权重 = false;
    bool 标记已满足 = false;
    std::uintptr_t 当前状态指针 = 0;
    std::string 当前状态主键{};
    std::string 摘要{};
};

class 需求类 : public 链表模板<需求主信息类> {
public:
    using 基类 = 链表模板<需求主信息类>;
    using 节点类 = typename 基类::节点类;

    struct 结构_子需求平分权重 {
        std::int64_t 安全权重 = 0;
        std::int64_t 服务权重 = 0;
    };

    static 结构_子需求平分权重 计算子需求平分权重(
        const std::int64_t 父需求安全权重,
        const std::int64_t 父需求服务权重,
        const std::int64_t 子需求数量) noexcept
    {
        结构_子需求平分权重 输出{};
        if (子需求数量 <= 0) {
            return 输出;
        }
        输出.安全权重 = 父需求安全权重 / 子需求数量;
        输出.服务权重 = 父需求服务权重 / 子需求数量;
        return 输出;
    }

    static 结构_子需求平分权重 计算子需求平分权重(const 节点类* 父需求) noexcept
    {
        if (!父需求 || 父需求->子节点数量 <= 0) {
            return {};
        }
        return 计算子需求平分权重(
            父需求->主信息.安全权重,
            父需求->主信息.服务权重,
            父需求->子节点数量);
    }

    std::size_t 刷新子需求权重(节点类* 父需求) noexcept
    {
        auto 锁 = this->获取锁();
        return 刷新子需求权重_已加锁(父需求);
    }

    节点类* 执行更新指令(
        节点类* 需求根节点,
        const 结构_需求树更新指令& 指令) noexcept
    {
        auto 锁 = this->获取锁();
        return 执行更新指令_已加锁(需求根节点, 指令);
    }

    // 方向 = 结果状态 - 初始状态，这里对应 需求状态 - 被需求当前状态。
    二次特征节点类* 获取需求方向(节点类* 需求节点) const noexcept;

private:
    static void 刷新同层环头_已加锁(节点类* 同层环头) noexcept
    {
        if (!同层环头) {
            return;
        }

        auto* 当前节点 = 同层环头;
        do {
            当前节点->同层环头 = 同层环头;
            当前节点 = 当前节点->下;
        } while (当前节点 && 当前节点 != 同层环头);
    }

    static 节点类* 获取独立树根_已加锁(节点类* 节点) noexcept
    {
        while (节点 && 节点->父) {
            节点 = static_cast<节点类*>(节点->父);
        }
        return 节点;
    }

    static 节点类* 按主键定位节点_已加锁(
        节点类* 需求根节点,
        const std::string& 主键) noexcept
    {
        if (!需求根节点 || 主键.empty()) {
            return nullptr;
        }

        for (auto* 当前节点 = 需求根节点->链下;
             当前节点 && 当前节点 != 需求根节点;
             当前节点 = 当前节点->链下) {
            if (当前节点->获取主键() == 主键) {
                return 当前节点;
            }
        }
        return nullptr;
    }

    static 节点类* 定位节点_已加锁(
        节点类* 需求根节点,
        const std::uintptr_t 节点指针,
        const std::string& 主键) noexcept
    {
        if (节点指针 != 0) {
            return reinterpret_cast<节点类*>(节点指针);
        }
        return 按主键定位节点_已加锁(需求根节点, 主键);
    }

    节点类* 添加子需求节点_已加锁(
        节点类* 父需求,
        const 结构_需求树更新指令& 指令) noexcept
    {
        if (!父需求) {
            return nullptr;
        }

        auto* 独立根 = 获取独立树根_已加锁(父需求);
        if (!独立根) {
            return nullptr;
        }

        需求主信息类 主信息{};
        主信息.类型 = 指令.需求类型;
        主信息.安全权重 = 指令.使用新安全权重 ? 指令.新安全权重 : 0;
        主信息.服务权重 = 指令.使用新服务权重 ? 指令.新服务权重 : 0;
        if (auto* 父状态 = reinterpret_cast<状态节点类*>(指令.当前状态指针)) {
            主信息.被需求当前状态.指针 = 父状态;
            主信息.被需求当前状态.主键 = 父状态->获取主键();
        } else if (!指令.当前状态主键.empty()) {
            主信息.被需求当前状态.主键 = 指令.当前状态主键;
        }

        主信息.需求主体 = 父需求->主信息.需求主体;
        主信息.需求场景 = 父需求->主信息.需求场景;
        主信息.被需求存在 = 父需求->主信息.被需求存在;
        主信息.需求虚拟存在 = 父需求->主信息.需求虚拟存在;
        主信息.需求状态 = 父需求->主信息.需求状态;

        auto* 原根 = this->根指针;
        this->根指针 = 独立根;
        auto* 新节点 = static_cast<节点类*>(this->添加子节点(父需求, 主信息));
        this->根指针 = 原根;
        return 新节点;
    }

    bool 删除节点_已加锁(节点类* 节点) noexcept
    {
        if (!节点) {
            return false;
        }

        auto* 独立根 = 获取独立树根_已加锁(节点);
        if (!独立根) {
            return false;
        }

        auto* 原根 = this->根指针;
        this->根指针 = 独立根;
        this->删除节点(节点);
        this->根指针 = 原根;
        return true;
    }

    节点类* 执行更新指令_已加锁(
        节点类* 需求根节点,
        const 结构_需求树更新指令& 指令) noexcept
    {
        if (!需求根节点) {
            return nullptr;
        }

        auto* 父需求 = 定位节点_已加锁(需求根节点, 指令.父需求指针, 指令.父需求主键);
        if (!父需求) {
            父需求 = 需求根节点;
        }

        auto* 目标需求 = 定位节点_已加锁(需求根节点, 指令.目标需求指针, 指令.目标需求主键);

        switch (指令.指令类型) {
        case 枚举_需求树更新指令类型::新增需求:
            if (!目标需求) {
                目标需求 = 添加子需求节点_已加锁(父需求, 指令);
            }
            if (父需求) {
                (void)刷新子需求权重_已加锁(父需求);
            }
            return 目标需求;

        case 枚举_需求树更新指令类型::更新需求:
        case 枚举_需求树更新指令类型::满足需求:
            if (!目标需求) {
                return nullptr;
            }
            if (指令.需求类型) {
                目标需求->主信息.类型 = 指令.需求类型;
            }
            if (指令.使用新安全权重) {
                目标需求->主信息.安全权重 = 指令.新安全权重;
            }
            if (指令.使用新服务权重) {
                目标需求->主信息.服务权重 = 指令.新服务权重;
            }
            if (指令.指令类型 == 枚举_需求树更新指令类型::满足需求 || 指令.标记已满足) {
                if (!指令.使用新安全权重) {
                    目标需求->主信息.安全权重 = 0;
                }
                if (!指令.使用新服务权重) {
                    目标需求->主信息.服务权重 = 0;
                }
            }
            if (auto* 当前状态 = reinterpret_cast<状态节点类*>(指令.当前状态指针)) {
                目标需求->主信息.被需求当前状态.指针 = 当前状态;
                目标需求->主信息.被需求当前状态.主键 = 当前状态->获取主键();
            } else if (!指令.当前状态主键.empty()) {
                目标需求->主信息.被需求当前状态.主键 = 指令.当前状态主键;
            }
            if (指令.指令类型 == 枚举_需求树更新指令类型::满足需求 || 指令.标记已满足) {
                目标需求->主信息.需求有效截止 = 结构体_时间戳::当前_微秒();
            }
            (void)刷新子需求权重_已加锁(目标需求);
            return 目标需求;

        case 枚举_需求树更新指令类型::删除需求:
            if (!目标需求 || 目标需求 == 需求根节点) {
                return nullptr;
            }
            父需求 = static_cast<节点类*>(目标需求->父);
            if (!删除节点_已加锁(目标需求)) {
                return nullptr;
            }
            if (父需求) {
                (void)刷新子需求权重_已加锁(父需求);
            }
            return 父需求;

        case 枚举_需求树更新指令类型::未定义:
        default:
            return nullptr;
        }
    }

    std::size_t 刷新子需求权重_已加锁(节点类* 父需求) noexcept
    {
        if (!父需求 || !父需求->子 || 父需求->子节点数量 <= 0) {
            return 0;
        }

        const auto 已刷新直接子节点数 = 刷新直接子需求权重_已加锁(父需求);
        auto* 首子节点 = 父需求->子;
        auto* 当前子节点 = 首子节点;
        std::size_t 已递归刷新数量 = 已刷新直接子节点数;

        do {
            已递归刷新数量 += 刷新子需求权重_已加锁(当前子节点);
            当前子节点 = 当前子节点->下;
        } while (当前子节点 && 当前子节点 != 首子节点);

        return 已递归刷新数量;
    }

    std::size_t 刷新直接子需求权重_已加锁(节点类* 父需求) noexcept
    {
        if (!父需求 || !父需求->子 || 父需求->子节点数量 <= 0) {
            return 0;
        }

        const auto 子需求权重 = 计算子需求平分权重(父需求);
        auto* 首子节点 = 父需求->子;
        auto* 当前子节点 = 首子节点;
        std::size_t 已刷新数量 = 0;

        do {
            当前子节点->主信息.安全权重 = 子需求权重.安全权重;
            当前子节点->主信息.服务权重 = 子需求权重.服务权重;
            ++已刷新数量;
            当前子节点 = 当前子节点->下;
        } while (当前子节点 && 当前子节点 != 首子节点);

        return 已刷新数量;
    }
};
