场景节点类* 私有_推导承载场景(
    自我类& 自我对象,
    const 任务节点* 任务,
    const 需求节点* 需求,
    场景节点类* 请求场景) noexcept
{
    if (请求场景) {
        return 请求场景;
    }
    if (任务 && 任务->主信息.场景.指针) {
        return reinterpret_cast<场景节点类*>(任务->主信息.场景.指针);
    }
    if (需求 && 需求->主信息.需求场景.指针) {
        return reinterpret_cast<场景节点类*>(需求->主信息.需求场景.指针);
    }
    if (auto* 自我内部世界 = 自我对象.确保自我内部世界()) {
        return 自我内部世界;
    }
    return 世界树.自我所在场景;
}

