bool 私有_用需求初始化任务虚拟存在(
    自我类& 自我对象,
    任务节点* 任务,
    需求节点* 来源需求,
    场景节点类* 请求场景,
    时间戳 now) noexcept
{
    if (!任务 || !来源需求) {
        return false;
    }
    auto* 承载场景 = 私有_推导承载场景(自我对象, 任务, 来源需求, 请求场景);

    auto* 发起存在 = 私有_解析存在引用(来源需求->主信息.需求主体);
    if (!发起存在) {
        发起存在 = 自我对象.获取自我存在();
    }
    auto* 当前状态 = 私有_解析状态引用(来源需求->主信息.被需求当前状态);
    auto* 目标状态 = 私有_解析状态引用(来源需求->主信息.需求状态);
    auto* 作用对象 = 私有_解析存在引用(来源需求->主信息.被需求存在);
    auto* 所属上下文 = reinterpret_cast<场景节点类*>(来源需求->主信息.需求场景.指针);
    if (!所属上下文) {
        所属上下文 = 承载场景;
    }

    bool 任一写入 = false;
    任一写入 = 私有_写任务虚拟存在指针特征(任务, "任务发起存在", 发起存在, 承载场景, now) || 任一写入;
    任一写入 = 私有_写任务虚拟存在指针特征(任务, "任务作用对象", 作用对象, 承载场景, now) || 任一写入;
    任一写入 = 私有_写任务虚拟存在指针特征(任务, "任务所属上下文", 所属上下文, 承载场景, now) || 任一写入;
    任一写入 = 私有_写任务虚拟存在指针特征(任务, "任务当前状态", 当前状态, 承载场景, now) || 任一写入;
    任一写入 = 私有_写任务虚拟存在指针特征(任务, "任务目标状态", 目标状态, 承载场景, now) || 任一写入;
    任一写入 = 私有_写任务虚拟存在指针特征(任务, "任务目标", 目标状态, 承载场景, now) || 任一写入;
    任一写入 = 私有_写任务虚拟存在指针特征(任务, "任务完成条件目标状态", 目标状态, 承载场景, now) || 任一写入;
    任一写入 = 私有_写任务虚拟存在I64特征(任务, "任务满足关系", static_cast<I64>(来源需求->主信息.满足关系), 承载场景, now) || 任一写入;
    任一写入 = 私有_写任务虚拟存在I64特征(任务, "任务安全权重", static_cast<I64>(来源需求->主信息.安全权重), 承载场景, now) || 任一写入;
    任一写入 = 私有_写任务虚拟存在I64特征(任务, "任务服务权重", static_cast<I64>(来源需求->主信息.服务权重), 承载场景, now) || 任一写入;
    任一写入 = 私有_写任务虚拟存在I64特征(任务, "任务综合优先级", static_cast<I64>(来源需求->主信息.安全权重 + 来源需求->主信息.服务权重), 承载场景, now) || 任一写入;
    任一写入 = 私有_写任务虚拟存在I64特征(任务, "任务语义状态", static_cast<I64>(任务类::读取任务状态(任务, 枚举_任务状态::未启动)), 承载场景, now) || 任一写入;
    return 任一写入;
}

void 私有_绑定任务需求关系(任务节点* 任务, 需求节点* 需求) noexcept
{
    if (!任务 || !需求) {
        return;
    }
    任务->主信息.对应需求.指针 = reinterpret_cast<需求节点类*>(需求);
    任务->主信息.对应需求.主键 = 需求->获取主键();
    (void)需求类::绑定对应任务(需求, reinterpret_cast<任务节点类*>(任务));
}

void 私有_准备任务运行壳(任务节点* 任务, 需求节点* 需求, 时间戳 now) noexcept
{
    if (!任务 || !需求) {
        return;
    }
    if (任务类::读取任务角色(任务) == 枚举_任务角色::未定义) {
        (void)任务类::写入任务角色(
            任务,
            需求->子 ? 枚举_任务角色::需求管理任务 : 枚举_任务角色::需求执行任务,
            任务->主信息.场景.指针,
            now);
    }
    任务->主信息.目标状态 = 需求->主信息.需求状态;
    任务->主信息.结果状态信息 = 需求->主信息.被需求当前状态;
    const auto 基准优先级 = 需求->主信息.安全权重 + 需求->主信息.服务权重;
    (void)任务类::写入任务基准优先级(任务, 基准优先级);
    (void)任务类::写入任务调度优先级(
        任务,
        基准优先级 + 任务类::读取任务局部优先级偏移(任务));
    任务类::确保任务初始状态(任务);
}

任务节点* 私有_按需求创建任务并初始化(
    自我类& 自我对象,
    需求节点* 需求,
    任务节点* 上级任务,
    时间戳 now) noexcept
{
    if (!需求) {
        return nullptr;
    }
    auto* 自我存在 = 自我对象.获取自我存在();
    if (!自我存在) {
        return nullptr;
    }
    (void)世界树.存在().确保存在三类根节点(自我存在);
    auto* 世界任务根 = reinterpret_cast<任务节点*>(世界树.存在().获取任务根节点(自我存在));
    if (!世界任务根) {
        return nullptr;
    }

    任务类 任务服务{};
    auto* 原根 = 任务服务.根指针;
    任务服务.根指针 = 世界任务根;
    auto* 任务 = 任务服务.创建任务头结点(
        reinterpret_cast<需求节点类*>(需求),
        上级任务,
        now);
    任务服务.根指针 = 原根;
    if (!任务) {
        return nullptr;
    }

    // 任务管理界面线程是任务域写入入口：创建后立即补运行壳和语义壳。
    私有_绑定任务需求关系(任务, 需求);
    私有_准备任务运行壳(任务, 需求, now);
    auto* 任务存在 = 私有_确保任务虚拟存在(
        自我对象,
        任务,
        需求,
        任务->主信息.场景.指针,
        now);
    if (任务存在) {
        (void)私有_用需求初始化任务虚拟存在(
            自我对象,
            任务,
            需求,
            任务->主信息.场景.指针,
            now);
    }
    return 任务;
}

结构_任务根节点 私有_构造任务根节点(
    const 任务管理线程协议::结构_任务治理请求& 请求,
    std::uint64_t 请求ID) noexcept
{
    auto* 任务 = reinterpret_cast<任务节点*>(请求.任务信息节点);
    结构_任务根节点 根节点{};
    根节点.任务根ID = 任务
        ? static_cast<std::uint64_t>(reinterpret_cast<std::uintptr_t>(任务))
        : 请求ID;
    根节点.来源需求指针 = reinterpret_cast<std::uintptr_t>(请求.来源需求);
    根节点.世界任务根指针 = reinterpret_cast<std::uintptr_t>(任务);
    根节点.父任务指针 = 任务 && 任务->父
        ? reinterpret_cast<std::uintptr_t>(任务->父)
        : 0;
    根节点.管理对象任务指针 = reinterpret_cast<std::uintptr_t>(任务);
    根节点.初始授权等级 = 1;
    根节点.只读 = false;
    return 根节点;
}

