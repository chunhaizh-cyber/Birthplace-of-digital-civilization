任务管理线程协议::结构_任务工作包 私有_构造任务工作包(
    const 任务管理线程协议::结构_任务治理请求& 请求,
    const 任务管理线程协议::结构_任务界面承接结果& 承接,
    存在节点类* 任务存在,
    时间戳 now) noexcept
{
    auto& 状态 = 私有_界面状态();
    任务管理线程协议::结构_任务工作包 工作包{};
    工作包.请求ID = 请求.请求ID;
    {
        std::lock_guard<std::mutex> 锁(状态.锁);
        工作包.工作包ID = 状态.下一个工作包ID++;
        状态.快照.最近工作包ID = 工作包.工作包ID;
    }
    工作包.幂等键 = 请求.幂等键;
    工作包.任务信息节点 = 请求.任务信息节点;
    工作包.任务虚拟存在 = 任务存在;
    工作包.来源需求 = 请求.来源需求;
    工作包.当前时间 = now;
    工作包.任务根节点 = 承接.任务根节点;
    工作包.任务控制态 = 承接.最新任务控制态;
    工作包.任务局部运行态 = 承接.最新任务局部运行态;
    工作包.控制事件 = 私有_构造控制事件(工作包.任务控制态);
    工作包.根层重判结果 = 枚举_任务管理根层重判结果::允许普通推进;
    工作包.执行前门控结果 = 枚举_任务管理执行前门控结果::允许普通执行;

    // 界面线程是任务推进模式的唯一裁决点。工作线程只按模式执行一轮，
    // 不再自行决定“一轮里既筹办又执行”。
    auto* 任务 = reinterpret_cast<任务节点*>(请求.任务信息节点);
    const auto 请求类型 = 私有_归一请求类型(请求);
    const auto 当前任务状态 = 任务 ? 任务类::读取任务状态(任务) : 枚举_任务状态::未定义;
    if (请求类型 == 任务管理线程协议::枚举_任务管理请求类型::发起任务
        && !请求.允许派发工作线程) {
        工作包.推进模式 = 任务管理线程协议::枚举_任务推进模式::只创建;
        工作包.允许筹办 = false;
        工作包.允许执行方法 = false;
    } else if (请求类型 == 任务管理线程协议::枚举_任务管理请求类型::同步回执) {
        工作包.推进模式 = 任务管理线程协议::枚举_任务推进模式::只同步回执;
        工作包.允许筹办 = false;
        工作包.允许执行方法 = false;
    } else if (请求类型 == 任务管理线程协议::枚举_任务管理请求类型::等待任务
        || 请求类型 == 任务管理线程协议::枚举_任务管理请求类型::取消任务
        || 请求类型 == 任务管理线程协议::枚举_任务管理请求类型::更新权重) {
        工作包.推进模式 = 任务管理线程协议::枚举_任务推进模式::只状态推进;
        工作包.允许筹办 = false;
        工作包.允许执行方法 = false;
    } else if (当前任务状态 == 枚举_任务状态::未启动
        || 当前任务状态 == 枚举_任务状态::待重筹办
        || 当前任务状态 == 枚举_任务状态::筹办中
        || 当前任务状态 == 枚举_任务状态::无法执行) {
        工作包.推进模式 = 任务管理线程协议::枚举_任务推进模式::只筹办;
        工作包.允许筹办 = true;
        工作包.允许执行方法 = false;
    } else if (当前任务状态 == 枚举_任务状态::就绪
        || 当前任务状态 == 枚举_任务状态::排队中
        || 当前任务状态 == 枚举_任务状态::执行中) {
        工作包.推进模式 = 任务管理线程协议::枚举_任务推进模式::只执行;
        工作包.允许筹办 = false;
        工作包.允许执行方法 = true;
    } else {
        工作包.推进模式 = 任务管理线程协议::枚举_任务推进模式::只状态推进;
        工作包.允许筹办 = false;
        工作包.允许执行方法 = false;
    }
    工作包.允许派生需求上报 = true;
    工作包.允许正式资产提交 = false;
    工作包.任务摘要 = 请求.请求摘要;
    工作包.摘要 = 请求.请求摘要;
    return 工作包;
}

I64 私有_方法自我能力值(const 方法类::节点类* 方法) noexcept
{
    if (!方法
        || 方法->主信息.首节点信息().动作句柄.类型 != 枚举_动作句柄类型::本能函数ID) {
        return 0;
    }
    const I64 能力值 = 方法->主信息.首节点信息().动作句柄.本能ID;
    return 本能方法类::是有效本能方法能力值(能力值) ? 能力值 : 0;
}

存在节点类* 私有_同步工作回执方法到任务壳(
    任务节点* 任务,
    const 任务管理线程协议::结构_任务工作结果& 工作结果,
    场景节点类* 请求场景,
    时间戳 now) noexcept
{
    if (!任务) {
        return nullptr;
    }

    // 界面线程不再裸写任务候选集 / 当前方法 / 执行回执镜像。
    // 它只把工作结果整理成输入参数场景，然后调度提交类本能方法；
    // 真实任务虚拟存在写入发生在“同步筹办/执行回执到任务”方法内部。
    auto* 方法 = 私有_工作结果当前方法(工作结果);
    const bool 回执阶段为执行 =
        工作结果.存在任务推进回执
        && 工作结果.任务推进回执.阶段 == 任务模块::筹办::枚举_任务推进阶段::执行;
    const bool 执行回执 =
        工作结果.已执行方法
        || 回执阶段为执行;
    const auto 本能ID = 执行回执
        ? 枚举_本能方法ID::自我_同步执行回执到任务
        : 枚举_本能方法ID::自我_同步筹办回执到任务;

    auto 上下文 = 私有_准备任务域本能调用(
        任务,
        本能ID,
        执行回执 ? "同步执行回执到任务" : "同步筹办回执到任务",
        请求场景,
        now);
    auto* 输入包 = 上下文.输入条件包;
    if (!输入包) {
        return nullptr;
    }
    私有_写工作结果输入特征(输入包, 工作结果, now);

    if (方法) {
        (void)私有_写基础节点指针特征(
            输入包,
            私有_特征_目标方法头节点(),
            方法,
            now);
        (void)私有_写基础节点I64特征(
            输入包,
            私有_特征_候选方法数量(),
            1,
            now);
    }
    if (const I64 能力值 = 私有_方法自我能力值(方法); 能力值 != 0) {
        (void)私有_写基础节点I64特征(
            输入包,
            私有_特征_选中自我能力值(),
            能力值,
            now);
    }
    {
        std::ostringstream 日志;
        auto* 回执方法 = 工作结果.存在任务推进回执 && 工作结果.任务推进回执.当前方法.指针
            ? reinterpret_cast<方法类::节点类*>(工作结果.任务推进回执.当前方法.指针)
            : nullptr;
        日志 << "任务管理界面线程/同步回执到任务壳"
            << " | 任务=" << 任务->获取主键()
            << " | 类型=" << (执行回执 ? "执行" : "筹办")
            << " | 工作结果方法=" << (方法 ? 方法->获取主键() : std::string("空"))
            << " | 回执方法=" << (回执方法 ? 回执方法->获取主键() : std::string("空"));
        项目运行日志(日志.str());
    }
    return 私有_执行任务域本能方法(本能ID, 上下文);
}

任务管理线程协议::枚举_任务界面调度动作 私有_裁决工作结果调度动作(
    bool 已派发,
    const 任务管理线程协议::结构_任务工作结果& 工作结果) noexcept
{
    using enum 任务管理线程协议::枚举_任务界面调度动作;
    if (工作结果.建议进入收束) {
        return 结束调度;
    }
    if (工作结果.建议界面线程下一轮) {
        return 重新排队;
    }
    if (工作结果.需要等待) {
        return 挂起等待;
    }
    if (已派发) {
        return 派发工作线程;
    }
    return 拒绝请求;
}

// 工作结果任务域提交点。
// 工作线程只给出建议状态和回执字段；本函数是界面线程唯一把工作结果
// 写入任务壳 / 任务虚拟存在的位置。若任务状态发生变化，这里同步生成
// 任务状态动作动态，并把动态引用放回治理结果，供上行和需求域继续串链。
void 私有_应用工作结果到任务壳(
    任务节点* 任务,
    任务管理线程协议::结构_任务工作结果& 工作结果,
    场景节点类* 请求场景,
    时间戳 now) noexcept
{
    if (!任务) {
        return;
    }
    (void)私有_同步工作回执方法到任务壳(任务, 工作结果, 请求场景, now);
    if (工作结果.治理结果.宿主任务新状态 != 枚举_任务状态::未定义) {
        auto 上下文 = 私有_准备任务域本能调用(
            任务,
            枚举_本能方法ID::自我_提交任务状态变化,
            "提交任务状态变化",
            请求场景,
            now);
        if (上下文.输入条件包) {
            私有_写工作结果输入特征(上下文.输入条件包, 工作结果, now);
            (void)私有_写基础节点I64特征(
                上下文.输入条件包,
                私有_特征_建议任务状态(),
                static_cast<I64>(工作结果.治理结果.宿主任务新状态),
                now);
            if (工作结果.存在任务推进回执
                && 工作结果.任务推进回执.建议任务阶段 != 枚举_任务阶段::未定义) {
                (void)私有_写基础节点I64特征(
                    上下文.输入条件包,
                    私有_特征_建议任务阶段(),
                    static_cast<I64>(工作结果.任务推进回执.建议任务阶段),
                    now);
            }
            if (auto* 状态提交运行 = 私有_执行任务域本能方法(
                    枚举_本能方法ID::自我_提交任务状态变化,
                    上下文)) {
                if (auto* 动态 = 私有_读取动作动态(状态提交运行)) {
                    工作结果.治理结果.任务状态动作动态 = 动态;
                    工作结果.治理结果.最新动态 = 动态;
                }
            }
        }
    }
    (void)任务类::写入任务等待缺口唤醒(
        任务,
        工作结果.产生派生需求
            && 任务类::读取任务状态(任务) == 枚举_任务状态::等待中,
        任务->主信息.场景.指针,
        now);
    (void)任务类::同步任务运行镜像特征(任务, 请求场景, now);
}

