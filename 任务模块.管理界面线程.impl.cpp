bool 私有_尝试复用幂等承接结果(
    const std::string& 幂等键,
    任务管理线程协议::结构_任务界面承接结果* 输出) noexcept
{
    if (幂等键.empty() || !输出) {
        return false;
    }
    auto& 状态 = 私有_界面状态();
    std::lock_guard<std::mutex> 锁(状态.锁);
    const auto 索引 = 状态.幂等索引.find(幂等键);
    if (索引 == 状态.幂等索引.end()) {
        return false;
    }
    const auto 记录迭代 = 状态.请求表.find(索引->second);
    if (记录迭代 == 状态.请求表.end()
        || !记录迭代->second.最近承接.已接收) {
        return false;
    }
    const auto& 最近承接 = 记录迭代->second.最近承接;
    const bool 可复用等待态 =
        最近承接.调度动作 == 任务管理线程协议::枚举_任务界面调度动作::挂起等待
        || 最近承接.请求状态 == 任务管理线程协议::枚举_任务界面请求状态::等待工作线程
        || 最近承接.下一步去向 == 枚举_任务管理下一步去向::保持等待;
    if (!可复用等待态) {
        return false;
    }
    *输出 = 最近承接;
    输出->推进事件列表.clear();
    输出->上行消息集.clear();
    输出->任务管理上位输入已刷新 = false;
    输出->任务管理治理触发 = false;
    输出->说明 = "复用任务管理界面线程幂等承接结果"
        + (输出->说明.empty() ? std::string{} : (" | " + 输出->说明));
    状态.快照.最近请求ID = 输出->请求ID;
    状态.快照.最近调度动作 = 私有_调度动作文本(输出->调度动作);
    状态.快照.最近说明 = 输出->说明;
    return true;
}

