const char* 私有_请求类型文本(
    const 任务管理线程协议::枚举_任务管理请求类型 类型) noexcept
{
    using enum 任务管理线程协议::枚举_任务管理请求类型;
    switch (类型) {
    case 发起任务: return "发起任务";
    case 调度任务: return "调度任务";
    case 重筹办任务: return "重筹办任务";
    case 等待任务: return "等待任务";
    case 取消任务: return "取消任务";
    case 更新权重: return "更新权重";
    case 同步回执: return "同步回执";
    default: return "未定义";
    }
}

任务管理线程协议::枚举_任务管理请求类型 私有_归一请求类型(
    const 任务管理线程协议::结构_任务治理请求& 请求) noexcept
{
    if (请求.请求类型 != 任务管理线程协议::枚举_任务管理请求类型::未定义) {
        if (!请求.任务信息节点
            && 请求.来源需求
            && 请求.允许创建任务信息节点
            && 请求.请求类型 == 任务管理线程协议::枚举_任务管理请求类型::调度任务) {
            return 任务管理线程协议::枚举_任务管理请求类型::发起任务;
        }
        return 请求.请求类型;
    }
    if (!请求.任务信息节点 && 请求.来源需求 && 请求.允许创建任务信息节点) {
        return 任务管理线程协议::枚举_任务管理请求类型::发起任务;
    }
    return 任务管理线程协议::枚举_任务管理请求类型::调度任务;
}

std::string 私有_构造任务治理幂等键(
    const 任务管理线程协议::结构_任务治理请求& 请求)
{
    const auto 类型 = 私有_归一请求类型(请求);
    auto* 任务 = reinterpret_cast<const 任务节点*>(请求.任务信息节点);
    auto* 需求 = reinterpret_cast<const 需求节点*>(请求.来源需求);
    std::ostringstream 输出;
    输出 << 私有_请求类型文本(类型);
    if (类型 == 任务管理线程协议::枚举_任务管理请求类型::发起任务) {
        输出 << "|需求=" << 私有_安全主键(需求);
        return 输出.str();
    }
    输出 << "|任务=" << 私有_安全主键(任务);
    if (需求) {
        输出 << "|需求=" << 私有_安全主键(需求);
    }
    if (类型 == 任务管理线程协议::枚举_任务管理请求类型::调度任务 && 任务) {
        输出 << "|状态=" << static_cast<int>(任务类::读取任务状态(任务))
            << "|阶段=" << static_cast<int>(任务类::读取任务当前阶段(任务))
            << "|运行=" << 任务类::读取任务运行次数(任务)
            << "|权重=" << 任务类::读取任务权重版本号(任务);
    }
    if (请求.请求ID != 0
        && 类型 == 任务管理线程协议::枚举_任务管理请求类型::同步回执) {
        输出 << "|请求=" << 请求.请求ID;
    }
    return 输出.str();
}

