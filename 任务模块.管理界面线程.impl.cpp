bool 私有_尝试复用幂等承接结果(
    const std::string& 幂等键,
    任务管理线程协议::结构_任务界面承接结果* 输出) noexcept
{
    if (幂等键.empty() || !输出) {
        return false;
    }
    auto& 状态 = 私有_界面状态();
    std::lock_guard<std::mutex> 锁(状态.锁);
    const auto 索引 = 状态.幂等索引.find(幂等键);
    if (索引 == 状态.幂等索引.end()) {
        return false;
    }
    const auto 记录迭代 = 状态.请求表.find(索引->second);
    if (记录迭代 == 状态.请求表.end()
        || !记录迭代->second.最近承接.已接收) {
        return false;
    }
    const auto& 最近承接 = 记录迭代->second.最近承接;
    const bool 可复用等待态 =
        最近承接.调度动作 == 任务管理线程协议::枚举_任务界面调度动作::挂起等待
        || 最近承接.请求状态 == 任务管理线程协议::枚举_任务界面请求状态::等待工作线程
        || 最近承接.下一步去向 == 枚举_任务管理下一步去向::保持等待;
    if (!可复用等待态) {
        return false;
    }
    *输出 = 最近承接;
    输出->推进事件列表.clear();
    输出->上行消息集.clear();
    输出->任务管理上位输入已刷新 = false;
    输出->任务管理治理触发 = false;
    输出->说明 = "复用任务管理界面线程幂等承接结果"
        + (输出->说明.empty() ? std::string{} : (" | " + 输出->说明));
    状态.快照.最近请求ID = 输出->请求ID;
    状态.快照.最近调度动作 = 私有_调度动作文本(输出->调度动作);
    状态.快照.最近说明 = 输出->说明;
    return true;
}

void 私有_登记请求已接收(
    const 任务管理线程协议::结构_任务治理请求& 请求,
    时间戳 now) noexcept
{
    auto& 状态 = 私有_界面状态();
    std::lock_guard<std::mutex> 锁(状态.锁);
    结构_任务界面请求记录& 记录 = 状态.请求表[请求.请求ID];
    if (记录.请求ID == 0) {
        记录.首次接收时间 = now;
    }
    记录.请求ID = 请求.请求ID;
    记录.幂等键 = 请求.幂等键;
    记录.请求状态 = 任务管理线程协议::枚举_任务界面请求状态::已接收;
    记录.最近更新时间 = now;
    状态.幂等索引[请求.幂等键] = 请求.请求ID;
    状态.快照.当前请求队列长度 = static_cast<std::uint64_t>(状态.请求表.size());
}

void 私有_记录承接结果(
    const 任务管理线程协议::结构_任务界面承接结果& 承接,
    bool 已完成,
    时间戳 now) noexcept
{
    auto& 状态 = 私有_界面状态();
    std::lock_guard<std::mutex> 锁(状态.锁);
    结构_任务界面请求记录& 记录 = 状态.请求表[承接.请求ID];
    if (记录.请求ID == 0) {
        记录.首次接收时间 = now;
    }
    记录.请求ID = 承接.请求ID;
    记录.幂等键 = 承接.幂等键;
    记录.请求状态 = 承接.请求状态;
    记录.调度动作 = 承接.调度动作;
    记录.最近承接 = 承接;
    记录.最近更新时间 = now;
    记录.已完成 = 已完成;
    if (!承接.幂等键.empty()) {
        状态.幂等索引[承接.幂等键] = 承接.请求ID;
    }
    状态.快照.当前请求队列长度 = static_cast<std::uint64_t>(状态.请求表.size());
}

void 私有_登记等待工作包(
    const 任务管理线程协议::结构_任务工作包& 工作包) noexcept
{
    auto& 状态 = 私有_界面状态();
    std::lock_guard<std::mutex> 锁(状态.锁);
    状态.等待工作包队列.push_back(工作包);
    状态.快照.当前等待工作包数 =
        static_cast<std::uint64_t>(状态.等待工作包队列.size());
}

void 私有_推入上行消息集(
    const std::vector<结构_任务管理上行消息>& 消息集,
    const 任务管理线程协议::结构_任务界面承接结果& 承接) noexcept
{
    if (消息集.empty()) {
        return;
    }
    auto& 状态 = 私有_界面状态();
    std::lock_guard<std::mutex> 锁(状态.锁);
    for (const auto& 消息 : 消息集) {
        状态.上行队列.push_back(消息);
    }
    状态.快照.累计上行消息数 += 消息集.size();
    状态.快照.当前上行队列长度 = 状态.上行队列.size();
    状态.快照.最近请求ID = 承接.请求ID;
}

void 私有_刷新快照(
    const 任务管理线程协议::结构_任务界面承接结果& 承接,
    const std::string& 说明) noexcept
{
    auto& 状态 = 私有_界面状态();
    std::lock_guard<std::mutex> 锁(状态.锁);
    状态.快照.已启动 = 状态.已启动;
    状态.快照.正在处理 = 状态.正在处理;
    状态.快照.故障 = 状态.故障;
    状态.快照.当前请求队列长度 = static_cast<std::uint64_t>(状态.请求表.size());
    状态.快照.当前等待工作包数 =
        static_cast<std::uint64_t>(状态.等待工作包队列.size());
    状态.快照.当前上行队列长度 = 状态.上行队列.size();
    状态.快照.最近请求ID = 承接.请求ID;
    状态.快照.最近任务主键 = 私有_安全主键(reinterpret_cast<任务节点*>(承接.任务信息节点));
    状态.快照.最近任务虚拟存在主键 = 私有_安全主键(承接.任务虚拟存在);
    状态.快照.最近需求主键 = 私有_安全主键(reinterpret_cast<需求节点*>(承接.来源需求));
    状态.快照.最近调度动作 = 私有_调度动作文本(承接.调度动作);
    状态.快照.最近说明 = 说明;
}

任务管理线程协议::结构_任务工作包 私有_构造任务工作包(
    const 任务管理线程协议::结构_任务治理请求& 请求,
    const 任务管理线程协议::结构_任务界面承接结果& 承接,
    存在节点类* 任务存在,
    时间戳 now) noexcept
{
    auto& 状态 = 私有_界面状态();
    任务管理线程协议::结构_任务工作包 工作包{};
    工作包.请求ID = 请求.请求ID;
    {
        std::lock_guard<std::mutex> 锁(状态.锁);
        工作包.工作包ID = 状态.下一个工作包ID++;
        状态.快照.最近工作包ID = 工作包.工作包ID;
    }
    工作包.幂等键 = 请求.幂等键;
    工作包.任务信息节点 = 请求.任务信息节点;
    工作包.任务虚拟存在 = 任务存在;
    工作包.来源需求 = 请求.来源需求;
    工作包.当前时间 = now;
    工作包.任务根节点 = 承接.任务根节点;
    工作包.任务控制态 = 承接.最新任务控制态;
    工作包.任务局部运行态 = 承接.最新任务局部运行态;
    工作包.控制事件 = 私有_构造控制事件(工作包.任务控制态);
    工作包.根层重判结果 = 枚举_任务管理根层重判结果::允许普通推进;
    工作包.执行前门控结果 = 枚举_任务管理执行前门控结果::允许普通执行;
    工作包.允许筹办 = true;
    工作包.允许执行方法 = true;
    工作包.允许派生需求上报 = true;
    工作包.允许正式资产提交 = false;
    工作包.任务摘要 = 请求.请求摘要;
    工作包.摘要 = 请求.请求摘要;
    return 工作包;
}

任务管理线程协议::枚举_任务界面调度动作 私有_裁决工作结果调度动作(
    bool 已派发,
    const 任务管理线程协议::结构_任务工作结果& 工作结果) noexcept
{
    using enum 任务管理线程协议::枚举_任务界面调度动作;
    if (工作结果.建议进入收束) {
        return 结束调度;
    }
    if (工作结果.建议界面线程下一轮) {
        return 重新排队;
    }
    if (工作结果.需要等待) {
        return 挂起等待;
    }
    if (已派发) {
        return 派发工作线程;
    }
    return 拒绝请求;
}

void 私有_应用工作结果到任务壳(
    任务节点* 任务,
    const 任务管理线程协议::结构_任务工作结果& 工作结果,
    场景节点类* 请求场景,
    时间戳 now) noexcept
{
    if (!任务) {
        return;
    }
    if (工作结果.治理结果.宿主任务新状态 != 枚举_任务状态::未定义) {
        任务类::应用任务状态(
            任务,
            工作结果.治理结果.宿主任务新状态,
            now);
    }
    (void)任务类::写入任务等待缺口唤醒(
        任务,
        工作结果.产生派生需求
            && 任务类::读取任务状态(任务) == 枚举_任务状态::等待中,
        任务->主信息.场景.指针,
        now);
    (void)任务类::同步任务运行镜像特征(任务, 请求场景, now);
}

void 私有_合并工作结果到承接(
    任务管理线程协议::结构_任务界面承接结果& 承接,
    bool 已派发,
    const 任务管理线程协议::结构_任务工作结果& 工作结果) noexcept
{
    承接.已派发工作线程 = 已派发;
    承接.请求状态 = 工作结果.已处理
        ? 任务管理线程协议::枚举_任务界面请求状态::已收到工作结果
        : (已派发
            ? 任务管理线程协议::枚举_任务界面请求状态::已派发工作线程
            : (工作结果.需要等待
                ? 任务管理线程协议::枚举_任务界面请求状态::等待工作线程
                : 任务管理线程协议::枚举_任务界面请求状态::故障));
    承接.调度动作 = 私有_裁决工作结果调度动作(已派发, 工作结果);
    承接.最新任务控制态 = 工作结果.最新任务控制态;
    承接.最新任务局部运行态 = 工作结果.最新任务局部运行态;
    承接.推进事件列表 = 工作结果.推进事件列表;
    承接.上行消息集 = 工作结果.上行消息集;
    承接.下一步去向 = 工作结果.下一步去向;
    承接.最近总控结果 = 工作结果.最近总控结果;
    承接.任务管理上位输入已刷新 = 工作结果.任务管理上位输入已刷新;
    承接.任务管理治理触发 = 工作结果.任务管理治理触发;
    承接.说明 = 工作结果.摘要.empty()
        ? (已派发 ? "任务已通过界面线程派发工作包" : "任务工作包未完成派发")
        : 工作结果.摘要;
}

