bool 启动任务管理界面线程() noexcept
{
    auto& 状态 = 私有_界面状态();
    std::lock_guard<std::mutex> 锁(状态.锁);
    状态.已启动 = true;
    状态.故障 = false;
    状态.快照.已启动 = true;
    return true;
}

void 停止任务管理界面线程() noexcept
{
    {
        auto& 状态 = 私有_界面状态();
        std::lock_guard<std::mutex> 锁(状态.锁);
        状态.已启动 = false;
        状态.正在处理 = false;
        状态.等待工作包队列.clear();
        状态.快照.已启动 = false;
        状态.快照.正在处理 = false;
        状态.快照.当前等待工作包数 = 0;
    }
    任务管理工作线程::停止任务管理工作线程壳();
}

bool 提交任务治理请求(
    自我类& 自我对象,
    const 任务管理线程协议::结构_任务治理请求& 请求,
    任务管理线程协议::结构_任务界面承接结果* 输出) noexcept
{
    (void)启动任务管理界面线程();
    auto& 状态 = 私有_界面状态();

    auto now = 私有_时间(请求.当前时间);
    auto 请求副本 = 请求;
    请求副本.请求类型 = 私有_归一请求类型(请求副本);
    if (请求副本.请求ID == 0) {
        std::lock_guard<std::mutex> 锁(状态.锁);
        请求副本.请求ID = 状态.下一个请求ID++;
    }
    if (请求副本.幂等键.empty()) {
        请求副本.幂等键 = 私有_构造任务治理幂等键(请求副本);
    }
    if (请求副本.允许复用等待任务) {
        任务管理线程协议::结构_任务界面承接结果 已有承接{};
        if (私有_尝试复用幂等承接结果(请求副本.幂等键, &已有承接)) {
            if (输出) {
                *输出 = 已有承接;
            }
            return 已有承接.已校验;
        }
    }

    {
        std::lock_guard<std::mutex> 锁(状态.锁);
        状态.正在处理 = true;
        状态.快照.正在处理 = true;
        状态.快照.累计接收请求数 += 1;
    }
    私有_登记请求已接收(请求副本, now);

    任务管理线程协议::结构_任务界面承接结果 承接{};
    承接.请求ID = 请求副本.请求ID;
    承接.幂等键 = 请求副本.幂等键;
    承接.任务信息节点 = 请求副本.任务信息节点;
    承接.来源需求 = 请求副本.来源需求;
    承接.已接收 = true;
    承接.请求状态 = 任务管理线程协议::枚举_任务界面请求状态::已接收;

    auto* 任务 = reinterpret_cast<任务节点*>(请求副本.任务信息节点);
    auto* 需求 = reinterpret_cast<需求节点*>(请求副本.来源需求);
    if (!任务
        && 需求
        && 请求副本.请求类型 == 任务管理线程协议::枚举_任务管理请求类型::发起任务
        && 请求副本.允许创建任务信息节点) {
        任务 = 私有_按需求创建任务并初始化(
            自我对象,
            需求,
            reinterpret_cast<任务节点*>(请求副本.上级任务信息节点),
            now);
        请求副本.任务信息节点 = reinterpret_cast<任务节点类*>(任务);
        承接.任务信息节点 = 请求副本.任务信息节点;
    }

    if (!需求
        || (!任务 && 请求副本.请求类型 != 任务管理线程协议::枚举_任务管理请求类型::发起任务)
        || (!任务 && 请求副本.请求类型 == 任务管理线程协议::枚举_任务管理请求类型::发起任务)) {
        承接.请求状态 = 任务管理线程协议::枚举_任务界面请求状态::校验失败;
        承接.调度动作 = 任务管理线程协议::枚举_任务界面调度动作::拒绝请求;
        承接.说明 = 请求副本.请求类型 == 任务管理线程协议::枚举_任务管理请求类型::发起任务
            ? "任务发起请求缺少来源需求或任务信息节点创建失败"
            : "任务治理请求缺少任务信息节点或来源需求";
        {
            std::lock_guard<std::mutex> 锁(状态.锁);
            状态.正在处理 = false;
            状态.故障 = true;
        }
        私有_刷新快照(承接, 承接.说明);
        私有_记录承接结果(承接, true, now);
        if (输出) {
            *输出 = 承接;
        }
        return false;
    }

    承接.已校验 = true;
    私有_绑定任务需求关系(任务, 需求);
    私有_准备任务运行壳(任务, 需求, now);

    auto* 任务存在 = 请求副本.允许创建任务虚拟存在
        ? 私有_确保任务虚拟存在(自我对象, 任务, 需求, 请求副本.所属场景, now)
        : reinterpret_cast<存在节点类*>(任务->主信息.任务虚拟存在.指针);
    if (!任务存在) {
        承接.请求状态 = 任务管理线程协议::枚举_任务界面请求状态::故障;
        承接.调度动作 = 任务管理线程协议::枚举_任务界面调度动作::拒绝请求;
        承接.说明 = "任务虚拟存在创建或解析失败，拒绝派发工作线程";
        {
            std::lock_guard<std::mutex> 锁(状态.锁);
            状态.正在处理 = false;
            状态.故障 = true;
        }
        私有_刷新快照(承接, 承接.说明);
        私有_记录承接结果(承接, true, now);
        const auto 错误文本 =
            "任务管理界面线程/任务语义绑定失败"
            " | 任务=" + 私有_安全主键(任务)
            + " | 需求=" + 私有_安全主键(需求)
            + " | 原因=" + 承接.说明;
        项目运行错误日志(错误文本);
        项目弹窗错误提示("鱼巢 - 任务语义绑定逻辑错误", 错误文本);
        if (输出) {
            *输出 = 承接;
        }
        return false;
    }

    承接.任务虚拟存在 = 任务存在;
    承接.已绑定任务虚拟存在 = true;
    承接.请求状态 = 任务管理线程协议::枚举_任务界面请求状态::已绑定任务虚拟存在;
    {
        std::lock_guard<std::mutex> 锁(状态.锁);
        状态.快照.累计绑定任务虚拟存在数 += 1;
    }

    if (请求副本.允许初始化任务语义) {
        承接.已初始化任务语义 =
            私有_用需求初始化任务虚拟存在(
                自我对象,
                任务,
                需求,
                请求副本.所属场景,
                now);
        if (承接.已初始化任务语义) {
            承接.请求状态 = 任务管理线程协议::枚举_任务界面请求状态::已初始化任务语义;
        }
    }

    承接.任务根节点 = 私有_构造任务根节点(请求副本, 请求副本.请求ID);
    承接.最新任务控制态 = 私有_构造任务控制态(承接.任务根节点, 任务);
    承接.最新任务局部运行态 = 私有_构造任务局部运行态(承接.任务根节点, 任务);
    auto 工作包 = 私有_构造任务工作包(请求副本, 承接, 任务存在, now);
    承接.已构造工作包 = true;

    if (!请求副本.允许派发工作线程) {
        if (工作包.推进模式 == 任务管理线程协议::枚举_任务推进模式::只创建) {
            // 发起任务的只创建请求已经在界面线程内完成任务信息节点和任务虚拟存在创建。
            // 它不是等待执行的工作包；后续筹办 / 执行必须由自我线程提交调度意图后重新进入任务管理线程。
            承接.请求状态 = 任务管理线程协议::枚举_任务界面请求状态::已完成;
            承接.调度动作 = 任务管理线程协议::枚举_任务界面调度动作::结束调度;
            承接.已进入等待队列 = false;
            承接.说明 = "任务发起请求已完成任务创建，未派发工作线程";
        } else {
            承接.请求状态 = 任务管理线程协议::枚举_任务界面请求状态::等待工作线程;
            承接.调度动作 = 任务管理线程协议::枚举_任务界面调度动作::挂起等待;
            承接.已进入等待队列 = true;
            承接.说明 = "任务治理请求只完成界面线程承接，未派发工作线程";
            私有_登记等待工作包(工作包);
        }
        {
            std::lock_guard<std::mutex> 锁(状态.锁);
            状态.正在处理 = false;
        }
        私有_刷新快照(承接, 承接.说明);
        私有_记录承接结果(承接, false, now);
        if (输出) {
            *输出 = 承接;
        }
        return true;
    }

    任务管理线程协议::结构_任务工作结果 工作结果{};
    const bool 已派发 = 任务管理工作线程::提交任务工作包(
        自我对象,
        工作包,
        &工作结果);

    私有_应用工作结果到任务壳(任务, 工作结果, 请求副本.所属场景, now);
    私有_合并工作结果到承接(承接, 已派发, 工作结果);

    {
        std::lock_guard<std::mutex> 锁(状态.锁);
        if (已派发) {
            状态.快照.累计派发工作包数 += 1;
        }
        状态.快照.累计收到工作结果数 += 1;
        状态.正在处理 = false;
        状态.故障 = 承接.请求状态 == 任务管理线程协议::枚举_任务界面请求状态::故障;
    }
    私有_推入上行消息集(承接.上行消息集, 承接);
    私有_刷新快照(承接, 承接.说明);
    私有_记录承接结果(
        承接,
        承接.调度动作 == 任务管理线程协议::枚举_任务界面调度动作::结束调度
            || 承接.调度动作 == 任务管理线程协议::枚举_任务界面调度动作::拒绝请求,
        now);

    if (输出) {
        *输出 = 承接;
    }
    return 承接.已校验;
}

// 显式任务状态推进请求提交点。
// 自我线程只能提交“等待 / 重筹办 / 取消”等意图；本函数在任务管理界面
// 线程内执行实际任务状态写入。若状态值发生变化，同时生成任务域动作动态。
bool 提交任务状态推进请求(
    const 任务管理线程协议::结构_任务状态推进请求& 请求) noexcept
{
    auto* 任务 = reinterpret_cast<任务节点*>(请求.任务信息节点);
    if (!任务) {
        return false;
    }
    const auto now = 私有_时间(请求.当前时间);
    auto* 场景 = 任务->主信息.场景.指针;

    bool 已写入 = false;
    if ((请求.写入任务状态 && 请求.建议任务状态 != 枚举_任务状态::未定义)
        || (请求.写入任务阶段 && 请求.建议任务阶段 != 枚举_任务阶段::未定义)) {
        const auto 提交状态 = 请求.写入任务状态
            && 请求.建议任务状态 != 枚举_任务状态::未定义
            ? 请求.建议任务状态
            : 任务类::读取任务状态(任务, 枚举_任务状态::未定义);
        if (提交状态 == 枚举_任务状态::未定义) {
            std::ostringstream 日志;
            日志 << "任务管理界面线程/任务状态推进"
                << " | 任务=" << 任务->获取主键()
                << " | 阶段推进缺少有效任务状态"
                << " | 原因=" << 请求.原因摘要;
            项目运行日志(日志.str());
            return false;
        }

        auto 上下文 = 私有_准备任务域本能调用(
            任务,
            枚举_本能方法ID::自我_提交任务状态变化,
            "显式提交任务状态变化",
            场景,
            now);
        if (上下文.输入条件包) {
            (void)私有_写基础节点I64特征(
                上下文.输入条件包,
                私有_特征_建议任务状态(),
                static_cast<I64>(提交状态),
                now);
            if (请求.写入任务阶段
                && 请求.建议任务阶段 != 枚举_任务阶段::未定义) {
                (void)私有_写基础节点I64特征(
                    上下文.输入条件包,
                    私有_特征_建议任务阶段(),
                    static_cast<I64>(请求.建议任务阶段),
                    now);
            }
            (void)私有_执行任务域本能方法(
                枚举_本能方法ID::自我_提交任务状态变化,
                上下文);
        }
        已写入 = true;
    }
    (void)任务类::写入任务等待缺口唤醒(
        任务,
        请求.等待缺口唤醒,
        场景,
        now);
    (void)任务类::同步任务运行镜像特征(任务, 场景, now);
    if (已写入) {
        std::ostringstream 日志;
        日志 << "任务管理界面线程/任务状态推进"
            << " | 任务=" << 任务->获取主键()
            << " | 状态=" << static_cast<int>(任务类::读取任务状态(任务))
            << " | 阶段=" << static_cast<int>(任务类::读取任务当前阶段(任务))
            << " | 原因=" << 请求.原因摘要;
        项目运行日志(日志.str());
    }
    return true;
}

bool 提交任务权重固化请求(
    const 任务管理线程协议::结构_任务权重固化请求& 请求) noexcept
{
    auto* 任务 = reinterpret_cast<任务节点*>(请求.任务信息节点);
    if (!任务) {
        return false;
    }
    const auto now = 私有_时间(请求.当前时间);
    auto* 场景 = 任务->主信息.场景.指针;
    (void)任务类::写入任务承接安全权重上限(
        任务,
        (std::max<I64>)(0, 请求.承接安全权重上限),
        场景,
        now);
    (void)任务类::写入任务承接服务权重上限(
        任务,
        (std::max<I64>)(0, 请求.承接服务权重上限),
        场景,
        now);
    (void)任务类::写入任务权重评分(
        任务,
        (std::max<I64>)(0, 请求.任务权重评分),
        场景,
        now);
    (void)任务类::写入任务权重版本号(
        任务,
        请求.任务权重版本号 > 0 ? 请求.任务权重版本号 : (now > 0 ? static_cast<I64>(now) : 1),
        场景,
        now);
    (void)任务类::同步任务运行镜像特征(任务, 场景, now);
    std::ostringstream 日志;
    日志 << "任务管理界面线程/任务承接权重固化"
        << " | 任务=" << 任务->获取主键()
        << " | 安全上限=" << 请求.承接安全权重上限
        << " | 服务上限=" << 请求.承接服务权重上限
        << " | 评分=" << 请求.任务权重评分
        << " | 原因=" << 请求.原因摘要;
    项目运行日志(日志.str());
    return true;
}

bool 提交任务价值结算回执(
    const 任务管理线程协议::结构_任务价值结算回执& 回执) noexcept
{
    auto* 任务 = reinterpret_cast<任务节点*>(回执.任务信息节点);
    if (!任务) {
        return false;
    }
    const auto now = 私有_时间(回执.当前时间);
    auto* 场景 = 任务->主信息.场景.指针;
    bool 已写入 = false;
    if (回执.写入已折算安全值) {
        (void)任务类::写入任务已折算安全值(
            任务,
            (std::max<I64>)(0, 回执.已折算安全值),
            场景,
            now);
        已写入 = true;
    }
    if (回执.写入已折算服务值) {
        (void)任务类::写入任务已折算服务值(
            任务,
            (std::max<I64>)(0, 回执.已折算服务值),
            场景,
            now);
        已写入 = true;
    }
    if (已写入) {
        (void)任务类::同步任务运行镜像特征(任务, 场景, now);
        std::ostringstream 日志;
        日志 << "任务管理界面线程/任务价值折算回执"
            << " | 任务=" << 任务->获取主键()
            << " | 已折算安全=" << 回执.已折算安全值
            << " | 已折算服务=" << 回执.已折算服务值
            << " | 事件=" << 回执.反馈事件主键;
        项目运行日志(日志.str());
    }
    return true;
}

bool 提取任务管理上行消息(
    std::vector<结构_任务管理上行消息>* 输出,
    std::size_t 最大数量) noexcept
{
    if (!输出) {
        return false;
    }
    auto& 状态 = 私有_界面状态();
    std::lock_guard<std::mutex> 锁(状态.锁);
    const auto 数量上限 = 最大数量 == 0 ? 状态.上行队列.size() : 最大数量;
    std::size_t 已取 = 0;
    while (!状态.上行队列.empty() && 已取 < 数量上限) {
        输出->push_back(状态.上行队列.front());
        状态.上行队列.pop_front();
        ++已取;
    }
    状态.快照.当前上行队列长度 = 状态.上行队列.size();
    return 已取 > 0;
}

bool 读取任务管理界面线程快照(
    任务管理线程协议::结构_任务界面线程快照* 输出) noexcept
{
    if (!输出) {
        return false;
    }
    auto& 状态 = 私有_界面状态();
    std::lock_guard<std::mutex> 锁(状态.锁);
    *输出 = 状态.快照;
    return true;
}

