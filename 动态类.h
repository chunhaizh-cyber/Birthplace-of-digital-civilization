#pragma once

#include "基础信息类.h"

class 动态类 {
public:
    explicit 动态类(基础信息类* 基础信息 = nullptr) noexcept;

    void 绑定基础信息(基础信息类* 基础信息) noexcept;

    动态节点主信息类* 取动态主信息(动态节点类* 节点) const noexcept;
    const 动态节点主信息类* 取动态主信息(const 动态节点类* 节点) const noexcept;

    动态节点类* 创建动态(场景节点类* 场景, 动态节点主信息类* 主信息);
    动态节点类* 创建动态(
        场景节点类* 场景,
        状态节点类* 初始状态,
        状态节点类* 结果状态,
        时间戳 起 = 0,
        时间戳 止 = 0
    );
    动态节点类* 创建叶子动态(
        场景节点类* 场景,
        状态节点类* 初始状态,
        状态节点类* 结果状态,
        时间戳 now = 0
    );
    动态节点类* 创建动作动态(
        场景节点类* 场景,
        基础信息节点类* 动态主体,
        特征节点类* 动态特征,
        时间戳 开始时间 = 0,
        时间戳 结束时间 = 0
    );
    动态节点类* 创建方法动作动态(
        场景节点类* 场景,
        基础信息节点类* 动态主体,
        特征节点类* 动态特征,
        方法节点类* 来源动作,
        const 语素入口节点类* 来源动作名 = nullptr,
        时间戳 开始时间 = 0,
        时间戳 结束时间 = 0,
        bool 执行成功 = false,
        I64 错误码 = 0,
        枚举_动作事件相位 相位 = 枚举_动作事件相位::未定义,
        场景节点类* 输入场景 = nullptr,
        场景节点类* 输出场景 = nullptr
    );
    动态节点类* 创建存在动态(
        场景节点类* 观察场景,
        存在节点类* 被观察存在,
        状态节点类* 初始状态,
        状态节点类* 结果状态,
        动态节点类* 来源低层动态 = nullptr,
        时间戳 now = 0
    );
    动态节点类* 创建场景动态(
        场景节点类* 父场景,
        场景节点类* 子场景,
        状态节点类* 初始状态,
        状态节点类* 结果状态,
        动态节点类* 来源低层动态 = nullptr,
        时间戳 now = 0
    );
    bool 允许生成状态动态(场景节点类* 场景, 状态节点类* 初始状态, 状态节点类* 结果状态) const noexcept;
    bool 允许生成存在动态(场景节点类* 观察场景, 存在节点类* 被观察存在, 状态节点类* 初始状态, 状态节点类* 结果状态) const noexcept;
    bool 允许生成场景动态(场景节点类* 父场景, 场景节点类* 子场景, 状态节点类* 初始状态, 状态节点类* 结果状态) const noexcept;
    bool 允许生成方法动作动态(const 方法节点类* 来源动作) const noexcept;
    bool 删除动态(动态节点类* 节点);

    std::vector<动态节点类*> 枚举全部动态() const;
    std::vector<动态节点类*> 获取场景动态(const 场景节点类* 场景) const;
    状态节点类* 查找相邻前状态(场景节点类* 场景, 状态节点类* 新状态) const;
    动态节点类* 状态记录后刷新动态(
        场景节点类* 场景,
        状态节点类* 新状态,
        时间戳 now = 0
    );

    bool 设置业务语义类型(动态节点类* 节点, 枚举_动态业务语义类型 类型);
    bool 设置主体特征(动态节点类* 节点, 基础信息节点类* 主体, 特征节点类* 特征);
    bool 设置聚合信息(动态节点类* 节点, std::uint32_t 动态层级, 枚举_动态聚合方式 聚合方式);
    bool 追加来源低层动态(动态节点类* 节点, 动态节点类* 来源低层动态);
    bool 绑定来源方法(
        动态节点类* 节点,
        方法节点类* 来源动作,
        const 语素入口节点类* 来源动作名 = nullptr,
        枚举_动作事件相位 相位 = 枚举_动作事件相位::未定义
    );
    bool 绑定来源场景(动态节点类* 节点, 场景节点类* 输入场景, 场景节点类* 输出场景);

private:
    基础信息类* 基础信息_ = nullptr;
};
