#pragma once

#include "基础信息类.h"

class 动态类 {
public:
    explicit 动态类(基础信息类* 基础信息 = nullptr) noexcept;

    void 绑定基础信息(基础信息类* 基础信息) noexcept;

    动态节点主信息类* 取动态主信息(动态节点类* 节点) const noexcept;
    const 动态节点主信息类* 取动态主信息(const 动态节点类* 节点) const noexcept;

    动态节点类* 创建动态(场景节点类* 场景, 动态节点主信息类* 主信息);
    动态节点类* 创建动态(
        场景节点类* 场景,
        状态节点类* 初始状态,
        状态节点类* 结果状态,
        时间戳 起 = 0,
        时间戳 止 = 0
    );
    bool 删除动态(动态节点类* 节点);

    std::vector<动态节点类*> 枚举全部动态() const;
    std::vector<动态节点类*> 获取场景动态(const 场景节点类* 场景) const;

    bool 设置主体特征(动态节点类* 节点, 基础信息节点类* 主体, 特征节点类* 特征);
    bool 绑定来源方法(
        动态节点类* 节点,
        方法节点类* 来源动作,
        const 词性节点类* 来源动作名 = nullptr,
        枚举_动作事件相位 相位 = 枚举_动作事件相位::未定义
    );
    bool 绑定来源场景(动态节点类* 节点, 场景节点类* 输入场景, 场景节点类* 输出场景);

private:
    基础信息类* 基础信息_ = nullptr;
};
