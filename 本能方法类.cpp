namespace {
    方法类::节点类* 私有_查找或创建本能方法首节点(
        自我类& 自我对象,
        枚举_本能方法ID 本能ID,
        const std::string& 调用点) noexcept
    {
        const auto* 元信息 = 私有_查询元信息(本能ID);
        if (!元信息 || !元信息->动作名称 || 元信息->动作名称[0] == '\0') return nullptr;

        auto* 自我存在 = 自我对象.确保自我存在(调用点 + "/自我存在");
        if (!自我存在) return nullptr;

        auto* 已有节点 = 私有_查找本能方法首节点(自我存在, 本能ID);
        if (!已有节点) {
            auto* 方法根节点 = 世界树.存在().获取方法根节点(自我存在);
            if (!方法根节点) return nullptr;

            方法首节点主信息类 主信息{};
            主信息.动作名 = 私有_动作名称词(*元信息);
            主信息.动作句柄.类型 = 枚举_动作句柄类型::本能函数ID;
            主信息.动作句柄.本能ID = static_cast<I64>(本能ID);
            主信息.来源 = 枚举_方法来源::本能;
            已有节点 = 私有_独立树添加方法子节点(方法根节点, 主信息);
        }

        私有_补齐本能方法结构(自我对象, 已有节点, *元信息, 调用点 + "/补齐结构");
        return 已有节点 && 私有_本能方法首节点已可执行(已有节点) ? 已有节点 : nullptr;
    }
}

std::size_t 本能方法类::注册默认自我本能方法集(
    自我类& 自我对象,
    const std::string& 调用点) noexcept
{
    std::size_t 已确保数量 = 0;
    for (const auto& 元信息 : g_默认自我本能方法表) {
        if (私有_查找或创建本能方法首节点(
            自我对象,
            元信息.ID,
            调用点 + "/" + (元信息.动作名称 ? 元信息.动作名称 : "未命名动作"))) {
            ++已确保数量;
        }
    }
    return 已确保数量;
}

bool 本能方法类::默认自我本能方法已注册(const 自我类& 自我对象) noexcept
{
    for (const auto& 元信息 : g_默认自我本能方法表) {
        const auto* 方法首节点 =
            私有_查找本能方法首节点(const_cast<存在节点类*>(自我对象.获取自我存在()), 元信息.ID);
        if (!方法首节点 || !私有_本能方法首节点已可执行(方法首节点)) {
            return false;
        }
    }
    return true;
}
