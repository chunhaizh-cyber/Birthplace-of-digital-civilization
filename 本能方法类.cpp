    方法节点* 私有_查找本能方法首节点(
        存在节点类* 自我存在,
        枚举_本能方法ID 本能ID) noexcept
    {
        if (!自我存在) return nullptr;

        auto* 方法根节点 = 世界树.存在().获取方法根节点(自我存在);
        const auto* 种子 = 私有_查询默认种子(本能ID);
        if (!种子 || !种子->动作名称 || 种子->动作名称[0] == '\0') {
            return nullptr;
        }
        const auto* 动作名称 = 种子 ? 私有_动作名称词(*种子) : nullptr;
        if (!方法根节点) return nullptr;

        return 私有_查找直接子节点_按谓词<方法节点>(方法根节点, [&](const 方法节点* 节点) noexcept {
            if (!节点) return false;
            const auto& 主信息 = 节点->主信息;
            if (主信息.节点种类() != 枚举_方法节点种类::方法首节点) return false;
            if (主信息.首节点信息().动作句柄.类型 == 枚举_动作句柄类型::本能函数ID
                && 主信息.首节点信息().动作句柄.本能ID == static_cast<I64>(本能ID)) {
                return true;
            }
            return 动作名称 && 私有_语素入口相同(主信息.首节点信息().动作名, 动作名称);
        });
    }

    bool 私有_本能方法首节点已可执行(const 方法节点* 方法首节点) noexcept
    {
        if (!方法首节点
            || 方法首节点->主信息.公共.节点种类 != 枚举_方法节点种类::方法首节点
            || !方法首节点->主信息.有动作骨架()
            || !方法首节点->子) {
            return false;
        }

        const auto* 首子节点 = static_cast<const 方法节点*>(方法首节点->子);
        auto* 当前 = 首子节点;
        do {
            if (当前
                && 当前->主信息.节点种类() == 枚举_方法节点种类::方法条件节点
                && 当前->主信息.取条件节点信息()) {
                return true;
            }
            当前 = static_cast<const 方法节点*>(当前->下);
        } while (当前 && 当前 != 首子节点);
        return false;
    }

    void 私有_补齐本能方法结构(
        自我类& 自我对象,
        方法节点* 方法首节点,
        const 结构_默认本能方法种子& 种子) noexcept
    {
        if (!方法首节点) return;

        auto* 自我存在 = 自我对象.确保自我存在();
        auto* 自我内部世界 = 自我对象.确保自我内部世界();
        if (!自我存在 || !自我内部世界) return;

        auto* 方法虚拟存在 = 世界树.存在().取或创建子存在_按名称类型(
            自我内部世界,
            私有_方法虚拟存在名称词(种子),
            私有_方法虚拟存在类型词());
        auto* 条件场景 = 方法虚拟存在
            ? 世界树.场景().取或创建子场景_按名称(
                方法虚拟存在,
                私有_条件场景名称词(种子),
                枚举_世界类型::内部世界)
            : nullptr;
        auto* 结果场景 = 方法虚拟存在
            ? 世界树.场景().取或创建子场景_按名称(
                方法虚拟存在,
                私有_结果场景名称词(种子),
                枚举_世界类型::内部世界)
            : nullptr;

        auto& 主信息 = 方法首节点->主信息;
        主信息.公共.节点种类 = 枚举_方法节点种类::方法首节点;
        主信息.首节点信息().动作名 = 私有_动作名称词(种子);
        主信息.首节点信息().动作句柄.类型 = 枚举_动作句柄类型::本能函数ID;
        主信息.首节点信息().动作句柄.本能ID = static_cast<I64>(种子.ID);
        主信息.首节点信息().动作句柄.外部实现主键.clear();
        主信息.首节点信息().动作句柄.动作序列主键.clear();
        私有_写入引用(主信息.首节点信息().方法虚拟存在, 方法虚拟存在);
        // 分类是方法虚拟存在上的身份特征，不再写成条件场景里的业务条件。
        (void)私有_写入方法类型特征(方法虚拟存在, 私有_推导本能方法类型(种子));
        (void)私有_写入本能方法分类特征(方法虚拟存在, 种子.分类);
        (void)本能方法类::补齐本能方法首节点(
            方法首节点,
            自我存在,
            结构体_时间戳::当前_微秒());
    }
