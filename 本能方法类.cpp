    bool 私有_写入方法类型特征(存在节点类* 方法虚拟存在, 枚举_方法类型 方法类型) noexcept
    {
        if (!方法虚拟存在 || 方法类型 == 枚举_方法类型::未定义) {
            return false;
        }

        auto* 宿主基础节点 = reinterpret_cast<基础信息节点类*>(方法虚拟存在);
        auto* 特征节点 = 宿主基础节点
            ? 世界树.取或创建子特征_按类型(宿主基础节点, 私有_方法特征_方法类型())
            : nullptr;
        if (!特征节点) {
            return false;
        }

        I64 当前值 = 0;
        const auto 新值 = static_cast<I64>(方法类型);
        if (世界树.读取特征_I64(特征节点, 当前值) && 当前值 == 新值) {
            return true;
        }

        return 世界树.写入特征_I64(特征节点, 新值, 结构体_时间戳::当前_微秒());
    }

