    方法节点* 私有_查找本能方法首节点(
        存在节点类* 自我存在,
        枚举_本能方法ID 本能ID) noexcept
    {
        if (!自我存在) return nullptr;

        auto* 方法根节点 = 世界树.存在().获取方法根节点(自我存在);
        const auto* 元信息 = 私有_查询元信息(本能ID);
        const auto* 动作名称 = 元信息 ? 私有_动作名称词(*元信息) : nullptr;
        if (!方法根节点) return nullptr;

        return 私有_查找直接子节点_按谓词<方法节点>(方法根节点, [&](const 方法节点* 节点) noexcept {
            if (!节点) return false;
            const auto& 主信息 = 节点->主信息;
            if (主信息.节点种类 != 枚举_方法节点种类::方法首节点) return false;
            if (主信息.动作句柄.类型 == 枚举_动作句柄类型::本能函数ID
                && 主信息.动作句柄.本能ID == static_cast<I64>(本能ID)) {
                return true;
            }
            return 动作名称 && 私有_词性相同(主信息.动作名, 动作名称);
        });
    }

    bool 私有_本能方法首节点已可执行(const 方法节点* 方法首节点) noexcept
    {
        return 方法首节点
            && 方法首节点->主信息.节点种类 == 枚举_方法节点种类::方法首节点
            && 方法首节点->主信息.有动作骨架()
            && !方法首节点->主信息.条件判定索引.empty();
    }

    void 私有_补齐本能方法结构(
        自我类& 自我对象,
        方法节点* 方法首节点,
        const 结构_本能方法元信息& 元信息,
        const std::string& 调用点) noexcept
    {
        if (!方法首节点) return;

        auto* 自我存在 = 自我对象.确保自我存在(调用点 + "/自我存在");
        auto* 自我内部世界 = 自我对象.确保自我内部世界(调用点 + "/自我内部世界");
        if (!自我存在 || !自我内部世界) return;

        auto* 方法虚拟存在 = 世界树.存在().取或创建子存在_按名称类型(
            自我内部世界,
            私有_方法虚拟存在名称词(元信息),
            私有_方法虚拟存在类型词());
        auto* 条件场景 = 方法虚拟存在
            ? 世界树.场景().取或创建子场景_按名称(
                方法虚拟存在,
                私有_条件场景名称词(元信息),
                枚举_世界类型::内部世界)
            : nullptr;
        auto* 结果场景 = 方法虚拟存在
            ? 世界树.场景().取或创建子场景_按名称(
                方法虚拟存在,
                私有_结果场景名称词(元信息),
                枚举_世界类型::内部世界)
            : nullptr;

        auto& 主信息 = 方法首节点->主信息;
        主信息.节点种类 = 枚举_方法节点种类::方法首节点;
        主信息.动作名 = 私有_动作名称词(元信息);
        主信息.动作句柄.类型 = 枚举_动作句柄类型::本能函数ID;
        主信息.动作句柄.本能ID = static_cast<I64>(元信息.ID);
        主信息.动作句柄.外部实现主键.clear();
        主信息.动作句柄.动作序列主键.clear();
        私有_写入引用(主信息.方法虚拟存在, 方法虚拟存在);
        (void)私有_写入方法类型特征(方法虚拟存在, 私有_推导本能方法类型(元信息));
        私有_写入引用(主信息.条件场景, 条件场景);
        私有_写入引用(主信息.结果场景, 结果场景);
        (void)方法类::补齐本能方法首节点(
            方法首节点,
            元信息,
            自我存在,
            结构体_时间戳::当前_微秒(),
            调用点 + "/完整首节点");
    }
