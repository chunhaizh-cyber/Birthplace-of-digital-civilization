    bool 私有_同步方法状态特征(
        方法节点* 方法首节点,
        存在节点类* 宿主存在,
        时间戳 now)
    {
        auto* 首节点 = 私有_归一到方法首节点(方法首节点);
        if (!首节点) {
            return false;
        }
        auto* 方法虚拟存在 = 方法虚拟存在服务类::确保方法虚拟存在(
            首节点,
            宿主存在,
            now);
        if (!方法虚拟存在) {
            return false;
        }

        I64 方法状态 = 0;
        if (!首节点->主信息.有动作骨架()) {
            方法状态 = 私有_方法状态_待动作骨架值();
        } else if (!首节点->主信息.首节点信息().动作句柄.有效()) {
            方法状态 = 私有_方法状态_待可执行入口值();
        } else if (私有_方法直接子节点数量(首节点, 枚举_方法节点种类::方法条件节点) <= 0) {
            方法状态 = 私有_方法状态_待条件节点值();
        } else if (私有_方法直接子节点数量(首节点, 枚举_方法节点种类::方法结果节点) <= 0
            && 私有_方法条件结果对数量(首节点) <= 0) {
            方法状态 = 私有_方法状态_待结果节点值();
        } else if (私有_方法条件结果对数量(首节点) <= 0) {
            方法状态 = 私有_方法状态_待条件结果对值();
        } else {
            // “方法状态=可用”的最低结构前提是已经存在至少一组条件结果对。
            方法状态 = 私有_方法状态_可用值();
        }

        return 私有_写入I64特征(
            reinterpret_cast<基础信息节点类*>(方法虚拟存在),
            私有_特征_方法状态(),
            方法状态,
            now);
    }

    bool 私有_写入方法自我能力特征(
        存在节点类* 方法虚拟存在,
        I64 自我能力值,
        时间戳 now)
    {
        if (!方法虚拟存在
            || 自我能力值 <= 0
            || 自我能力值 > static_cast<I64>((std::numeric_limits<std::uint32_t>::max)())) {
            return false;
        }
        if (!本能方法类::是默认自我本能方法(
            static_cast<枚举_本能方法ID>(static_cast<std::uint32_t>(自我能力值)))) {
            return false;
        }

        auto* 抽象根 = 世界树.取或创建抽象特征根();
        auto* 自我能力抽象 = 抽象根
            ? 世界树.取或创建抽象特征_按类型(抽象根, 私有_特征_自我能力())
            : nullptr;
        auto* 实例特征 = 自我能力抽象
            ? 世界树.取或创建实例特征_按抽象特征(
                reinterpret_cast<基础信息节点类*>(方法虚拟存在),
                自我能力抽象)
            : nullptr;
        if (实例特征
            && 世界树.写入特征_I64(实例特征, 自我能力值, now)) {
            (void)世界树.解析实例特征命中抽象特征(实例特征);
            return true;
        }

        return 世界树.写入特征_I64(
            reinterpret_cast<基础信息节点类*>(方法虚拟存在),
            私有_特征_自我能力(),
            自我能力值,
            now);
    }

