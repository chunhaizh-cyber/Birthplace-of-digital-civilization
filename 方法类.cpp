    void 私有_写入本能方法能力签名(
        方法主信息类& 主信息,
        枚举_本能方法ID 本能ID,
        const std::vector<结构_本能方法结果声明>& 声明集) noexcept
    {
        auto* 首信息 = 主信息.取首节点信息();
        if (!首信息) {
            return;
        }

        结构_方法能力签名 签名{};
        签名.能力键 = std::string("本能结果能力|") + std::to_string(static_cast<I64>(本能ID));
        签名.成熟度阶段 = 首信息->成熟度阶段;
        签名.可被方法查找命中 = false;

        for (const auto& 声明 : 声明集) {
            if (!声明.特征类型 && !声明.是否根写入原语) {
                continue;
            }
            结构_方法结果项 结果项{};
            结果项.特征类型 = 声明.特征类型;
            结果项.作用对象 = 私有_推断结果作用对象(声明.特征类型);
            结果项.方向 = 私有_结果变化方向枚举(声明);
            结果项.最小变化量 = 声明.方向 == 2 ? 0 : 声明.方向;
            结果项.是否主结果 = 声明.参与方法方向索引 && !声明.是否根写入原语;
            结果项.是否必要结果 = 声明.参与方法方向索引 && !声明.是否根写入原语;
            结果项.是否副结果 = !结果项.是否主结果 && !声明.是否根写入原语;
            签名.结果包.结果项集.push_back(std::move(结果项));
            签名.可被方法查找命中 = 签名.可被方法查找命中 || 声明.可被方法查找命中;
            签名.是否根写入原语 = 签名.是否根写入原语 || 声明.是否根写入原语;
        }

        首信息->能力.能力面.能力签名集.clear();
        if (!签名.结果包.结果项集.empty()) {
            首信息->能力.能力面.能力签名集.push_back(std::move(签名));
        }
    }

