方法类::节点类* 方法类::注册方法首节点(
    节点类* 方法根节点,
    const 词性节点类* 动作名,
    const 结构体_动作句柄& 动作句柄,
    枚举_方法作用域 作用域,
    const std::vector<std::string>& 影响度量签名,
    时间戳 now,
    const std::string& 调用点,
    const 词性节点类* 主结果特征类型)
{
    (void)调用点;
    if (!方法根节点) return nullptr;
    if (!动作名 && !动作句柄.有效() && !主结果特征类型) return nullptr;
    if (now == 0) now = 结构体_时间戳::当前_微秒();

    auto* 根节点 = 私有_取独立树根(方法根节点);
    auto* 已有节点 = 动作句柄.有效()
        ? 查找方法首节点_按动作句柄(根节点, 动作句柄)
        : nullptr;
    if (!已有节点 && 动作名) {
        已有节点 = 查找方法首节点_按动作名(根节点, 动作名);
    }
    if (!已有节点 && !动作名 && !动作句柄.有效() && 主结果特征类型) {
        已有节点 = 私有_查找需求位方法首节点_按主结果特征类型(根节点, 主结果特征类型);
    }

    if (!已有节点) {
        方法首节点主信息类 主信息{};
        主信息.动作名 = 动作名;
        主信息.动作句柄 = 动作句柄;
        主信息.主结果特征类型 = 主结果特征类型;
        if (动作句柄.类型 == 枚举_动作句柄类型::本能函数ID) {
            主信息.来源 = 枚举_方法来源::本能;
        }
        主信息.作用域 = 作用域;
        主信息.影响度量签名 = 影响度量签名;
        已有节点 = 私有_独立树添加方法子节点(根节点, 主信息);
    } else {
        auto& 主信息 = 已有节点->主信息;
        主信息.节点种类 = 枚举_方法节点种类::方法首节点;
        if (动作名) {
            主信息.动作名 = 动作名;
        }
        if (动作句柄.有效()) {
            主信息.动作句柄 = 动作句柄;
        }
        if (主结果特征类型 && !主信息.主结果特征类型) {
            主信息.主结果特征类型 = 主结果特征类型;
        }
        if (主信息.来源 == 枚举_方法来源::未定义
            && 主信息.动作句柄.类型 == 枚举_动作句柄类型::本能函数ID) {
            主信息.来源 = 枚举_方法来源::本能;
        }
        (void)按注册入口对齐方法作用域(已有节点, 作用域, 调用点 + "/对齐作用域");
        for (const auto& 签名 : 影响度量签名) {
            if (签名.empty()) continue;
            if (std::find(主信息.影响度量签名.begin(), 主信息.影响度量签名.end(), 签名)
                == 主信息.影响度量签名.end()) {
                主信息.影响度量签名.push_back(签名);
            }
        }
    }

    (void)初始化方法虚拟存在信息(已有节点, nullptr, now, "方法类::注册方法首节点/初始化方法虚拟存在");
    return 已有节点;
}

方法类::节点类* 方法类::注册需求位方法首节点(
    节点类* 方法根节点,
    const 词性节点类* 主结果特征类型,
    枚举_方法来源 来源,
    时间戳 now,
    const std::string& 调用点)
{
    auto* 节点 = 注册方法首节点(
        方法根节点,
        nullptr,
        结构体_动作句柄{},
        枚举_方法作用域::未定义,
        {},
        now,
        调用点 + "/最小建头",
        主结果特征类型);
    if (节点 && 来源 != 枚举_方法来源::未定义 && 节点->主信息.来源 == 枚举_方法来源::未定义) {
        节点->主信息.来源 = 来源;
    }
    return 节点;
}

方法类::节点类* 方法类::注册需求方向方法首节点(
    节点类* 方法根节点,
    const 词性节点类* 主结果特征类型,
    二次特征节点类* 结果方向,
    枚举_方法来源 来源,
    时间戳 now,
    const std::string& 调用点)
{
    if (!结果方向) {
        return 注册需求位方法首节点(
            方法根节点,
            主结果特征类型,
            来源,
            now,
            调用点 + "/无方向回退");
    }
    if (!方法根节点 || !主结果特征类型) {
        return nullptr;
    }
    if (now == 0) {
        now = 结构体_时间戳::当前_微秒();
    }

    auto* 根节点 = 私有_取独立树根(方法根节点);
    auto* 节点 = 私有_查找需求方向方法首节点(
        根节点,
        主结果特征类型,
        结果方向);
    if (!节点) {
        方法首节点主信息类 主信息{};
        主信息.来源 = 来源;
        主信息.主结果特征类型 = 主结果特征类型;
        主信息.结果方向索引.push_back(私有_生成引用(结果方向));
        const auto 影响签名 = 私有_需求方向影响签名(主结果特征类型, 结果方向);
        if (!影响签名.empty()) {
            主信息.影响度量签名.push_back(影响签名);
        }
        节点 = 私有_独立树添加方法子节点(根节点, 主信息);
    }
    else {
        auto& 主信息 = 节点->主信息;
        主信息.节点种类 = 枚举_方法节点种类::方法首节点;
        if (!主信息.主结果特征类型) {
            主信息.主结果特征类型 = 主结果特征类型;
        }
        if (来源 != 枚举_方法来源::未定义 && 主信息.来源 == 枚举_方法来源::未定义) {
            主信息.来源 = 来源;
        }
        if (!私有_方法包含结果方向(节点, 结果方向)) {
            主信息.结果方向索引.push_back(私有_生成引用(结果方向));
        }
        const auto 影响签名 = 私有_需求方向影响签名(主结果特征类型, 结果方向);
        if (!影响签名.empty()
            && std::find(主信息.影响度量签名.begin(), 主信息.影响度量签名.end(), 影响签名)
                == 主信息.影响度量签名.end()) {
            主信息.影响度量签名.push_back(影响签名);
        }
    }

    (void)初始化方法虚拟存在信息(节点, nullptr, now, "方法类::注册需求位方法首节点/初始化方法虚拟存在");
    return 节点;
}

std::size_t 方法类::补齐全部方法首节点到根链(
    节点类* 方法根节点,
    存在节点类* 宿主存在,
    时间戳 now,
    const std::string& 调用点)
{
    if (!方法根节点) return 0;
    if (now == 0) now = 结构体_时间戳::当前_微秒();

    auto* 根节点 = 私有_取独立树根(方法根节点);
    if (!根节点 || !根节点->子) return 0;

    std::size_t 已补齐数量 = 0;
    auto* 首节点 = static_cast<方法节点*>(根节点->子);
    auto* 当前 = 首节点;
    do {
        if (当前 && 当前->主信息.节点种类 == 枚举_方法节点种类::方法首节点) {
            已补齐数量 += 初始化方法虚拟存在信息(
                当前,
                宿主存在,
                now,
                调用点 + "/方法首节点") ? 1u : 0u;
        }
        当前 = static_cast<方法节点*>(当前->下);
    } while (当前 && 当前 != 首节点);

    return 已补齐数量;
}

方法类::节点类* 方法类::查找或创建_本能方法首节点(
    存在节点类* 宿主存在,
    枚举_本能方法ID 本能ID,
    时间戳 now,
    const std::string& 调用点)
{
    if (!宿主存在) return nullptr;
    if (now == 0) now = 结构体_时间戳::当前_微秒();

    const auto* 元信息 = 本能方法类::查询元信息(本能ID);
    if (!元信息) return nullptr;

    auto* 方法根节点 = 世界树.存在().获取方法根节点(宿主存在);
    if (!方法根节点) return nullptr;

    结构体_动作句柄 动作句柄{};
    动作句柄.类型 = 枚举_动作句柄类型::本能函数ID;
    动作句柄.本能ID = static_cast<I64>(本能ID);

    auto* 动作名 = 语素集.添加词性词(元信息->动作名称 ? 元信息->动作名称 : "", "动词");
    auto* 方法首节点 = 注册方法首节点(
        方法根节点,
        动作名,
        动作句柄,
        枚举_方法作用域::主体局部,
        {},
        now,
        调用点 + "/注册");
    if (!方法首节点) return nullptr;

    if (!补齐本能方法首节点(
        方法首节点,
        *元信息,
        宿主存在,
        now,
        调用点 + "/补齐首节点")) {
        return nullptr;
    }

    if (auto* 方法虚拟存在 = 取或创建_方法虚拟存在(方法首节点, 宿主存在, now, 调用点 + "/虚拟存在")) {
        const auto 方法类型 = 元信息->分类 == 枚举_本能方法分类::外部动作
            ? 枚举_方法类型::外部方法
            : 枚举_方法类型::内部方法;
        (void)私有_写入方法类型特征(方法虚拟存在, 方法类型, now);
    }
    return 方法首节点;
}

bool 方法类::补齐本能方法首节点(
    节点类* 方法首节点,
    const 结构_本能方法元信息& 元信息,
    存在节点类* 宿主存在,
    时间戳 now,
    const std::string& 调用点)
{
    auto* 首节点 = 私有_归一到方法首节点(方法首节点);
    if (!首节点) {
        return false;
    }
    if (now == 0) {
        now = 结构体_时间戳::当前_微秒();
    }

    auto& 主信息 = 首节点->主信息;
    主信息.节点种类 = 枚举_方法节点种类::方法首节点;
    主信息.动作名 = 语素集.添加词性词(元信息.动作名称 ? 元信息.动作名称 : "", "动词");
    主信息.动作句柄.类型 = 枚举_动作句柄类型::本能函数ID;
    主信息.动作句柄.本能ID = static_cast<I64>(元信息.ID);
    主信息.动作句柄.外部实现主键.clear();
    主信息.动作句柄.动作序列主键.clear();
    主信息.来源 = 枚举_方法来源::本能;
    私有_补齐本能方法结果声明元数据(主信息, 元信息);

    if (!初始化方法虚拟存在信息(首节点, 宿主存在, now, 调用点 + "/初始化")) {
        return false;
    }

    auto* 条件场景 = 取或创建_方法条件场景(首节点, 宿主存在, now, 调用点 + "/条件场景");
    if (!条件场景) {
        return false;
    }

    私有_确保本能方法条件特征(条件场景, 元信息);
    auto 条件索引 = 私有_场景二次特征索引(条件场景);
    if (条件索引.empty()) {
        return false;
    }

    私有_写入引用(主信息.条件场景, 条件场景);
    if (主信息.条件主键.empty()) {
        主信息.条件主键 = 条件场景->获取主键();
    }
    if (主信息.条件场景模板主键.empty()) {
        主信息.条件场景模板主键 = 条件场景->获取主键();
    }
    私有_吸收引用列表(主信息.条件判定索引, 条件索引);
    (void)创建条件节点(
        首节点,
        条件场景,
        主信息.条件判定索引,
        主信息.条件场景模板主键,
        now,
        now,
        调用点 + "/条件节点");
    auto* 结果场景 = 取或创建_方法结果场景(
        首节点,
        宿主存在,
        now,
        调用点 + "/结果场景");
    if (结果场景) {
        私有_写入引用(主信息.结果场景, 结果场景);
        if (主信息.结果主键.empty()) {
            主信息.结果主键 = 结果场景->获取主键();
        }
        私有_补齐本能方法结果方向索引(首节点, 结果场景, 元信息);
    }
    if (auto* 初始状态 = 私有_确保方法结果初始状态(
        首节点,
        宿主存在,
        条件场景,
        now,
        调用点 + "/结果初始状态")) {
        私有_吸收引用列表(
            主信息.结果初始状态,
            std::vector<可解析引用<状态节点类>>{ 私有_生成引用(初始状态) });
    }
    if (结果场景) {
        (void)创建结果节点(
            首节点,
            结果场景,
            主信息.结果方向索引,
            调用点 + "/结果节点");
    }
    return 私有_方法首节点具备可执行动作条件(首节点);
}

存在节点类* 方法类::取或创建_方法虚拟存在(
    节点类* 方法首节点,
    存在节点类* 宿主存在,
    时间戳 now,
    const std::string& 调用点)
{
    (void)调用点;
    auto* 首节点 = 私有_归一到方法首节点(方法首节点);
    if (!首节点) return nullptr;
    if (now == 0) now = 结构体_时间戳::当前_微秒();

    auto& 主信息 = 首节点->主信息;
    if (主信息.方法虚拟存在.指针) {
        return 主信息.方法虚拟存在.指针;
    }

    if (!宿主存在) {
        宿主存在 = 私有_推断宿主存在(首节点);
    }

    场景节点类* 承载世界 = nullptr;
    if (宿主存在) {
        承载世界 = 世界树.取或创建存在内部世界(宿主存在);
    }
    if (!承载世界) {
        if (!世界树.虚拟世界) {
            世界树.初始化默认世界();
        }
        承载世界 = 世界树.虚拟世界;
    }
    if (!承载世界) return nullptr;

    auto* 方法虚拟存在 = 世界树.存在().取或创建子存在_按名称类型(
        承载世界,
        私有_方法虚拟存在名称词(主信息),
        私有_方法虚拟存在类型词());
    if (!方法虚拟存在) return nullptr;

    私有_写入引用(主信息.方法虚拟存在, 方法虚拟存在);
    if (方法首节点 && 方法首节点 != 首节点 && !方法首节点->主信息.方法虚拟存在.指针) {
        私有_写入引用(方法首节点->主信息.方法虚拟存在, 方法虚拟存在);
    }

    (void)私有_写入方法类型特征(方法虚拟存在, 私有_推导方法类型(主信息), now);
    return 方法虚拟存在;
}

