方法类::节点类* 方法类::创建条件节点(
    节点类* 首节点,
    场景节点类* 条件场景模板,
    const std::vector<可解析引用<二次特征节点类>>& 条件判定索引,
    std::string 条件模板主键,
    时间戳 时间起,
    时间戳 时间止)
{
    auto* 方法首节点 = 私有_归一到方法首节点(首节点);
    if (!方法首节点) return nullptr;

    if (!条件场景模板) {
        if (auto* 条件子节点 = 私有_取首个条件子节点(方法首节点)) {
            if (auto* 条件信息 = 条件子节点->主信息.取条件节点信息()) {
                条件场景模板 = 条件信息->条件场景.指针;
            }
        }
    }
    if (条件模板主键.empty() && 条件场景模板) {
        条件模板主键 = 条件场景模板->获取主键();
    }

    auto 条件索引 = 条件判定索引;
    if (条件索引.empty()) {
        if (auto* 条件子节点 = 私有_取首个条件子节点(方法首节点)) {
            if (auto* 条件信息 = 条件子节点->主信息.取条件节点信息()) {
                条件索引 = 条件信息->条件判定索引;
            }
        }
    }
    if (条件索引.empty() && 条件场景模板) {
        条件索引 = 私有_场景二次特征索引(条件场景模板);
    }
    世界树.二次特征生成().确保二次特征列表具备默认值(条件索引);

    auto* 已有节点 = 私有_查找直接子方法节点_按谓词(方法首节点, [&](const 方法节点* 节点) {
        if (!节点 || 节点->主信息.节点种类() != 枚举_方法节点种类::方法条件节点) {
            return false;
        }
        const auto* 条件信息 = 节点->主信息.取条件节点信息();
        if (!条件信息) {
            return false;
        }
        if (!条件模板主键.empty()
            && (条件信息->条件主键 == 条件模板主键
                || 条件信息->条件场景模板主键 == 条件模板主键)) {
            return true;
        }
        if (条件场景模板 && 私有_场景状态语义等价(条件信息->条件场景.指针, 条件场景模板)) {
            return true;
        }
        return !条件索引.empty() && 私有_二次特征引用集合相等(条件信息->条件判定索引, 条件索引);
    });

    if (已有节点) {
        auto& 主信息 = 已有节点->主信息;
        auto* 条件信息 = 主信息.取条件节点信息();
        if (!条件信息) {
            return nullptr;
        }
        if (条件场景模板 && !条件信息->条件场景.指针) {
            私有_写入引用(条件信息->条件场景, 条件场景模板);
        }
        if (!条件模板主键.empty() && 条件信息->条件主键.empty()) {
            条件信息->条件主键 = 条件模板主键;
        }
        if (!条件模板主键.empty() && 条件信息->条件场景模板主键.empty()) {
            条件信息->条件场景模板主键 = std::move(条件模板主键);
        }
        私有_吸收引用列表(条件信息->条件判定索引, 条件索引);
        世界树.二次特征生成().确保二次特征列表具备默认值(条件信息->条件判定索引);
        if (时间起 != 0 && (条件信息->时间起 == 0 || 时间起 < 条件信息->时间起)) {
            条件信息->时间起 = 时间起;
        }
        if (时间止 != 0 && 时间止 > 条件信息->时间止) {
            条件信息->时间止 = 时间止;
        }
        return 已有节点;
    }

    auto 主信息 = 方法主信息类::构造条件节点();
    auto* 条件信息 = 主信息.取条件节点信息();
    if (!条件信息) {
        return nullptr;
    }
    if (条件场景模板) {
        私有_写入引用(条件信息->条件场景, 条件场景模板);
    }
    条件信息->条件主键 = 条件模板主键;
    条件信息->条件场景模板主键 = std::move(条件模板主键);
    条件信息->时间起 = 时间起;
    条件信息->时间止 = 时间止;
    条件信息->条件判定索引 = std::move(条件索引);

    return 私有_独立树添加方法子节点(方法首节点, 主信息);
}

方法类::节点类* 方法类::创建结果节点(
    节点类* 父节点,
    场景节点类* 结果场景模板,
    const std::vector<可解析引用<二次特征节点类>>& 结果方向索引)
{
    if (!父节点) return nullptr;

    auto* 方法首节点 = 私有_归一到方法首节点(父节点);
    if (!方法首节点) return nullptr;

    if (!结果场景模板) {
        if (auto* 结果子节点 = 私有_取首个结果子节点(方法首节点)) {
            if (auto* 结果信息 = 结果子节点->主信息.取结果节点信息()) {
                结果场景模板 = 结果信息->结果场景.指针;
            }
        }
    }

    const auto 结果主键 = 结果场景模板 ? 结果场景模板->获取主键() : std::string{};
    auto 方向索引 = 结果方向索引;
    if (方向索引.empty() && 结果场景模板) {
        方向索引 = 私有_场景二次特征索引(结果场景模板);
    }

    auto* 已有节点 = 私有_查找直接子方法节点_按谓词(static_cast<方法节点*>(父节点), [&](const 方法节点* 节点) {
        if (!节点 || 节点->主信息.节点种类() != 枚举_方法节点种类::方法结果节点) {
            return false;
        }
        const auto* 结果信息 = 节点->主信息.取结果节点信息();
        if (!结果信息) {
            return false;
        }
        if (!结果主键.empty() && 结果信息->结果主键 == 结果主键) {
            return true;
        }
        if (结果场景模板 && 私有_场景状态语义等价(结果信息->结果场景.指针, 结果场景模板)) {
            return true;
        }
        return false;
    });

    if (已有节点) {
        auto& 主信息 = 已有节点->主信息;
        auto* 结果信息 = 主信息.取结果节点信息();
        if (!结果信息) {
            return nullptr;
        }
        if (结果场景模板 && !结果信息->结果场景.指针) {
            私有_写入引用(结果信息->结果场景, 结果场景模板);
        }
        if (!结果主键.empty() && 结果信息->结果主键.empty()) {
            结果信息->结果主键 = 结果主键;
        }
        私有_吸收结果节点方向索引(
            主信息,
            方法首节点->主信息.首个结果能力特征类型(),
            方向索引);
        return 已有节点;
    }

    auto 主信息 = 方法主信息类::构造结果节点();
    auto* 结果信息 = 主信息.取结果节点信息();
    if (!结果信息) {
        return nullptr;
    }
    if (结果场景模板) {
        私有_写入引用(结果信息->结果场景, 结果场景模板);
    }
    结果信息->结果主键 = 结果主键;
    私有_吸收结果节点方向索引(
        主信息,
        方法首节点->主信息.首个结果能力特征类型(),
        方向索引);

    return 私有_独立树添加方法子节点(static_cast<方法节点*>(父节点), 主信息);
}
