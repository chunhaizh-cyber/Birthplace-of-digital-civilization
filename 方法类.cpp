bool 方法类::记录方法虚拟存在特征(
    节点类* 方法首节点,
    const 语素入口节点类* 特征类型,
    const 特征值& 值,
    存在节点类* 宿主存在,
    时间戳 now)
{
    if (!特征类型) return false;
    if (now == 0) now = 结构体_时间戳::当前_微秒();

    auto* 方法虚拟存在 = 取或创建_方法虚拟存在(方法首节点, 宿主存在, now);
    if (!方法虚拟存在) return false;

    if (const auto* 标量值 = std::get_if<I64>(&值)) {
        return 世界树.写入特征_I64(方法虚拟存在, 特征类型, *标量值, now);
    }
    if (const auto* 指针值 = std::get_if<指针句柄>(&值)) {
        return 世界树.写入特征_指针(
            方法虚拟存在,
            特征类型,
            reinterpret_cast<const void*>(指针值->指针),
            now);
    }
    if (const auto* Vec句柄 = std::get_if<VecU句柄>(&值)) {
        const auto* Vec值 = 世界树.值池().取VecU只读指针(*Vec句柄);
        return Vec值
            ? 世界树.写入特征_VecU(方法虚拟存在, 特征类型, *Vec值, 枚举_轮廓维度::未定义, now)
            : false;
    }
    return false;
}

