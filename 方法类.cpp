    void 私有_补齐本能方法结果方向索引(
        方法节点* 方法首节点,
        场景节点类* 结果场景,
        枚举_本能方法ID 本能ID) noexcept
    {
        if (!方法首节点 || !结果场景) {
            return;
        }

        const auto 声明集 = 私有_查询本能方法结果声明集(本能ID);
        if (声明集.empty()) {
            return;
        }

        for (const auto& 声明 : 声明集) {
            if (!声明.特征类型 && !声明.是否根写入原语) {
                continue;
            }

            const auto 链键 = 私有_本能结果方向链键(声明);
            const auto 特征键 = 声明.特征类型 ? 声明.特征类型->获取主键() : std::string{};
            const auto 描述词面 = 私有_结果方向描述文本(声明);
            const auto 概念文本 = 特征键.empty()
                ? std::string("结果方向_") + 描述词面
                : std::string("结果方向_") + 特征键 + "_" + 描述词面;
            auto* 概念入口 = 语素集.添加信息入口词(
                概念文本,
                枚举_信息入口类型::关系模板入口);
            // 场景实例使用组合概念入口；方向描述入口自身会对应抽象二次特征概念。
            auto* 方向节点 = 世界树.二次特征生成().取或创建场景条件二次特征_枚举(
                结果场景,
                链键,
                声明.方向,
                声明.方向描述,
                概念入口);
            if (!方向节点) {
                continue;
            }

            (void)世界树.二次特征().写入差值结果(方向节点, 0, 声明.方向);
            if (auto* 方向主信息 = 世界树.二次特征().取二次特征主信息(方向节点)) {
                方向主信息->形态 = 二次特征主信息类::枚举_形态::结果;
            }
        }
    }

    bool 私有_二次特征引用相等(
        const 可解析引用<二次特征节点类>& 左,
        const 可解析引用<二次特征节点类>& 右)
    {
        const auto 左键 = 私有_引用键(左);
        const auto 右键 = 私有_引用键(右);
        if (!左键.empty() || !右键.empty()) {
            return !左键.empty() && 左键 == 右键;
        }
        return 左.指针 && 左.指针 == 右.指针;
    }

    bool 私有_二次特征引用集合相等(
        const std::vector<可解析引用<二次特征节点类>>& 左,
        const std::vector<可解析引用<二次特征节点类>>& 右)
    {
        if (左.size() != 右.size()) return false;
        if (左.empty()) return true;

        for (const auto& 左项 : 左) {
            const bool 命中 = std::any_of(右.begin(), 右.end(), [&](const auto& 右项) {
                return 私有_二次特征引用相等(左项, 右项);
            });
            if (!命中) return false;
        }
        return true;
    }

