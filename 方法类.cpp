    状态节点类* 私有_查找场景状态_按主体特征(
        场景节点类* 场景,
        基础信息节点类* 状态主体,
        特征节点类* 状态特征) noexcept
    {
        if (!场景 || !状态主体 || !状态特征) {
            return nullptr;
        }

        const auto 主体主键 = 状态主体->获取主键();
        const auto 特征主键 = 状态特征->获取主键();
        for (auto* 状态节点 : 世界树.状态().获取场景状态(场景)) {
            const auto* 状态主信息 = 世界树.状态().取状态主信息(状态节点);
            if (!状态主信息) {
                continue;
            }

            const bool 主体命中 =
                状态主信息->状态主体.指针 == 状态主体
                || (!主体主键.empty() && 状态主信息->状态主体.主键 == 主体主键);
            const bool 特征命中 =
                状态主信息->状态特征.指针 == 状态特征
                || (!特征主键.empty() && 状态主信息->状态特征.主键 == 特征主键);
            if (主体命中 && 特征命中) {
                return 状态节点;
            }
        }
        return nullptr;
    }

