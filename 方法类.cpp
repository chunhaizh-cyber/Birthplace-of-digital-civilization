方法类::节点类* 方法类::创建条件节点(
    节点类* 首节点,
    场景节点类* 条件场景模板,
    const std::vector<可解析引用<二次特征节点类>>& 条件判定索引,
    std::string 条件模板主键,
    时间戳 时间起,
    时间戳 时间止,
    const std::string& 调用点)
{
    (void)调用点;
    auto* 方法首节点 = 私有_归一到方法首节点(首节点);
    if (!方法首节点) return nullptr;

    if (!条件场景模板 && 方法首节点->主信息.条件场景.指针) {
        条件场景模板 = 方法首节点->主信息.条件场景.指针;
    }
    if (条件模板主键.empty() && 条件场景模板) {
        条件模板主键 = 条件场景模板->获取主键();
    }

    auto 条件索引 = 条件判定索引;
    if (条件索引.empty()) {
        条件索引 = 方法首节点->主信息.条件判定索引;
    }
    if (条件索引.empty() && 条件场景模板) {
        条件索引 = 私有_场景二次特征索引(条件场景模板);
    }
    世界树.二次特征生成().确保二次特征列表具备默认值(条件索引);

    auto* 已有节点 = 私有_查找直接子方法节点_按谓词(方法首节点, [&](const 方法节点* 节点) {
        if (!节点 || 节点->主信息.节点种类 != 枚举_方法节点种类::方法条件节点) {
            return false;
        }
        if (!条件模板主键.empty()
            && (节点->主信息.条件主键 == 条件模板主键
                || 节点->主信息.条件场景模板主键 == 条件模板主键)) {
            return true;
        }
        if (条件场景模板 && 私有_引用键(节点->主信息.条件场景) == 条件场景模板->获取主键()) {
            return true;
        }
        return !条件索引.empty() && 私有_二次特征引用集合相等(节点->主信息.条件判定索引, 条件索引);
    });

    if (已有节点) {
        auto& 主信息 = 已有节点->主信息;
        if (条件场景模板 && !主信息.条件场景.指针) {
            私有_写入引用(主信息.条件场景, 条件场景模板);
        }
        if (!条件模板主键.empty() && 主信息.条件主键.empty()) {
            主信息.条件主键 = 条件模板主键;
        }
        if (!条件模板主键.empty() && 主信息.条件场景模板主键.empty()) {
            主信息.条件场景模板主键 = std::move(条件模板主键);
        }
        私有_吸收引用列表(主信息.条件判定索引, 条件索引);
        世界树.二次特征生成().确保二次特征列表具备默认值(主信息.条件判定索引);
        if (时间起 != 0 && (主信息.时间起 == 0 || 时间起 < 主信息.时间起)) {
            主信息.时间起 = 时间起;
        }
        if (时间止 != 0 && 时间止 > 主信息.时间止) {
            主信息.时间止 = 时间止;
        }
        return 已有节点;
    }

    方法条件节点主信息类 主信息{};
    主信息.动作名 = 方法首节点->主信息.动作名;
    主信息.动作句柄 = 方法首节点->主信息.动作句柄;
    主信息.来源 = 方法首节点->主信息.来源;
    主信息.作用域 = 方法首节点->主信息.作用域;
    主信息.主结果特征类型 = 方法首节点->主信息.主结果特征类型;
    主信息.结果初始状态 = 方法首节点->主信息.结果初始状态;
    主信息.条件场景 = 条件场景模板 ? 私有_生成引用(条件场景模板) : 方法首节点->主信息.条件场景;
    主信息.条件主键 = 条件模板主键;
    主信息.条件场景模板主键 = std::move(条件模板主键);
    主信息.时间起 = 时间起;
    主信息.时间止 = 时间止;
    主信息.条件判定索引 = std::move(条件索引);
    私有_写入方法节点引用(主信息.父方法, 方法首节点);

    return 私有_独立树添加方法子节点(方法首节点, 主信息);
}

方法类::节点类* 方法类::创建结果节点(
    节点类* 父节点,
    场景节点类* 结果场景模板,
    const std::vector<可解析引用<二次特征节点类>>& 结果方向索引,
    const std::string& 调用点)
{
    (void)调用点;
    if (!父节点) return nullptr;

    auto* 方法首节点 = 私有_归一到方法首节点(父节点);
    if (!方法首节点) return nullptr;

    if (!结果场景模板 && 方法首节点->主信息.结果场景.指针) {
        结果场景模板 = 方法首节点->主信息.结果场景.指针;
    }

    const auto 结果主键 = 结果场景模板 ? 结果场景模板->获取主键() : std::string{};
    auto 方向索引 = 结果方向索引;
    if (方向索引.empty() && 结果场景模板) {
        方向索引 = 私有_场景二次特征索引(结果场景模板);
    }

    auto* 已有节点 = 私有_查找直接子方法节点_按谓词(static_cast<方法节点*>(父节点), [&](const 方法节点* 节点) {
        if (!节点 || 节点->主信息.节点种类 != 枚举_方法节点种类::方法结果节点) {
            return false;
        }
        if (!结果主键.empty() && 节点->主信息.结果主键 == 结果主键) {
            return true;
        }
        if (结果场景模板 && 私有_引用键(节点->主信息.结果场景) == 结果场景模板->获取主键()) {
            return true;
        }
        return !方向索引.empty() && 私有_二次特征引用集合相等(节点->主信息.结果方向索引, 方向索引);
    });

    if (已有节点) {
        auto& 主信息 = 已有节点->主信息;
        if (结果场景模板 && !主信息.结果场景.指针) {
            私有_写入引用(主信息.结果场景, 结果场景模板);
        }
        if (!结果主键.empty() && 主信息.结果主键.empty()) {
            主信息.结果主键 = 结果主键;
        }
        私有_吸收引用列表(主信息.结果方向索引, 方向索引);
        return 已有节点;
    }

    方法结果节点主信息类 主信息{};
    主信息.动作名 = 方法首节点->主信息.动作名;
    主信息.动作句柄 = 方法首节点->主信息.动作句柄;
    主信息.来源 = 方法首节点->主信息.来源;
    主信息.作用域 = 方法首节点->主信息.作用域;
    主信息.主结果特征类型 = 方法首节点->主信息.主结果特征类型;
    主信息.结果初始状态 = 方法首节点->主信息.结果初始状态;
    主信息.结果场景 = 结果场景模板 ? 私有_生成引用(结果场景模板) : 方法首节点->主信息.结果场景;
    主信息.结果主键 = 结果主键;
    主信息.结果方向索引 = std::move(方向索引);
    私有_写入方法节点引用(主信息.父方法, static_cast<方法节点*>(父节点));

    return 私有_独立树添加方法子节点(static_cast<方法节点*>(父节点), 主信息);
}
