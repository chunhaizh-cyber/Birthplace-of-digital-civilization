    方法节点* 私有_查找需求方向方法首节点(
        方法节点* 方法根节点,
        const 语素入口节点类* 主结果特征类型,
        const 二次特征节点类* 结果方向) noexcept
    {
        if (!方法根节点 || !主结果特征类型 || !结果方向) return nullptr;

        auto* 首节点 = static_cast<方法节点*>(方法根节点->子);
        if (!首节点) return nullptr;

        方法节点* 待学习占位方法 = nullptr;
        auto* 当前 = 首节点;
        do {
            if (当前
                && 当前->主信息.公共.节点种类 == 枚举_方法节点种类::方法首节点
                && 私有_方法首节点包含结果能力(当前, 主结果特征类型, 结果方向)) {
                if (当前->主信息.有动作骨架()) {
                    return 当前;
                }
                if (!待学习占位方法) {
                    待学习占位方法 = 当前;
                }
            }
            当前 = static_cast<方法节点*>(当前->下);
        } while (当前 && 当前 != 首节点);

        return 待学习占位方法;
    }

