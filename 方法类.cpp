    template<class T节点>
    std::string 私有_引用键(const 可解析引用<T节点>& 引用)
    {
        if (!引用.主键.empty()) {
            return 引用.主键;
        }
        return 引用.指针 ? 引用.指针->获取主键() : std::string{};
    }

    template<class T节点>
    bool 私有_引用已存在(const std::vector<可解析引用<T节点>>& 列表, T节点* 节点)
    {
        if (!节点) return false;
        const auto 节点主键 = 节点->获取主键();
        for (const auto& 项 : 列表) {
            if (项.指针 == 节点) return true;
            if (!项.主键.empty() && 项.主键 == 节点主键) return true;
        }
        return false;
    }

    template<class T节点>
    void 私有_吸收引用列表(std::vector<可解析引用<T节点>>& 目标, const std::vector<可解析引用<T节点>>& 来源)
    {
        for (const auto& 项 : 来源) {
            auto* 节点 = 项.指针;
            if (节点) {
                if (!私有_引用已存在(目标, 节点)) {
                    目标.push_back(私有_生成引用(节点));
                }
                continue;
            }

            if (项.主键.empty()) continue;
            const bool 已存在 = std::any_of(目标.begin(), 目标.end(), [&](const auto& 已有) {
                return 私有_引用键(已有) == 项.主键;
            });
            if (!已存在) {
                目标.push_back(项);
            }
        }
    }

    template<class T节点>
    T节点* 私有_取独立树根(T节点* 节点) noexcept
    {
        while (节点 && 节点->父) {
            节点 = static_cast<T节点*>(节点->父);
        }
        return 节点;
    }

    方法节点* 私有_归一到方法首节点(方法节点* 节点) noexcept
    {
        auto* 当前 = 节点;
        while (当前) {
            if (当前->主信息.节点种类 == 枚举_方法节点种类::方法首节点) {
                return 当前;
            }
            当前 = static_cast<方法节点*>(当前->父);
        }
        return nullptr;
    }

    template<class T谓词>
    方法节点* 私有_查找直接子方法节点_按谓词(方法节点* 父节点, T谓词&& 谓词) noexcept
    {
        if (!父节点 || !父节点->子) return nullptr;

        auto* 首节点 = static_cast<方法节点*>(父节点->子);
        auto* 当前 = 首节点;
        do {
            if (当前 && 谓词(当前)) {
                return 当前;
            }
            当前 = static_cast<方法节点*>(当前->下);
        } while (当前 && 当前 != 首节点);
        return nullptr;
    }

    方法节点* 私有_独立树添加方法子节点(方法节点* 父节点, const 方法信息基类& 主信息)
    {
        if (!父节点) return nullptr;

        auto* 独立根 = 私有_取独立树根(父节点);
        if (!独立根) return nullptr;

        方法类 临时树{};
        auto* 原临时根 = 临时树.根指针;
        临时树.根指针 = 独立根;

        auto* 新节点 = 临时树.添加子节点(父节点, 主信息);

        临时树.根指针 = 原临时根;
        return static_cast<方法节点*>(新节点);
    }

    std::string 私有_动作句柄键(const 结构体_动作句柄& 句柄)
    {
        switch (句柄.类型) {
        case 枚举_动作句柄类型::本能函数ID:
            return 句柄.本能ID != 0 ? std::string("innate_") + std::to_string(句柄.本能ID) : std::string{};
        case 枚举_动作句柄类型::外部实现主键:
            return 句柄.外部实现主键.empty() ? std::string{} : std::string("ext_") + 句柄.外部实现主键;
        case 枚举_动作句柄类型::动作序列主键:
            return 句柄.动作序列主键.empty() ? std::string{} : std::string("seq_") + 句柄.动作序列主键;
        default:
            return {};
        }
    }

    std::string 私有_方法标识文本(const 方法信息基类& 主信息)
    {
        std::string 文本{};
        if (主信息.动作名) {
            文本 = 主信息.动作名->获取主键();
        }

        if (文本.empty() && 主信息.主结果特征类型) {
            const auto 结果特征键 = 主信息.主结果特征类型->获取主键();
            if (!结果特征键.empty()) {
                文本 = 结果特征键;
            }
        }

        const auto 句柄键 = 私有_动作句柄键(主信息.动作句柄);
        if (文本.empty()) {
            return 句柄键.empty() ? std::string("未命名方法") : 句柄键;
        }
        if (句柄键.empty()) {
            return 文本;
        }
        return 文本 + "_" + 句柄键;
    }

