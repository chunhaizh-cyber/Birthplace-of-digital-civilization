    bool 私有_动作句柄相等(const 结构体_动作句柄& 左, const 结构体_动作句柄& 右) noexcept
    {
        return 左.类型 == 右.类型
            && 左.本能ID == 右.本能ID
            && 左.外部实现主键 == 右.外部实现主键
            && 左.动作序列主键 == 右.动作序列主键;
    }

    bool 私有_语素入口主键相等(const 语素入口节点类* 左, const 语素入口节点类* 右) noexcept
    {
        if (左 == 右) return true;
        if (!左 || !右) return false;
        const auto 左主键 = 左->获取主键();
        const auto 右主键 = 右->获取主键();
        return !左主键.empty() && 左主键 == 右主键;
    }

    template<class T节点>
    void 私有_写入引用(可解析引用<T节点>& 输出, T节点* 节点) noexcept
    {
        输出.指针 = 节点;
        输出.主键 = 节点 ? 节点->获取主键() : std::string{};
    }

    template<class T节点>
    可解析引用<T节点> 私有_生成引用(T节点* 节点)
    {
        可解析引用<T节点> 输出{};
        私有_写入引用(输出, 节点);
        return 输出;
    }

    template<class T节点>
    std::string 私有_引用键(const 可解析引用<T节点>& 引用)
    {
        if (!引用.主键.empty()) {
            return 引用.主键;
        }
        return 引用.指针 ? 引用.指针->获取主键() : std::string{};
    }

    bool 私有_场景状态语义等价(const 场景节点类* 左, const 场景节点类* 右) noexcept
    {
        if (!左 || !右) return false;
        const auto 左状态集 = 世界树.状态().获取场景状态(左);
        const auto 右状态集 = 世界树.状态().获取场景状态(右);
        if (左状态集.empty() && 右状态集.empty()) {
            return 左 == 右 || 左->获取主键() == 右->获取主键();
        }
        if (左状态集.size() != 右状态集.size()) {
            return false;
        }
        for (const auto* 左状态 : 左状态集) {
            bool 已匹配 = false;
            for (const auto* 右状态 : 右状态集) {
                const auto 比较 = 世界树.特征().比较状态(左状态, 右状态);
                if (比较.可比较 && 比较.关系 == 枚举_三向关系::等于) {
                    已匹配 = true;
                    break;
                }
            }
            if (!已匹配) {
                return false;
            }
        }
        return true;
    }

    template<class T节点>
    bool 私有_引用已存在(const std::vector<可解析引用<T节点>>& 列表, T节点* 节点)
    {
        if (!节点) return false;
        const auto 节点主键 = 节点->获取主键();
        for (const auto& 项 : 列表) {
            if (项.指针 == 节点) return true;
            if (!项.主键.empty() && 项.主键 == 节点主键) return true;
        }
        return false;
    }

    template<class T节点>
    void 私有_吸收引用列表(std::vector<可解析引用<T节点>>& 目标, const std::vector<可解析引用<T节点>>& 来源)
    {
        for (const auto& 项 : 来源) {
            auto* 节点 = 项.指针;
            if (节点) {
                if (!私有_引用已存在(目标, 节点)) {
                    目标.push_back(私有_生成引用(节点));
                }
                continue;
            }

            if (项.主键.empty()) continue;
            const bool 已存在 = std::any_of(目标.begin(), 目标.end(), [&](const auto& 已有) {
                return 私有_引用键(已有) == 项.主键;
            });
            if (!已存在) {
                目标.push_back(项);
            }
        }
    }

    template<class T节点>
    T节点* 私有_取独立树根(T节点* 节点) noexcept
    {
        while (节点 && 节点->父) {
            节点 = static_cast<T节点*>(节点->父);
        }
        return 节点;
    }

    方法节点* 私有_归一到方法首节点(方法节点* 节点) noexcept
    {
        auto* 当前 = 节点;
        while (当前) {
            if (当前->主信息.公共.节点种类 == 枚举_方法节点种类::方法首节点) {
                return 当前;
            }
            当前 = static_cast<方法节点*>(当前->父);
        }
        return nullptr;
    }

    template<class T谓词>
    方法节点* 私有_查找直接子方法节点_按谓词(方法节点* 父节点, T谓词&& 谓词) noexcept
    {
        if (!父节点 || !父节点->子) return nullptr;

        auto* 首节点 = static_cast<方法节点*>(父节点->子);
        auto* 当前 = 首节点;
        do {
            if (当前 && 谓词(当前)) {
                return 当前;
            }
            当前 = static_cast<方法节点*>(当前->下);
        } while (当前 && 当前 != 首节点);
        return nullptr;
    }

    方法节点* 私有_取首个条件子节点(方法节点* 方法首节点) noexcept
    {
        return 私有_查找直接子方法节点_按谓词(方法首节点, [](const 方法节点* 节点) noexcept {
            return 节点
                && 节点->主信息.节点种类() == 枚举_方法节点种类::方法条件节点
                && 节点->主信息.取条件节点信息();
        });
    }

    方法节点* 私有_取首个结果子节点(方法节点* 方法首节点) noexcept
    {
        return 私有_查找直接子方法节点_按谓词(方法首节点, [](const 方法节点* 节点) noexcept {
            return 节点
                && 节点->主信息.节点种类() == 枚举_方法节点种类::方法结果节点
                && 节点->主信息.取结果节点信息();
        });
    }

    方法节点* 私有_独立树添加方法子节点(方法节点* 父节点, const 方法主信息类& 主信息)
    {
        if (!父节点) return nullptr;

        auto* 独立根 = 私有_取独立树根(父节点);
        if (!独立根) return nullptr;

        方法类 临时树{};
        auto* 原临时根 = 临时树.根指针;
        临时树.根指针 = 独立根;

        auto* 新节点 = 临时树.添加子节点(父节点, 主信息);

        临时树.根指针 = 原临时根;
        return static_cast<方法节点*>(新节点);
    }

    std::string 私有_动作句柄键(const 结构体_动作句柄& 句柄)
    {
        switch (句柄.类型) {
        case 枚举_动作句柄类型::本能函数ID:
            return 句柄.本能ID != 0 ? std::string("innate_") + std::to_string(句柄.本能ID) : std::string{};
        case 枚举_动作句柄类型::外部实现主键:
            return 句柄.外部实现主键.empty() ? std::string{} : std::string("ext_") + 句柄.外部实现主键;
        case 枚举_动作句柄类型::动作序列主键:
            return 句柄.动作序列主键.empty() ? std::string{} : std::string("seq_") + 句柄.动作序列主键;
        default:
            return {};
        }
    }

    std::string 私有_方法标识文本(const 方法主信息类& 主信息)
    {
        std::string 文本{};
        if (主信息.首节点信息().动作名) {
            文本 = 主信息.首节点信息().动作名->获取主键();
        }

        if (文本.empty()) {
            const auto* 结果特征类型 = 主信息.首个结果能力特征类型();
            const auto 结果特征键 = 结果特征类型 ? 结果特征类型->获取主键() : std::string{};
            if (!结果特征键.empty()) {
                文本 = 结果特征键;
            }
        }

        const auto 句柄键 = 私有_动作句柄键(主信息.首节点信息().动作句柄);
        if (文本.empty()) {
            return 句柄键.empty() ? std::string("未命名方法") : 句柄键;
        }
        if (句柄键.empty()) {
            return 文本;
        }
        return 文本 + "_" + 句柄键;
    }

