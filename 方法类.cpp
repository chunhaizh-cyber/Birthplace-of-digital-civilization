方法类::节点类* 方法类::注册方法首节点(
    节点类* 方法根节点,
    const 语素入口节点类* 动作名,
    const 结构体_动作句柄& 动作句柄,
    时间戳 now,
    const 语素入口节点类* 主结果特征类型)
{
    if (!方法根节点) return nullptr;
    if (!动作名 && !动作句柄.有效() && !主结果特征类型) return nullptr;
    if (now == 0) now = 结构体_时间戳::当前_微秒();

    auto* 根节点 = 私有_取独立树根(方法根节点);
    auto* 已有节点 = 动作句柄.有效()
        ? 查找方法首节点_按动作句柄(根节点, 动作句柄)
        : nullptr;
    if (!已有节点 && 动作名) {
        已有节点 = 查找方法首节点_按动作名(根节点, 动作名);
    }
    if (!已有节点 && !动作名 && !动作句柄.有效() && 主结果特征类型) {
        已有节点 = 私有_查找需求位方法首节点_按主结果特征类型(根节点, 主结果特征类型);
    }

    if (!已有节点) {
        auto 主信息 = 方法主信息类::构造首节点(动作名, 动作句柄);
        if (主结果特征类型) {
            结构_方法结果项 结果项{};
            结果项.特征类型 = 主结果特征类型;
            私有_追加首节点结果项(
                主信息,
                std::move(结果项),
                true,
                false);
        }
        if (动作句柄.类型 == 枚举_动作句柄类型::本能函数ID) {
            主信息.首节点信息().来源 = 枚举_方法来源::本能;
        }
        已有节点 = 私有_独立树添加方法子节点(根节点, 主信息);
    } else {
        auto& 主信息 = 已有节点->主信息;
        主信息.公共.节点种类 = 枚举_方法节点种类::方法首节点;
        if (动作名) {
            主信息.首节点信息().动作名 = 动作名;
        }
        if (动作句柄.有效()) {
            主信息.首节点信息().动作句柄 = 动作句柄;
        }
        if (主结果特征类型
            && !私有_方法首节点包含结果能力(已有节点, 主结果特征类型, nullptr)) {
            结构_方法结果项 结果项{};
            结果项.特征类型 = 主结果特征类型;
            私有_追加首节点结果项(
                主信息,
                std::move(结果项),
                true,
                false);
        }
        if (主信息.首节点信息().来源 == 枚举_方法来源::未定义
            && 主信息.首节点信息().动作句柄.类型 == 枚举_动作句柄类型::本能函数ID) {
            主信息.首节点信息().来源 = 枚举_方法来源::本能;
        }
    }

    (void)方法虚拟存在服务类::同步方法节点到虚拟存在(已有节点, nullptr, now);
    return 已有节点;
}

