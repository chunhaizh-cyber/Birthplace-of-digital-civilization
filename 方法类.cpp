    void 私有_补齐本能方法结果声明元数据(
        方法信息基类& 主信息,
        const 结构_本能方法元信息& 元信息) noexcept
    {
        const auto 声明集 = 私有_查询本能方法结果声明集(元信息.ID);
        if (声明集.empty()) {
            return;
        }

        for (const auto& 声明 : 声明集) {
            if (!声明.特征类型) {
                continue;
            }
            if (!主信息.主结果特征类型 && 声明.参与方法方向索引) {
                主信息.主结果特征类型 = 声明.特征类型;
            }
            const auto 签名 = 私有_本能结果方向签名(声明);
            if (std::find(主信息.影响度量签名.begin(), 主信息.影响度量签名.end(), 签名)
                == 主信息.影响度量签名.end()) {
                主信息.影响度量签名.push_back(签名);
            }
        }
    }

    void 私有_补齐本能方法结果方向索引(
        方法节点* 方法首节点,
        场景节点类* 结果场景,
        const 结构_本能方法元信息& 元信息) noexcept
    {
        if (!方法首节点 || !结果场景) {
            return;
        }

        const auto 声明集 = 私有_查询本能方法结果声明集(元信息.ID);
        if (声明集.empty()) {
            return;
        }

        auto& 方向索引 = 方法首节点->主信息.结果方向索引;
        for (const auto& 声明 : 声明集) {
            if (!声明.特征类型) {
                continue;
            }

            const auto 签名 = 私有_本能结果方向签名(声明);
            const auto 特征键 = 声明.特征类型->获取主键();
            const auto 概念文本 = 特征键.empty()
                ? std::string("结果方向_") + 声明.方向文本
                : std::string("结果方向_") + 特征键 + "_" + 声明.方向文本;
            auto* 方向节点 = 世界树.二次特征生成().取或创建场景条件二次特征_枚举(
                结果场景,
                签名,
                声明.方向,
                声明.方向文本,
                概念文本.c_str());
            if (!方向节点) {
                continue;
            }

            (void)世界树.二次特征().写入差值结果(方向节点, 0, 声明.方向);
            if (auto* 方向主信息 = 世界树.二次特征().取二次特征主信息(方向节点)) {
                方向主信息->形态 = 二次特征主信息类::枚举_形态::结果;
            }
            if (声明.参与方法方向索引 && !私有_引用已存在(方向索引, 方向节点)) {
                方向索引.push_back(私有_生成引用(方向节点));
            }
        }
    }

