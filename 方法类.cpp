    方法节点* 私有_查找需求位方法首节点_按主结果特征类型(
        方法节点* 方法根节点,
        const 词性节点类* 主结果特征类型) noexcept
    {
        if (!方法根节点 || !主结果特征类型) return nullptr;

        auto* 首节点 = static_cast<方法节点*>(方法根节点->子);
        if (!首节点) return nullptr;

        auto* 当前 = 首节点;
        do {
            if (当前
                && 当前->主信息.节点种类 == 枚举_方法节点种类::方法首节点
                && 当前->主信息.是仅结果特征需求位()
                && 当前->主信息.结果方向索引.empty()
                && 私有_词性主键相等(当前->主信息.主结果特征类型, 主结果特征类型)) {
                return 当前;
            }
            当前 = static_cast<方法节点*>(当前->下);
        } while (当前 && 当前 != 首节点);

        return nullptr;
    }

