方法类::节点类* 方法类::注册方法首节点(
    节点类* 方法根节点,
    const 语素入口节点类* 动作名,
    const 结构体_动作句柄& 动作句柄,
    枚举_方法作用域 作用域,
    时间戳 now,
    const 语素入口节点类* 主结果特征类型)
{
    if (!方法根节点) return nullptr;
    if (!动作名 && !动作句柄.有效() && !主结果特征类型) return nullptr;
    if (now == 0) now = 结构体_时间戳::当前_微秒();

    auto* 根节点 = 私有_取独立树根(方法根节点);
    auto* 已有节点 = 动作句柄.有效()
        ? 查找方法首节点_按动作句柄(根节点, 动作句柄)
        : nullptr;
    if (!已有节点 && 动作名) {
        已有节点 = 查找方法首节点_按动作名(根节点, 动作名);
    }
    if (!已有节点 && !动作名 && !动作句柄.有效() && 主结果特征类型) {
        已有节点 = 私有_查找需求位方法首节点_按主结果特征类型(根节点, 主结果特征类型);
    }

    if (!已有节点) {
        auto 主信息 = 方法主信息类::构造首节点(动作名, 动作句柄);
        if (主结果特征类型) {
            结构_方法结果项 结果项{};
            结果项.特征类型 = 主结果特征类型;
            结果项.作用对象 = 私有_推断结果作用对象(主结果特征类型);
            私有_追加首节点结果项(
                主信息,
                std::move(结果项),
                true,
                false);
        }
        if (动作句柄.类型 == 枚举_动作句柄类型::本能函数ID) {
            主信息.首节点信息().来源 = 枚举_方法来源::本能;
        }
        主信息.首节点信息().作用域 = 作用域;
        已有节点 = 私有_独立树添加方法子节点(根节点, 主信息);
    } else {
        auto& 主信息 = 已有节点->主信息;
        主信息.公共.节点种类 = 枚举_方法节点种类::方法首节点;
        if (动作名) {
            主信息.首节点信息().动作名 = 动作名;
        }
        if (动作句柄.有效()) {
            主信息.首节点信息().动作句柄 = 动作句柄;
        }
        if (主结果特征类型
            && !私有_方法首节点包含结果能力(已有节点, 主结果特征类型, nullptr)) {
            结构_方法结果项 结果项{};
            结果项.特征类型 = 主结果特征类型;
            结果项.作用对象 = 私有_推断结果作用对象(主结果特征类型);
            私有_追加首节点结果项(
                主信息,
                std::move(结果项),
                true,
                false);
        }
        if (主信息.首节点信息().来源 == 枚举_方法来源::未定义
            && 主信息.首节点信息().动作句柄.类型 == 枚举_动作句柄类型::本能函数ID) {
            主信息.首节点信息().来源 = 枚举_方法来源::本能;
        }
        (void)按注册入口对齐方法作用域(已有节点, 作用域);
    }

    (void)方法虚拟存在服务类::同步方法节点到虚拟存在(已有节点, nullptr, now);
    return 已有节点;
}

方法类::节点类* 方法类::注册需求位方法首节点(
    节点类* 方法根节点,
    const 语素入口节点类* 主结果特征类型,
    枚举_方法来源 来源,
    时间戳 now)
{
    auto* 节点 = 注册方法首节点(
        方法根节点,
        nullptr,
        结构体_动作句柄{},
        枚举_方法作用域::未定义,
        now,
        主结果特征类型);
    if (节点 && 来源 != 枚举_方法来源::未定义 && 节点->主信息.首节点信息().来源 == 枚举_方法来源::未定义) {
        节点->主信息.首节点信息().来源 = 来源;
    }
    return 节点;
}

方法类::节点类* 方法类::注册需求方向方法首节点(
    节点类* 方法根节点,
    const 语素入口节点类* 主结果特征类型,
    二次特征节点类* 结果方向,
    枚举_方法来源 来源,
    时间戳 now)
{
    if (!结果方向) {
        return 注册需求位方法首节点(
            方法根节点,
            主结果特征类型,
            来源,
            now);
    }
    if (!方法根节点 || !主结果特征类型) {
        return nullptr;
    }
    if (now == 0) {
        now = 结构体_时间戳::当前_微秒();
    }

    auto* 根节点 = 私有_取独立树根(方法根节点);
    auto* 节点 = 私有_查找需求方向方法首节点(
        根节点,
        主结果特征类型,
        结果方向);
    if (!节点) {
        // 方法方向精确匹配失败时，先回退到同一结果特征的可执行方法。
        // 例如“安全值达到目标值”的需求，满足关系可能是“等于”，但真正可执行的方法是“增加安全值”。
        // 方法头只做候选召回；能否绑定参数仍由后续条件节点检查。
        节点 = 私有_查找可执行结果能力方法首节点_按主结果特征类型(
            根节点,
            主结果特征类型);
    }
    if (!节点) {
        auto 主信息 = 方法主信息类::构造首节点();
        auto* 首节点信息 = 主信息.取首节点信息();
        if (首节点信息) {
            首节点信息->来源 = 来源;
        }
        结构_方法结果项 结果项{};
        结果项.特征类型 = 主结果特征类型;
        结果项.作用对象 = 私有_推断结果作用对象(主结果特征类型);
        结果项.方向 = 私有_二次特征方向枚举(结果方向);
        私有_追加首节点结果项(主信息, std::move(结果项), true, false);
        节点 = 私有_独立树添加方法子节点(根节点, 主信息);
    }
    else {
        auto& 主信息 = 节点->主信息;
        主信息.公共.节点种类 = 枚举_方法节点种类::方法首节点;
        if (来源 != 枚举_方法来源::未定义 && 主信息.首节点信息().来源 == 枚举_方法来源::未定义) {
            主信息.首节点信息().来源 = 来源;
        }
        if (!私有_方法首节点包含结果能力(节点, 主结果特征类型, 结果方向)) {
            结构_方法结果项 结果项{};
            结果项.特征类型 = 主结果特征类型;
            结果项.作用对象 = 私有_推断结果作用对象(主结果特征类型);
            结果项.方向 = 私有_二次特征方向枚举(结果方向);
            私有_追加首节点结果项(主信息, std::move(结果项), true, false);
        }
    }

    (void)方法虚拟存在服务类::同步方法节点到虚拟存在(节点, nullptr, now);
    return 节点;
}

方法类::节点类* 方法类::查找或创建_本能方法首节点(
    存在节点类* 宿主存在,
    枚举_本能方法ID 本能ID,
    时间戳 now)
{
    if (!宿主存在) return nullptr;
    if (now == 0) now = 结构体_时间戳::当前_微秒();

    const auto* 动作名称 = 本能方法类::查询默认动作名称(本能ID);
    if (!动作名称 || 动作名称[0] == '\0') return nullptr;

    auto* 方法根节点 = 世界树.存在().获取方法根节点(宿主存在);
    if (!方法根节点) return nullptr;

    结构体_动作句柄 动作句柄{};
    动作句柄.类型 = 枚举_动作句柄类型::本能函数ID;
    动作句柄.本能ID = static_cast<I64>(本能ID);

    auto* 动作名 = 语素集.添加信息入口词(
        动作名称,
        枚举_信息入口类型::动态模板入口);
    auto* 方法首节点 = 注册方法首节点(
        方法根节点,
        动作名,
        动作句柄,
        枚举_方法作用域::主体局部,
        now);
    if (!方法首节点) return nullptr;

    if (!补齐本能方法首节点(
        方法首节点,
        宿主存在,
        now)) {
        return nullptr;
    }

    return 方法首节点;
}

bool 方法类::是有效本能方法能力值(
    I64 自我能力值) noexcept
{
    if (自我能力值 <= 0
        || 自我能力值 > static_cast<I64>((std::numeric_limits<std::uint32_t>::max)())) {
        return false;
    }
    return 本能方法类::是默认自我本能方法(
        static_cast<枚举_本能方法ID>(static_cast<std::uint32_t>(自我能力值)));
}

方法类::节点类* 方法类::取或确保本能方法首节点_按能力值(
    存在节点类* 宿主存在,
    I64 自我能力值,
    时间戳 now)
{
    if (!宿主存在 || !是有效本能方法能力值(自我能力值)) {
        return nullptr;
    }
    return 查找或创建_本能方法首节点(
        宿主存在,
        static_cast<枚举_本能方法ID>(static_cast<std::uint32_t>(自我能力值)),
        now);
}

bool 方法类::补齐本能方法首节点(
    节点类* 方法首节点,
    存在节点类* 宿主存在,
    时间戳 now)
{
    auto* 首节点 = 私有_归一到方法首节点(方法首节点);
    if (!首节点) {
        return false;
    }
    if (now == 0) {
        now = 结构体_时间戳::当前_微秒();
    }

    auto& 主信息 = 首节点->主信息;
    主信息.公共.节点种类 = 枚举_方法节点种类::方法首节点;
    主信息.首节点信息().动作句柄.类型 = 枚举_动作句柄类型::本能函数ID;
    主信息.首节点信息().动作句柄.外部实现主键.clear();
    主信息.首节点信息().动作句柄.动作序列主键.clear();
    主信息.首节点信息().来源 = 枚举_方法来源::本能;
    const auto 本能ID = static_cast<枚举_本能方法ID>(
        static_cast<std::uint32_t>(主信息.首节点信息().动作句柄.本能ID));
    const auto 默认成熟度 = 私有_默认成熟度阶段(主信息);
    if (主信息.首节点信息().成熟度阶段 < 默认成熟度) {
        主信息.首节点信息().成熟度阶段 = 默认成熟度;
    }
    私有_补齐本能方法结果声明元数据(主信息, 本能ID);

    if (!方法虚拟存在服务类::同步方法节点到虚拟存在(首节点, 宿主存在, now)) {
        return false;
    }

    auto* 条件场景 = 取或创建_方法条件场景(首节点, 宿主存在, now);
    if (!条件场景) {
        return false;
    }

    auto 条件索引 = 私有_场景二次特征索引(条件场景);

    std::string 条件场景模板主键 = 条件场景->获取主键();
    (void)创建条件节点(
        首节点,
        条件场景,
        条件索引,
        条件场景模板主键,
        now,
        now);
    auto* 结果场景 = 取或创建_方法结果场景(
        首节点,
        宿主存在,
        now);
    if (结果场景) {
        私有_补齐本能方法结果方向索引(首节点, 结果场景, 本能ID);
    }
    auto* 初始状态 = 私有_确保方法结果初始状态(
        首节点,
        宿主存在,
        条件场景,
        now);
    if (结果场景) {
        auto* 结果节点 = 创建结果节点(
            首节点,
            结果场景,
            {});
        auto* 结果信息 = 结果节点 ? 结果节点->主信息.取结果节点信息() : nullptr;
        if (结果信息 && 初始状态) {
            私有_吸收引用列表(
                结果信息->结果初始状态,
                std::vector<可解析引用<状态节点类>>{ 私有_生成引用(初始状态) });
        }
    }
    (void)私有_同步方法状态特征(首节点, 宿主存在, now);
    return 私有_方法首节点具备可执行动作条件(首节点);
}

存在节点类* 方法类::取或创建_方法虚拟存在(
    节点类* 方法首节点,
    存在节点类* 宿主存在,
    时间戳 now)
{
    auto* 首节点 = 私有_归一到方法首节点(方法首节点);
    if (!首节点) return nullptr;
    if (now == 0) now = 结构体_时间戳::当前_微秒();

    auto& 主信息 = 首节点->主信息;
    if (主信息.首节点信息().方法虚拟存在.指针) {
        return 主信息.首节点信息().方法虚拟存在.指针;
    }

    if (!宿主存在) {
        宿主存在 = 私有_推断宿主存在(首节点);
    }

    场景节点类* 承载世界 = nullptr;
    if (宿主存在) {
        承载世界 = 世界树.取或创建存在内部世界(宿主存在);
    }
    if (!承载世界) {
        if (!世界树.虚拟世界) {
            世界树.初始化默认世界();
        }
        承载世界 = 世界树.虚拟世界;
    }
    if (!承载世界) return nullptr;

    auto* 方法虚拟存在 = 世界树.存在().取或创建子存在_按名称类型(
        承载世界,
        私有_方法虚拟存在名称词(主信息),
        私有_方法虚拟存在类型词());
    if (!方法虚拟存在) return nullptr;

    私有_写入引用(主信息.首节点信息().方法虚拟存在, 方法虚拟存在);

    (void)私有_写入方法类型特征(方法虚拟存在, 私有_推导方法类型(主信息), now);
    return 方法虚拟存在;
}

场景节点类* 方法类::取或创建_方法条件场景(
    节点类* 方法首节点,
    存在节点类* 宿主存在,
    时间戳 now)
{
    auto* 首节点 = 私有_归一到方法首节点(方法首节点);
    if (!首节点) return nullptr;
    if (now == 0) now = 结构体_时间戳::当前_微秒();

    if (方法首节点 && 方法首节点->主信息.节点种类() == 枚举_方法节点种类::方法条件节点) {
        if (auto* 条件信息 = 方法首节点->主信息.取条件节点信息();
            条件信息 && 条件信息->条件场景.指针) {
            return 条件信息->条件场景.指针;
        }
    }

    if (auto* 条件子节点 = 私有_取首个条件子节点(首节点)) {
        if (auto* 条件信息 = 条件子节点->主信息.取条件节点信息();
            条件信息 && 条件信息->条件场景.指针) {
            return 条件信息->条件场景.指针;
        }
    }

    auto* 方法虚拟存在 = 取或创建_方法虚拟存在(首节点, 宿主存在, now);
    if (!方法虚拟存在) return nullptr;

    auto* 条件场景 = 世界树.场景().取或创建子场景_按名称(
        方法虚拟存在,
        私有_方法条件场景名称词(首节点->主信息),
        枚举_世界类型::内部世界);
    if (!条件场景) return nullptr;

    (void)世界树.场景().绑定宿主(条件场景, 方法虚拟存在);
    if (方法首节点 && 方法首节点 != 首节点
        && 方法首节点->主信息.节点种类() == 枚举_方法节点种类::方法条件节点) {
        if (auto* 条件信息 = 方法首节点->主信息.取条件节点信息();
            条件信息 && !条件信息->条件场景.指针) {
            私有_写入引用(条件信息->条件场景, 条件场景);
        }
    }
    return 条件场景;
}

场景节点类* 方法类::取或创建_方法结果场景(
    节点类* 方法首节点,
    存在节点类* 宿主存在,
    时间戳 now)
{
    auto* 首节点 = 私有_归一到方法首节点(方法首节点);
    if (!首节点) return nullptr;
    if (now == 0) now = 结构体_时间戳::当前_微秒();

    if (方法首节点 && 方法首节点->主信息.节点种类() == 枚举_方法节点种类::方法结果节点) {
        if (auto* 结果信息 = 方法首节点->主信息.取结果节点信息();
            结果信息 && 结果信息->结果场景.指针) {
            return 结果信息->结果场景.指针;
        }
    }

    if (auto* 结果子节点 = 私有_取首个结果子节点(首节点)) {
        if (auto* 结果信息 = 结果子节点->主信息.取结果节点信息();
            结果信息 && 结果信息->结果场景.指针) {
            return 结果信息->结果场景.指针;
        }
    }

    auto* 方法虚拟存在 = 取或创建_方法虚拟存在(首节点, 宿主存在, now);
    if (!方法虚拟存在) return nullptr;

    auto* 结果场景 = 世界树.场景().取或创建子场景_按名称(
        方法虚拟存在,
        私有_方法结果场景名称词(首节点->主信息),
        枚举_世界类型::内部世界);
    if (!结果场景) return nullptr;

    (void)世界树.场景().绑定宿主(结果场景, 方法虚拟存在);
    if (方法首节点 && 方法首节点 != 首节点
        && 方法首节点->主信息.节点种类() == 枚举_方法节点种类::方法结果节点) {
        if (auto* 结果信息 = 方法首节点->主信息.取结果节点信息();
            结果信息 && !结果信息->结果场景.指针) {
            私有_写入引用(结果信息->结果场景, 结果场景);
        }
    }
    return 结果场景;
}

bool 方法类::记录方法虚拟存在特征(
    节点类* 方法首节点,
    const 语素入口节点类* 特征类型,
    const 特征值& 值,
    存在节点类* 宿主存在,
    时间戳 now)
{
    if (!特征类型) return false;
    if (now == 0) now = 结构体_时间戳::当前_微秒();

    auto* 方法虚拟存在 = 取或创建_方法虚拟存在(方法首节点, 宿主存在, now);
    if (!方法虚拟存在) return false;

    if (const auto* 标量值 = std::get_if<I64>(&值)) {
        return 世界树.写入特征_I64(方法虚拟存在, 特征类型, *标量值, now);
    }
    if (const auto* 指针值 = std::get_if<指针句柄>(&值)) {
        return 世界树.写入特征_指针(
            方法虚拟存在,
            特征类型,
            reinterpret_cast<const void*>(指针值->指针),
            now);
    }
    if (const auto* Vec句柄 = std::get_if<VecU句柄>(&值)) {
        const auto* Vec值 = 世界树.值池().取VecU只读指针(*Vec句柄);
        return Vec值
            ? 世界树.写入特征_VecU(方法虚拟存在, 特征类型, *Vec值, 枚举_轮廓维度::未定义, now)
            : false;
    }
    return false;
}

