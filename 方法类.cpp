    std::int32_t 私有_结果变化方向编码(枚举_结果变化方向 方向) noexcept
    {
        switch (方向) {
        case 枚举_结果变化方向::增加:
        case 枚举_结果变化方向::趋向目标:
        case 枚举_结果变化方向::从无到有:
        case 枚举_结果变化方向::从未完成到完成:
        case 枚举_结果变化方向::已形成:
        case 枚举_结果变化方向::已识别:
        case 枚举_结果变化方向::已配对:
        case 枚举_结果变化方向::趋向可用:
        case 枚举_结果变化方向::更完整:
        case 枚举_结果变化方向::更稳定:
        case 枚举_结果变化方向::上升:
            return 1;
        case 枚举_结果变化方向::减少:
        case 枚举_结果变化方向::从有到无:
            return -1;
        case 枚举_结果变化方向::保持:
            return 0;
        default:
            return 2;
        }
    }

    枚举_结果变化方向 私有_二次特征方向枚举(const 二次特征节点类* 结果方向) noexcept
    {
        const auto* 主信息 = 世界树.二次特征().取二次特征主信息(结果方向);
        const auto 编码 = 私有_二次特征比较结果编码(主信息);
        if (编码 > 0) return 枚举_结果变化方向::增加;
        if (编码 < 0) return 枚举_结果变化方向::减少;
        if (编码 == 0) return 枚举_结果变化方向::保持;
        return 枚举_结果变化方向::未定义;
    }

    bool 私有_结果项匹配(
        const 结构_方法结果项& 结果项,
        const 语素入口节点类* 主结果特征类型,
        const 二次特征节点类* 结果方向 = nullptr) noexcept
    {
        if (!私有_语素入口主键相等(结果项.特征类型, 主结果特征类型)) {
            return false;
        }
        if (!结果方向) {
            return true;
        }
        const auto 需求方向 = 私有_结果变化方向编码(私有_二次特征方向枚举(结果方向));
        const auto 项方向 = 私有_结果变化方向编码(结果项.方向);
        return 需求方向 != 2 && 项方向 == 需求方向;
    }

    bool 私有_方法首节点包含结果能力(
        const 方法节点* 方法首节点,
        const 语素入口节点类* 主结果特征类型,
        const 二次特征节点类* 结果方向 = nullptr) noexcept
    {
        if (!方法首节点 || !主结果特征类型) {
            return false;
        }
        const auto* 首信息 = 方法首节点->主信息.取首节点信息();
        if (!首信息) {
            return false;
        }
        for (const auto& 签名 : 首信息->能力.能力面.能力签名集) {
            if (!签名.可被方法查找命中 || 签名.是否根写入原语) {
                continue;
            }
            for (const auto& 结果项 : 签名.结果包.结果项集) {
                if (私有_结果项匹配(结果项, 主结果特征类型, 结果方向)) {
                    return true;
                }
            }
        }
        return false;
    }

    bool 私有_结果项已存在(
        const std::vector<结构_方法结果项>& 结果项集,
        const 结构_方法结果项& 候选) noexcept
    {
        for (const auto& 结果项 : 结果项集) {
            if (私有_语素入口主键相等(结果项.特征类型, 候选.特征类型)
                && 结果项.作用对象 == 候选.作用对象
                && 结果项.方向 == 候选.方向
                && 结果项.绑定名 == 候选.绑定名
                && 结果项.是否主结果 == 候选.是否主结果
                && 结果项.是否必要结果 == 候选.是否必要结果
                && 结果项.是否副结果 == 候选.是否副结果
                && 结果项.是否风险结果 == 候选.是否风险结果) {
                return true;
            }
        }
        return false;
    }

    void 私有_追加首节点结果项(
        方法主信息类& 主信息,
        结构_方法结果项 结果项,
        bool 可被方法查找命中,
        bool 是否根写入原语) noexcept
    {
        auto* 首信息 = 主信息.取首节点信息();
        if (!首信息 || (!结果项.特征类型 && !是否根写入原语)) {
            return;
        }
        auto& 签名集 = 首信息->能力.能力面.能力签名集;
        if (签名集.empty()) {
            结构_方法能力签名 签名{};
            签名.能力键 = "运行时结果能力";
            签名.成熟度阶段 = 首信息->成熟度阶段;
            签名.可被方法查找命中 = 可被方法查找命中;
            签名.是否根写入原语 = 是否根写入原语;
            签名集.push_back(std::move(签名));
        }
        auto& 签名 = 签名集.front();
        if (签名.成熟度阶段 == 0) {
            签名.成熟度阶段 = 首信息->成熟度阶段;
        }
        签名.可被方法查找命中 = 签名.可被方法查找命中 || 可被方法查找命中;
        签名.是否根写入原语 = 签名.是否根写入原语 || 是否根写入原语;
        if (!私有_结果项已存在(签名.结果包.结果项集, 结果项)) {
            签名.结果包.结果项集.push_back(std::move(结果项));
        }
    }

    void 私有_追加结果节点结果项(
        方法主信息类& 主信息,
        结构_方法结果项 结果项) noexcept
    {
        auto* 结果信息 = 主信息.取结果节点信息();
        if (!结果信息 || !结果项.特征类型) {
            return;
        }
        if (!私有_结果项已存在(结果信息->结果包.结果项集, 结果项)) {
            结果信息->结果包.结果项集.push_back(std::move(结果项));
        }
    }

    void 私有_吸收结果节点方向索引(
        方法主信息类& 主信息,
        const 语素入口节点类* 目标特征类型,
        const std::vector<可解析引用<二次特征节点类>>& 方向索引) noexcept
    {
        for (const auto& 方向引用 : 方向索引) {
            auto* 方向节点 = 私有_解析二次特征引用(方向引用);
            结构_方法结果项 结果项{};
            结果项.特征类型 = 目标特征类型;
            结果项.作用对象 = 私有_推断结果作用对象(目标特征类型);
            结果项.方向 = 私有_二次特征方向枚举(方向节点);
            私有_追加结果节点结果项(主信息, std::move(结果项));
        }
    }

    方法节点* 私有_查找需求位方法首节点_按主结果特征类型(
        方法节点* 方法根节点,
        const 语素入口节点类* 主结果特征类型) noexcept
    {
        if (!方法根节点 || !主结果特征类型) return nullptr;

        auto* 首节点 = static_cast<方法节点*>(方法根节点->子);
        if (!首节点) return nullptr;

        方法节点* 待学习占位方法 = nullptr;
        auto* 当前 = 首节点;
        do {
            if (当前
                && 当前->主信息.公共.节点种类 == 枚举_方法节点种类::方法首节点
                && 私有_方法首节点包含结果能力(当前, 主结果特征类型, nullptr)) {
                if (当前->主信息.有动作骨架()) {
                    return 当前;
                }
                if (!待学习占位方法) {
                    待学习占位方法 = 当前;
                }
            }
            当前 = static_cast<方法节点*>(当前->下);
        } while (当前 && 当前 != 首节点);

        return 待学习占位方法;
    }

