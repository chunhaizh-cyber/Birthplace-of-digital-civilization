    const 语素入口节点类* 私有_二次特征特征类型(
        const 二次特征主信息类* 主信息,
        const 语素入口节点类* 兜底特征类型) noexcept
    {
        if (!主信息) {
            return 兜底特征类型;
        }

        const auto* 来源特征类型 = 私有_选择一致特征类型(
            私有_状态特征类型(私有_解析状态引用(主信息->来源起始状态)),
            私有_状态特征类型(私有_解析状态引用(主信息->来源结果状态)));
        if (来源特征类型) {
            return 来源特征类型;
        }

        const auto* 左对象状态 = dynamic_cast<状态节点类*>(私有_解析基础信息引用(主信息->左对象));
        const auto* 右对象状态 = dynamic_cast<状态节点类*>(私有_解析基础信息引用(主信息->右对象));
        const auto* 左右特征类型 = 私有_选择一致特征类型(
            私有_状态特征类型(左对象状态),
            私有_状态特征类型(右对象状态));
        return 左右特征类型 ? 左右特征类型 : 兜底特征类型;
    }

    std::int32_t 私有_二次特征比较结果编码(const 二次特征主信息类* 主信息) noexcept
    {
        if (!主信息) {
            return 2;
        }
        if (主信息->方向区间 != 二次特征主信息类::枚举_方向区间::未定义) {
            return 二次特征主信息类::方向区间编码(主信息->方向区间);
        }
        if (主信息->差值.has_value()) {
            return 二次特征主信息类::方向区间编码(
                二次特征主信息类::由差值判断方向区间(*主信息->差值));
        }
        if (主信息->值形态 == 枚举_二次特征值形态::枚举
            && 主信息->离散编码 >= -1
            && 主信息->离散编码 <= 1) {
            return 主信息->离散编码;
        }
        if (主信息->值形态 == 枚举_二次特征值形态::布尔) {
            return 主信息->是否满足 ? 1 : 0;
        }
        return 2;
    }

