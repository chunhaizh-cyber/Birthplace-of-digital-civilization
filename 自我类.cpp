结构_自我根需求压力评估 自我类::评估根需求压力() const noexcept
{
    const auto 饱和压力 = [](I64 差值, I64 权重) noexcept -> I64 {
        const I64 有效差值 = (std::max<I64>)(0, 差值);
        const I64 有效权重 = (std::max<I64>)(1, 权重);
        if (有效差值 == 0) {
            return 0;
        }
        const long double 值 =
            static_cast<long double>(有效差值) *
            static_cast<long double>(有效权重);
        if (值 >= static_cast<long double>((std::numeric_limits<I64>::max)())) {
            return (std::numeric_limits<I64>::max)();
        }
        return static_cast<I64>(值);
    };

    结构_自我根需求压力评估 评估{};
    评估.安全根需求 = 获取安全根需求();
    评估.服务根需求 = 获取服务根需求();
    评估.安全差值 = 安全根方向差值();
    评估.服务差值 = 私有_饱和减(根目标服务值(), 获取服务值());
    评估.安全压力 = 饱和压力(评估.安全差值, roots_.安全);
    评估.服务压力 = 饱和压力(评估.服务差值, roots_.服务);

    if (!评估.安全根需求 && !评估.服务根需求) {
        return 评估;
    }
    if (!评估.安全根需求) {
        评估.选中需求 = 评估.服务根需求;
        return 评估;
    }
    if (!评估.服务根需求) {
        评估.选中需求 = 评估.安全根需求;
        return 评估;
    }

    if (评估.安全压力 >= 评估.服务压力) {
        评估.选中需求 = 评估.安全根需求;
    }
    else {
        评估.选中需求 = 评估.服务根需求;
    }
    return 评估;
}

