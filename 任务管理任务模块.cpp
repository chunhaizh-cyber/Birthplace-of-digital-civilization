    bool 私有_绑定宿主任务方法(
        自我类& 自我对象,
        任务节点* 宿主任务,
        结构_任务管理结果& 结果,
        时间戳 now,
        const std::string& 调用点,
        方法节点*& 输出方法) noexcept
    {
        输出方法 = nullptr;
        结果.当前方法来源 = 枚举_任务管理方法来源::未定义;
        if (!宿主任务) {
            return false;
        }

        auto& 任务主信息 = 宿主任务->主信息;
        if (任务主信息.执行方法.指针) {
            输出方法 = reinterpret_cast<方法节点*>(任务主信息.执行方法.指针);
            结果.当前方法来源 = 枚举_任务管理方法来源::已挂方法复用;
            if (!自我对象.获取当前主方法()) {
                (void)自我对象.设置当前主方法(输出方法, now, 调用点 + "/回填当前主方法");
            }
            return false;
        }

        if (结果.上下文.当前方法
            && 私有_方法属于宿主任务(结果.上下文.当前方法, 宿主任务)) {
            私有_完成方法挂接(
                自我对象,
                宿主任务,
                结果.上下文.当前方法,
                now,
                调用点 + "/当前主方法回填",
                枚举_任务管理方法来源::当前主方法回填,
                结果,
                输出方法);
            return 输出方法 != nullptr;
        }

        if (auto* 回看方法 = 私有_从最近结果回看方法(结果, 宿主任务)) {
            私有_完成方法挂接(
                自我对象,
                宿主任务,
                回看方法,
                now,
                调用点 + "/最近结果回看",
                枚举_任务管理方法来源::最近结果回看,
                结果,
                输出方法);
            return 输出方法 != nullptr;
        }

        bool 使用了条件节点 = false;
        if (auto* 首节点候选 = 私有_查找宿主任务方法首节点候选(
            自我对象,
            宿主任务,
            结果,
            使用了条件节点)) {
            私有_完成方法挂接(
                自我对象,
                宿主任务,
                首节点候选,
                now,
                调用点 + "/方法树首节点候选",
                使用了条件节点
                    ? 枚举_任务管理方法来源::条件节点筛选通过
                    : 枚举_任务管理方法来源::方法树首节点候选,
                结果,
                输出方法);
            return 输出方法 != nullptr;
        }

        if (!私有_允许默认本能兜底(自我对象, 宿主任务)) {
            return false;
        }

        (void)本能方法类::注册默认自我本能方法集(自我对象, 调用点 + "/默认本能方法");
        const auto 本能ID = 私有_选择本能方法ID(自我对象, 宿主任务);
        auto* 方法节点 = 私有_查找本能方法首节点(自我对象, 本能ID);
        if (!方法节点) {
            return false;
        }

        私有_完成方法挂接(
            自我对象,
            宿主任务,
            方法节点,
            now,
            调用点 + "/默认本能兜底",
            枚举_任务管理方法来源::默认本能兜底,
            结果,
            输出方法);
        return 输出方法 != nullptr;
    }

