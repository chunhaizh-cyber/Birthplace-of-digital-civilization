bool 任务管理任务模块::执行一步治理(
    自我类& 自我对象,
    时间戳 now,
    const std::string& 调用点,
    结构_任务管理结果* 输出) noexcept
{
    const auto 当前时间 = 私有_归一化时间(now);
    结构_任务管理结果 结果{};
    结果.上下文 = 读取任务管理上下文(自我对象, 当前时间);
    结果.最近根层重判结果 = 结果.上下文.最近根层重判结果;
    结果.最近执行前门控结果 = 结果.上下文.最近执行前门控结果;
    结果.最近上层反馈摘要 = 结果.上下文.最近上层反馈摘要;

    if (!结果.上下文.当前管理任务) {
        (void)自我初始化模块::执行自我初始化(自我对象, 调用点 + "/补齐治理任务", nullptr);
        结果.上下文 = 读取任务管理上下文(自我对象, 当前时间);
        结果.已补齐治理任务 = 结果.上下文.当前管理任务 != nullptr;
        结果.最近根层重判结果 = 结果.上下文.最近根层重判结果;
        结果.最近执行前门控结果 = 结果.上下文.最近执行前门控结果;
        结果.最近上层反馈摘要 = 结果.上下文.最近上层反馈摘要;
    }

    auto* 当前管理任务 = 结果.上下文.当前管理任务;
    if (!当前管理任务) {
        结果.上下文.当前管理任务 = nullptr;
        结果.上下文.当前宿主任务 = nullptr;
        结果.上下文.当前方法 = nullptr;
        私有_设定治理结论(
            结果,
            枚举_任务管理功能域::筹办,
            枚举_任务管理缺口类型::治理任务缺失,
            枚举_任务管理下一步去向::回到重筹办,
            枚举_任务管理治理状态迁移::回到重筹办,
            枚举_任务管理总控结果::宿主待重筹办,
            枚举_任务管理反馈类型::重筹办提示,
            "任务管理任务缺失");
        私有_写回治理稳定字段(
            自我对象,
            nullptr,
            当前时间,
            调用点 + "/稳定字段",
            结果);
        结果.摘要 = 私有_治理摘要文本(结果);
        私有_缓存最近治理结果(结果);
        if (输出) *输出 = 结果;
        return 结果.已补齐治理任务 || 结果.已生成治理证据;
    }

    if (当前管理任务->主信息.来源需求.指针 != 结果.上下文.当前主需求) {
        私有_写入引用(当前管理任务->主信息.来源需求, 结果.上下文.当前主需求);
    }
    (void)私有_迁移任务状态(当前管理任务, 枚举_任务状态::运行中, 当前时间);

    auto* 宿主任务 = 私有_选择宿主任务(自我对象, 结果.上下文);
    if (宿主任务 && 宿主任务 != 结果.上下文.当前宿主任务) {
        结果.已选中宿主任务 = 自我对象.设置当前主任务(
            宿主任务,
            当前时间,
            调用点 + "/设置当前宿主任务");
        结果.上下文.当前宿主任务 = 宿主任务;
    }

    if (!宿主任务) {
        结果.上下文.当前宿主任务 = nullptr;
        结果.上下文.当前方法 = nullptr;
        私有_设定治理结论(
            结果,
            枚举_任务管理功能域::筹办,
            枚举_任务管理缺口类型::宿主任务缺失,
            枚举_任务管理下一步去向::回到重筹办,
            枚举_任务管理治理状态迁移::回到重筹办,
            枚举_任务管理总控结果::宿主待重筹办,
            枚举_任务管理反馈类型::重筹办提示,
            "未选中宿主任务");
        私有_写回治理稳定字段(
            自我对象,
            nullptr,
            当前时间,
            调用点 + "/稳定字段",
            结果);
        结果.摘要 = 私有_治理摘要文本(结果);
        私有_缓存最近治理结果(结果);
        if (输出) *输出 = 结果;
        return 结果.已补齐治理任务 || 结果.已选中宿主任务 || 结果.已生成治理证据;
    }

    结果.宿主任务旧状态 = 宿主任务->主信息.状态;
    auto* 待回接业务子任务 = 私有_查找待回接的已完成业务子任务(宿主任务, 结果.上下文);
    if (待回接业务子任务) {
        结果.当前子任务节点 = 待回接业务子任务;
    }

    方法节点* 当前方法 = 结果.上下文.当前方法;
    const bool 已受上位输入约束 = 私有_应用上位输入约束(结果);
    if (!已受上位输入约束) {
        const bool 已绑定方法 = 私有_绑定宿主任务方法(
            自我对象,
            宿主任务,
            结果,
            当前时间,
            调用点 + "/绑定宿主方法",
            当前方法);
        结果.已绑定方法挂点 = 已绑定方法;
        结果.上下文.当前方法 = 当前方法;

        if (!当前方法 && 宿主任务->主信息.执行方法.指针) {
            当前方法 = reinterpret_cast<方法节点*>(宿主任务->主信息.执行方法.指针);
            结果.上下文.当前方法 = 当前方法;
        }

        if (结果.上下文.当前方法 && 结果.上下文.当前方法 != 自我对象.获取当前主方法()) {
            (void)自我对象.设置当前主方法(结果.上下文.当前方法, 当前时间, 调用点 + "/同步当前主方法");
        }
    }

    if (!已受上位输入约束 && !结果.上下文.当前方法) {
        if (宿主任务->主信息.状态 != 枚举_任务状态::挂起) {
            结果.已推进任务状态 = 私有_迁移任务状态(宿主任务, 枚举_任务状态::挂起, 当前时间);
        }
        私有_设定治理结论(
            结果,
            枚举_任务管理功能域::筹办,
            枚举_任务管理缺口类型::方法挂点缺失,
            枚举_任务管理下一步去向::绑定方法,
            枚举_任务管理治理状态迁移::进入挂起,
            枚举_任务管理总控结果::宿主已挂起,
            枚举_任务管理反馈类型::重筹办提示,
            "宿主任务缺少方法挂点");
    }
    else if (!已受上位输入约束) {
        switch (宿主任务->主信息.状态) {
        case 枚举_任务状态::完成:
        case 枚举_任务状态::失败:
        case 枚举_任务状态::取消:
            私有_设定治理结论(
                结果,
                枚举_任务管理功能域::收束,
                枚举_任务管理缺口类型::任务已终结,
                枚举_任务管理下一步去向::进入收束,
                枚举_任务管理治理状态迁移::进入收束,
                枚举_任务管理总控结果::宿主进入收束,
                枚举_任务管理反馈类型::收束回执,
                "宿主任务已终结，进入收束");
            break;
        case 枚举_任务状态::运行中:
            if (私有_业务子任务执行已完成(宿主任务, 结果)) {
                结果.已推进任务状态 = 私有_迁移任务状态(宿主任务, 枚举_任务状态::完成, 当前时间)
                    || 结果.已推进任务状态;
                私有_设定治理结论(
                    结果,
                    枚举_任务管理功能域::收束,
                    枚举_任务管理缺口类型::任务已终结,
                    枚举_任务管理下一步去向::进入收束,
                    枚举_任务管理治理状态迁移::进入收束,
                    枚举_任务管理总控结果::宿主进入收束,
                    枚举_任务管理反馈类型::收束回执,
                    "业务子任务已完成业务执行，进入收束");
            }
            else if (待回接业务子任务) {
                私有_设定治理结论(
                    结果,
                    枚举_任务管理功能域::维护,
                    枚举_任务管理缺口类型::任务运行中,
                    枚举_任务管理下一步去向::保持运行,
                    枚举_任务管理治理状态迁移::保持运行,
                    枚举_任务管理总控结果::宿主保持运行,
                    枚举_任务管理反馈类型::治理推进,
                    "直接业务子任务已完成，回接父任务结果");
            }
            else if (私有_可进入等待分支(宿主任务, 结果)) {
                私有_设定治理结论(
                    结果,
                    枚举_任务管理功能域::等待,
                    枚举_任务管理缺口类型::无缺口,
                    枚举_任务管理下一步去向::保持等待,
                    枚举_任务管理治理状态迁移::保持等待,
                    枚举_任务管理总控结果::宿主保持等待,
                    枚举_任务管理反馈类型::等待维持,
                    "当前无新增缺口，保持等待");
            }
            else {
                私有_设定治理结论(
                    结果,
                    枚举_任务管理功能域::维护,
                    枚举_任务管理缺口类型::任务运行中,
                    枚举_任务管理下一步去向::保持运行,
                    枚举_任务管理治理状态迁移::保持运行,
                    枚举_任务管理总控结果::宿主保持运行,
                    枚举_任务管理反馈类型::治理推进,
                    "宿主任务运行中");
            }
            break;
        case 枚举_任务状态::挂起:
            结果.已推进任务状态 = 私有_迁移任务状态(宿主任务, 枚举_任务状态::运行中, 当前时间);
            私有_设定治理结论(
                结果,
                枚举_任务管理功能域::纠偏,
                枚举_任务管理缺口类型::任务待启动,
                枚举_任务管理下一步去向::进入运行,
                枚举_任务管理治理状态迁移::进入运行,
                枚举_任务管理总控结果::宿主已进入运行,
                枚举_任务管理反馈类型::治理推进,
                "宿主任务已推进到运行中");
            break;
        case 枚举_任务状态::未定义:
        case 枚举_任务状态::未启动:
        default:
            结果.已推进任务状态 = 私有_迁移任务状态(宿主任务, 枚举_任务状态::运行中, 当前时间);
            私有_设定治理结论(
                结果,
                枚举_任务管理功能域::维护,
                枚举_任务管理缺口类型::任务待启动,
                枚举_任务管理下一步去向::进入运行,
                枚举_任务管理治理状态迁移::进入运行,
                枚举_任务管理总控结果::宿主已进入运行,
                枚举_任务管理反馈类型::治理推进,
                "宿主任务已推进到运行中");
            break;
        }
    }

    结果.宿主任务新状态 = 宿主任务->主信息.状态;
    私有_同步治理上下文镜像(结果);

    (void)私有_记录治理证据_指针(
        自我对象,
        私有_管理特征_当前宿主任务指针(),
        宿主任务,
        私有_任务标题(宿主任务),
        "任务管理_宿主任务切换",
        结果.上下文.当前方法,
        宿主任务,
        当前时间,
        调用点 + "/证据/宿主任务",
        结果);

    (void)私有_记录治理证据_指针(
        自我对象,
        私有_管理特征_当前方法指针(),
        结果.上下文.当前方法,
        私有_方法标题(结果.上下文.当前方法),
        "任务管理_当前方法挂点",
        结果.上下文.当前方法,
        宿主任务,
        当前时间,
        调用点 + "/证据/当前方法",
        结果);

    (void)私有_记录治理证据_I64(
        自我对象,
        私有_管理特征_宿主任务状态(),
        static_cast<I64>(结果.宿主任务新状态),
        "任务管理_宿主任务状态迁移",
        结果.上下文.当前方法,
        宿主任务,
        当前时间,
        调用点 + "/证据/宿主任务状态",
        结果);

    const bool 允许推进步骤结果子链 =
        结果.当前下一步去向 != 枚举_任务管理下一步去向::进入收束
        || 结果.当前功能域 == 枚举_任务管理功能域::等待;
    if (允许推进步骤结果子链) {
        (void)私有_推进步骤结果子链(
            自我对象,
            宿主任务,
            结果,
            当前时间,
            调用点 + "/步骤结果子链");
    }

    if (!结果.当前步骤节点) {
        结果.当前步骤节点 = 结果.上下文.当前步骤;
    }
    if (!结果.最新结果节点) {
        结果.最新结果节点 = 结果.上下文.最近结果;
    }
    if (结果.已创建补条件子任务) {
        结果.最近反馈摘要 += "；已创建补条件子任务=" + 私有_任务标题(结果.当前子任务节点);
    }
    if (结果.已复用补条件子任务) {
        结果.最近反馈摘要 += "；已复用补条件子任务=" + 私有_任务标题(结果.当前子任务节点);
    }

    if (结果.当前方法来源 != 枚举_任务管理方法来源::未定义) {
        结果.最近反馈摘要 += "；方法来源=" + 私有_方法来源文本(结果.当前方法来源);
    }
    if (结果.当前步骤位类型 != 枚举_任务管理步骤位类型::未定义) {
        结果.最近反馈摘要 += "；步骤位=" + 私有_步骤位类型文本(结果.当前步骤位类型);
    }

    (void)私有_写回任务目标状态(
        自我对象,
        宿主任务,
        结果,
        当前时间,
        调用点 + "/目标状态");

    (void)私有_写回任务结果状态(
        自我对象,
        宿主任务,
        结果,
        当前时间,
        调用点 + "/结果状态");

    私有_整理任务双面摘要(宿主任务, 结果);
    if (待回接业务子任务) {
        if (结果.宿主目标结果已对齐) {
            私有_设定治理结论(
                结果,
                枚举_任务管理功能域::等待,
                枚举_任务管理缺口类型::无缺口,
                枚举_任务管理下一步去向::保持等待,
                枚举_任务管理治理状态迁移::保持等待,
                枚举_任务管理总控结果::宿主保持等待,
                枚举_任务管理反馈类型::等待维持,
                "直接业务子任务已完成，父任务结果已对齐，进入等待");
        }
        else if (结果.宿主目标结果待推进) {
            私有_设定治理结论(
                结果,
                枚举_任务管理功能域::维护,
                枚举_任务管理缺口类型::任务运行中,
                枚举_任务管理下一步去向::保持运行,
                枚举_任务管理治理状态迁移::保持运行,
                枚举_任务管理总控结果::宿主保持运行,
                枚举_任务管理反馈类型::治理推进,
                "直接业务子任务已完成，但父任务仍有差额，继续派生下一段业务任务");
        }
        私有_同步治理上下文镜像(结果);
    }
    私有_整理后果治理(结果, 待回接业务子任务 != nullptr);
    私有_同步治理上下文镜像(结果);
    if (结果.已由目标差额驱动 && 结果.当前步骤语义类型 == 枚举_任务管理步骤语义类型::治理步骤) {
        结果.当前步骤语义类型 = 枚举_任务管理步骤语义类型::业务推进步骤;
    }
    (void)私有_创建或复用业务子任务头(
        自我对象,
        宿主任务,
        结果,
        当前时间,
        调用点 + "/业务子任务");
    auto* 学习子任务 = 私有_创建或复用学习子任务头(
        自我对象,
        当前管理任务,
        宿主任务,
        结果,
        当前时间,
        调用点 + "/学习子任务");
    auto* 学习方法 = 私有_写回学习方法骨架(
        自我对象,
        宿主任务,
        学习子任务,
        结果,
        当前时间,
        调用点 + "/学习方法");
    (void)私有_同步学习任务实体(
        宿主任务,
        学习子任务,
        学习方法,
        结果,
        当前时间);
    (void)私有_写回待学习方法数量(
        自我对象,
        当前管理任务,
        当前时间,
        调用点 + "/学习计数");
    (void)私有_写回尝试学习状态(
        自我对象,
        当前时间,
        调用点 + "/学习状态");
    if (!结果.目标结果差额摘要.empty()) {
        结果.最近反馈摘要 += "；" + 结果.目标结果差额摘要;
    }
    if (结果.已创建业务子任务) {
        结果.最近反馈摘要 += "；已创建业务子任务=" + 私有_任务标题(结果.当前子任务节点);
    }
    if (结果.已复用业务子任务) {
        结果.最近反馈摘要 += "；已复用业务子任务=" + 私有_任务标题(结果.当前子任务节点);
    }
    if (结果.已创建学习子任务) {
        结果.最近反馈摘要 += "；已创建学习子任务=" + 私有_任务标题(学习子任务);
    }
    if (结果.已复用学习子任务) {
        结果.最近反馈摘要 += "；已复用学习子任务=" + 私有_任务标题(学习子任务);
    }
    if ((结果.已创建学习子任务 || 结果.已复用学习子任务)
        && 结果.当前缺口类型 == 枚举_任务管理缺口类型::无缺口
        && 结果.当前下一步去向 == 枚举_任务管理下一步去向::保持等待) {
        结果.最近反馈摘要 += "；稳定等待态已触发方法复盘学习";
    }
    if (结果.已创建学习方法头) {
        结果.最近反馈摘要 += "；已创建学习方法头=" + 私有_方法标题(学习方法);
    }
    if (结果.已复用学习方法头) {
        结果.最近反馈摘要 += "；已复用学习方法头=" + 私有_方法标题(学习方法);
    }
    if (结果.已创建学习方法条件节点) {
        结果.最近反馈摘要 += "；已创建学习方法条件节点";
    }
    if (结果.已创建学习方法结果节点) {
        结果.最近反馈摘要 += "；已创建学习方法结果节点";
    }
    if (!结果.最近上层反馈摘要.empty()) {
        结果.最近反馈摘要 += "；上层反馈=" + 结果.最近上层反馈摘要;
    }

    私有_写回治理稳定字段(
        自我对象,
        宿主任务,
        当前时间,
        调用点 + "/证据/稳定字段",
        结果);

    (void)私有_记录治理证据_指针(
        自我对象,
        私有_管理特征_当前步骤指针(),
        结果.当前步骤节点,
        私有_任务标题(结果.当前步骤节点),
        "任务管理_当前步骤推进",
        结果.上下文.当前方法,
        宿主任务,
        当前时间,
        调用点 + "/证据/当前步骤",
        结果);

    (void)私有_记录治理证据_指针(
        自我对象,
        私有_管理特征_最近结果指针(),
        结果.最新结果节点,
        私有_任务标题(结果.最新结果节点),
        "任务管理_最近结果推进",
        结果.上下文.当前方法,
        宿主任务,
        当前时间,
        调用点 + "/证据/最近结果",
        结果);

    结果.摘要 = 私有_治理摘要文本(结果);
    私有_缓存最近治理结果(结果);

    if (输出) {
        *输出 = 结果;
    }

    return 结果.已补齐治理任务
        || 结果.已选中宿主任务
        || 结果.已绑定方法挂点
        || 结果.已推进任务状态
        || 结果.已创建步骤节点
        || 结果.已创建结果节点
        || 结果.已创建业务子任务
        || 结果.已创建学习子任务
        || 结果.已创建学习方法头
        || 结果.已创建学习方法条件节点
        || 结果.已创建学习方法结果节点
        || 结果.已写回目标状态
        || 结果.已生成治理证据;
}
