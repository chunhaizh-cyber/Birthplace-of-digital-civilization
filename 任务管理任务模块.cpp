    bool 私有_迁移任务状态(任务节点* 节点, 枚举_任务状态 新状态, 时间戳 now) noexcept
    {
        if (!节点) return false;

        auto& 主信息 = 节点->主信息;
        if (主信息.状态 == 新状态) {
            return false;
        }

        if ((新状态 == 枚举_任务状态::运行中) && 主信息.启动时间 == 0) {
            主信息.启动时间 = now;
        }
        if ((新状态 == 枚举_任务状态::完成
            || 新状态 == 枚举_任务状态::失败
            || 新状态 == 枚举_任务状态::取消)
            && 主信息.完成时间 == 0) {
            主信息.完成时间 = now;
        }

        主信息.状态 = 新状态;
        return true;
    }

