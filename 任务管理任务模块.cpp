    bool 私有_学习方法有条件侧(const 方法节点* 方法头) noexcept
    {
        if (!方法头) {
            return false;
        }
        if (方法头->主信息.条件场景.指针) {
            return true;
        }

        auto* 首节点 = static_cast<const 方法节点*>(方法头->子);
        if (!首节点) {
            return false;
        }

        auto* 当前 = 首节点;
        do {
            if (当前 && 当前->主信息.节点种类 == 枚举_方法节点种类::方法条件节点) {
                return true;
            }
            当前 = static_cast<const 方法节点*>(当前->下);
        } while (当前 && 当前 != 首节点);

        return false;
    }

