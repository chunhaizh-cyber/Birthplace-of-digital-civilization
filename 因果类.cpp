    const 二次特征主信息类* 私有_取因果主状态迁移主信息(
        基础信息类* 基础信息,
        const 因果主信息类& 主信息) noexcept
    {
        if (!基础信息) {
            return nullptr;
        }
        auto 取迁移主信息 = [&](const 可解析引用<二次特征节点类>& 引用) -> const 二次特征主信息类* {
            if (引用.指针) {
                return 基础信息->取主信息<二次特征主信息类>(引用.指针);
            }
            if (!引用.主键.empty()) {
                if (auto* 节点 = 基础信息->查找主键(引用.主键)) {
                    return 基础信息->取主信息<二次特征主信息类>(static_cast<二次特征节点类*>(节点));
                }
            }
            return nullptr;
        };

        if (const auto* 主迁移主信息 = 取迁移主信息(主信息.主状态迁移)) {
            return 主迁移主信息;
        }
        for (const auto& 状态迁移模板 : 主信息.状态迁移模板) {
            if (const auto* 迁移主信息 = 取迁移主信息(状态迁移模板)) {
                return 迁移主信息;
            }
        }
        return nullptr;
    }

