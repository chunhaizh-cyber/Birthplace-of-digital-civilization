    bool 私有_按动态补齐抽象因果模板(
        基础信息类* 基础信息,
        因果模板节点类* 模板节点,
        因果主信息类* 模板主信息,
        const 因果主信息类& 原型,
        动态节点类* 证据动态)
    {
        if (!基础信息 || !模板节点 || !模板主信息 || !证据动态) {
            return false;
        }
        const auto* 动态主信息 = 世界树.动态().取动态主信息(证据动态);
        if (!动态主信息 || !动态主信息->初始状态.指针 || !动态主信息->结果状态.指针) {
            return false;
        }
        const auto* 初始状态主信息 = 基础信息->取主信息<状态节点主信息类>(动态主信息->初始状态.指针);
        const auto* 结果状态主信息 = 基础信息->取主信息<状态节点主信息类>(动态主信息->结果状态.指针);
        if (!初始状态主信息 || !结果状态主信息) {
            return false;
        }

        const 二次特征主信息类* 样本迁移主信息 = nullptr;
        if (!原型.状态迁移模板.empty() && 原型.状态迁移模板.front().指针) {
            样本迁移主信息 = 基础信息->取主信息<二次特征主信息类>(原型.状态迁移模板.front().指针);
        }
        if (!样本迁移主信息) {
            auto* 样本迁移节点 = 私有_由状态生成状态迁移特征(
                动态主信息->初始状态.指针,
                动态主信息->结果状态.指针);
            样本迁移主信息 = 样本迁移节点
                ? 基础信息->取主信息<二次特征主信息类>(样本迁移节点)
                : nullptr;
        }
        if (!样本迁移主信息) {
            return false;
        }
        const bool 样本是值对迁移 = 私有_迁移是值对型(样本迁移主信息);
        if (样本是值对迁移) {
            if (!私有_迁移值对自洽(样本迁移主信息)) {
                return false;
            }
        }
        else if (!私有_迁移方向自洽(样本迁移主信息)) {
            return false;
        }

        bool 已存在同结构迁移 = false;
        for (auto& 状态迁移模板 : 模板主信息->状态迁移模板) {
            auto* 模板迁移节点 = 状态迁移模板.指针;
            if (!模板迁移节点 && !状态迁移模板.主键.empty()) {
                模板迁移节点 = static_cast<二次特征节点类*>(基础信息->查找主键(状态迁移模板.主键));
            }
            const auto* 模板迁移主信息 = 模板迁移节点
                ? 基础信息->取主信息<二次特征主信息类>(模板迁移节点)
                : nullptr;
            if (!模板迁移主信息) {
                continue;
            }
            if (!私有_迁移结构一致(模板迁移主信息, 样本迁移主信息)) {
                continue;
            }
            已存在同结构迁移 = true;
            if (!私有_状态迁移可归并(模板迁移主信息, 样本迁移主信息)) {
                return false;
            }
            auto* 条件模板特征 = 私有_确保抽象条件模板(
                基础信息,
                模板节点,
                模板主信息,
                初始状态主信息);
            if (!条件模板特征) {
                return false;
            }
            私有_扩张抽象状态迁移模板(
                基础信息,
                模板迁移节点,
                样本迁移主信息,
                初始状态主信息,
                结果状态主信息);
            状态迁移模板 = 私有_生成引用(模板迁移节点);
            if (!模板主信息->主状态迁移.有效()) {
                模板主信息->主状态迁移 = 状态迁移模板;
            }
            return true;
        }

        if (已存在同结构迁移) {
            return false;
        }

        auto* 条件模板特征 = 私有_确保抽象条件模板(
            基础信息,
            模板节点,
            模板主信息,
            初始状态主信息);
        if (!条件模板特征) {
            return false;
        }

        auto* 条件模板状态 = 私有_创建抽象状态模板(
            基础信息,
            模板节点,
            初始状态主信息,
            条件模板特征);
        auto* 结果模板状态 = 私有_创建抽象状态模板(基础信息, 模板节点, 结果状态主信息);
        auto* 新迁移模板 = 私有_创建抽象状态迁移模板(
            基础信息,
            模板节点,
            条件模板状态,
            结果模板状态,
            样本迁移主信息);
        if (新迁移模板) {
            模板主信息->状态迁移模板.push_back(私有_生成引用(新迁移模板));
            if (!模板主信息->主状态迁移.有效()) {
                模板主信息->主状态迁移 = 私有_生成引用(新迁移模板);
            }
        }
        return 新迁移模板 != nullptr;
    }

    因果模板节点类* 私有_取抽象因果信息根节点(基础信息类* 基础信息)
    {
        if (!基础信息) return nullptr;

        for (auto* 节点 : 基础信息->枚举子节点_按类型<因果主信息类>(nullptr)) {
            auto* 因果节点 = static_cast<因果模板节点类*>(节点);
            if (私有_是抽象因果信息根节点(基础信息, 因果节点)) {
                return 因果节点;
            }
        }
        return nullptr;
    }

    因果模板节点类* 私有_取或创建抽象因果信息根节点(基础信息类* 基础信息)
    {
        if (!基础信息) return nullptr;
        if (auto* 已有 = 私有_取抽象因果信息根节点(基础信息)) {
            return 已有;
        }

        auto* 新主信息 = new 因果主信息类(枚举_因果形态::抽象);
        新主信息->名称 = 私有_取因果信息根名称词();
        新主信息->类型 = 私有_取因果信息根名称词();
        return static_cast<因果模板节点类*>(基础信息->添加子节点(nullptr, 新主信息));
    }

    因果模板节点类* 私有_查找抽象因果模板(
        基础信息类* 基础信息,
        const 因果主信息类& 原型)
    {
        if (!基础信息) return nullptr;

        auto* 因果信息根 = 私有_取抽象因果信息根节点(基础信息);
        if (!因果信息根) return nullptr;

        const auto* 原型主迁移主信息 = 私有_取因果主状态迁移主信息(基础信息, 原型);
        if (!原型主迁移主信息) {
            return nullptr;
        }

        for (auto* 节点 : 基础信息->枚举子节点_按类型<因果主信息类>(因果信息根)) {
            auto* 模板节点 = static_cast<因果模板节点类*>(节点);
            auto* 模板主信息 = 基础信息->取主信息<因果主信息类>(模板节点);
            if (!模板主信息 || !模板主信息->是抽象因果()) continue;
            const auto* 模板主迁移主信息 = 私有_取因果主状态迁移主信息(基础信息, *模板主信息);
            if (!模板主迁移主信息) continue;
            if (!私有_动作模板一致(基础信息, 模板主信息, 原型)) continue;
            if (!私有_状态迁移可归并(模板主迁移主信息, 原型主迁移主信息)) continue;
            return 模板节点;
        }
        return nullptr;
    }

    bool 私有_合并抽象因果原型(
        基础信息类* 基础信息,
        因果模板节点类* 模板节点,
        因果主信息类* 模板主信息,
        const 因果主信息类& 原型,
        动态节点类* 证据动态) noexcept
    {
        if (!基础信息 || !模板节点 || !模板主信息) return false;

        模板主信息->形态 = 枚举_因果形态::抽象;
        if (!模板主信息->名称) 模板主信息->名称 = 原型.名称;
        if (!模板主信息->类型) 模板主信息->类型 = 原型.类型;
        if (!模板主信息->动作模板.指针 && !原型.动作模板.主键.empty()) {
            模板主信息->动作模板 = 原型.动作模板;
        }
        if (模板主信息->动作名称.empty()) {
            模板主信息->动作名称 = 原型.动作名称;
        }
        if (模板主信息->动作语义键.empty()) {
            模板主信息->动作语义键 = 原型.动作语义键;
        }
        if (模板主信息->适用锚点类型 == 枚举_因果锚点类型::未定义) {
            模板主信息->适用锚点类型 = 原型.适用锚点类型;
        }
        if (模板主信息->适用层级 == 0) {
            模板主信息->适用层级 = 原型.适用层级;
        }
        if (模板主信息->来源类型 == 枚举_因果来源类型::未定义) {
            模板主信息->来源类型 = 原型.来源类型;
        }
        if (模板主信息->置信度 == 0) {
            模板主信息->置信度 = 原型.置信度;
        }
        模板主信息->已验证 = 模板主信息->已验证 || 原型.已验证;

        if (!私有_按动态补齐抽象因果模板(基础信息, 模板节点, 模板主信息, 原型, 证据动态)) {
            return false;
        }

        if (证据动态 && !私有_引用已存在(模板主信息->证据动态样本, 证据动态)) {
            模板主信息->证据动态样本.push_back(私有_生成引用(证据动态));
            ++模板主信息->成立次数;
        }

        私有_刷新因果稳定性(模板主信息, 私有_取动态命中时间(证据动态));
        return true;
    }

    因果模板节点类* 私有_取或创建抽象因果模板(
        基础信息类* 基础信息,
        const 因果主信息类& 原型,
        动态节点类* 证据动态)
    {
        if (!基础信息) return nullptr;

        if (auto* 已有模板 = 私有_查找抽象因果模板(基础信息, 原型)) {
            auto* 既有主信息 = 基础信息->取主信息<因果主信息类>(已有模板);
            return 私有_合并抽象因果原型(基础信息, 已有模板, 既有主信息, 原型, 证据动态)
                ? 已有模板
                : nullptr;
        }

        auto* 新主信息 = new 因果主信息类(枚举_因果形态::抽象);
        新主信息->名称 = 原型.名称;
        新主信息->类型 = 原型.类型;
        新主信息->适用锚点类型 = 原型.适用锚点类型;
        新主信息->适用层级 = 原型.适用层级;
        新主信息->动作模板 = 原型.动作模板;
        新主信息->动作名称 = 原型.动作名称;
        新主信息->动作语义键 = 原型.动作语义键;
        新主信息->来源类型 = 原型.来源类型;
        新主信息->置信度 = 原型.置信度;
        新主信息->已验证 = 原型.已验证;
        auto* 因果信息根 = 私有_取或创建抽象因果信息根节点(基础信息);
        if (!因果信息根) {
            delete 新主信息;
            return nullptr;
        }
        auto* 新模板节点 = static_cast<因果模板节点类*>(基础信息->添加子节点(因果信息根, 新主信息));
        if (!私有_按动态补齐抽象因果模板(基础信息, 新模板节点, 新主信息, 原型, 证据动态)) {
            基础信息->删除节点(新模板节点);
            return nullptr;
        }
        if (证据动态) {
            新主信息->证据动态样本.push_back(私有_生成引用(证据动态));
            新主信息->成立次数 = 1;
            私有_刷新因果稳定性(新主信息, 私有_取动态命中时间(证据动态));
        }
        return 新模板节点;
    }

