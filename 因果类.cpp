    void 私有_并入区间(std::optional<I64区间>& 区间, I64 值) noexcept
    {
        if (!区间.has_value() || !区间->有效()) {
            区间 = I64区间::点(值);
            return;
        }
        区间->低值 = std::min(区间->低值, 值);
        区间->高值 = std::max(区间->高值, 值);
    }

    enum class 枚举_模板值形态 : std::uint8_t {
        未定义 = 0,
        标量 = 1,
        指针 = 2,
        向量 = 3,
        空 = 4,
    };

    枚举_模板值形态 私有_取特征模板值形态(const 特征节点主信息类* 特征主信息) noexcept
    {
        if (!特征主信息) {
            return 枚举_模板值形态::未定义;
        }
        if (私有_取特征模板区间(特征主信息).has_value() || std::holds_alternative<I64>(特征主信息->当前值)) {
            return 枚举_模板值形态::标量;
        }
        if (std::holds_alternative<指针句柄>(特征主信息->当前值)) {
            return 枚举_模板值形态::指针;
        }
        if (std::holds_alternative<VecU句柄>(特征主信息->当前值)) {
            return 枚举_模板值形态::向量;
        }
        return 枚举_模板值形态::空;
    }

