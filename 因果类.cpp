    {
        if (!基础信息) {
            return {};
        }
        auto* 特征节点 = 特征引用.指针;
        if (!特征节点 && !特征引用.主键.empty()) {
            特征节点 = static_cast<特征节点类*>(基础信息->查找主键(特征引用.主键));
        }
        if (!特征节点) {
            return 特征引用.主键.empty() ? std::string{} : std::string("特征#") + 特征引用.主键;
        }
        const auto* 特征主信息 = 基础信息->取主信息<特征节点主信息类>(特征节点);
        auto 短语 = 私有_基础节点短名(基础信息, 特征节点);
        if (!特征主信息) {
            return 短语;
        }
        if (const auto 区间 = 私有_取特征模板区间(特征主信息)) {
            if (区间->是否点()) {
                短语 += "为" + std::to_string(区间->点值());
            }
            else {
                短语 += "在[" + std::to_string(区间->低值) + "," + std::to_string(区间->高值) + "]范围内";
            }
        }
        return 短语;
    }

    std::string 私有_连接短语(const std::vector<std::string>& 短语列表, const char* 分隔符)
    {
        std::ostringstream 输出;
        bool 首项 = true;
        for (const auto& 短语 : 短语列表) {
            if (短语.empty()) continue;
            if (!首项) 输出 << 分隔符;
            输出 << 短语;
            首项 = false;
        }
        return 输出.str();
    }

