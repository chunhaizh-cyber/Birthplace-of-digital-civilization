    bool 私有_引用键一致(
        基础信息类* 基础信息,
        const 可解析引用<基础信息节点类>& 左,
        const 可解析引用<基础信息节点类>& 右) noexcept
    {
        if (左.指针 && 右.指针) {
            return 左.指针 == 右.指针;
        }
        if (!左.主键.empty() && !右.主键.empty()) {
            return 左.主键 == 右.主键;
        }
        if (左.指针 && !右.主键.empty()) {
            return 左.指针->获取主键() == 右.主键;
        }
        if (右.指针 && !左.主键.empty()) {
            return 右.指针->获取主键() == 左.主键;
        }
        const auto* 左节点 = 私有_解析引用指针(基础信息, 左);
        const auto* 右节点 = 私有_解析引用指针(基础信息, 右);
        return 左节点 && 右节点 && 左节点 == 右节点;
    }

