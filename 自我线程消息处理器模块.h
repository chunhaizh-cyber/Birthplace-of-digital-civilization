using 需求节点 = 需求类::节点类;

struct 结构_需求分支刷新范围 {
    std::unordered_set<std::string> 变更前节点主键集合{};
    std::unordered_set<std::string> 当前节点主键集合{};
    std::unordered_set<const 词性节点类*> 受影响类型集合{};
};

struct 结构_消息处理动作 {
    bool 更新线程控制态 = false;
    bool 更新线程上下文包 = false;
    bool 更新需求树或队列 = false;
    bool 更新任务控制态 = false;
    bool 更新任务局部运行态摘要 = false;
    bool 更新工作线程状态表 = false;
    bool 更新结果缓存 = false;
    bool 更新学习候选池 = false;
    bool 触发根层重判 = false;
    bool 触发主派发决议 = false;
    bool 可能实例化新任务 = false;
    bool 可能触发收束 = false;
    bool 可能触发停止 = false;
    bool 进入学习后置队列 = false;
    bool 需要路由回父任务 = false;
    bool 仅写摘要 = false;
};

struct 结构_单消息处理结果 {
    结构_消息处理动作 动作{};
    bool 触发需求队列刷新 = false;
    bool 有派生需求待实例化 = false;
    bool 有子结果待路由 = false;
};

struct 结构_消息批次处理摘要 {
    bool 触发需求队列刷新 = false;
    bool 触发根层重判 = false;
    bool 触发主派发决议 = false;
    bool 可能实例化新任务 = false;
    bool 可能触发收束 = false;
    bool 可能触发停止 = false;
    bool 进入学习后置队列 = false;
    bool 有派生需求待实例化 = false;
    bool 有子结果待路由 = false;
};

struct 结构_需求列表刷新决策 {
    bool 需要刷新 = false;
    bool 可以局部刷新 = false;
};

inline 需求节点* 按主键定位需求节点(
    需求节点* 需求根节点,
    const std::string& 主键) noexcept
{
    if (!需求根节点 || 主键.empty()) {
        return nullptr;
    }

    for (auto* 当前节点 = 需求根节点->链下;
         当前节点 && 当前节点 != 需求根节点;
         当前节点 = 当前节点->链下) {
        if (当前节点->获取主键() == 主键) {
            return 当前节点;
        }
    }
    return nullptr;
}

inline 需求节点* 定位需求节点(
    需求节点* 需求根节点,
    const std::uintptr_t 节点指针,
    const std::string& 主键) noexcept
{
    if (节点指针 != 0) {
        return reinterpret_cast<需求节点*>(节点指针);
    }
    return 按主键定位需求节点(需求根节点, 主键);
}

inline void 收集需求子树快照(
    const 需求节点* 根节点,
    std::unordered_set<std::string>* 输出节点主键集合,
    std::unordered_set<const 词性节点类*>* 输出类型集合) noexcept
{
    if (!根节点) {
        return;
    }

    if (输出节点主键集合) {
        输出节点主键集合->insert(根节点->获取主键());
    }
    if (输出类型集合 && 根节点->主信息.类型) {
        输出类型集合->insert(根节点->主信息.类型);
    }

    if (!根节点->子) {
        return;
    }

    auto* 首子节点 = 根节点->子;
    auto* 当前子节点 = 首子节点;
    do {
        收集需求子树快照(当前子节点, 输出节点主键集合, 输出类型集合);
        当前子节点 = 当前子节点->下;
    } while (当前子节点 && 当前子节点 != 首子节点);
}

inline 结构_单消息处理结果 从动作生成单消息处理结果(
    const 自我线程消息协议::结构_治理消息& 消息,
    const 结构_消息处理动作& 动作) noexcept
{
    结构_单消息处理结果 结果{};
    结果.动作 = 动作;
    结果.触发需求队列刷新 =
        动作.更新需求树或队列
        || 动作.更新结果缓存
        || 动作.可能实例化新任务;
    结果.有派生需求待实例化 =
        消息.头.大类 == 自我线程消息协议::枚举_消息大类::派生需求消息;
    结果.有子结果待路由 = 动作.需要路由回父任务;
    return 结果;
}

inline 结构_消息处理动作 计算消息处理动作(const 自我线程消息协议::结构_治理消息& 消息) noexcept
{
    using namespace 自我线程消息协议;

    结构_消息处理动作 动作{};
    动作.需要路由回父任务 = 自我线程消息路由::是否需要路由回父任务(消息);

    switch (消息.头.大类) {
    case 枚举_消息大类::控制消息:
        动作.更新线程控制态 = true;
        动作.更新任务控制态 = true;
        动作.更新工作线程状态表 = true;
        动作.触发主派发决议 = true;
        动作.仅写摘要 = (消息.头.子类 == 枚举_消息子类::配置热更新);
        动作.触发根层重判 = !动作.仅写摘要;
        动作.可能触发收束 =
            消息.头.子类 == 枚举_消息子类::收束
            || 消息.头.子类 == 枚举_消息子类::打断;
        动作.可能触发停止 = 消息.头.子类 == 枚举_消息子类::停止;
        break;

    case 枚举_消息大类::全局环境消息:
        动作.更新线程上下文包 = true;
        动作.更新需求树或队列 = true;
        动作.触发根层重判 = true;
        动作.触发主派发决议 = true;
        动作.可能实例化新任务 = 消息.头.子类 != 枚举_消息子类::局部外部反馈;
        break;

    case 枚举_消息大类::工作线程状态消息:
        动作.更新工作线程状态表 = true;
        动作.更新任务控制态 = true;
        动作.仅写摘要 = true;
        动作.触发根层重判 =
            消息.头.子类 == 枚举_消息子类::工作线程阻塞
            || 消息.头.子类 == 枚举_消息子类::工作线程超时
            || 消息.头.子类 == 枚举_消息子类::工作线程故障
            || 消息.头.子类 == 枚举_消息子类::工作线程已收束
            || 消息.头.子类 == 枚举_消息子类::工作线程已停止;
        动作.触发主派发决议 = 动作.触发根层重判;
        动作.可能触发收束 =
            消息.头.子类 == 枚举_消息子类::工作线程超时
            || 消息.头.子类 == 枚举_消息子类::工作线程故障;
        动作.可能触发停止 = 消息.头.子类 == 枚举_消息子类::工作线程故障;
        break;

    case 枚举_消息大类::任务推进关键事件消息:
        动作.更新任务控制态 = true;
        动作.更新任务局部运行态摘要 = true;
        动作.仅写摘要 = true;
        动作.触发根层重判 =
            消息.头.子类 == 枚举_消息子类::当前步骤阻塞
            || 消息.头.子类 == 枚举_消息子类::当前步骤解除阻塞
            || 消息.头.子类 == 枚举_消息子类::局部失败;
        动作.触发主派发决议 =
            动作.触发根层重判
            || 消息.头.子类 == 枚举_消息子类::当前步骤可执行;
        break;

    case 枚举_消息大类::派生需求消息:
        动作.更新需求树或队列 = true;
        动作.更新任务控制态 = true;
        动作.触发根层重判 = true;
        动作.触发主派发决议 = true;
        动作.可能实例化新任务 = true;
        break;

    case 枚举_消息大类::结果回流消息:
        动作.更新结果缓存 = true;
        动作.更新任务控制态 = true;
        动作.更新需求树或队列 = true;
        动作.更新学习候选池 = true;
        动作.触发根层重判 = true;
        动作.触发主派发决议 = true;
        动作.进入学习后置队列 = true;
        动作.可能实例化新任务 = 消息.头.子类 == 枚举_消息子类::待重试结果;
        动作.可能触发收束 =
            消息.头.子类 == 枚举_消息子类::中断结果
            || 消息.头.子类 == 枚举_消息子类::收束结果
            || 消息.头.子类 == 枚举_消息子类::异常结果;
        动作.可能触发停止 = 消息.头.子类 == 枚举_消息子类::异常结果;
        break;

    case 枚举_消息大类::学习与审计消息:
        动作.更新学习候选池 = true;
        动作.进入学习后置队列 = true;
        动作.仅写摘要 = true;
        break;

    case 枚举_消息大类::未定义:
    default:
        break;
    }

    return 动作;
}

