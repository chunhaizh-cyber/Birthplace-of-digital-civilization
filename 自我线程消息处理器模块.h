inline 结构_单消息处理结果 处理治理消息(
    const 自我线程消息协议::结构_治理消息& 消息) noexcept
{
    using namespace 自我线程消息协议;

    switch (消息.头.大类) {
    case 枚举_消息大类::控制消息:
        return 处理控制消息_(消息);
    case 枚举_消息大类::全局环境消息:
        return 处理全局环境消息_(消息);
    case 枚举_消息大类::工作线程状态消息:
        return 处理工作线程状态消息_(消息);
    case 枚举_消息大类::任务推进关键事件消息:
        return 处理任务推进关键事件_(消息);
    case 枚举_消息大类::派生需求消息:
        return 处理派生需求消息_(消息);
    case 枚举_消息大类::结果回流消息:
        return 处理结果回流消息_(消息);
    case 枚举_消息大类::学习与审计消息:
        return 处理学习与审计消息_(消息);
    case 枚举_消息大类::未定义:
    default:
        return {};
    }
}

inline 结构_消息批次处理摘要 处理治理消息批次(
    const std::vector<自我线程消息协议::结构_治理消息>& 消息列表) noexcept
{
    结构_消息批次处理摘要 摘要{};
    for (const auto& 消息 : 消息列表) {
        const auto 结果 = 处理治理消息(消息);
        摘要.触发需求队列刷新 =
            摘要.触发需求队列刷新 || 结果.触发需求队列刷新;
        摘要.触发根层重判 =
            摘要.触发根层重判 || 结果.动作.触发根层重判;
        摘要.触发主派发决议 =
            摘要.触发主派发决议 || 结果.动作.触发主派发决议;
        摘要.可能实例化新任务 =
            摘要.可能实例化新任务 || 结果.动作.可能实例化新任务;
        摘要.可能触发收束 =
            摘要.可能触发收束 || 结果.动作.可能触发收束;
        摘要.可能触发停止 =
            摘要.可能触发停止 || 结果.动作.可能触发停止;
        摘要.进入学习后置队列 =
            摘要.进入学习后置队列 || 结果.动作.进入学习后置队列;
        摘要.有派生需求待实例化 =
            摘要.有派生需求待实例化 || 结果.有派生需求待实例化;
        摘要.有子结果待路由 =
            摘要.有子结果待路由 || 结果.有子结果待路由;
    }
    return 摘要;
}

inline 结构_消息批次处理摘要 处理推进事件批次(
    const std::vector<结构_任务推进事件>& 推进事件列表) noexcept
{
    std::vector<自我线程消息协议::结构_治理消息> 桥接消息列表{};
    桥接消息列表.reserve(推进事件列表.size());
    for (const auto& 事件 : 推进事件列表) {
        桥接消息列表.push_back(自我线程消息协议::从任务推进事件桥接治理消息(事件));
    }
    return 处理治理消息批次(桥接消息列表);
}

inline 结构_需求列表刷新决策 判定需求列表刷新决策(
    const bool 服务优先需求列表为空,
    const bool 安全优先需求列表为空,
    const bool 已应用需求树变更,
    const bool 存在受影响类型,
    const 结构_消息批次处理摘要& 本轮消息处理摘要,
    const 结构_消息批次处理摘要& 推进事件处理摘要) noexcept
{
    结构_需求列表刷新决策 决策{};
    决策.需要刷新 =
        服务优先需求列表为空
        || 安全优先需求列表为空
        || 已应用需求树变更
        || 本轮消息处理摘要.触发需求队列刷新
        || 推进事件处理摘要.触发需求队列刷新;

    决策.可以局部刷新 =
        决策.需要刷新
        && 已应用需求树变更
        && 存在受影响类型
        && !服务优先需求列表为空
        && !安全优先需求列表为空
        && !本轮消息处理摘要.触发需求队列刷新
        && !推进事件处理摘要.触发需求队列刷新;

    return 决策;
}

inline bool 应用需求树变更批次(
    自我类& 自我对象,
    const std::deque<自我线程消息协议::结构_治理消息>& 本轮冻结消息,
    结构_需求分支刷新范围* 输出刷新范围) noexcept
{
    auto* 自我存在 = 自我对象.获取自我存在();
    auto* 需求根节点 = 自我存在 ? 世界树.存在().获取需求根节点(自我存在) : nullptr;
    if (!需求根节点 || 本轮冻结消息.empty()) {
        return false;
    }

    需求类 需求服务{};
    std::size_t 已应用数量 = 0;
    for (const auto& 消息 : 本轮冻结消息) {
        if (!消息.带需求树变更) {
            continue;
        }

        auto* 父需求 = 定位需求节点(
            需求根节点,
            消息.需求树变更.父需求指针,
            消息.需求树变更.父需求主键);
        if (!父需求) {
            父需求 = 需求根节点;
        }
        auto* 目标需求 = 定位需求节点(
            需求根节点,
            消息.需求树变更.目标需求指针,
            消息.需求树变更.目标需求主键);

        const auto 变更类型 = 消息.需求树变更.指令类型;
        auto* 变更前受影响子树根 =
            (变更类型 == 枚举_需求树更新指令类型::新增需求
                || 变更类型 == 枚举_需求树更新指令类型::删除需求)
            ? 父需求
            : (目标需求 ? 目标需求 : 父需求);
        if (输出刷新范围) {
            收集需求子树快照(
                变更前受影响子树根,
                &输出刷新范围->变更前节点主键集合,
                &输出刷新范围->受影响类型集合);
            if (消息.需求树变更.需求类型) {
                输出刷新范围->受影响类型集合.insert(消息.需求树变更.需求类型);
            }
        }

        auto* 结果节点 = 需求服务.执行更新指令(需求根节点, 消息.需求树变更);
        if (!结果节点) {
            continue;
        }

        if (输出刷新范围) {
            auto* 变更后受影响子树根 =
                (变更类型 == 枚举_需求树更新指令类型::新增需求
                    || 变更类型 == 枚举_需求树更新指令类型::删除需求)
                ? 结果节点
                : (目标需求 ? 目标需求 : 结果节点);
            收集需求子树快照(
                变更后受影响子树根,
                &输出刷新范围->当前节点主键集合,
                &输出刷新范围->受影响类型集合);
        }

        ++已应用数量;
    }

    return 已应用数量 > 0;
}

