    枚举_主信息类型 私有_取基础信息类型(const 基础信息节点类* 节点) noexcept
    {
        return (节点 && 节点->主信息)
            ? 节点->主信息->主信息类型
            : 枚举_主信息类型::未定义;
    }

    bool 私有_引用指向(const 可解析引用<基础信息节点类>& 引用, const 基础信息节点类* 节点)
    {
        if (!节点) return false;
        if (引用.指针 == 节点) return true;
        return !引用.主键.empty() && 引用.主键 == 节点->获取主键();
    }

    template<class T节点>
    void 私有_追加唯一引用(std::vector<可解析引用<T节点>>& 列表, T节点* 节点)
    {
        if (!节点) return;
        const std::string 主键 = 节点->获取主键();
        for (const auto& 已有 : 列表) {
            if (已有.指针 == 节点) return;
            if (!主键.empty() && 已有.主键 == 主键) return;
        }
        列表.emplace_back(节点);
    }

    bool 私有_场景索引包含(const 场景节点类* 场景, const 基础信息节点类* 基础信息节点)
    {
        if (!场景 || !基础信息节点 || !场景->主信息) return false;
        auto* 场景主信息 = dynamic_cast<const 场景节点主信息类*>(场景->主信息);
        if (!场景主信息) return false;

        const auto 检查列表 = [&](const auto& 列表) {
            for (const auto& 引用 : 列表) {
                if (私有_引用指向(引用, 基础信息节点)) return true;
            }
            return false;
        };

        return 检查列表(场景主信息->状态索引)
            || 检查列表(场景主信息->动态索引)
            || 检查列表(场景主信息->关系索引)
            || 检查列表(场景主信息->二次特征索引)
            || 检查列表(场景主信息->实例因果索引);
    }

    void 私有_把基础信息挂入场景索引(场景节点类* 场景, 基础信息节点类* 基础信息节点)
    {
        if (!场景 || !基础信息节点 || !场景->主信息 || !基础信息节点->主信息) return;
        auto* 场景主信息 = dynamic_cast<场景节点主信息类*>(场景->主信息);
        if (!场景主信息) return;

        switch (基础信息节点->主信息->主信息类型) {
        case 枚举_主信息类型::状态:
            私有_追加唯一引用(场景主信息->状态索引, static_cast<状态节点类*>(基础信息节点));
            break;
        case 枚举_主信息类型::动态:
            私有_追加唯一引用(场景主信息->动态索引, static_cast<动态节点类*>(基础信息节点));
            break;
        case 枚举_主信息类型::二次特征_修饰:
            私有_追加唯一引用(场景主信息->二次特征索引, static_cast<二次特征节点类*>(基础信息节点));
            break;
        case 枚举_主信息类型::关系:
        case 枚举_主信息类型::关系信息:
            私有_追加唯一引用(场景主信息->关系索引, static_cast<二次特征节点类*>(基础信息节点));
            break;
        case 枚举_主信息类型::因果:
            私有_追加唯一引用(场景主信息->实例因果索引, static_cast<因果实例节点类*>(基础信息节点));
            break;
        default:
            break;
        }
    }

    std::string 私有_获取语素节点显示词(const 语素节点类* 节点)
    {
        if (!节点 || !节点->主信息) return {};
        if (auto* 词 = dynamic_cast<const 词主信息类*>(节点->主信息)) {
            return 词->词;
        }
        if (auto* 词性 = dynamic_cast<const 语素入口主信息类*>(节点->主信息)) {
            if (节点->父 && 节点->父->主信息) {
                if (auto* 父词 = dynamic_cast<const 词主信息类*>(节点->父->主信息)) {
                    return 父词->词;
                }
            }
            return 枚举_词性_工厂::根据枚举类型获取文本(词性->词性);
        }
        return {};
    }

