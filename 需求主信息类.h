#pragma once

#include <cstdint>
#include <vector>

#include "语言节点类型.h"
#include "语素节点类型.h"
#include "基础信息节点类型.h"
#include "双向链表模板核心.h"

class 任务节点类;

enum class 枚举_需求结构角色 : std::uint8_t {
    未定义 = 0, // 尚未判断该需求在需求树中的职责。
    管理需求 = 1, // 非叶子需求：目标是组织、等待并汇总子需求。
    执行需求 = 2, // 叶子需求：目标是直接推动被需求对象到目标状态。
};

// ============================================================================
// 需求主信息
// - 需求只表达“某个对象从当前状态到目标状态”的目标关系。
// - 单个执行需求节点只表达一个原子目标；多目标需求由管理需求的直接子链展开。
// - 需求主信息不保存目标值类型，不解释集合、区间、枚举或二次特征。
// - 状态值比较由特征类或状态比较服务完成，需求类只接收三向比较结果。
// - 需求树的父子结构由节点自身的父/子/上/下指针表达，不在主信息中重复存储。
// - 每个需求节点拥有一个对应任务；任务列表只保存历史、候选和重建痕迹。
// ============================================================================
class 需求主信息类 {
public:
    const 自然句节点类* 描述信息 = nullptr; // 对需求的人类可读说明，可为空。
    const 语素入口节点类* 类型 = nullptr; // 需求类型词，用于区分任务状态需求、方法状态需求等类别。
    结构_统计 统计{}; // 需求被观察、调度、更新的运行统计。

    枚举_需求结构角色 结构角色 = 枚举_需求结构角色::未定义; // 该需求在需求树中承担管理还是执行职责。
    可解析引用<存在节点类> 需求主体{}; // 提出或拥有该需求的存在，通常是自我存在。
    可解析引用<场景节点类> 需求场景{}; // 该需求成立或被调度时所处的场景。
    可解析引用<存在节点类> 被需求存在{}; // 需要被改变、被满足或被管理的目标存在。
    可解析引用<状态节点类> 被需求当前状态{}; // 被需求存在当前已知的状态。
    可解析引用<状态节点类> 需求状态{}; // 希望被需求存在达到的目标状态。
    三向关系掩码 满足关系 = 关系_等于; // 当前状态与目标状态满足哪些三向关系时视为满足。
    const 语素入口节点类* 目标特征类型缓存 = nullptr; // 从状态特征解析出的目标特征类型缓存，不作为权威语义源。

    可解析引用<任务节点类> 对应任务{}; // 该需求节点当前权威运行壳。
    std::vector<可解析引用<任务节点类>> 任务列表{}; // 该需求关联过的任务集合，用于候选、历史和重建。

    时间戳 需求有效截止 = 0; // 需求失效时间；0 表示尚未设置失效时间。
    bool 是否阻塞父任务执行 = true; // 活动子需求判定闸门；旁支、观察或弱证据需求可设为 false。
    std::int64_t 安全权重 = 0; // 安全维度调度权重，越高越优先。
    std::int64_t 服务权重 = 0; // 服务维度调度权重，越高越优先。

    std::int64_t 累计安全结算 = 0; // 任务反馈账本已回写到该需求的安全增量。
    std::int64_t 累计服务结算 = 0; // 任务反馈账本已回写到该需求的服务增量。
    std::string 最近结算任务主键{}; // 最近一次贡献结算的任务。
    时间戳 最近结算时间 = 0; // 最近一次结算时间。

    // 派生需求归因：用于记录来源、自检归因、控制面板观察和重复触发统计。
    // 需求去重不使用派生归因；需求去重以同父子链上的目标特征类型和方向重叠为准。
    std::string 派生来源方法主键{};
    std::uintptr_t 派生需求类型抽象特征指针 = 0;
    std::int64_t 派生需求类型值 = 0;
    std::uintptr_t 派生方法需求位抽象特征指针 = 0;
    std::int64_t 派生方法需求位值 = 0;
    std::uintptr_t 派生本能能力缺口类型抽象特征指针 = 0;
    std::int64_t 派生本能能力缺口类型值 = 0;

    std::int64_t 比较(const 需求主信息类* 对象, 枚举_比较字段 字段) const {
        return 对象 ? 比较(*对象, 字段) : -1;
    }

    std::int64_t 比较(const 需求主信息类& 对象, 枚举_比较字段 字段) const {
        switch (字段) {
        case 枚举_比较字段::类型:
            return 类型 == 对象.类型 ? 1 : 0;
        case 枚举_比较字段::产生需求主体:
            return 私有_引用相等(需求主体, 对象.需求主体) ? 1 : 0;
        case 枚举_比较字段::需求对象:
            return 私有_引用相等(被需求存在, 对象.被需求存在) ? 1 : 0;
        case 枚举_比较字段::需求状态:
            return 私有_引用相等(需求状态, 对象.需求状态) ? 1 : 0;
        default:
            return -1;
        }
    }

private:
    template<class T节点>
    static bool 私有_引用相等(const 可解析引用<T节点>& a, const 可解析引用<T节点>& b) noexcept {
        if (a.指针 || b.指针) return a.指针 == b.指针;
        return a.主键 == b.主键;
    }
};
