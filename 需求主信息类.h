#pragma once

#include "语言节点类型.h"
#include "语素节点类型.h"
#include "基础信息节点类型.h"
#include "双向链表模板核心.h"

class 任务节点类;

// ============================================================================
// 需求 / 任务 / 方法
// - 高层结构只保留“定义字段”，不在这里塞清理逻辑与深层嵌套对象。
// ============================================================================
class 需求主信息类 {
public:
    const 自然句节点类* 描述信息 = nullptr;
    const 词性节点类* 类型 = nullptr;
    结构_统计 统计{};

    可解析引用<存在节点类> 需求主体{};
    可解析引用<场景节点类> 需求场景{};
    可解析引用<存在节点类> 被需求存在{};
    可解析引用<状态节点类> 需求状态{};
    可解析引用<存在节点类> 需求虚拟存在{};
    可解析引用<任务节点类> 相关任务{};
    可解析引用<状态节点类> 被需求当前状态{};
    时间戳 需求有效截止 = 0;
    std::int64_t 安全权重 = 0;
    std::int64_t 服务权重 = 0;

    std::int64_t 比较(const 需求主信息类* 对象, 枚举_比较字段 字段) const {
        return 对象 ? 比较(*对象, 字段) : -1;
    }

    std::int64_t 比较(const 需求主信息类& 对象, 枚举_比较字段 字段) const {
        switch (字段) {
        case 枚举_比较字段::类型:
            return 类型 == 对象.类型 ? 1 : 0;
        case 枚举_比较字段::产生需求主体:
            return 私有_引用相等(需求主体, 对象.需求主体) ? 1 : 0;
        case 枚举_比较字段::需求对象:
            return 私有_引用相等(被需求存在, 对象.被需求存在) ? 1 : 0;
        case 枚举_比较字段::需求状态:
            return 私有_引用相等(需求状态, 对象.需求状态) ? 1 : 0;
        default:
            return -1;
        }
    }

private:
    template<class T节点>
    static bool 私有_引用相等(const 可解析引用<T节点>& a, const 可解析引用<T节点>& b) noexcept {
        if (a.指针 || b.指针) return a.指针 == b.指针;
        return a.主键 == b.主键;
    }
};
