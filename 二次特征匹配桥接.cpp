#include "二次特征匹配桥接.h"

import 二次特征匹配模块;

bool 二次特征满足_桥接(
    const 二次特征主信息类* 条件,
    const 二次特征主信息类* 状态,
    const 二次特征匹配配置_桥接& 配置) noexcept
{
    二次特征匹配模块::结构_匹配配置 模块配置{};
    模块配置.使用对象绑定 = 配置.使用对象绑定;
    模块配置.条件空指针视为通配 = 配置.条件空指针视为通配;
    模块配置.允许用概念名称弱匹配 = 配置.允许用概念名称弱匹配;
    return 二次特征匹配模块::二次特征_满足(条件, 状态, 模块配置);
}

bool 条件索引满足_桥接(
    const std::vector<const 二次特征节点类*>& 条件索引,
    const std::vector<const 二次特征节点类*>& 当前判定集,
    std::int64_t* 输出满足数,
    const 二次特征匹配配置_桥接& 配置) noexcept
{
    二次特征匹配模块::结构_匹配配置 模块配置{};
    模块配置.使用对象绑定 = 配置.使用对象绑定;
    模块配置.条件空指针视为通配 = 配置.条件空指针视为通配;
    模块配置.允许用概念名称弱匹配 = 配置.允许用概念名称弱匹配;
    return 二次特征匹配模块::条件索引_满足(条件索引, 当前判定集, 输出满足数, 模块配置);
}

二次特征匹配结果_桥接 结果摘要匹配目标_桥接(
    const std::vector<const 二次特征节点类*>& 摘要索引,
    const std::vector<const 二次特征节点类*>& 目标判定集,
    const 二次特征匹配配置_桥接& 配置) noexcept
{
    二次特征匹配模块::结构_匹配配置 模块配置{};
    模块配置.使用对象绑定 = 配置.使用对象绑定;
    模块配置.条件空指针视为通配 = 配置.条件空指针视为通配;
    模块配置.允许用概念名称弱匹配 = 配置.允许用概念名称弱匹配;

    const auto 模块结果 = 二次特征匹配模块::结果摘要_匹配目标(摘要索引, 目标判定集, 模块配置);
    return {
        模块结果.命中数,
        模块结果.摘要数,
        模块结果.目标数,
        模块结果.精确率_Q10000,
        模块结果.召回率_Q10000,
        模块结果.得分_Q10000,
    };
}
