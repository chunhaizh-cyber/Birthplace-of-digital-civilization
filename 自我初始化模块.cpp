    任务节点* 私有_确保任务执行学习任务(
        自我类& 自我对象,
        任务节点* 宿主任务,
        方法节点* 目标方法,
        时间戳 now,
        const std::string& 调用点) noexcept
    {
        if (!宿主任务 || !目标方法) {
            return nullptr;
        }

        auto* 来源需求 = reinterpret_cast<需求节点*>(宿主任务->主信息.来源需求.指针);
        if (!来源需求) {
            来源需求 = 自我对象.获取当前主需求();
        }
        if (!来源需求) {
            来源需求 = 自我对象.获取安全根需求();
        }
        if (!来源需求) {
            return nullptr;
        }

        const auto* 学习任务名称 = 私有_任务执行学习任务名称词(宿主任务);
        auto* 学习任务 = 私有_查找直接子节点_按谓词<任务节点>(宿主任务, [&](const 任务节点* 节点) noexcept {
            return 节点
                && 节点->主信息.节点种类 == 枚举_任务节点种类::头结点
                && 节点->主信息.名称 == 学习任务名称
                && 节点->主信息.类型 == 私有_学习任务类型词()
                && 节点->主信息.状态 != 枚举_任务状态::完成
                && 节点->主信息.状态 != 枚举_任务状态::取消;
        });

        任务类 任务服务{};
        if (!学习任务) {
            学习任务 = 任务服务.创建任务头结点(
                来源需求,
                宿主任务,
                now,
                调用点 + "/创建学习任务");
            if (!学习任务) {
                return nullptr;
            }
        }

        学习任务->主信息.名称 = 学习任务名称;
        学习任务->主信息.类型 = 私有_学习任务类型词();
        if (学习任务->主信息.状态 == 枚举_任务状态::未启动) {
            学习任务->主信息.状态 = 枚举_任务状态::筹办中;
        }
        私有_绑定等待学习方法(学习任务, 目标方法);

        if (auto* 尝试学习方法 = 私有_确保尝试学习本能方法头(
            自我对象,
            now,
            调用点 + "/尝试学习方法")) {
            私有_绑定任务执行方法(学习任务, 尝试学习方法);
            if (!学习任务->主信息.当前步骤节点.有效()) {
                auto* 学习步骤 = 任务服务.创建任务步骤节点(
                    学习任务,
                    nullptr,
                    reinterpret_cast<方法节点类*>(尝试学习方法),
                    nullptr,
                    now,
                    调用点 + "/学习步骤");
                if (学习步骤 && 学习步骤->主信息.状态 == 枚举_任务状态::未启动) {
                    学习步骤->主信息.状态 = 枚举_任务状态::就绪;
                }
            }
        }

        return 学习任务;
    }

