    任务节点* 私有_查找任务管理本体步骤(任务节点* 管理任务, const char* 步骤名称) noexcept
    {
        if (!管理任务) {
            return nullptr;
        }
        const auto* 类型词 = 私有_任务管理本体步骤类型词(步骤名称);
        std::vector<任务节点*> 待查{ 管理任务 };
        std::size_t 保护计数 = 0;
        while (!待查.empty() && ++保护计数 < 2048) {
            auto* 当前 = 待查.back();
            待查.pop_back();
            if (当前
                && 当前 != 管理任务
                && 当前->主信息.节点种类 == 枚举_任务节点种类::步骤节点
                && 当前->主信息.类型 == 类型词) {
                return 当前;
            }
            if (!当前 || !当前->子) {
                continue;
            }
            auto* 首子 = reinterpret_cast<任务节点*>(当前->子);
            auto* 子 = 首子;
            std::size_t 子保护 = 0;
            do {
                if (子) {
                    待查.push_back(子);
                }
                子 = 子 ? reinterpret_cast<任务节点*>(子->下) : nullptr;
                ++子保护;
            } while (子 && 子 != 首子 && 子保护 < 512);
        }
        return nullptr;
    }

