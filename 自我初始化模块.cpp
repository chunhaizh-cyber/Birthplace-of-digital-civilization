    bool 私有_状态目标已达到(
        const 状态节点类* 起点状态,
        const 状态节点类* 当前状态,
        const 状态节点类* 目标状态) noexcept
    {
        const auto* 当前特征类型 = 私有_状态节点目标特征类型(当前状态);
        const auto* 目标特征类型 = 私有_状态节点目标特征类型(目标状态);
        if (!当前状态 || !目标状态 || !私有_词性同一(当前特征类型, 目标特征类型)) {
            return false;
        }

        I64 当前值 = 0;
        I64 目标值 = 0;
        if (!私有_读取状态I64值(当前状态, 当前值)
            || !私有_读取状态I64值(目标状态, 目标值)) {
            return 当前状态 == 目标状态;
        }

        I64 起点值 = 0;
        if (!私有_读取状态I64值(起点状态, 起点值)) {
            return 当前值 == 目标值;
        }
        if (目标值 > 起点值) {
            return 当前值 >= 目标值;
        }
        if (目标值 < 起点值) {
            return 当前值 <= 目标值;
        }
        return 当前值 == 目标值;
    }

