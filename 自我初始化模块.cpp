    bool 私有_方法节点属于树(
        const 方法节点* 根节点,
        const 方法节点* 候选节点) noexcept
    {
        if (!根节点 || !候选节点) {
            return false;
        }
        if (根节点 == 候选节点) {
            return true;
        }
        if (!根节点->子) {
            return false;
        }
        auto* 首节点 = static_cast<const 方法节点*>(根节点->子);
        auto* 当前 = 首节点;
        std::size_t 保护计数 = 0;
        do {
            if (当前 == 候选节点) {
                return true;
            }
            if (当前 && 私有_方法节点属于树(当前, 候选节点)) {
                return true;
            }
            当前 = 当前 ? static_cast<const 方法节点*>(当前->下) : nullptr;
            ++保护计数;
        } while (当前 && 当前 != 首节点 && 保护计数 < 4096);
        return false;
    }

    bool 私有_是任务筹办本能方法(const 方法节点* 方法) noexcept
    {
        return 方法
            && 方法->主信息.节点种类 == 枚举_方法节点种类::方法首节点
            && 方法->主信息.动作句柄.类型 == 枚举_动作句柄类型::本能函数ID
            && 方法->主信息.动作句柄.本能ID == static_cast<I64>(枚举_本能方法ID::自我_任务筹办);
    }

