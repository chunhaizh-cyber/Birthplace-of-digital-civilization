bool 自我初始化模块::执行自我初始化(
    自我类& 自我对象,
    const std::string& 调用点,
    结构_自我初始化结果* 输出) noexcept
{
    世界树.初始化();
    世界树.初始化默认世界();
    自我特征定义类::初始化依赖语素();

    自我类::当前活动自我_.store(&自我对象, std::memory_order_release);
    (void)自我对象.确保自我现实场景_(调用点 + "/自我现实场景");
    (void)自我对象.确保自我内部世界_(调用点 + "/自我内部世界");

    自我对象.初始化自我特征与默认值_(调用点 + "/初始化自我特征");
    自我对象.从特征恢复运行态_(调用点 + "/恢复运行态");
    (void)本能方法类::注册默认自我本能方法集(自我对象, 调用点 + "/默认本能方法");
    私有_确保本能动作运行时已注册(自我对象, 调用点 + "/运行时本能动作");
    自我对象.确保自我根需求与根任务已初始化_(调用点 + "/初始化根需求与根任务");
    自我对象.确保主链镜像已初始化_(调用点 + "/初始化主链镜像");
    私有_补齐初始化任务解决方法集(自我对象, 调用点 + "/初始化任务解决方法集");
    自我对象.重算时序回归步长_按当前双值_();
    自我对象.同步运行态特征_(调用点 + "/同步运行态特征");

    auto 结果 = 私有_计算自我初始化结果(自我对象, false);
    自我对象.已初始化_.store(结果.初始化完成, std::memory_order_release);

    结果 = 私有_计算自我初始化结果(自我对象, true);
    if (输出) {
        *输出 = 结果;
    }
    return 结果.初始化完成;
}

