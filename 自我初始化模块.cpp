    bool 私有_方法命中任务目标(
        const 方法节点* 方法,
        const std::vector<std::string>& 目标特征主键列表) noexcept
    {
        if (!方法 || 目标特征主键列表.empty()) {
            return false;
        }

        if (方法->主信息.主结果特征类型) {
            const auto 主结果特征主键 = 方法->主信息.主结果特征类型->获取主键();
            if (std::find(目标特征主键列表.begin(), 目标特征主键列表.end(), 主结果特征主键)
                != 目标特征主键列表.end()) {
                return true;
            }
        }

        const auto 状态命中目标 = [&](const 可解析引用<状态节点类>& 状态引用) noexcept {
            const auto* 特征类型 = 私有_状态节点目标特征类型(状态引用.指针);
            if (!特征类型) {
                return false;
            }
            const auto 特征主键 = 特征类型->获取主键();
            return std::find(目标特征主键列表.begin(), 目标特征主键列表.end(), 特征主键)
                != 目标特征主键列表.end();
        };
        for (const auto& 状态引用 : 方法->主信息.结果初始状态) {
            if (状态命中目标(状态引用)) {
                return true;
            }
        }

        if (!方法->子) {
            return false;
        }
        auto* 首节点 = static_cast<const 方法节点*>(方法->子);
        auto* 当前 = 首节点;
        do {
            if (当前 && 当前->主信息.节点种类 == 枚举_方法节点种类::方法结果节点) {
                if (当前->主信息.主结果特征类型) {
                    const auto 主结果特征主键 = 当前->主信息.主结果特征类型->获取主键();
                    if (std::find(目标特征主键列表.begin(), 目标特征主键列表.end(), 主结果特征主键)
                        != 目标特征主键列表.end()) {
                        return true;
                    }
                }
                for (const auto& 状态引用 : 当前->主信息.结果初始状态) {
                    if (状态命中目标(状态引用)) {
                        return true;
                    }
                }
            }
            当前 = 当前 ? static_cast<const 方法节点*>(当前->下) : nullptr;
        } while (当前 && 当前 != 首节点);
        return false;
    }

