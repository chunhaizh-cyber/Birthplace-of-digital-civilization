    template<class T节点, class T谓词>
    T节点* 私有_查找直接子节点_按谓词(T节点* 父节点, T谓词&& 谓词) noexcept
    {
        if (!父节点 || !父节点->子) {
            return nullptr;
        }

        auto* 首节点 = static_cast<T节点*>(父节点->子);
        auto* 当前 = 首节点;
        do {
            if (当前 && 谓词(当前)) {
                return 当前;
            }
            当前 = static_cast<T节点*>(当前->下);
        } while (当前 && 当前 != 首节点);
        return nullptr;
    }

    std::string 私有_安全词(const 词性节点类* 词) noexcept
    {
        if (!词) {
            return {};
        }
        try {
            return 语素集.获取词(词);
        }
        catch (...) {
            return {};
        }
    }

    std::string 私有_需求标题(const 任务节点* 宿主任务) noexcept
    {
        auto* 来源需求 = 宿主任务
            ? reinterpret_cast<需求节点*>(宿主任务->主信息.来源需求.指针)
            : nullptr;
        if (来源需求 && 来源需求->主信息.类型) {
            const auto 标题 = 私有_安全词(来源需求->主信息.类型);
            if (!标题.empty()) {
                return 标题;
            }
        }

        if (auto* 自我对象 = 自我类::当前活动自我()) {
            if (宿主任务 == 自我对象->获取安全根任务()) {
                if (auto* 安全根需求 = 自我对象->获取安全根需求()) {
                    const auto 标题 = 私有_安全词(安全根需求->主信息.类型);
                    if (!标题.empty()) {
                        return 标题;
                    }
                }
                return "自我的安全需求";
            }
            if (宿主任务 == 自我对象->获取服务根任务()) {
                if (auto* 服务根需求 = 自我对象->获取服务根需求()) {
                    const auto 标题 = 私有_安全词(服务根需求->主信息.类型);
                    if (!标题.empty()) {
                        return 标题;
                    }
                }
                return "自我的服务需求";
            }
        }

        return "当前需求";
    }

