    bool 私有_方法是治理本能方法(const 方法节点* 方法) noexcept
    {
        if (!方法 || 方法->主信息.动作句柄.类型 != 枚举_动作句柄类型::本能函数ID) {
            return false;
        }

        switch (static_cast<枚举_本能方法ID>(方法->主信息.动作句柄.本能ID)) {
        case 枚举_本能方法ID::自我_任务筹办:
        case 枚举_本能方法ID::自我_任务执行一步:
        case 枚举_本能方法ID::自我_学习任务:
            return true;
        default:
            return false;
        }
    }

