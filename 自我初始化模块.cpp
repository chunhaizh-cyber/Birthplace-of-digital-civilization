    void 私有_收集任务目标特征主键(
        const 任务节点* 宿主任务,
        std::vector<std::string>& 输出) noexcept
    {
        if (!宿主任务) {
            return;
        }

        const auto 追加主键 = [&](const std::string& 主键) noexcept {
            if (主键.empty()) {
                return;
            }
            if (std::find(输出.begin(), 输出.end(), 主键) == 输出.end()) {
                输出.push_back(主键);
            }
        };

        if (const auto* 目标特征类型 = 私有_任务首个目标特征类型(宿主任务)) {
            追加主键(目标特征类型->获取主键());
        }

        const auto* 来源需求 = reinterpret_cast<const 需求节点*>(宿主任务->主信息.来源需求.指针);
        if (来源需求) {
            if (const auto* 来源需求特征类型 = 私有_状态节点目标特征类型(来源需求->主信息.需求状态.指针)) {
                追加主键(来源需求特征类型->获取主键());
            }
            if (const auto* 当前状态特征类型 = 私有_状态节点目标特征类型(来源需求->主信息.被需求当前状态.指针)) {
                追加主键(当前状态特征类型->获取主键());
            }
        }
    }

    bool 私有_方法命中任务目标(
        const 方法节点* 方法,
        const std::vector<std::string>& 目标特征主键列表) noexcept
    {
        if (!方法 || 目标特征主键列表.empty()) {
            return false;
        }

        if (方法->主信息.主结果特征类型) {
            const auto 主结果特征主键 = 方法->主信息.主结果特征类型->获取主键();
            if (std::find(目标特征主键列表.begin(), 目标特征主键列表.end(), 主结果特征主键)
                != 目标特征主键列表.end()) {
                return true;
            }
        }

        for (const auto& 影响签名 : 方法->主信息.影响度量签名) {
            if (std::find(目标特征主键列表.begin(), 目标特征主键列表.end(), 影响签名)
                != 目标特征主键列表.end()) {
                return true;
            }
        }
        return false;
    }

    方法节点* 私有_按任务目标查找方法头(
        方法节点* 方法根节点,
        const std::vector<std::string>& 目标特征主键列表) noexcept
    {
        if (!方法根节点 || !方法根节点->子 || 目标特征主键列表.empty()) {
            return nullptr;
        }

        auto* 首节点 = static_cast<方法节点*>(方法根节点->子);
        auto* 当前 = 首节点;
        方法节点* 最优方法 = nullptr;
        std::int32_t 最优分数 = -1;
        do {
            if (!当前
                || 当前->主信息.节点种类 != 枚举_方法节点种类::方法首节点
                || 私有_方法是治理本能方法(当前)
                || !私有_方法命中任务目标(当前, 目标特征主键列表)) {
                当前 = static_cast<方法节点*>(当前->下);
                continue;
            }

            std::int32_t 当前分数 = 0;
            if (当前->主信息.主结果特征类型) {
                const auto 主结果特征主键 = 当前->主信息.主结果特征类型->获取主键();
                if (std::find(目标特征主键列表.begin(), 目标特征主键列表.end(), 主结果特征主键)
                    != 目标特征主键列表.end()) {
                    当前分数 += 100;
                }
            }
            if (当前->主信息.有动作骨架()) {
                当前分数 += 20;
            }
            if (当前->主信息.有最小出生锚点()) {
                当前分数 += 10;
            }
            if (!当前->主信息.条件判定索引.empty()) {
                当前分数 += 5;
            }

            if (当前分数 > 最优分数) {
                最优方法 = 当前;
                最优分数 = 当前分数;
            }
            当前 = static_cast<方法节点*>(当前->下);
        } while (当前 && 当前 != 首节点);

        return 最优方法;
    }

    方法节点* 私有_解析或创建任务目标方法(
        自我类& 自我对象,
        任务节点* 宿主任务,
        时间戳 now,
        const std::string& 调用点,
        bool* 输出已新建占位方法 = nullptr) noexcept
    {
        if (输出已新建占位方法) {
            *输出已新建占位方法 = false;
        }
        if (!宿主任务) {
            return nullptr;
        }

        auto* 已选方法 = reinterpret_cast<方法节点*>(宿主任务->主信息.执行方法.指针);
        std::vector<std::string> 目标特征主键列表{};
        私有_收集任务目标特征主键(宿主任务, 目标特征主键列表);
        if (已选方法
            && !私有_方法是治理本能方法(已选方法)
            && 私有_方法命中任务目标(已选方法, 目标特征主键列表)) {
            return 已选方法;
        }

        auto* 自我存在 = 自我对象.确保自我存在(调用点 + "/自我存在");
        auto* 方法根节点 = 自我存在 ? 世界树.存在().获取方法根节点(自我存在) : nullptr;
        if (!方法根节点) {
            return nullptr;
        }

        auto* 目标方法 = 私有_按任务目标查找方法头(方法根节点, 目标特征主键列表);
        if (!目标方法) {
            const auto* 目标特征类型 = 私有_任务首个目标特征类型(宿主任务);
            if (目标特征类型) {
                目标方法 = 方法类::注册需求位方法首节点(
                    方法根节点,
                    目标特征类型,
                    枚举_方法来源::运行时学习沉淀,
                    now,
                    调用点 + "/注册目标方法");
                if (目标方法 && 输出已新建占位方法) {
                    *输出已新建占位方法 = true;
                }
                if (目标方法) {
                    私有_写入任务节点引用(目标方法->主信息.来源任务, 宿主任务);
                }
            }
        }

        if (目标方法) {
            私有_绑定任务执行方法(宿主任务, 目标方法);
            私有_绑定任务候选方法(宿主任务, 目标方法);
        }
        return 目标方法;
    }

