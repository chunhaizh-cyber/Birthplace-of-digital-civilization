    任务节点* 私有_查找最新步骤节点(任务节点* 任务头) noexcept
    {
        任务头 = 私有_归一到任务头结点(任务头);
        if (!任务头) {
            return nullptr;
        }

        std::vector<任务节点*> 待查{ 任务头 };
        任务节点* 最新步骤 = nullptr;
        时间戳 最新时间 = 0;
        while (!待查.empty()) {
            auto* 当前 = 待查.back();
            待查.pop_back();
            if (!当前) {
                continue;
            }
            if (当前 != 任务头 && 任务类::读取任务节点种类(当前) == 枚举_任务节点种类::步骤节点) {
                const auto 当前时间 = 当前->主信息.完成时间 != 0
                    ? 当前->主信息.完成时间
                    : (当前->主信息.启动时间 != 0 ? 当前->主信息.启动时间 : 当前->主信息.创建时间);
                if (!最新步骤 || 当前时间 >= 最新时间) {
                    最新步骤 = 当前;
                    最新时间 = 当前时间;
                }
            }
            if (!当前->子) {
                continue;
            }
            auto* 首子 = static_cast<任务节点*>(当前->子);
            auto* 子 = 首子;
            do {
                if (子) {
                    待查.push_back(子);
                }
                子 = 子 ? static_cast<任务节点*>(子->下) : nullptr;
            } while (子 && 子 != 首子);
        }
        return 最新步骤;
    }

    方法节点* 私有_解析或创建任务目标方法(
        自我类& 自我对象,
        任务节点* 宿主任务,
        时间戳 now,
        bool* 输出已新建占位方法 = nullptr) noexcept
    {
        if (输出已新建占位方法) {
            *输出已新建占位方法 = false;
        }
        if (!宿主任务) {
            return nullptr;
        }

        if (auto* 已绑定方法 = 私有_任务已绑定非治理方法(宿主任务)) {
            私有_绑定任务执行方法(宿主任务, 已绑定方法);
            私有_绑定任务候选方法(宿主任务, 已绑定方法);
            return 已绑定方法;
        }

        auto* 自我存在 = 自我对象.确保自我存在();
        auto* 方法根节点 = 自我存在 ? 世界树.存在().获取方法根节点(自我存在) : nullptr;
        if (!方法根节点) {
            return nullptr;
        }

        方法节点* 目标方法 = nullptr;
        const auto* 目标特征类型 = 私有_任务首个目标特征类型(宿主任务);
        if (目标特征类型) {
            auto* 需求方向 = 私有_任务需求方向(宿主任务);
            目标方法 = 方法类::注册需求方向方法首节点(
                方法根节点,
                目标特征类型,
                需求方向,
                枚举_方法来源::运行时方法沉淀,
                now);
            if (目标方法 && 输出已新建占位方法) {
                *输出已新建占位方法 = true;
            }
            if (目标方法) {
                私有_写入任务节点引用(目标方法->主信息.首节点信息().来源任务, 宿主任务);
            }
        }

        if (目标方法) {
            私有_绑定任务执行方法(宿主任务, 目标方法);
            私有_绑定任务候选方法(宿主任务, 目标方法);
        }
        return 目标方法;
    }

