    void 私有_同步任务完成到需求并结算权重(
        自我类& 自我对象,
        任务节点* 宿主任务,
        const 结构_任务步骤执行结果_最小& 执行结果,
        bool 执行前已完成,
        时间戳 now,
        const std::string& 调用点) noexcept
    {
        if (!宿主任务
            || 执行前已完成
            || 执行结果.记账结果 != 枚举_任务执行记账结果::成功
            || 宿主任务->主信息.状态 != 枚举_任务状态::完成) {
            return;
        }

        auto* 来源需求 = 私有_任务来源需求(宿主任务);
        if (!来源需求) {
            return;
        }

        auto* 实际状态 = 宿主任务->主信息.结果状态信息.指针
            ? 宿主任务->主信息.结果状态信息.指针
            : 来源需求->主信息.需求状态.指针;
        if (实际状态) {
            私有_写入状态节点引用(来源需求->主信息.被需求当前状态, 实际状态);
            私有_写入状态节点引用(宿主任务->主信息.结果状态信息, 实际状态);
        }

        const bool 需求状态已达到 =
            !来源需求->主信息.需求状态.指针
            || 私有_状态目标已达到(
                来源需求->主信息.被需求当前状态.指针,
                实际状态,
                来源需求->主信息.需求状态.指针);
        if (!需求状态已达到) {
            宿主任务->主信息.状态 = 枚举_任务状态::待重筹办;
            宿主任务->主信息.完成时间 = 0;
            return;
        }

        if (来源需求->主信息.安全权重 > 0) {
            (void)自我对象.应用自我需求安全值增加(
                来源需求->主信息.安全权重,
                now,
                调用点 + "/安全权重结算");
        }
        if (来源需求->主信息.服务权重 > 0) {
            (void)自我对象.应用自我需求服务值增加(
                来源需求->主信息.服务权重,
                now,
                调用点 + "/服务权重结算");
        }
    }

