    方法节点* 私有_查找方法结果节点(方法节点* 方法头) noexcept
    {
        if (!方法头 || !方法头->子) {
            return nullptr;
        }

        auto* 起点 = static_cast<方法节点*>(方法头->子);
        auto* 当前 = 起点;
        do {
            if (当前 && 当前->主信息.节点种类 == 枚举_方法节点种类::方法结果节点) {
                return 当前;
            }
            当前 = static_cast<方法节点*>(当前->下);
        } while (当前 && 当前 != 起点);
        return nullptr;
    }

