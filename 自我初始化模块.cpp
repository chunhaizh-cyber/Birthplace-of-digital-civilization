    void 私有_追加方法结果状态(
        const 方法节点* 方法,
        std::vector<const 状态节点类*>& 输出) noexcept
    {
        if (!方法) {
            return;
        }

        const auto 追加状态 = [&](const 可解析引用<状态节点类>& 引用) noexcept {
            auto* 状态 = 引用.指针;
            if (!状态 || !世界树.状态().取状态主信息(状态)) {
                return;
            }
            if (std::find(输出.begin(), 输出.end(), 状态) == 输出.end()) {
                输出.push_back(状态);
            }
        };

        for (const auto& 状态引用 : 方法->主信息.结果初始状态) {
            追加状态(状态引用);
        }

        if (!方法->子) {
            return;
        }
        auto* 首节点 = static_cast<方法节点*>(方法->子);
        auto* 当前 = 首节点;
        do {
            if (当前 && 当前->主信息.节点种类 == 枚举_方法节点种类::方法结果节点) {
                for (const auto& 状态引用 : 当前->主信息.结果初始状态) {
                    追加状态(状态引用);
                }
            }
            当前 = 当前 ? static_cast<方法节点*>(当前->下) : nullptr;
        } while (当前 && 当前 != 首节点);
    }

    std::int32_t 私有_任务状态方法精确分(
        const 任务节点* 宿主任务,
        const 方法节点* 方法) noexcept
    {
        if (!宿主任务 || !方法) {
            return 0;
        }

        auto* 来源需求 = 私有_任务来源需求(宿主任务);
        auto* 当前状态节点 = 来源需求 ? 来源需求->主信息.被需求当前状态.指针 : nullptr;
        auto* 目标状态节点 = 来源需求 ? 来源需求->主信息.需求状态.指针 : nullptr;
        if (!当前状态节点) {
            当前状态节点 = 宿主任务->主信息.结果状态信息.指针;
        }
        if (!目标状态节点) {
            目标状态节点 = 宿主任务->主信息.目标状态.指针;
        }

        枚举_任务状态 当前状态 = 枚举_任务状态::未定义;
        枚举_任务状态 目标状态 = 枚举_任务状态::未定义;
        if (!私有_读取任务状态值(当前状态节点, 当前状态)
            || !私有_读取任务状态值(目标状态节点, 目标状态)) {
            return 0;
        }

        const auto 当前序 = 私有_任务状态推进序(当前状态);
        const auto 目标序 = 私有_任务状态推进序(目标状态);
        if (当前序 == 0 || 目标序 == 0) {
            return 0;
        }
        const auto 方向 = (目标序 > 当前序) ? 1 : (目标序 < 当前序 ? -1 : 0);

        std::vector<const 状态节点类*> 方法结果状态集{};
        私有_追加方法结果状态(方法, 方法结果状态集);

        std::int32_t 最优分 = 0;
        for (const auto* 方法结果状态节点 : 方法结果状态集) {
            枚举_任务状态 候选状态 = 枚举_任务状态::未定义;
            if (!私有_读取任务状态值(方法结果状态节点, 候选状态)) {
                continue;
            }

            const auto 候选序 = 私有_任务状态推进序(候选状态);
            if (候选序 == 0) {
                continue;
            }

            std::int32_t 当前分 = 0;
            if (候选状态 == 目标状态) {
                当前分 = 120;
            }
            else if (方向 != 0) {
                const bool 同向 =
                    方向 > 0
                    ? (候选序 > 当前序)
                    : (候选序 < 当前序);
                const bool 过头 =
                    方向 > 0
                    ? (候选序 > 目标序)
                    : (候选序 < 目标序);
                if (同向) {
                    const auto 距离 = 候选序 > 目标序 ? (候选序 - 目标序) : (目标序 - 候选序);
                    当前分 = 过头
                        ? (40 - (std::min)(距离, 40))
                        : (90 - (std::min)(距离, 90));
                }
            }

            if (私有_任务状态是异常终态(候选状态) && 候选状态 != 目标状态) {
                当前分 -= 80;
            }
            最优分 = (std::max)(最优分, 当前分);
        }

        return 最优分;
    }

