    std::string 私有_JSON转义(const std::string& 输入)
    {
        std::ostringstream 输出;
        for (const unsigned char 字符 : 输入) {
            switch (字符) {
            case '\\': 输出 << "\\\\"; break;
            case '"': 输出 << "\\\""; break;
            case '\b': 输出 << "\\b"; break;
            case '\f': 输出 << "\\f"; break;
            case '\n': 输出 << "\\n"; break;
            case '\r': 输出 << "\\r"; break;
            case '\t': 输出 << "\\t"; break;
            default:
                if (字符 < 0x20U) {
                    输出 << "\\u"
                        << "00"
                        << "0123456789abcdef"[字符 >> 4U]
                        << "0123456789abcdef"[字符 & 0x0FU];
                }
                else {
                    输出 << static_cast<char>(字符);
                }
                break;
            }
        }
        return 输出.str();
    }

    std::string 私有_构造验收JSON(const 结构_验收批量结果& 批量结果)
    {
        std::ostringstream 输出;
        输出 << "{\n"
            << "  \"suite\": \"" << 私有_JSON转义(批量结果.套件名称) << "\",\n"
            << "  \"suite_version\": \"" << 私有_JSON转义(批量结果.套件版本) << "\",\n"
            << "  \"repeat_count\": " << 批量结果.重复次数 << ",\n"
            << "  \"passed\": " << (批量结果.通过 ? "true" : "false") << ",\n"
            << "  \"exit_code\": " << 批量结果.退出码 << ",\n"
            << "  \"generated_at_us\": " << 批量结果.生成时间_微秒 << ",\n"
            << "  \"runs\": [\n";

        for (std::size_t i = 0; i < 批量结果.运行列表.size(); ++i) {
            const auto& 运行 = 批量结果.运行列表[i];
            输出 << "    {\n"
                << "      \"index\": " << 运行.运行序号 << ",\n"
                << "      \"passed\": " << (运行.通过 ? "true" : "false") << ",\n"
                << "      \"exit_code\": " << 运行.退出码 << ",\n"
                << "      \"generated_at_us\": " << 运行.生成时间_微秒 << ",\n"
                << "      \"scene_count\": " << 运行.场景列表.size() << ",\n"
                << "      \"summary\": \"" << 私有_JSON转义(运行.文本报告) << "\",\n"
                << "      \"scenes\": [\n";

            for (std::size_t j = 0; j < 运行.场景列表.size(); ++j) {
                const auto& 场景 = 运行.场景列表[j];
                输出 << "        {\n"
                    << "          \"name\": \"" << 私有_JSON转义(场景.名称) << "\",\n"
                    << "          \"passed\": " << (场景.通过 ? "true" : "false") << ",\n"
                    << "          \"summary\": \"" << 私有_JSON转义(场景.摘要) << "\"\n"
                    << "        }";
                if (j + 1 < 运行.场景列表.size()) {
                    输出 << ",";
                }
                输出 << "\n";
            }

            输出 << "      ]\n"
                << "    }";
            if (i + 1 < 批量结果.运行列表.size()) {
                输出 << ",";
            }
            输出 << "\n";
        }

        输出 << "  ]\n"
            << "}\n";
        return 输出.str();
    }

