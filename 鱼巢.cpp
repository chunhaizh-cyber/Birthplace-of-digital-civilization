    bool 私有_等待任务管理摘要快照稳定(
        std::chrono::milliseconds 最大等待 = std::chrono::milliseconds(6000))
    {
        项目运行日志("控制面板摘要/等待稳定 | 开始");
        auto& 自我线程 = 获取全局自我线程();
        const auto 开始 = std::chrono::steady_clock::now();
        const auto 截止 = 开始 + 最大等待;
        int 连续空闲拍 = 0;
        任务管理工作线程::结构_工作线程实例快照 最近快照{};
        任务管理线程协议::结构_任务界面线程快照 最近界面快照{};
        bool 有快照 = false;
        bool 有界面快照 = false;

        while (std::chrono::steady_clock::now() < 截止) {
            有快照 = 任务管理工作线程::读取任务管理工作线程快照(&最近快照);
            有界面快照 = 任务管理界面线程::读取任务管理界面线程快照(&最近界面快照);
            const bool 首轮完成 = 自我线程.是否首轮运行已完成();
            const auto 最近去向 = 自我线程.读取最近最终去向();
            const bool 工作线程空闲 =
                !有快照 || (!最近快照.正在执行 && 最近快照.当前排队数 == 0);
            const bool 界面线程空闲 =
                !有界面快照
                || (!最近界面快照.正在处理
                    && 最近界面快照.当前上行队列长度 == 0
                    && 最近界面快照.当前等待工作包数 == 0);
            const bool 自我线程本轮稳定 =
                最近去向 != 枚举_自我线程最终去向::继续提交
                && 最近去向 != 枚举_自我线程最终去向::回到重筹办
                && 最近去向 != 枚举_自我线程最终去向::转入缺口承接;

            if (首轮完成 && 工作线程空闲 && 界面线程空闲 && 自我线程本轮稳定) {
                ++连续空闲拍;
                if (连续空闲拍 >= 3) {
                    break;
                }
            } else {
                连续空闲拍 = 0;
            }

            std::this_thread::sleep_for(std::chrono::milliseconds(100));
        }

        const auto 耗时 = std::chrono::duration_cast<std::chrono::milliseconds>(
            std::chrono::steady_clock::now() - 开始).count();
        const bool 已稳定 = 连续空闲拍 >= 3;
        项目运行日志(
            "控制面板摘要/等待稳定 | 耗时ms=" + std::to_string(耗时)
            + " | 稳定=" + 私有_布尔文本(已稳定)
            + " | 连续空闲拍=" + std::to_string(连续空闲拍)
            + " | 工作线程="
            + (有快照
                ? (std::string("已启动=") + 私有_布尔文本(最近快照.已启动)
                    + "/执行中=" + 私有_布尔文本(最近快照.正在执行)
                    + "/排队=" + std::to_string(最近快照.当前排队数)
                    + "/推进=" + std::to_string(最近快照.累计推进次数))
                : std::string("无快照"))
            + " | 界面线程="
            + (有界面快照
                ? (std::string("已启动=") + 私有_布尔文本(最近界面快照.已启动)
                    + "/处理中=" + 私有_布尔文本(最近界面快照.正在处理)
                    + "/请求队列=" + std::to_string(最近界面快照.当前请求队列长度)
                    + "/上行队列=" + std::to_string(最近界面快照.当前上行队列长度)
                    + "/等待工作包=" + std::to_string(最近界面快照.当前等待工作包数)
                    + "/工作结果=" + std::to_string(最近界面快照.累计收到工作结果数))
                : std::string("无快照")));
        return 已稳定;
    }

