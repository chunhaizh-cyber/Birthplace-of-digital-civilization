    void 私有_枚举任务子节点(
        const 任务类::节点类* 父节点,
        const std::function<void(const 任务类::节点类*)>& 回调)
    {
        if (!父节点 || !父节点->子) {
            return;
        }

        auto* 首节点 = static_cast<const 任务类::节点类*>(父节点->子);
        auto* 当前 = 首节点;
        do {
            回调(当前);
            当前 = static_cast<const 任务类::节点类*>(当前->下);
        } while (当前 && 当前 != 首节点);
    }

    void 私有_前序遍历任务树(
        const 任务类::节点类* 父节点,
        const std::function<void(const 任务类::节点类*)>& 回调)
    {
        私有_枚举任务子节点(父节点, [&](const 任务类::节点类* 子节点) {
            回调(子节点);
            私有_前序遍历任务树(子节点, 回调);
        });
    }

    struct 结构_任务节点统计 {
        std::size_t 头结点数 = 0;
        std::size_t 步骤节点数 = 0;
    };

    bool 私有_任务头绑定需求(const 任务类::节点类* 节点) noexcept
    {
        return 节点
            && 任务类::读取任务节点种类(节点) == 枚举_任务节点种类::头结点
            && (节点->主信息.对应需求.指针
                || !节点->主信息.对应需求.主键.empty());
    }

    bool 私有_任务节点属于需求任务链(const 任务类::节点类* 节点) noexcept
    {
        std::size_t 保护 = 0;
        for (auto* 当前 = 节点;
             当前 && 保护 < 128;
             当前 = reinterpret_cast<const 任务类::节点类*>(当前->父), ++保护) {
            if (任务类::读取任务节点种类(当前) == 枚举_任务节点种类::头结点) {
                return 私有_任务头绑定需求(当前);
            }
            if (当前 == reinterpret_cast<const 任务类::节点类*>(当前->父)) {
                break;
            }
        }
        return false;
    }

    结构_任务节点统计 私有_统计任务节点(const 任务类::节点类* 任务根节点)
    {
        结构_任务节点统计 输出{};
        if (!任务根节点) {
            return 输出;
        }

        私有_前序遍历任务树(任务根节点, [&](const 任务类::节点类* 节点) {
            if (!节点) {
                return;
            }
            if (!私有_任务节点属于需求任务链(节点)) {
                return;
            }

            switch (任务类::读取任务节点种类(节点)) {
            case 枚举_任务节点种类::头结点:
                ++输出.头结点数;
                break;
            case 枚举_任务节点种类::步骤节点:
                ++输出.步骤节点数;
                break;
            default:
                break;
            }
        });
        return 输出;
    }

