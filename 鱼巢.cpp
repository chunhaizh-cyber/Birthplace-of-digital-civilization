    bool 私有_需求节点目标特征匹配(
        const 需求类::节点类* 需求,
        const 语素入口节点类* 目标特征类型) noexcept
    {
        if (!需求 || !目标特征类型) {
            return false;
        }
        if (需求->主信息.目标特征类型缓存 == 目标特征类型) {
            return true;
        }
        const auto 目标特征主键 = 目标特征类型->获取主键();
        if (目标特征主键.empty()) {
            return false;
        }
        if (需求->主信息.目标特征类型缓存
            && 需求->主信息.目标特征类型缓存->获取主键() == 目标特征主键) {
            return true;
        }
        auto* 目标状态 = 需求->主信息.需求状态.指针
            ? 需求->主信息.需求状态.指针
            : 需求->主信息.被需求当前状态.指针;
        auto* 状态主信息 = 目标状态 ? 世界树.状态().取状态主信息(目标状态) : nullptr;
        auto* 特征主信息 = 状态主信息 && 状态主信息->状态特征.指针
            ? 世界树.特征().取特征主信息(状态主信息->状态特征.指针)
            : nullptr;
        return 特征主信息
            && 特征主信息->类型
            && 特征主信息->类型->获取主键() == 目标特征主键;
    }

    bool 私有_读取根需求差距子需求快照(
        需求类::节点类* 根需求,
        const 语素入口节点类* 目标特征类型,
        const 结构_根需求差距镜像& 根镜像,
        结构_根需求差距子需求快照& 输出) noexcept
    {
        if (!根需求 || !目标特征类型) {
            return false;
        }

        结构_根需求差距子需求快照 快照{};
        快照.已读取根 = true;
        快照.根需求主键 = 根需求->获取主键();
        快照.目标特征主键 = 目标特征类型->获取主键();
        快照.直接活动同目标不重复 = true;

        auto* 首子 = reinterpret_cast<需求类::节点类*>(根需求->子);
        auto* 当前 = 首子;
        std::size_t 保护计数 = 0;
        需求类::节点类* 首个同目标子需求 = nullptr;
        if (当前) {
            do {
                if (当前 && 私有_需求节点目标特征匹配(当前, 目标特征类型)) {
                    ++快照.直接同目标总数;
                    if (当前->主信息.需求有效截止 == 0) {
                        ++快照.直接活动同目标数量;
                    }
                    if (!首个同目标子需求) {
                        首个同目标子需求 = 当前;
                    }
                }
                当前 = 当前 ? reinterpret_cast<需求类::节点类*>(当前->下) : nullptr;
                ++保护计数;
            } while (当前 && 当前 != 首子 && 保护计数 < 100000);
        }

        快照.直接活动同目标不重复 = 快照.直接活动同目标数量 <= 1;
        if (!首个同目标子需求) {
            输出 = std::move(快照);
            return true;
        }

        auto* 子需求 = 首个同目标子需求;
        快照.有目标子需求 = true;
        快照.子需求主键 = 子需求->获取主键();
        if (auto* 父需求 = static_cast<需求类::节点类*>(子需求->父)) {
            快照.子需求父主键 = 父需求->获取主键();
        }
        快照.子安全权重 = 子需求->主信息.安全权重;
        快照.子服务权重 = 子需求->主信息.服务权重;
        const bool 有当前值 = 私有_读取状态引用I64值(
            子需求->主信息.被需求当前状态,
            快照.子当前值);
        const bool 有目标值 = 私有_读取状态引用I64值(
            子需求->主信息.需求状态,
            快照.子目标值);
        快照.字段完整 =
            子需求->父 == 根需求
            && 子需求->主信息.被需求存在.有效()
            && 子需求->主信息.被需求当前状态.有效()
            && 子需求->主信息.需求状态.有效()
            && 私有_需求节点目标特征匹配(子需求, 目标特征类型)
            && 子需求->主信息.满足关系 != 0
            && !子需求->主信息.派生来源方法主键.empty();
        快照.已任务化 = 子需求->主信息.对应任务.有效();
        快照.当前目标值匹配根 =
            根镜像.有当前状态
            && 根镜像.有目标状态
            && 有当前值
            && 有目标值
            && 快照.子当前值 == 根镜像.当前值
            && 快照.子目标值 == 根镜像.目标值;
        输出 = std::move(快照);
        return true;
    }

