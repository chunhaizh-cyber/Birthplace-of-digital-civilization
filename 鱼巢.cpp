    const 任务类::节点类* 私有_查找直接业务子任务(const 任务类::节点类* 宿主任务)
    {
        if (!宿主任务 || !宿主任务->子) {
            return nullptr;
        }

        static const 词性节点类* s_业务子任务类型 = 语素集.添加词性词("业务子任务", "名词");
        const auto* 首节点 = static_cast<const 任务类::节点类*>(宿主任务->子);
        auto* 当前 = 首节点;
        do {
            if (当前
                && 当前->主信息.节点种类 == 枚举_任务节点种类::头结点
                && 当前->主信息.类型 == s_业务子任务类型
                && 当前->主信息.状态 != 枚举_任务状态::完成
                && 当前->主信息.状态 != 枚举_任务状态::失败
                && 当前->主信息.状态 != 枚举_任务状态::取消) {
                return 当前;
            }
            当前 = static_cast<const 任务类::节点类*>(当前->下);
        } while (当前 && 当前 != 首节点);

        return nullptr;
    }

    bool 私有_设置任务目标结果差额(
        任务类::节点类* 任务,
        枚举_任务状态 目标状态,
        枚举_任务状态 结果状态,
        时间戳 当前时间,
        const std::string& 调用点,
        std::string* 输出摘要 = nullptr)
    {
        if (!任务) {
            return false;
        }

        auto* 场景 = 任务->主信息.场景.指针
            ? 任务->主信息.场景.指针
            : (自我.获取自我现实场景() ? 自我.获取自我现实场景() : 自我.获取自我内部世界());
        if (!场景) {
            return false;
        }

        static const 词性节点类* s_任务目标状态 = 语素集.添加词性词("任务目标状态", "名词");
        static const 词性节点类* s_任务结果状态 = 语素集.添加词性词("任务结果状态", "名词");
        static const 词性节点类* s_任务虚拟存在类型 = 语素集.添加词性词("任务虚拟存在", "名词");
        auto* 任务虚拟存在 = 任务->主信息.任务虚拟存在.指针
            ? 任务->主信息.任务虚拟存在.指针
            : 世界树.存在().取或创建子存在_按名称类型(
                场景,
                语素集.添加词性词("任务账本_" + 私有_验收任务标题(任务), "专有名词"),
                s_任务虚拟存在类型);
        if (!任务虚拟存在) {
            return false;
        }
        任务->主信息.任务虚拟存在.指针 = 任务虚拟存在;
        任务->主信息.任务虚拟存在.主键 = 任务虚拟存在->获取主键();

        auto* 目标特征 = 世界树.取或创建子特征_按类型(任务虚拟存在, s_任务目标状态);
        auto* 结果特征 = 世界树.取或创建子特征_按类型(任务虚拟存在, s_任务结果状态);
        if (!目标特征 || !结果特征) {
            return false;
        }

        auto* 目标状态节点 = 世界树.状态().创建状态(
            场景,
            任务虚拟存在,
            目标特征,
            特征值{ static_cast<I64>(目标状态) },
            当前时间,
            当前时间);
        auto* 结果状态节点 = 世界树.状态().创建状态(
            场景,
            任务虚拟存在,
            结果特征,
            特征值{ static_cast<I64>(结果状态) },
            当前时间,
            当前时间);
        if (!目标状态节点 || !结果状态节点) {
            return false;
        }

        (void)世界树.状态().标记变化(
            目标状态节点,
            true,
            "白盒验收",
            调用点 + "/任务目标状态差额");
        (void)世界树.状态().标记变化(
            结果状态节点,
            true,
            "白盒验收",
            调用点 + "/任务结果状态差额");

        任务->主信息.目标状态.指针 = 目标状态节点;
        任务->主信息.目标状态.主键 = 目标状态节点->获取主键();
        任务->主信息.结果状态信息.指针 = 结果状态节点;
        任务->主信息.结果状态信息.主键 = 结果状态节点->获取主键();
        任务->主信息.状态 = 枚举_任务状态::运行中;
        任务->主信息.统计.记录观测(当前时间);

        if (输出摘要) {
            std::ostringstream 摘要;
            摘要 << "宿主=" << 私有_验收任务标题(任务)
                << " | 目标状态=" << static_cast<int>(目标状态)
                << " | 结果状态=" << static_cast<int>(结果状态)
                << " | 结论=" << (目标状态 != 结果状态 ? "存在差额" : "已对齐");
            *输出摘要 = 摘要.str();
        }
        return true;
    }

