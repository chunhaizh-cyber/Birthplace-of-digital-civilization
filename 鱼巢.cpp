    bool 私有_执行需求树同目标主动复用检查(std::string& 摘要)
    {
        auto* 自我存在 = 世界树.自我指针;
        auto* 需求根节点 = 自我存在 ? 世界树.存在().获取需求根节点(自我存在) : nullptr;
        if (!需求根节点) {
            摘要 = "需求根节点为空";
            项目运行错误日志("需求树同目标主动复用检查 | 失败 | 原因=需求根节点为空");
            return false;
        }

        需求类 需求工具{};
        需求类::节点类* 父需求 = nullptr;
        需求类::节点类* 候选需求 = nullptr;

        std::lock_guard<std::recursive_mutex> 借用锁{ 需求类::借用需求树全局互斥() };

        std::size_t 保护计数 = 0;
        for (auto* 当前 = 需求根节点->链下;
             当前 && 当前 != 需求根节点 && 保护计数 < 100000;
             当前 = 当前->链下, ++保护计数) {
            if (!当前 || 当前->主信息.需求有效截止 != 0) {
                continue;
            }

            auto* 当前父需求 = static_cast<需求类::节点类*>(当前->父);
            if (!当前父需求) {
                continue;
            }

            const auto 目标视图集 = 需求工具.收集直接子需求目标视图(当前父需求);
            const auto 命中 = std::find_if(
                目标视图集.begin(),
                目标视图集.end(),
                [当前](const 需求类::结构_需求目标视图项& 项) noexcept {
                    return 项.需求节点 == 当前 && 项.有效();
                });

            if (命中 != 目标视图集.end()) {
                父需求 = 当前父需求;
                候选需求 = 当前;
                break;
            }
        }

        if (!父需求 || !候选需求) {
            摘要 = "未找到可主动复用的活动原子目标需求";
            项目运行错误日志("需求树同目标主动复用检查 | 失败 | 原因=无可复用候选");
            return false;
        }

        const auto 总数前 = 私有_计数需求树节点_不含根(需求根节点);
        const auto 直接子数前 = 私有_计数直接子需求(父需求);

        结构_需求树更新指令 指令{};
        指令.指令类型 = 枚举_需求树更新指令类型::新增需求;
        指令.父需求指针 = reinterpret_cast<std::uintptr_t>(父需求);
        指令.需求类型 = 候选需求->主信息.类型;
        指令.需求名称 = 候选需求->主信息.类型;
        指令.需求主体指针 = reinterpret_cast<std::uintptr_t>(候选需求->主信息.需求主体.指针);
        指令.需求主体主键 = 候选需求->主信息.需求主体.主键;
        指令.需求场景指针 = reinterpret_cast<std::uintptr_t>(候选需求->主信息.需求场景.指针);
        指令.需求场景主键 = 候选需求->主信息.需求场景.主键;
        指令.被需求存在指针 = reinterpret_cast<std::uintptr_t>(候选需求->主信息.被需求存在.指针);
        指令.被需求存在主键 = 候选需求->主信息.被需求存在.主键;
        指令.当前状态指针 = reinterpret_cast<std::uintptr_t>(候选需求->主信息.被需求当前状态.指针);
        指令.当前状态主键 = 候选需求->主信息.被需求当前状态.主键;
        指令.需求状态指针 = reinterpret_cast<std::uintptr_t>(候选需求->主信息.需求状态.指针);
        指令.需求状态主键 = 候选需求->主信息.需求状态.主键;
        指令.满足关系 = 候选需求->主信息.满足关系 != 0
            ? 候选需求->主信息.满足关系
            : 关系_等于;
        指令.使用新安全权重 = true;
        指令.新安全权重 = 候选需求->主信息.安全权重;
        指令.使用新服务权重 = true;
        指令.新服务权重 = 候选需求->主信息.服务权重;
        指令.允许与父需求目标重叠 = true;
        指令.派生来源方法主键 = 候选需求->主信息.派生来源方法主键;
        指令.派生需求类型抽象特征指针 = 候选需求->主信息.派生需求类型抽象特征指针;
        指令.派生需求类型值 = 候选需求->主信息.派生需求类型值;
        指令.派生方法需求位抽象特征指针 = 候选需求->主信息.派生方法需求位抽象特征指针;
        指令.派生方法需求位值 = 候选需求->主信息.派生方法需求位值;
        指令.派生本能能力缺口类型抽象特征指针 = 候选需求->主信息.派生本能能力缺口类型抽象特征指针;
        指令.派生本能能力缺口类型值 = 候选需求->主信息.派生本能能力缺口类型值;

        auto* 复用结果 = 需求工具.执行更新指令(需求根节点, 指令);
        const auto 总数后 = 私有_计数需求树节点_不含根(需求根节点);
        const auto 直接子数后 = 私有_计数直接子需求(父需求);

        const bool 通过 = 复用结果 == 候选需求
            && 总数后 == 总数前
            && 直接子数后 == 直接子数前;

        std::ostringstream 输出;
        输出 << "父需求=" << 父需求->获取主键()
            << " | 候选=" << 候选需求->获取主键()
            << " | 返回=" << (复用结果 ? 复用结果->获取主键() : std::string("空"))
            << " | 总数=" << 总数前 << "->" << 总数后
            << " | 直接子数=" << 直接子数前 << "->" << 直接子数后;
        摘要 = 输出.str();

        if (通过) {
            项目运行日志("需求树同目标主动复用检查 | 通过 | " + 摘要);
        } else {
            项目运行错误日志("需求树同目标主动复用检查 | 失败 | " + 摘要);
        }
        return 通过;
    }

