    bool 私有_等待最近治理结果(
        const std::function<bool(const 结构_任务管理结果&)>& 判定,
        结构_任务管理结果* 输出,
        int 超时毫秒 = 4000)
    {
        const auto 开始 = std::chrono::steady_clock::now();
        结构_任务管理结果 最近结果{};
        while (std::chrono::duration_cast<std::chrono::milliseconds>(
            std::chrono::steady_clock::now() - 开始).count() <= 超时毫秒) {
            if (任务管理任务模块::读取最近治理结果(&最近结果) && 判定(最近结果)) {
                if (输出) {
                    *输出 = 最近结果;
                }
                return true;
            }
            std::this_thread::sleep_for(std::chrono::milliseconds(150));
        }
        if (输出) {
            *输出 = 最近结果;
        }
        return false;
    }

