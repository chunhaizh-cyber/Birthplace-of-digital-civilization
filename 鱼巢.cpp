    std::string 私有_拼接片段(const std::vector<std::string>& 片段)
    {
        std::ostringstream 输出;
        bool 首段 = true;
        for (const auto& 片段文本 : 片段) {
            if (片段文本.empty()) {
                continue;
            }
            if (!首段) {
                输出 << "；";
            }
            输出 << 片段文本;
            首段 = false;
        }
        return 输出.str();
    }

