        因果主信息->锚点信息.主键 = 自我存在->获取主键();
        因果主信息->动作时间 = 当前时间;
        因果主信息->结果时间 = 当前时间;
        因果主信息->条件时间段 = 结构_时间段{ 当前时间, 当前时间 };
        因果主信息->来源类型 = 枚举_因果来源类型::执行生成;
        因果主信息->来源任务主键 = std::hash<std::string>{}("M1半白盒轻量因果借用");
        因果主信息->来源方法主键 = 自我.获取当前主方法()
            ? std::hash<std::string>{}(自我.获取当前主方法()->获取主键())
            : 0ULL;
        因果主信息->置信度 = 10000;
        因果主信息->动作语义键 = "M1/半白盒/轻量因果链/借用底座";

