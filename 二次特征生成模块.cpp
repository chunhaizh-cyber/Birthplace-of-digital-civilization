std::vector<二次特征节点类*> 二次特征生成模块::刷新场景存在型基础二次特征(
    场景节点类* 场景,
    std::int64_t 接近阈值_mm,
    std::int64_t 冲突阈值_mm)
{
    std::vector<二次特征节点类*> 结果{};
    if (!世界树_ || !场景) return 结果;

    auto 存在列表 = 世界树_->存在().获取子存在(场景);
    if (存在列表.size() < 2) return 结果;

    if (接近阈值_mm < 0) 接近阈值_mm = 0;
    if (冲突阈值_mm < 0) 冲突阈值_mm = 0;

    for (std::size_t i = 0; i < 存在列表.size(); ++i) {
        auto* 左存在 = 存在列表[i];
        const auto* 左主信息 = 世界树_->存在().取存在主信息(左存在);
        if (!左主信息 || !左主信息->有位置历史) continue;

        for (std::size_t j = i + 1; j < 存在列表.size(); ++j) {
            auto* 右存在 = 存在列表[j];
            const auto* 右主信息 = 世界树_->存在().取存在主信息(右存在);
            if (!右主信息 || !右主信息->有位置历史) continue;

            auto* 左对象 = static_cast<基础信息节点类*>(左存在);
            auto* 右对象 = static_cast<基础信息节点类*>(右存在);
            私有_规范化左右对象(左对象, 右对象);

            const auto 距离_mm = 私有_计算距离_mm(左主信息->最近观测位置_mm, 右主信息->最近观测位置_mm);
            const auto 接近余量 = 接近阈值_mm - 距离_mm;
            const auto 冲突余量 = 冲突阈值_mm - 距离_mm;

            {
                二次特征主信息类 模板{};
                模板.种类 = 枚举_二次特征种类::存在比较;
                模板.域 = 枚举_二次特征域::存在;
                模板.主母式 = 枚举_二次特征主母式::存在关系;
                模板.粒度 = 枚举_二次特征粒度::当前场景;
                模板.值形态 = 枚举_二次特征值形态::标量;
                模板.左对象 = 私有_生成引用(左对象);
                模板.右对象 = 私有_生成引用(右对象);
                模板.来源场景 = 私有_生成引用(场景);
                模板.刷新度量签名_链键();

                if (auto* 节点 = 私有_按签名取或创建二次特征(世界树_, 场景, 模板)) {
                    auto* 主信息 = 世界树_->二次特征().取二次特征主信息(节点);
                    if (主信息) {
                        主信息->种类 = 枚举_二次特征种类::存在比较;
                        主信息->概念名称 = 私有_取概念名称词("接近程度");
                        世界树_->二次特征().绑定场景(节点, 场景);
                        世界树_->二次特征().设置生成语义(
                            节点,
                            枚举_二次特征域::存在,
                            枚举_二次特征主母式::存在关系,
                            枚举_二次特征粒度::当前场景,
                            枚举_二次特征基准类型::未定义,
                            枚举_时间归一方式::无,
                            枚举_二次特征值形态::标量);
                        世界树_->二次特征().绑定来源(节点, nullptr, nullptr, nullptr, 场景, 0, 0);
                        世界树_->二次特征().设置左右对象(节点, 左对象, 右对象);
                        世界树_->二次特征().写入标量值(节点, 接近余量, 接近余量 >= 0, 10000);
                        结果.push_back(节点);
                    }
                }
            }

            {
                二次特征主信息类 模板{};
                模板.种类 = 枚举_二次特征种类::存在比较;
                模板.域 = 枚举_二次特征域::存在;
                模板.主母式 = 枚举_二次特征主母式::存在关系;
                模板.粒度 = 枚举_二次特征粒度::当前场景;
                模板.值形态 = 枚举_二次特征值形态::布尔;
                模板.左对象 = 私有_生成引用(左对象);
                模板.右对象 = 私有_生成引用(右对象);
                模板.来源场景 = 私有_生成引用(场景);
                模板.刷新度量签名_链键();

                if (auto* 节点 = 私有_按签名取或创建二次特征(世界树_, 场景, 模板)) {
                    auto* 主信息 = 世界树_->二次特征().取二次特征主信息(节点);
                    if (主信息) {
                        主信息->种类 = 枚举_二次特征种类::存在比较;
                        主信息->概念名称 = 私有_取概念名称词("冲突");
                        世界树_->二次特征().绑定场景(节点, 场景);
                        世界树_->二次特征().设置生成语义(
                            节点,
                            枚举_二次特征域::存在,
                            枚举_二次特征主母式::存在关系,
                            枚举_二次特征粒度::当前场景,
                            枚举_二次特征基准类型::未定义,
                            枚举_时间归一方式::无,
                            枚举_二次特征值形态::布尔);
                        世界树_->二次特征().绑定来源(节点, nullptr, nullptr, nullptr, 场景, 0, 0);
                        世界树_->二次特征().设置左右对象(节点, 左对象, 右对象);
                        世界树_->二次特征().写入标量值(节点, 冲突余量, 冲突余量 >= 0, 10000);
                        结果.push_back(节点);
                    }
                }
            }
        }
    }

    return 结果;
}
