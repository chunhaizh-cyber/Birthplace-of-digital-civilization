    bool 私有_同类事件来源流(const 结构_动态视图片段& 左, const 结构_动态视图片段& 右) noexcept
    {
        const auto* 左主信息 = 左.动态主信息;
        const auto* 右主信息 = 右.动态主信息;
        if (!左主信息 || !右主信息) return false;

        const auto 左来源动作键 = !左主信息->来源动作.主键.empty()
            ? 左主信息->来源动作.主键
            : 私有_取节点键(reinterpret_cast<const 基础信息节点类*>(左主信息->来源动作.指针));
        const auto 右来源动作键 = !右主信息->来源动作.主键.empty()
            ? 右主信息->来源动作.主键
            : 私有_取节点键(reinterpret_cast<const 基础信息节点类*>(右主信息->来源动作.指针));

        return 左主信息->来源类型 == 右主信息->来源类型
            && 左主信息->业务语义类型 == 右主信息->业务语义类型
            && 左主信息->来源动作相位 == 右主信息->来源动作相位
            && 左来源动作键 == 右来源动作键
            && 私有_取词键(左主信息->来源动作名) == 私有_取词键(右主信息->来源动作名)
            && 私有_取节点键(左主信息->来源输入场景.指针) == 私有_取节点键(右主信息->来源输入场景.指针)
            && 私有_取节点键(左主信息->来源输出场景.指针) == 私有_取节点键(右主信息->来源输出场景.指针);
    }

    bool 私有_值形态不适合同类事件段聚合(const 特征值& 值) noexcept
    {
        return std::holds_alternative<VecU句柄>(值)
            || std::holds_alternative<指针句柄>(值);
    }

    std::uint64_t 私有_计算聚合路径签名(const std::vector<结构_动态视图片段>& 片段列表)
    {
        std::string 材料{};
        材料.reserve(片段列表.size() * 48);
        for (const auto& 片段 : 片段列表) {
            if (!材料.empty()) 材料.push_back('|');
            材料 += 私有_取节点键(片段.动态);
        }
        return static_cast<std::uint64_t>(std::hash<std::string>{}(材料));
    }

    bool 私有_聚合来源链相同(
        世界树类* 世界树,
        const 动态节点主信息类* 主信息,
        const std::vector<结构_动态视图片段>& 片段列表)
    {
        if (!世界树 || !主信息) return false;
        if (主信息->来源低层动态.size() != 片段列表.size()) return false;

        for (std::size_t i = 0; i < 片段列表.size(); ++i) {
            const auto& 引用 = 主信息->来源低层动态[i];
            const auto* 节点 = 引用.指针;
            const auto 当前键 = !引用.主键.empty() ? 引用.主键 : 私有_取节点键(节点);
            if (当前键 != 私有_取节点键(片段列表[i].动态)) {
                return false;
            }
        }
        return true;
    }

    std::int64_t 私有_计算距离_mm(const Vector3D& 左位置, const Vector3D& 右位置) noexcept
    {
        const long double dx = static_cast<long double>(左位置.x) - static_cast<long double>(右位置.x);
        const long double dy = static_cast<long double>(左位置.y) - static_cast<long double>(右位置.y);
        const long double dz = static_cast<long double>(左位置.z) - static_cast<long double>(右位置.z);
        return static_cast<std::int64_t>(std::llround(std::sqrt(dx * dx + dy * dy + dz * dz)));
    }

    void 私有_规范化左右对象(基础信息节点类*& 左对象, 基础信息节点类*& 右对象)
    {
        if (!左对象 || !右对象) return;
        if (私有_取节点键(左对象) > 私有_取节点键(右对象)) {
            std::swap(左对象, 右对象);
        }
    }

    二次特征节点类* 私有_按签名取或创建二次特征(
        世界树类* 世界树,
        场景节点类* 场景,
        const 二次特征主信息类& 模板)
    {
        if (!世界树 || !场景) return nullptr;

        for (auto* 现有节点 : 世界树->二次特征().获取子二次特征(场景)) {
            const auto* 现有主信息 = 世界树->二次特征().取二次特征主信息(现有节点);
            if (现有主信息 && 现有主信息->度量签名_链键 == 模板.度量签名_链键) {
                return 现有节点;
            }
        }

        auto* 新主信息 = new 二次特征主信息类(模板);
        return 世界树->创建二次特征(场景, 新主信息);
    }
