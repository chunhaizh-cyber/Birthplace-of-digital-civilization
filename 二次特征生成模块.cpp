std::vector<二次特征节点类*> 二次特征生成模块::获取动态二次特征(
    const 动态节点类& 动态,
    const 二次特征生成配置& 配置)
{
    if (!世界树_) return {};

    auto* 动态节点 = const_cast<动态节点类*>(&动态);
    auto* 父节点 = 动态节点 ? static_cast<基础信息节点类*>(动态节点->父) : nullptr;
    auto* 场景 = 父节点 && 世界树_->基础信息().取主信息<场景节点主信息类>(父节点)
        ? static_cast<场景节点类*>(父节点)
        : nullptr;
    return 私有_获取动态二次特征(世界树_, 场景, 动态节点, 配置);
}

