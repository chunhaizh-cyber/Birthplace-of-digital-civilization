std::vector<二次特征节点类*> 二次特征生成模块::生成动态型基础二次特征(
    场景节点类* 场景,
    动态节点类* 动态)
{
    std::vector<二次特征节点类*> 结果{};
    if (!世界树_ || !场景 || !动态) return 结果;

    结构_动态视图片段 片段{};
    if (!私有_解析动态视图片段(世界树_, 动态, 片段, false, true)) {
        return 结果;
    }
    const auto* 动态主信息 = 片段.动态主信息;
    auto* 起始状态 = 片段.起始状态;
    auto* 结果状态 = 片段.结果状态;
    auto* 动态主体 = 片段.主体;
    const auto 动态粒度 = 私有_推断动态粒度(*动态主信息);
    const auto 差值 = 片段.差值;
    const auto 持续时长_us = (片段.起时间 != 0 && 片段.止时间 >= 片段.起时间)
        ? (片段.止时间 - 片段.起时间)
        : 0;

    const auto 配置共同字段 = [&](二次特征节点类* 节点,
                                 const char* 概念名称,
                                 枚举_二次特征主母式 主母式,
                                 枚举_二次特征基准类型 基准类型,
                                 枚举_时间归一方式 时间归一方式,
                                 枚举_二次特征值形态 值形态) {
        auto* 主信息 = 世界树_->二次特征().取二次特征主信息(节点);
        if (!主信息) return false;

        主信息->种类 = 枚举_二次特征种类::动态比较;
        主信息->概念名称 = 私有_取概念名称词(概念名称);
        世界树_->二次特征().绑定场景(节点, 场景);
        世界树_->二次特征().设置生成语义(
            节点,
            枚举_二次特征域::动态,
            主母式,
            动态粒度,
            基准类型,
            时间归一方式,
            值形态);
        世界树_->二次特征().绑定来源(
            节点,
            起始状态,
            结果状态,
            动态,
            场景,
            动态主信息->动态层级,
            动态主信息->动态路径签名);
        世界树_->二次特征().设置主体客体(节点, 动态主体, nullptr);
        return true;
    };

    {
        二次特征主信息类 模板{};
        模板.种类 = 枚举_二次特征种类::动态比较;
        模板.域 = 枚举_二次特征域::动态;
        模板.主母式 = 枚举_二次特征主母式::差分;
        模板.粒度 = 动态粒度;
        模板.基准类型 = 枚举_二次特征基准类型::起始态;
        模板.值形态 = 枚举_二次特征值形态::标量;
        模板.主体 = 私有_生成引用(动态主体);
        模板.来源起始状态 = 私有_生成引用(起始状态);
        模板.来源结果状态 = 私有_生成引用(结果状态);
        模板.来源动态 = 私有_生成引用(动态);
        模板.来源场景 = 私有_生成引用(场景);
        模板.来源动态层级 = 动态主信息->动态层级;
        模板.来源动态路径签名 = 动态主信息->动态路径签名;
        模板.刷新度量签名_链键();

        if (auto* 节点 = 私有_按签名取或创建二次特征(世界树_, 场景, 模板)) {
            if (配置共同字段(
                    节点,
                    "变化量",
                    枚举_二次特征主母式::差分,
                    枚举_二次特征基准类型::起始态,
                    枚举_时间归一方式::无,
                    枚举_二次特征值形态::标量)) {
                世界树_->二次特征().写入标量值(节点, 差值, 差值 != 0, 10000);
                结果.push_back(节点);
            }
        }
    }

    {
        const std::int32_t 方向编码 = 差值 > 0 ? 1 : (差值 < 0 ? -1 : 0);
        const std::string 方向语义 = 差值 > 0 ? "上升" : (差值 < 0 ? "下降" : "持平");

        二次特征主信息类 模板{};
        模板.种类 = 枚举_二次特征种类::动态比较;
        模板.域 = 枚举_二次特征域::动态;
        模板.主母式 = 枚举_二次特征主母式::状态转移;
        模板.粒度 = 动态粒度;
        模板.基准类型 = 枚举_二次特征基准类型::起始态;
        模板.值形态 = 枚举_二次特征值形态::枚举;
        模板.主体 = 私有_生成引用(动态主体);
        模板.来源起始状态 = 私有_生成引用(起始状态);
        模板.来源结果状态 = 私有_生成引用(结果状态);
        模板.来源动态 = 私有_生成引用(动态);
        模板.来源场景 = 私有_生成引用(场景);
        模板.来源动态层级 = 动态主信息->动态层级;
        模板.来源动态路径签名 = 动态主信息->动态路径签名;
        模板.刷新度量签名_链键();

        if (auto* 节点 = 私有_按签名取或创建二次特征(世界树_, 场景, 模板)) {
            if (配置共同字段(
                    节点,
                    "变化方向",
                    枚举_二次特征主母式::状态转移,
                    枚举_二次特征基准类型::起始态,
                    枚举_时间归一方式::无,
                    枚举_二次特征值形态::枚举)) {
                世界树_->二次特征().写入离散值(节点, 方向编码, 方向语义, 枚举_二次特征值形态::枚举, 10000);
                结果.push_back(节点);
            }
        }
    }

    if (持续时长_us > 0) {
        const auto 每秒速率 = static_cast<I64>(
            std::llround(static_cast<long double>(差值) * 1000000.0L / static_cast<long double>(持续时长_us)));

        二次特征主信息类 模板{};
        模板.种类 = 枚举_二次特征种类::动态比较;
        模板.域 = 枚举_二次特征域::动态;
        模板.主母式 = 枚举_二次特征主母式::时间归一;
        模板.粒度 = 动态粒度;
        模板.基准类型 = 枚举_二次特征基准类型::起始态;
        模板.时间归一方式 = 枚举_时间归一方式::总时长;
        模板.值形态 = 枚举_二次特征值形态::标量;
        模板.主体 = 私有_生成引用(动态主体);
        模板.来源起始状态 = 私有_生成引用(起始状态);
        模板.来源结果状态 = 私有_生成引用(结果状态);
        模板.来源动态 = 私有_生成引用(动态);
        模板.来源场景 = 私有_生成引用(场景);
        模板.来源动态层级 = 动态主信息->动态层级;
        模板.来源动态路径签名 = 动态主信息->动态路径签名;
        模板.刷新度量签名_链键();

        if (auto* 节点 = 私有_按签名取或创建二次特征(世界树_, 场景, 模板)) {
            if (配置共同字段(
                    节点,
                    "变化率",
                    枚举_二次特征主母式::时间归一,
                    枚举_二次特征基准类型::起始态,
                    枚举_时间归一方式::总时长,
                    枚举_二次特征值形态::标量)) {
                世界树_->二次特征().写入标量值(节点, 每秒速率, true, 10000);
                结果.push_back(节点);
            }
        }
    }

    return 结果;
}

动态节点类* 二次特征生成模块::尝试生成连续同向聚合动态(
    场景节点类* 场景,
    动态节点类* 动态)
{
    if (!世界树_ || !场景 || !动态) return nullptr;

    结构_动态视图片段 当前片段{};
    if (!私有_解析动态视图片段(世界树_, 动态, 当前片段, true, true)) {
        return nullptr;
    }

    auto 场景动态 = 世界树_->动态().获取场景动态(场景);
    if (场景动态.size() < 2) {
        return nullptr;
    }

    std::vector<结构_动态视图片段> 逆序片段{};
    逆序片段.push_back(当前片段);
    I64 当前链起始值 = 当前片段.起始值;
    时间戳 当前链起始时间 = 当前片段.起时间;

    bool 已遇到当前 = false;
    for (auto it = 场景动态.rbegin(); it != 场景动态.rend(); ++it) {
        auto* 候选动态 = *it;
        if (候选动态 == 动态) {
            已遇到当前 = true;
            continue;
        }
        if (!已遇到当前) continue;

        结构_动态视图片段 候选片段{};
        if (!私有_解析动态视图片段(世界树_, 候选动态, 候选片段, true, true)) {
            continue;
        }
        if (!私有_同一主体特征流(当前片段, 候选片段)) {
            continue;
        }
        if (候选片段.方向 != 当前片段.方向) {
            break;
        }
        if (候选片段.结果值 != 当前链起始值) {
            break;
        }
        if (候选片段.止时间 != 0 && 当前链起始时间 != 0 && 候选片段.止时间 > 当前链起始时间) {
            break;
        }

        逆序片段.push_back(候选片段);
        当前链起始值 = 候选片段.起始值;
        当前链起始时间 = 候选片段.起时间;
    }

    if (逆序片段.size() < 2) {
        return nullptr;
    }

    std::vector<结构_动态视图片段> 片段列表(逆序片段.rbegin(), 逆序片段.rend());

    for (auto* 现有动态 : 场景动态) {
        const auto* 现有主信息 = 世界树_->动态().取动态主信息(现有动态);
        if (!现有主信息) continue;
        if (现有主信息->聚合方式 != 枚举_动态聚合方式::连续同向段
            || 现有主信息->动态层级 != 当前片段.动态主信息->动态层级 + 1) {
            continue;
        }
        if (私有_聚合来源链相同(世界树_, 现有主信息, 片段列表)) {
            return 现有动态;
        }
    }

    auto* 聚合主信息 = new 动态节点主信息类{};
    聚合主信息->初始状态 = 私有_生成引用(片段列表.front().起始状态);
    聚合主信息->结果状态 = 私有_生成引用(片段列表.back().结果状态);
    聚合主信息->动态主体 = 私有_生成引用(当前片段.主体);
    聚合主信息->动态特征 = 私有_生成引用(当前片段.特征);
    聚合主信息->时间 = 结构_时间段{ 片段列表.front().起时间, 片段列表.back().止时间 };
    聚合主信息->名称 = 当前片段.动态主信息->名称;
    聚合主信息->类型 = 当前片段.动态主信息->类型;
    聚合主信息->来源类型 = 当前片段.动态主信息->来源类型;
    聚合主信息->来源动作 = 当前片段.动态主信息->来源动作;
    聚合主信息->来源动作名 = 当前片段.动态主信息->来源动作名;
    聚合主信息->来源动作相位 = 当前片段.动态主信息->来源动作相位;
    聚合主信息->来源输入场景 = 当前片段.动态主信息->来源输入场景;
    聚合主信息->来源输出场景 = 当前片段.动态主信息->来源输出场景;
    聚合主信息->来源执行成功 = 当前片段.动态主信息->来源执行成功;
    聚合主信息->来源错误码 = 当前片段.动态主信息->来源错误码;
    聚合主信息->动态层级 = 当前片段.动态主信息->动态层级 + 1;
    聚合主信息->聚合方式 = 枚举_动态聚合方式::连续同向段;
    聚合主信息->动态路径签名 = 私有_计算聚合路径签名(片段列表);
    for (const auto& 片段 : 片段列表) {
        聚合主信息->来源低层动态.push_back(私有_生成引用(片段.动态));
    }

    return 世界树_->动态().创建动态(场景, 聚合主信息);
}

