std::optional<结构_任务管理控制请求> 私有_构造任务管理控制请求(
    const 自我类& 自我对象,
    const 结构_任务根节点& 任务根节点,
    const 结构_任务控制事件& 控制事件,
    const 时间戳 now,
    const std::uint64_t 回复目标消息主键) noexcept
{
    const auto 控制意图 = 私有_映射任务控制事件到控制意图(控制事件.事件类型);
    if (控制意图 == 枚举_任务管理控制意图值::未定义) {
        return std::nullopt;
    }

    结构_任务管理控制请求 请求{};
    请求.请求主键 =
        std::string("thread-control/")
        + std::to_string(任务根节点.任务根ID)
        + "/"
        + std::to_string(now)
        + "/"
        + std::to_string(static_cast<int>(控制事件.事件类型));
    请求.目标线程主键 = std::to_string(任务根节点.任务根ID);
    if (auto* 当前主任务 = 自我对象.获取当前主任务()) {
        请求.目标任务主键 = 当前主任务->获取主键();
    }
    请求.回复目标消息主键 = 回复目标消息主键;
    请求.控制意图值 = 控制意图;
    请求.发起者主键 = "自我线程";
    请求.发起者类型摘要 = "自我线程";
    请求.发生时间 = now;
    请求.请求原因 = 控制事件.摘要;
    请求.是否需要确认 =
        控制意图 == 枚举_任务管理控制意图值::请求停止
        || 控制意图 == 枚举_任务管理控制意图值::请求收束
        || 控制意图 == 枚举_任务管理控制意图值::请求打断;
    请求.请求预算 = 控制事件.时间预算_ms;
    请求.请求优先级 = 控制事件.优先级;
    请求.请求摘要 =
        控制事件.摘要.empty()
        ? std::string("自我线程下发控制意图: ") + 任务管理控制意图值文本(控制意图)
        : 控制事件.摘要;
    return 请求;
}

