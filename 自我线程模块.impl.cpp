bool 自我线程类::投递治理消息(const 自我线程消息协议::结构_治理消息& 消息)
{
    std::scoped_lock 锁(状态锁_);
    auto 副本 = 消息;
    if (副本.头.产生时间 == 0) {
        副本.头.产生时间 = 结构体_时间戳::当前_微秒();
    }
    if (副本.头.来源链 == 自我线程消息协议::枚举_消息来源链::未定义) {
        副本.头.来源链 = 自我线程消息协议::枚举_消息来源链::线程直达消息;
    }
    if (副本.头.优先级 == 自我线程消息协议::枚举_治理消息优先级::未定义) {
        副本.头.优先级 = 自我线程消息协议::默认消息优先级(副本.头.处理类型);
    }
    if (!副本.头.可抢占) {
        副本.头.可抢占 = 自我线程消息协议::默认可抢占(副本.头.处理类型);
    }
    if (副本.头.消息主键 == 0) {
        副本.头.消息主键 = ++治理事件序号_;
    }
    if (副本.载荷键.empty()) {
        副本.载荷键 = 自我线程消息批次执行器::生成消息幂等键(副本);
    }
    if (副本.摘要.empty()) {
        std::ostringstream 输出;
        输出 << 私有_治理消息处理类型文本(副本.头.处理类型) << "入队";
        副本.摘要 = 输出.str();
    }

    for (auto& 已有事件 : 治理mailbox_) {
        if (已有事件.载荷键 != 副本.载荷键 || 已有事件.轨迹.重放代数 != 副本.轨迹.重放代数) {
            continue;
        }
        if (私有_治理消息优先级值(副本.头.优先级) > 私有_治理消息优先级值(已有事件.头.优先级)) {
            已有事件.头.优先级 = 副本.头.优先级;
        }
        if (!副本.摘要.empty()) {
            已有事件.摘要 = 副本.摘要;
        }
        if (!副本.来源.来源主观察特征.empty()) {
            已有事件.来源.来源主观察特征 = 副本.来源.来源主观察特征;
        }
        if (副本.来源.允许正式资产提交) {
            已有事件.来源.允许正式资产提交 = true;
        }
        if (!副本.变化项集.empty()) {
            已有事件.变化项集 = 副本.变化项集;
        }
        std::stable_sort(治理mailbox_.begin(), 治理mailbox_.end(), 私有_消息排在前面);
        刷新治理恢复事件镜像_已加锁();
        唤醒条件_.notify_one();
        return true;
    }

    治理mailbox_.push_back(std::move(副本));
    std::stable_sort(治理mailbox_.begin(), 治理mailbox_.end(), 私有_消息排在前面);
    刷新治理恢复事件镜像_已加锁();
    唤醒条件_.notify_one();
    return true;
}

