动态节点类* 私有_创建线程状态变更动态(
    场景节点类* 自我内部世界,
    存在节点类* 线程存在,
    特征节点类* 特征节点,
    状态节点类* 旧状态节点,
    状态节点类* 新状态节点,
    const 自我线程消息协议::结构_线程状态变化消息段& 消息段,
    const 时间戳 now) noexcept
{
    if (!自我内部世界 || !线程存在 || !特征节点 || !新状态节点) {
        return nullptr;
    }

    auto* 动态节点 = 世界树.动态().创建动态(
        自我内部世界,
        旧状态节点,
        新状态节点,
        now,
        now);
    if (!动态节点) {
        return nullptr;
    }

    auto* 动态主信息 = 世界树.动态().取动态主信息(动态节点);
    const auto* 动作词 = 语素集.添加词性词("线程状态变更", "动词");
    if (动态主信息) {
        动态主信息->名称 = 动作词;
        动态主信息->类型 = 动作词;
        动态主信息->来源动作名 = 动作词;
        动态主信息->来源动作相位 =
            消息段.是否故障触发 ? 枚举_动作事件相位::失败 : 枚举_动作事件相位::完成;
        动态主信息->来源执行成功 = !消息段.是否故障触发;
        动态主信息->时间 = 结构_时间段{ now, now };
    }

    (void)世界树.动态().设置主体特征(动态节点, 线程存在, 特征节点);
    (void)世界树.动态().绑定来源场景(动态节点, 自我内部世界, 自我内部世界);
    return 动态节点;
}

bool 私有_落线程状态变化到自我内部世界(
    const 自我线程消息协议::结构_治理消息& 消息,
    const std::string& 调用点) noexcept
{
    const auto* 消息段 = 自我线程消息协议::获取线程状态变化消息段(消息);
    if (!消息段
        || !自我线程消息协议::是否线程状态型特征(消息段->状态特征类型)) {
        return false;
    }

    auto* 自我内部世界 = 自我.确保自我内部世界(调用点 + "/自我内部世界");
    auto* 线程存在 = 私有_解析线程状态消息中的线程存在(
        *消息段,
        调用点 + "/解析线程存在");
    if (!自我内部世界 || !线程存在) {
        return false;
    }

    auto* 特征节点 = 私有_确保线程特征节点(线程存在, 消息段->状态特征类型);
    if (!特征节点) {
        return false;
    }

    const auto now = 消息段->发生时间 == 0 ? 结构体_时间戳::当前_微秒() : 消息段->发生时间;
    const std::string 动作语义键 = "线程状态变更";
    状态节点类* 旧状态节点 = nullptr;
    if (消息段->旧状态 != 消息段->新状态) {
        旧状态节点 = 私有_创建线程状态节点(
            自我内部世界,
            线程存在,
            特征节点,
            消息段->旧状态,
            now,
            false,
            动作语义键,
            "治理前镜像");
    }

    if (!世界树.写入特征_I64(特征节点, 消息段->新状态, now)) {
        return false;
    }

    auto* 新状态节点 = 私有_创建线程状态节点(
        自我内部世界,
        线程存在,
        特征节点,
        消息段->新状态,
        now,
        true,
        动作语义键,
        私有_线程状态结果说明(*消息段));
    if (!新状态节点) {
        return false;
    }

    // 线程状态镜像先以“特征值 + 状态节点”作为正式账，
    // 避免在自我线程启动早期把线程状态变化强行送入动态聚合链。
    (void)旧状态节点;
    return true;
}

