需求节点* 私有_确保派生需求节点(
    任务节点* 关联任务头,
    const 结构_任务管理派生需求消息& 派生需求,
    时间戳 now,
    const std::string& 调用点) noexcept
{
    auto* 父需求 = reinterpret_cast<需求节点*>(关联任务头 ? 关联任务头->主信息.来源需求.指针 : nullptr);
    if (!父需求) {
        父需求 = 自我.获取当前主需求();
    }
    if (!父需求) {
        父需求 = 自我.获取安全根需求();
    }
    if (!父需求) {
        return nullptr;
    }

    const auto* 需求类型 = 私有_派生需求需求类型词(派生需求.派生需求类型);
    需求类 需求服务{};
    结构_需求树更新指令 指令{};
    指令.指令类型 = 枚举_需求树更新指令类型::新增需求;
    指令.父需求指针 = reinterpret_cast<std::uintptr_t>(父需求);
    指令.父需求主键 = 父需求->获取主键();
    指令.需求类型 = 需求类型;
    指令.需求名称 = 需求类型;
    指令.使用新安全权重 = true;
    指令.使用新服务权重 = true;
    指令.新安全权重 =
        (父需求->主信息.安全权重 > 0 ? (std::max<std::int64_t>)(1, 父需求->主信息.安全权重 / 2) : 100)
        + (派生需求.是否可能影响安全 ? 200 : 0)
        + (派生需求.阻塞级别 >= 枚举_任务管理阻塞级别::严重 ? 100 : 0);
    指令.新服务权重 =
        (父需求->主信息.服务权重 > 0 ? (std::max<std::int64_t>)(1, 父需求->主信息.服务权重 / 2) : 100)
        + (派生需求.是否可能影响服务 ? 200 : 0)
        + (派生需求.是否影响父任务推进 ? 100 : 0);
    (void)now;
    auto* 新需求节点 = 需求服务.执行更新指令(父需求, 指令);
    if (!新需求节点) {
        return nullptr;
    }

    if (关联任务头) {
        新需求节点->主信息.来源任务头结点.指针 =
            reinterpret_cast<任务节点类*>(关联任务头);
        新需求节点->主信息.来源任务头结点.主键 = 关联任务头->获取主键();
    } else {
        新需求节点->主信息.来源任务头结点 = {};
    }

    if (auto* 步骤节点 = 私有_按主键解析任务节点(派生需求.消息头.步骤主键);
        步骤节点 && 步骤节点->主信息.节点种类 == 枚举_任务节点种类::步骤节点) {
        新需求节点->主信息.来源步骤节点.指针 =
            reinterpret_cast<任务节点类*>(步骤节点);
        新需求节点->主信息.来源步骤节点.主键 = 步骤节点->获取主键();
    } else {
        新需求节点->主信息.来源步骤节点 = {};
    }

    新需求节点->主信息.来源方法主键 = 派生需求.当前方法主键;
    return 新需求节点;
}

