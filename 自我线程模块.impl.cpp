template<class T节点, class T谓词>
T节点* 私有_查找直接子节点_按谓词(T节点* 父节点, T谓词&& 谓词) noexcept
{
    if (!父节点 || !父节点->子) {
        return nullptr;
    }

    auto* 首节点 = reinterpret_cast<T节点*>(父节点->子);
    auto* 当前 = 首节点;
    do {
        if (当前 && 谓词(当前)) {
            return 当前;
        }
        当前 = reinterpret_cast<T节点*>(当前->下);
    } while (当前 && 当前 != 首节点);
    return nullptr;
}

template<class T节点>
bool 私有_节点属于独立树(const T节点* 根节点, const T节点* 候选节点) noexcept
{
    if (!根节点 || !候选节点) {
        return false;
    }

    if (根节点 == 候选节点) {
        return true;
    }

    for (auto* 当前节点 = reinterpret_cast<const T节点*>(根节点->链下);
         当前节点 && 当前节点 != 根节点;
         当前节点 = reinterpret_cast<const T节点*>(当前节点->链下)) {
        if (当前节点 == 候选节点) {
            return true;
        }
    }
    return false;
}

