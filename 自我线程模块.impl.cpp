std::string 私有_特征值摘要(const 特征值& 值)
{
    return std::visit(
        [](const auto& 当前值) -> std::string {
            using 当前类型 = std::decay_t<decltype(当前值)>;
            if constexpr (std::is_same_v<当前类型, std::monostate>) {
                return "空";
            } else if constexpr (std::is_same_v<当前类型, bool>) {
                return 当前值 ? "true" : "false";
            } else if constexpr (std::is_same_v<当前类型, std::string>) {
                return 当前值.empty() ? "空字符串" : 当前值;
            } else if constexpr (std::is_same_v<当前类型, VecU句柄>) {
                return std::string("VecU#") + std::to_string(当前值.主信息指针);
            } else if constexpr (std::is_same_v<当前类型, 指针句柄>) {
                return std::string("Ptr#") + std::to_string(当前值.指针);
            } else {
                std::ostringstream 输出;
                输出 << 当前值;
                return 输出.str();
            }
        },
        值);
}

