std::string 私有_安全词(const 词性节点类* 词) noexcept
{
    if (!词) {
        return {};
    }

    const auto* 语素根 = reinterpret_cast<const 词性节点类*>(语素集.根指针);
    if (!私有_节点属于独立树(语素根, 词)) {
        return {};
    }

    try {
        return 语素集.获取词(词);
    }
    catch (...) {
        return {};
    }
}

