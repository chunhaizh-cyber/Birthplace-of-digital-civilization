bool 自我线程类::启动(const std::string& 调用点)
{
    if (运行中_.load(std::memory_order_acquire)) {
        return true;
    }

    if (工作线程_.joinable()) {
        工作线程_.join();
    }

    bool 来自故障恢复 = false;
    {
        std::scoped_lock 锁(状态锁_);
        来自故障恢复 = 待故障恢复启动_;
    }

    if (!自我初始化(调用点 + "/初始化")) {
        return false;
    }

    停止请求_.store(false, std::memory_order_release);
    const auto now = 结构体_时间戳::当前_微秒();
    (void)私有_确保自我线程存在(调用点 + "/线程存在");
    枚举_线程生命周期状态 旧生命周期 = 枚举_线程生命周期状态::未启动;
    {
        std::scoped_lock 锁(状态锁_);
        旧生命周期 = 生命周期_;
        生命周期_ = 枚举_线程生命周期状态::启动中;
        本次启动来自故障恢复_ = 来自故障恢复;
        if (来自故障恢复) {
            待故障恢复启动_ = false;
            ++累计恢复次数_;
            最近恢复摘要_ = "故障恢复后重新启动";
        } else {
            最近恢复摘要_.clear();
        }
        记录阶段事件_已加锁(
            now,
            枚举_自我线程运行阶段::线程启动中,
            "自我线程准备启动");
    }
    (void)私有_切换自我线程生命周期并上报(
        旧生命周期,
        枚举_线程生命周期状态::启动中,
        now,
        "线程启动",
        来自故障恢复 ? "自我线程/故障恢复重启" : "自我线程/启动",
        来自故障恢复 ? "故障恢复后重新启动" : "自我线程准备启动",
        false,
        false,
        调用点 + "/生命周期上报");

    工作线程_ = std::thread([this]() { 主循环_(); });
    return true;
}

