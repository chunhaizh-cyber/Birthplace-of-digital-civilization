void 自我线程类::主循环_()
{
    {
        std::scoped_lock 锁(状态锁_);
        生命周期_ = 枚举_线程生命周期状态::运行中;
        健康运行_ = true;
        运行中_.store(true, std::memory_order_release);
        记录阶段事件_已加锁(
            结构体_时间戳::当前_微秒(),
            枚举_自我线程运行阶段::运行态建立,
            "自我线程已进入运行态");
    }

    while (!停止请求_.load(std::memory_order_acquire)) {
        const auto now = 结构体_时间戳::当前_微秒();
        try {
            const auto 结果 = 执行主循环一轮_(now);
            const auto 摘要 = 私有_运行摘要(结果);
            {
                std::scoped_lock 锁(状态锁_);
                ++Tick计数_;
                最近Tick时间_ = now;
                首轮运行已完成_ = true;
                健康运行_ = true;
                最近循环结果_ = 结果;
                记录阶段事件_已加锁(now, 结果.当前运行阶段, 摘要);
            }
        }
        catch (const std::exception& 异常) {
            std::scoped_lock 锁(状态锁_);
            停止请求_.store(true, std::memory_order_release);
            故障收口_已加锁(now, 异常.what());
        }
        catch (...) {
            std::scoped_lock 锁(状态锁_);
            停止请求_.store(true, std::memory_order_release);
            故障收口_已加锁(now, "unknown");
        }

        std::unique_lock 锁(状态锁_);
        if (停止请求_.load(std::memory_order_acquire)) {
            break;
        }
        唤醒条件_.wait_for(锁, 配置_.Tick间隔, [this]() {
            return 停止请求_.load(std::memory_order_acquire)
                || !治理mailbox_.empty()
                || !本轮冻结治理批次_.empty();
        });
    }

    {
        std::scoped_lock 锁(状态锁_);
        运行中_.store(false, std::memory_order_release);
        if (生命周期_ != 枚举_线程生命周期状态::故障) {
            生命周期_ = 枚举_线程生命周期状态::已停止;
            记录阶段事件_已加锁(
                结构体_时间戳::当前_微秒(),
                枚举_自我线程运行阶段::线程停止,
                "自我线程已停止");
        }
    }
}

自我线程类::结构_循环结果 自我线程类::执行主循环一轮_(const 时间戳 now)
{
    结构_循环结果 结果{};
    结果.当前运行阶段 = 枚举_自我线程运行阶段::主循环消息归并;
    结果.当前最终去向 = 枚举_自我线程最终去向::继续提交;
    自我线程消息协议::结构_治理消息 已消费消息{};
    std::deque<自我线程消息协议::结构_治理消息> 本轮冻结消息{};
    自我线程消息批次执行器::结构_治理消息批次 本轮治理消息批次{};
    bool 命中治理邮箱事件 = false;
    {
        std::scoped_lock 锁(状态锁_);
        if (自我线程消息批次执行器::冻结消息批次(&治理mailbox_, &本轮冻结治理批次_)) {
            本轮冻结消息 = 本轮冻结治理批次_;
            本轮治理消息批次 =
                自我线程消息批次执行器::从冻结治理邮箱批次桥接消息批次(本轮冻结消息);
            已消费消息 = 自我线程消息批次执行器::归并冻结批次(本轮冻结消息, now);
            命中治理邮箱事件 = true;
            最近显式治理事件ID_ = 已消费消息.头.消息主键;
            累计显式治理事件消费数_ += static_cast<std::uint64_t>(本轮冻结消息.size());
            刷新治理恢复事件镜像_已加锁();
        }
    }

    if (!命中治理邮箱事件) {
        已消费消息 = 自我线程消息协议::构造治理消息(
            自我线程消息协议::枚举_消息子类::普通推进心跳,
            now);
        已消费消息.头.消息主键 = ++治理事件序号_;
        已消费消息.载荷键 = 自我线程消息批次执行器::生成消息幂等键(已消费消息);
        已消费消息.摘要 = "mailbox 空，按普通推进心跳消费";
    }

    if (!命中治理邮箱事件) {
        已消费消息.消费次数 += 1;
    }
    结果.最近消费治理事件幂等键 = 已消费消息.载荷键;
    结果.最近消费治理事件重放代数 = 已消费消息.重放代数;

    const auto 显式事件 = 自我线程消息协议::构造任务管理显式事件控制(已消费消息);
    (void)任务管理任务模块::设置治理显式事件控制(显式事件);

    if (自我线程消息协议::是否启用治理恢复重建控制(已消费消息)) {
        (void)任务管理任务模块::设置治理恢复重建控制(
            已消费消息.恢复快照,
            "自我线程类::执行主循环一轮_/恢复重建");
    } else {
        任务管理任务模块::清除治理恢复重建控制();
    }

    if (!自我.已初始化() || !自我.获取自我存在()) {
        if (自我初始化("自我线程类::执行主循环一轮_/补齐初始化")) {
            结果.已补齐初始化 = true;
            结果.当前运行阶段 = 枚举_自我线程运行阶段::主循环初始化补齐;
        }
    } else if (!自我.获取当前主需求() || !自我.获取当前主任务()) {
        if (自我初始化("自我线程类::执行主循环一轮_/回填主链")) {
            结果.已回填主链 = true;
            结果.当前运行阶段 = 枚举_自我线程运行阶段::主循环主链回填;
        }
    }

    自我线程消息处理器::结构_需求分支刷新范围 需求树变更刷新范围{};
    const bool 已应用需求树变更 = 自我线程消息处理器::应用需求树变更批次(
        自我,
        本轮冻结消息,
        &需求树变更刷新范围);
    if (已应用需求树变更) {
        结果.需求树已更新 = true;
    }

    const auto 线程裁决结果 = 自我线程消息批次执行器::计算线程裁决结果(自我);
    结果.当前运行阶段 = 枚举_自我线程运行阶段::主循环根层裁决;

    const auto 任务根节点 = 自我线程消息批次执行器::根据主导需求创建任务根节点(
        自我,
        now,
        线程裁决结果.根层重判结果,
        线程裁决结果.上层反馈摘要);
    auto 任务控制态 = 自我线程消息批次执行器::初始化任务控制态(
        任务根节点,
        已消费消息,
        线程裁决结果.执行前门控结果);
    结构_任务局部运行态 任务局部运行态{};
    const auto 工作线程控制事件 = 自我线程消息批次执行器::构造工作线程控制事件(任务控制态);

    任务管理工作线程::结构_工作线程桥接结果 桥接结果{};
    (void)任务管理工作线程::推进任务管理工作线程(
        自我,
        任务根节点,
        &任务控制态,
        &任务局部运行态,
        now,
        工作线程控制事件,
        线程裁决结果.根层重判结果,
        线程裁决结果.执行前门控结果,
        线程裁决结果.上层反馈摘要,
        "自我线程类::执行主循环一轮_/任务治理工作线程",
        &桥接结果);

    结果.任务管理上位输入已刷新 = 桥接结果.任务管理上位输入已刷新;
    结果.任务管理治理触发 = 桥接结果.任务管理治理触发;
    if (桥接结果.任务管理治理触发) {
        const auto& 治理结果 = 桥接结果.治理结果;
        结果.任务管理证据触发 = 桥接结果.已写入动态节点 || 桥接结果.已写入因果候选;
        结果.任务管理当前去向 = 私有_任务管理去向文本(治理结果.当前下一步去向);
        结果.当前运行阶段 = 枚举_自我线程运行阶段::主循环结果回流;
    }

    if (结果.任务管理当前去向.empty() && !任务局部运行态.当前等待原因.empty()) {
        结果.任务管理当前去向 = 任务局部运行态.当前等待原因;
    }

    const auto 本轮消息处理摘要 =
        自我线程消息处理器::处理治理消息批次(本轮治理消息批次.全部消息);
    const auto 推进事件处理摘要 =
        自我线程消息处理器::处理推进事件批次(桥接结果.推进事件列表);
    结果.有派生需求待实例化 =
        结果.有派生需求待实例化 || 推进事件处理摘要.有派生需求待实例化;
    结果.有子结果待路由 =
        结果.有子结果待路由 || 推进事件处理摘要.有子结果待路由;

    自我线程消息处理器::结构_需求列表刷新决策 需求列表刷新决策{};
    {
        std::scoped_lock 锁(状态锁_);
        需求列表刷新决策 = 自我线程消息处理器::判定需求列表刷新决策(
            服务优先需求列表_.empty(),
            安全优先需求列表_.empty(),
            已应用需求树变更,
            !需求树变更刷新范围.受影响类型集合.empty(),
            本轮消息处理摘要,
            推进事件处理摘要);
    }
    if (需求列表刷新决策.需要刷新) {
        std::scoped_lock 锁(状态锁_);
        if (需求列表刷新决策.可以局部刷新) {
            私有_按受影响分支增量刷新需求列表(
                自我,
                需求树变更刷新范围,
                枚举_需求列表排序主轴::服务优先,
                &服务优先需求列表_);
            私有_按受影响分支增量刷新需求列表(
                自我,
                需求树变更刷新范围,
                枚举_需求列表排序主轴::安全优先,
                &安全优先需求列表_);
        } else {
            服务优先需求列表_ = 私有_从需求树构建需求列表(自我, 枚举_需求列表排序主轴::服务优先);
            安全优先需求列表_ = 私有_从需求树构建需求列表(自我, 枚举_需求列表排序主轴::安全优先);
        }
    }
    结果.当前最终去向 = 私有_映射批次执行去向(
        自我线程消息批次执行器::裁决批次执行去向(
            自我,
            线程裁决结果.根层重判结果,
            线程裁决结果.执行前门控结果,
            桥接结果));

    {
        std::scoped_lock 锁(状态锁_);
        结果.治理mailbox待消费数 = 治理mailbox_.size();
    }

    已消费消息.最后消费结果 = 私有_线程最终去向文本(结果.当前最终去向);
    结果.最近消费治理事件结果 = 已消费消息.最后消费结果;
    结果.最近消费治理事件摘要 = 私有_消息摘要(已消费消息);

    任务管理任务模块::清除治理显式事件控制();
    {
        std::scoped_lock 锁(状态锁_);
        if (!本轮冻结治理批次_.empty()) {
            本轮冻结治理批次_.clear();
            刷新治理恢复事件镜像_已加锁();
        }
    }
    if (结果.当前最终去向 == 枚举_自我线程最终去向::停止) {
        停止请求_.store(true, std::memory_order_release);
    }

    return 结果;
}

