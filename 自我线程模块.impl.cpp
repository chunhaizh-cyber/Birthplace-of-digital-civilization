    上下文->结果.任务管理上位输入已刷新 = 批次摘要.触发需求队列刷新;
    上下文->结果.有派生需求待实例化 = 批次摘要.有派生需求待实例化;
    上下文->结果.有子结果待路由 = 批次摘要.有子结果待路由;

    for (auto& 消息 : 上下文->本轮冻结消息) {
        const auto 归并结果 = 私有_归并治理消息并对账(&执行尝试汇总表_, &消息);
        if (归并结果.命中执行尝试闭环) {
            上下文->结果.任务管理证据触发 = true;
        }

        bool 已落账 = false;
        if (归并结果.允许继续落账
            && 自我线程消息协议::是否线程状态变化治理消息(消息)) {
            已落账 = 私有_落线程状态变化到自我内部世界(
                消息,
                {});
        } else if (归并结果.允许继续落账
            && 消息.附加载荷.任务管理派生需求.has_value()) {
            已落账 = 私有_落任务管理派生需求到自我内部世界(
                消息,
                {});
        } else if (归并结果.允许继续落账
            && 消息.附加载荷.任务管理结果状态.has_value()) {
            已落账 = 私有_落任务管理结果到自我内部世界(
                消息,
                {},
                &归并结果.新增服务对象状态变化集,
                &归并结果.新增关键中间状态变化集);
        } else if (归并结果.允许继续落账 && !消息.变化项集.empty()) {
            已落账 = 私有_落治理变化项集到自我内部世界(
                消息,
                {});
        }

        if (已落账) {
            上下文->结果.任务管理证据触发 = true;
        }
        上下文->已消费消息 = 消息;
    }

    {
        std::scoped_lock 锁(状态锁_);
        本轮冻结治理批次_.clear();
        上下文->结果.治理mailbox待消费数 = 治理mailbox_.size();
        刷新治理恢复事件镜像_已加锁();
    }
