需求类::节点类* 私有_选择当前主需求候选_按根权重(
    const 自我类& 自我对象) noexcept
{
    const auto 核心状态 = 读取自我核心状态快照_最小_v0(自我对象);
    auto* 安全根需求 = 自我对象.获取安全根需求();
    auto* 服务根需求 = 自我对象.获取服务根需求();
    if (!安全根需求) return 服务根需求;
    if (!服务根需求) return 安全根需求;

    const auto& 根权重 = 自我对象.根任务权重();
    const long double 安全压力 =
        static_cast<long double>(自我对象.安全根方向差值()) *
        static_cast<long double>((std::max<I64>)(根权重.安全, 1));
    const long double 服务压力 =
        static_cast<long double>((std::max<I64>)(0, 自我对象.根目标服务值() - 核心状态.服务值)) *
        static_cast<long double>((std::max<I64>)(根权重.服务, 1));

    return 安全压力 >= 服务压力 ? 安全根需求 : 服务根需求;
}

