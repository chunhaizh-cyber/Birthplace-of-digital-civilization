bool 自我线程类::启动(const std::string& 调用点)
{
    if (运行中_.load(std::memory_order_acquire)) {
        return true;
    }

    if (工作线程_.joinable()) {
        工作线程_.join();
    }

    bool 来自故障恢复 = false;
    {
        std::scoped_lock 锁(状态锁_);
        来自故障恢复 = 待故障恢复启动_;
    }

    if (!自我初始化(调用点 + "/初始化")) {
        return false;
    }

    停止请求_.store(false, std::memory_order_release);
    {
        std::scoped_lock 锁(状态锁_);
        生命周期_ = 枚举_线程生命周期状态::启动中;
        本次启动来自故障恢复_ = 来自故障恢复;
        if (来自故障恢复) {
            待故障恢复启动_ = false;
            ++累计恢复次数_;
            最近恢复摘要_ = "故障恢复后重新启动";
        } else {
            最近恢复摘要_.clear();
        }
        记录阶段事件_已加锁(
            结构体_时间戳::当前_微秒(),
            枚举_自我线程运行阶段::线程启动中,
            "自我线程准备启动");
    }

    工作线程_ = std::thread([this]() { 主循环_(); });
    return true;
}

