结构_消息归并与对账结果 私有_归并治理消息并对账(
    std::unordered_map<std::string, 自我线程消息协议::结构_执行尝试汇总>* 执行尝试汇总表,
    自我线程消息协议::结构_治理消息* 消息)
{
    结构_消息归并与对账结果 结果{};
    if (!执行尝试汇总表 || !消息) {
        return 结果;
    }

    using namespace 自我线程消息协议;

    if (消息->附加载荷.任务管理线程状态.has_value()) {
        const auto& 任务管理线程状态 = *消息->附加载荷.任务管理线程状态;
        const auto 尝试键 = 构造任务管理执行尝试键(任务管理线程状态.消息头);
        auto& 汇总 = (*执行尝试汇总表)[尝试键];
        const auto 对账结果 = 合并线程状态消息到执行尝试汇总(&汇总, 任务管理线程状态);
        结果.命中执行尝试闭环 = true;
        结果.允许继续落账 = 私有_执行尝试对账结果允许继续落账(对账结果);
        消息->附加载荷.执行尝试汇总 = 汇总;
        消息->附加载荷.执行对账结果 = 对账结果;
        return 结果;
    }

    if (消息->附加载荷.任务管理结果状态.has_value()) {
        const auto& 任务管理结果状态 = *消息->附加载荷.任务管理结果状态;
        const auto 尝试键 = 构造任务管理执行尝试键(任务管理结果状态.消息头);
        auto& 汇总 = (*执行尝试汇总表)[尝试键];
        const auto 对账结果 = 合并结果消息到执行尝试汇总(
            &汇总,
            任务管理结果状态,
            &结果.新增服务对象状态变化集,
            &结果.新增关键中间状态变化集);
        结果.命中执行尝试闭环 = true;
        结果.允许继续落账 = 私有_执行尝试对账结果允许继续落账(对账结果);
        消息->附加载荷.执行尝试汇总 = 汇总;
        消息->附加载荷.执行对账结果 = 对账结果;
        return 结果;
    }

    return 结果;
}

bool 私有_消息排在前面(
    const 自我线程消息协议::结构_治理消息& 左,
    const 自我线程消息协议::结构_治理消息& 右) noexcept
{
    const auto 左优先级 = 私有_治理消息优先级值(左.头.优先级);
    const auto 右优先级 = 私有_治理消息优先级值(右.头.优先级);
    if (左优先级 != 右优先级) {
        return 左优先级 > 右优先级;
    }
    if (左.头.产生时间 != 右.头.产生时间) {
        return 左.头.产生时间 < 右.头.产生时间;
    }
    return 左.头.消息主键 < 右.头.消息主键;
}

