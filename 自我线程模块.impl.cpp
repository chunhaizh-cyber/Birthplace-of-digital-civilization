enum class 枚举_需求列表排序主轴 : std::uint8_t {
    服务优先 = 0,
    安全优先 = 1,
};

using 需求节点 = 需求类::节点类;

bool 私有_需求列表项排序在前(
    const 自我线程类::结构_需求列表项& 左,
    const 自我线程类::结构_需求列表项& 右,
    const 枚举_需求列表排序主轴 排序主轴) noexcept
{
    if (排序主轴 == 枚举_需求列表排序主轴::服务优先) {
        if (左.类型服务权重汇总 != 右.类型服务权重汇总) {
            return 左.类型服务权重汇总 > 右.类型服务权重汇总;
        }
        if (左.类型安全权重汇总 != 右.类型安全权重汇总) {
            return 左.类型安全权重汇总 > 右.类型安全权重汇总;
        }
    } else {
        if (左.类型安全权重汇总 != 右.类型安全权重汇总) {
            return 左.类型安全权重汇总 > 右.类型安全权重汇总;
        }
        if (左.类型服务权重汇总 != 右.类型服务权重汇总) {
            return 左.类型服务权重汇总 > 右.类型服务权重汇总;
        }
    }

    if (左.需求类型名称 != 右.需求类型名称) {
        return 左.需求类型名称 < 右.需求类型名称;
    }

    if (排序主轴 == 枚举_需求列表排序主轴::服务优先) {
        if (左.节点服务权重 != 右.节点服务权重) {
            return 左.节点服务权重 > 右.节点服务权重;
        }
        if (左.节点安全权重 != 右.节点安全权重) {
            return 左.节点安全权重 > 右.节点安全权重;
        }
    } else {
        if (左.节点安全权重 != 右.节点安全权重) {
            return 左.节点安全权重 > 右.节点安全权重;
        }
        if (左.节点服务权重 != 右.节点服务权重) {
            return 左.节点服务权重 > 右.节点服务权重;
        }
    }

    return 左.需求节点主键 < 右.需求节点主键;
}

std::vector<自我线程类::结构_需求列表项> 私有_从需求树构建需求列表(
    const 自我类& 自我对象,
    const 枚举_需求列表排序主轴 排序主轴)
{
    auto* 安全根需求 = 自我对象.获取安全根需求();
    auto* 服务根需求 = 自我对象.获取服务根需求();
    if (安全根需求) {
        安全根需求->主信息.安全权重 = 自我对象.获取安全值();
        安全根需求->主信息.服务权重 = 0;
    }
    if (服务根需求) {
        服务根需求->主信息.安全权重 = 0;
        服务根需求->主信息.服务权重 = 自我对象.获取服务值();
    }
    需求类 需求服务{};
    if (安全根需求) {
        (void)需求服务.刷新子需求权重(安全根需求);
    }
    if (服务根需求) {
        (void)需求服务.刷新子需求权重(服务根需求);
    }

    std::vector<const 需求节点*> 节点列表{};
    auto* 自我存在 = 自我对象.获取自我存在();
    auto* 需求根节点 = 自我存在 ? 世界树.存在().获取需求根节点(自我存在) : nullptr;
    if (!需求根节点) {
        return {};
    }

    for (auto* 当前节点 = 需求根节点->链下;
         当前节点 && 当前节点 != 需求根节点;
         当前节点 = 当前节点->链下) {
        节点列表.push_back(当前节点);
    }

    std::unordered_map<const 词性节点类*, I64> 类型安全权重汇总{};
    std::unordered_map<const 词性节点类*, I64> 类型服务权重汇总{};
    std::unordered_map<const 词性节点类*, std::string> 类型标题{};
    for (const auto* 节点 : 节点列表) {
        const auto* 类型 = 节点->主信息.类型;
        类型安全权重汇总[类型] += 节点->主信息.安全权重;
        类型服务权重汇总[类型] += 节点->主信息.服务权重;
        auto& 标题 = 类型标题[类型];
        if (标题.empty()) {
            标题 = 私有_需求类型标题(类型);
        }
    }

    std::stable_sort(
        节点列表.begin(),
        节点列表.end(),
        [&](const 需求节点* 左, const 需求节点* 右) {
            自我线程类::结构_需求列表项 左列表项{};
            左列表项.需求类型名称 = 类型标题[左 ? 左->主信息.类型 : nullptr];
            左列表项.需求节点主键 = 左 ? 左->获取主键() : std::string{};
            左列表项.节点安全权重 = 左 ? 左->主信息.安全权重 : 0;
            左列表项.节点服务权重 = 左 ? 左->主信息.服务权重 : 0;
            左列表项.类型安全权重汇总 = 类型安全权重汇总[左 ? 左->主信息.类型 : nullptr];
            左列表项.类型服务权重汇总 = 类型服务权重汇总[左 ? 左->主信息.类型 : nullptr];

            自我线程类::结构_需求列表项 右列表项{};
            右列表项.需求类型名称 = 类型标题[右 ? 右->主信息.类型 : nullptr];
            右列表项.需求节点主键 = 右 ? 右->获取主键() : std::string{};
            右列表项.节点安全权重 = 右 ? 右->主信息.安全权重 : 0;
            右列表项.节点服务权重 = 右 ? 右->主信息.服务权重 : 0;
            右列表项.类型安全权重汇总 = 类型安全权重汇总[右 ? 右->主信息.类型 : nullptr];
            右列表项.类型服务权重汇总 = 类型服务权重汇总[右 ? 右->主信息.类型 : nullptr];

            return 私有_需求列表项排序在前(左列表项, 右列表项, 排序主轴);
        });

    std::vector<自我线程类::结构_需求列表项> 需求列表{};
    需求列表.reserve(节点列表.size());
    for (const auto* 节点 : 节点列表) {
        if (!节点) {
            continue;
        }

        自我线程类::结构_需求列表项 列表项{};
        列表项.需求类型 = 节点->主信息.类型;
        列表项.需求类型名称 = 私有_需求类型标题(节点->主信息.类型);
        列表项.需求节点指针 = reinterpret_cast<std::uintptr_t>(节点);
        列表项.需求节点主键 = 节点->获取主键();
        列表项.节点安全权重 = 节点->主信息.安全权重;
        列表项.节点服务权重 = 节点->主信息.服务权重;
        列表项.类型安全权重汇总 = 类型安全权重汇总[节点->主信息.类型];
        列表项.类型服务权重汇总 = 类型服务权重汇总[节点->主信息.类型];
        需求列表.push_back(std::move(列表项));
    }

    return 需求列表;
}

void 私有_按受影响分支增量刷新需求列表(
    const 自我类& 自我对象,
    const 自我线程消息处理器::结构_需求分支刷新范围& 刷新范围,
    const 枚举_需求列表排序主轴 排序主轴,
    std::vector<自我线程类::结构_需求列表项>* 需求列表)
{
    if (!需求列表 || 刷新范围.受影响类型集合.empty()) {
        return;
    }

    auto* 自我存在 = 自我对象.获取自我存在();
    auto* 需求根节点 = 自我存在 ? 世界树.存在().获取需求根节点(自我存在) : nullptr;
    if (!需求根节点) {
        需求列表->clear();
        return;
    }

    std::unordered_map<const 词性节点类*, I64> 类型安全权重汇总{};
    std::unordered_map<const 词性节点类*, I64> 类型服务权重汇总{};
    std::unordered_map<const 词性节点类*, std::string> 类型标题{};
    std::vector<const 需求节点*> 受影响节点列表{};

    for (auto* 当前节点 = 需求根节点->链下;
         当前节点 && 当前节点 != 需求根节点;
         当前节点 = 当前节点->链下) {
        const auto* 类型 = 当前节点->主信息.类型;
        if (刷新范围.受影响类型集合.count(类型) > 0) {
            类型安全权重汇总[类型] += 当前节点->主信息.安全权重;
            类型服务权重汇总[类型] += 当前节点->主信息.服务权重;
            auto& 标题 = 类型标题[类型];
            if (标题.empty()) {
                标题 = 私有_需求类型标题(类型);
            }
        }

        if (刷新范围.当前节点主键集合.count(当前节点->获取主键()) > 0) {
            受影响节点列表.push_back(当前节点);
        }
    }

    需求列表->erase(
        std::remove_if(
            需求列表->begin(),
            需求列表->end(),
            [&](自我线程类::结构_需求列表项& 列表项) {
                if (刷新范围.变更前节点主键集合.count(列表项.需求节点主键) > 0) {
                    return true;
                }
                if (刷新范围.受影响类型集合.count(列表项.需求类型) > 0) {
                    列表项.类型安全权重汇总 = 类型安全权重汇总[列表项.需求类型];
                    列表项.类型服务权重汇总 = 类型服务权重汇总[列表项.需求类型];
                }
                return false;
            }),
        需求列表->end());

    需求列表->reserve(需求列表->size() + 受影响节点列表.size());
    for (const auto* 节点 : 受影响节点列表) {
        自我线程类::结构_需求列表项 列表项{};
        列表项.需求类型 = 节点->主信息.类型;
        列表项.需求类型名称 = 私有_需求类型标题(节点->主信息.类型);
        列表项.需求节点指针 = reinterpret_cast<std::uintptr_t>(节点);
        列表项.需求节点主键 = 节点->获取主键();
        列表项.节点安全权重 = 节点->主信息.安全权重;
        列表项.节点服务权重 = 节点->主信息.服务权重;
        列表项.类型安全权重汇总 = 类型安全权重汇总[节点->主信息.类型];
        列表项.类型服务权重汇总 = 类型服务权重汇总[节点->主信息.类型];
        需求列表->push_back(std::move(列表项));
    }

    std::stable_sort(
        需求列表->begin(),
        需求列表->end(),
        [&](const 自我线程类::结构_需求列表项& 左, const 自我线程类::结构_需求列表项& 右) {
            return 私有_需求列表项排序在前(左, 右, 排序主轴);
        });
}

