bool 上报线程状态变化(
    const 自我线程消息协议::结构_线程状态变化消息段& 消息段,
    const 结构_线程存在初始化参数* 线程存在初始化,
    const std::string& 调用点)
{
    auto 线程状态消息段 = 消息段;
    if (线程状态消息段.发生时间 == 0) {
        线程状态消息段.发生时间 = 结构体_时间戳::当前_微秒();
    }

    结构_线程存在初始化参数 默认初始化{};
    const 结构_线程存在初始化参数* 初始化参数 = 线程存在初始化;
    if (!初始化参数) {
        默认初始化 = 私有_从线程状态消息推断线程初始化参数(线程状态消息段);
        初始化参数 = &默认初始化;
    }

    存在节点类* 线程存在 = nullptr;
    if (!私有_确保线程状态主体存在(
        *初始化参数,
        调用点 + "/确保线程主体存在",
        &线程存在)) {
        return false;
    }

    if (线程状态消息段.线程存在主键 == 0) {
        线程状态消息段.线程存在主键 = 私有_存在主键(线程存在);
    }
    if (线程状态消息段.线程标签.empty()) {
        线程状态消息段.线程标签 = 私有_推断线程标签(*初始化参数);
    }
    if (线程状态消息段.线程种类.empty()) {
        线程状态消息段.线程种类 = 私有_推断线程种类(*初始化参数);
    }
    if (线程状态消息段.线程存在类型 == 自我线程消息协议::枚举_存在类型::未定义) {
        线程状态消息段.线程存在类型 = 私有_推断线程存在类型(*初始化参数);
    }
    if (线程状态消息段.直属拥有者主键 == 0) {
        线程状态消息段.直属拥有者主键 = 私有_确保线程直属拥有者主键(
            *初始化参数,
            &线程状态消息段.直属拥有者类型,
            调用点 + "/直属拥有者");
    }
    if (线程状态消息段.关联线程主键 == 0) {
        线程状态消息段.关联线程主键 = 线程状态消息段.线程存在主键;
    }
    if (线程状态消息段.决策者主键 == 0
        && (线程状态消息段.决策者类型 == 线程状态消息段.线程存在类型
            || 线程状态消息段.决策者类型 == 自我线程消息协议::枚举_存在类型::自我线程)) {
        线程状态消息段.决策者主键 = 线程状态消息段.线程存在主键;
    }
    if (线程状态消息段.决策者标签.empty()
        && 线程状态消息段.决策者主键 == 线程状态消息段.线程存在主键) {
        线程状态消息段.决策者标签 = 线程状态消息段.线程标签;
    }

    auto 消息 = 自我线程消息协议::从线程状态变化构造治理消息(
        线程状态消息段);
    return 获取全局自我线程().投递治理消息(消息);
}

