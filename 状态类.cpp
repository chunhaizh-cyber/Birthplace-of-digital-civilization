#include "状态类.h"

#include <algorithm>

namespace {
    template<class T节点>
    可解析引用<T节点> 私有_生成引用(T节点* 节点)
    {
        可解析引用<T节点> out{};
        out.指针 = 节点;
        if (节点) out.主键 = 节点->获取主键();
        return out;
    }
}

状态类::状态类(基础信息类* 基础信息) noexcept
    : 基础信息_(基础信息)
{
}

void 状态类::绑定基础信息(基础信息类* 基础信息) noexcept
{
    基础信息_ = 基础信息;
}

状态节点主信息类* 状态类::取状态主信息(状态节点类* 节点) const noexcept
{
    return 基础信息_ ? 基础信息_->取主信息<状态节点主信息类>(节点) : nullptr;
}

const 状态节点主信息类* 状态类::取状态主信息(const 状态节点类* 节点) const noexcept
{
    return 基础信息_ ? 基础信息_->取主信息<状态节点主信息类>(节点) : nullptr;
}

状态节点类* 状态类::创建状态(场景节点类* 场景, 状态节点主信息类* 主信息)
{
    if (!基础信息_ || !主信息) return nullptr;

    if (主信息->状态域 == 枚举_状态域::未定义) {
        主信息->状态域 = 私有_推断状态域(主信息->状态主体.指针);
    }
    if (!主信息->对应信息节点.有效()) {
        主信息->对应信息节点 = 主信息->状态特征.有效()
            ? 私有_生成引用(主信息->状态特征.指针)
            : 私有_生成引用(主信息->状态主体.指针);
    }

    auto* 节点 = static_cast<状态节点类*>(基础信息_->添加子节点(场景, 主信息));
    auto* 场景主信息 = 基础信息_->取主信息<场景节点主信息类>(场景);
    if (节点 && 场景主信息) {
        场景主信息->状态索引.push_back(私有_生成引用(节点));
    }
    return 节点;
}

状态节点类* 状态类::创建状态(
    场景节点类* 场景,
    基础信息节点类* 主体,
    特征节点类* 特征,
    const 特征值& 状态值,
    时间戳 发生时间,
    时间戳 收到时间)
{
    auto* 主信息 = new 状态节点主信息类{};
    主信息->状态主体 = 私有_生成引用(主体);
    主信息->状态特征 = 私有_生成引用(特征);
    主信息->状态值 = 状态值;
    主信息->发生时间 = 发生时间;
    主信息->收到时间 = 收到时间;
    主信息->状态域 = 私有_推断状态域(主体);
    主信息->对应信息节点 = 特征 ? 私有_生成引用(特征) : 私有_生成引用(主体);
    return 创建状态(场景, 主信息);
}

bool 状态类::删除状态(状态节点类* 节点)
{
    if (!基础信息_ || !节点) return false;

    auto* 父节点 = static_cast<基础信息节点类*>(节点->父);
    auto* 场景主信息 = 基础信息_->取主信息<场景节点主信息类>(父节点);
    if (场景主信息) {
        场景主信息->状态索引.erase(
            std::remove_if(
                场景主信息->状态索引.begin(),
                场景主信息->状态索引.end(),
                [&](const auto& 项) { return 项.指针 == 节点 || 项.主键 == 节点->获取主键(); }),
            场景主信息->状态索引.end());
    }

    return 基础信息_->删除节点(节点);
}

std::vector<状态节点类*> 状态类::枚举全部状态() const
{
    std::vector<状态节点类*> out;
    if (!基础信息_) return out;

    for (auto* 节点 : 基础信息_->枚举节点_按类型<状态节点主信息类>()) {
        out.push_back(static_cast<状态节点类*>(节点));
    }
    return out;
}

std::vector<状态节点类*> 状态类::获取场景状态(const 场景节点类* 场景) const
{
    std::vector<状态节点类*> out;
    const auto* 场景主信息 = 基础信息_ ? 基础信息_->取主信息<场景节点主信息类>(场景) : nullptr;
    if (!场景主信息) return out;

    for (const auto& 项 : 场景主信息->状态索引) {
        if (项.指针) out.push_back(项.指针);
    }
    return out;
}

bool 状态类::绑定主体特征(状态节点类* 节点, 基础信息节点类* 主体, 特征节点类* 特征)
{
    auto* 主信息 = 取状态主信息(节点);
    if (!主信息) return false;

    主信息->状态主体 = 私有_生成引用(主体);
    主信息->状态特征 = 私有_生成引用(特征);
    主信息->状态域 = 私有_推断状态域(主体);
    if (!主信息->对应信息节点.有效()) {
        主信息->对应信息节点 = 特征 ? 私有_生成引用(特征) : 私有_生成引用(主体);
    }
    return true;
}

bool 状态类::写入状态值(状态节点类* 节点, const 特征值& 值, 时间戳 发生时间, 时间戳 收到时间)
{
    auto* 主信息 = 取状态主信息(节点);
    if (!主信息) return false;

    主信息->状态值 = 值;
    if (发生时间 != 0) 主信息->发生时间 = 发生时间;
    if (收到时间 != 0) 主信息->收到时间 = 收到时间;
    主信息->统计.记录观测(收到时间 != 0 ? 收到时间 : 结构体_时间戳::当前_微秒());
    return true;
}

bool 状态类::标记变化(
    状态节点类* 节点,
    bool 是否变化,
    const std::string& 原因类别,
    const std::string& 原因说明)
{
    auto* 主信息 = 取状态主信息(节点);
    if (!主信息) return false;

    主信息->是否变化 = 是否变化;
    主信息->变化原因类别 = 原因类别;
    主信息->变化原因说明 = 原因说明;
    return true;
}

枚举_状态域 状态类::私有_推断状态域(const 基础信息节点类* 主体) noexcept
{
    if (!主体 || !主体->主信息) return 枚举_状态域::未定义;

    if (dynamic_cast<存在节点主信息类*>(主体->主信息)
        || dynamic_cast<场景节点主信息类*>(主体->主信息)
        || dynamic_cast<特征节点主信息类*>(主体->主信息)) {
        return 枚举_状态域::世界状态;
    }
    return 枚举_状态域::内部状态;
}
