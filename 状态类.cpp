#include "状态类.h"

#include <algorithm>

namespace {
    template<class T节点>
    可解析引用<T节点> 私有_生成引用(T节点* 节点)
    {
        可解析引用<T节点> out{};
        out.指针 = 节点;
        if (节点) out.主键 = 节点->获取主键();
        return out;
    }

    template<class T节点>
    bool 私有_引用已存在(const std::vector<可解析引用<T节点>>& 列表, T节点* 节点)
    {
        for (const auto& 项 : 列表) {
            if (项.指针 == 节点) return true;
            if (节点 && !项.主键.empty() && 项.主键 == 节点->获取主键()) return true;
        }
        return false;
    }

    template<class T节点>
    bool 私有_节点匹配(const 可解析引用<T节点>& 引用, const T节点* 节点) noexcept
    {
        if (!节点) return false;
        if (引用.指针 == 节点) return true;
        return !引用.主键.empty() && 引用.主键 == 节点->获取主键();
    }
}

