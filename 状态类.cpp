    template<class T节点>
    可解析引用<T节点> 私有_生成引用(T节点* 节点)
    {
        可解析引用<T节点> out{};
        out.指针 = 节点;
        if (节点) out.主键 = 节点->获取主键();
        return out;
    }

    template<class T节点>
    bool 私有_引用已存在(const std::vector<可解析引用<T节点>>& 列表, T节点* 节点)
    {
        for (const auto& 项 : 列表) {
            if (项.指针 == 节点) return true;
            if (节点 && !项.主键.empty() && 项.主键 == 节点->获取主键()) return true;
        }
        return false;
    }

    template<class T节点>
    bool 私有_节点匹配(const 可解析引用<T节点>& 引用, const T节点* 节点) noexcept
    {
        if (!节点) return false;
        if (引用.指针 == 节点) return true;
        return !引用.主键.empty() && 引用.主键 == 节点->获取主键();
    }

    状态节点类* 私有_解析状态引用(基础信息类* 基础信息, const 可解析引用<状态节点类>& 引用)
    {
        if (!基础信息) return nullptr;
        if (引用.指针 && 基础信息->取主信息<状态节点主信息类>(引用.指针)) {
            return 引用.指针;
        }
        if (!引用.主键.empty()) {
            auto* 节点 = 基础信息->查找主键(引用.主键);
            if (节点 && 基础信息->取主信息<状态节点主信息类>(节点)) {
                return static_cast<状态节点类*>(节点);
            }
        }
        return nullptr;
    }

    bool 私有_状态引用匹配(const 可解析引用<状态节点类>& 引用, const 状态节点类* 节点) noexcept
    {
        if (!节点) return false;
        if (引用.指针 == 节点) return true;
        return !引用.主键.empty() && 引用.主键 == 节点->获取主键();
    }

    bool 私有_基础引用匹配状态(const 可解析引用<基础信息节点类>& 引用, const 状态节点类* 节点) noexcept
    {
        if (!节点) return false;
        if (引用.指针 == 节点) return true;
        return !引用.主键.empty() && 引用.主键 == 节点->获取主键();
    }

    bool 私有_状态同主体特征(
        const 状态节点主信息类* 左,
        const 状态节点主信息类* 右) noexcept
    {
        if (!左 || !右) return false;
        if (!左->状态主体.有效() || !右->状态主体.有效()) return false;
        if (!左->状态特征.有效() || !右->状态特征.有效()) return false;
        const bool 主体相同 =
            左->状态主体.指针 == 右->状态主体.指针
            || (!左->状态主体.主键.empty() && 左->状态主体.主键 == 右->状态主体.主键);
        const bool 特征相同 =
            左->状态特征.指针 == 右->状态特征.指针
            || (!左->状态特征.主键.empty() && 左->状态特征.主键 == 右->状态特征.主键);
        return 主体相同 && 特征相同;
    }

    bool 私有_状态在范围内(
        const 状态节点主信息类* 主信息,
        const 基础信息节点类* 主体,
        const 特征节点类* 特征) noexcept
    {
        if (!主信息) return false;
        if (主体 && !私有_节点匹配(主信息->状态主体, 主体)) return false;
        if (特征 && !私有_节点匹配(主信息->状态特征, 特征)) return false;
        return true;
    }

    时间戳 私有_状态排序时间(const 状态节点主信息类& 主信息) noexcept
    {
        return 主信息.发生时间 != 0 ? 主信息.发生时间 : 主信息.收到时间;
    }

    bool 私有_状态排序晚于(
        const 状态节点类* 左节点,
        const 状态节点主信息类* 左主信息,
        const 状态节点类* 右节点,
        const 状态节点主信息类* 右主信息) noexcept
    {
        if (!左节点 || !左主信息 || !右节点 || !右主信息) return false;
        const auto 左时间 = 私有_状态排序时间(*左主信息);
        const auto 右时间 = 私有_状态排序时间(*右主信息);
        if (左时间 != 右时间) return 左时间 > 右时间;
        return 左节点->获取主键() > 右节点->获取主键();
    }
