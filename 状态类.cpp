bool 状态类::删除状态(状态节点类* 节点)
{
    if (!基础信息_ || !节点) return false;

    auto* 父节点 = static_cast<基础信息节点类*>(节点->父);
    auto* 场景主信息 = 基础信息_->取主信息<场景节点主信息类>(父节点);
    if (场景主信息) {
        场景主信息->状态索引.erase(
            std::remove_if(
                场景主信息->状态索引.begin(),
                场景主信息->状态索引.end(),
                [&](const auto& 项) { return 项.指针 == 节点 || 项.主键 == 节点->获取主键(); }),
            场景主信息->状态索引.end());
    }

    return 基础信息_->删除节点(节点);
}

std::vector<状态节点类*> 状态类::枚举全部状态() const
{
    std::vector<状态节点类*> out;
    if (!基础信息_) return out;

    for (auto* 节点 : 基础信息_->枚举节点_按类型<状态节点主信息类>()) {
        out.push_back(static_cast<状态节点类*>(节点));
    }
    return out;
}

std::vector<状态节点类*> 状态类::获取场景状态(const 场景节点类* 场景) const
{
    std::vector<状态节点类*> out;
    const auto* 场景主信息 = 基础信息_ ? 基础信息_->取主信息<场景节点主信息类>(场景) : nullptr;
    if (!场景主信息) return out;

    for (const auto& 项 : 场景主信息->状态索引) {
        if (项.指针) out.push_back(项.指针);
    }
    std::stable_sort(out.begin(), out.end(), [this](const auto* 左, const auto* 右) {
        const auto* 左主信息 = 取状态主信息(左);
        const auto* 右主信息 = 取状态主信息(右);
        const auto 左时间 = 左主信息 ? 私有_取状态排序时间(*左主信息) : 0;
        const auto 右时间 = 右主信息 ? 私有_取状态排序时间(*右主信息) : 0;
        if (左时间 != 右时间) return 左时间 < 右时间;
        if (!左 || !右) return 左 < 右;
        return 左->获取主键() < 右->获取主键();
    });
    return out;
}

bool 状态类::是否当前活动状态(const 场景节点类* 场景, const 状态节点类* 节点) const
{
    if (!基础信息_ || !场景 || !节点) return false;

    const auto* 目标主信息 = 取状态主信息(节点);
    const auto* 场景主信息 = 基础信息_->取主信息<场景节点主信息类>(场景);
    if (!目标主信息 || !场景主信息) return false;

    bool 在活动索引中 = false;
    for (const auto& 项 : 场景主信息->状态索引) {
        auto* 候选节点 = 私有_解析状态引用(基础信息_, 项);
        const auto* 候选主信息 = 取状态主信息(候选节点);
        if (!候选主信息 || !私有_状态同主体特征(候选主信息, 目标主信息)) continue;
        if (候选节点 == 节点 || (候选节点 && 候选节点->获取主键() == 节点->获取主键())) {
            在活动索引中 = true;
            continue;
        }
        if (私有_状态排序晚于(候选节点, 候选主信息, 节点, 目标主信息)) {
            return false;
        }
    }
    return 在活动索引中;
}

std::uint64_t 状态类::统计状态引用数量(const 状态节点类* 节点) const
{
    if (!基础信息_ || !节点) return 0;

    std::uint64_t 数量 = 0;
    for (auto* 基础节点 : 基础信息_->枚举节点_按类型<动态节点主信息类>()) {
        const auto* 主信息 = 基础信息_->取主信息<动态节点主信息类>(基础节点);
        if (!主信息) continue;
        if (私有_状态引用匹配(主信息->初始状态, 节点)) ++数量;
        if (私有_状态引用匹配(主信息->结果状态, 节点)) ++数量;
    }
    for (auto* 基础节点 : 基础信息_->枚举节点_按类型<二次特征主信息类>()) {
        const auto* 主信息 = 基础信息_->取主信息<二次特征主信息类>(基础节点);
        if (!主信息) continue;
        if (私有_状态引用匹配(主信息->来源起始状态, 节点)) ++数量;
        if (私有_状态引用匹配(主信息->来源结果状态, 节点)) ++数量;
        if (私有_基础引用匹配状态(主信息->左对象, 节点)) ++数量;
        if (私有_基础引用匹配状态(主信息->右对象, 节点)) ++数量;
    }
    return 数量;
}

bool 状态类::可从活动状态链清理(const 场景节点类* 场景, const 状态节点类* 节点) const
{
    if (!基础信息_ || !场景 || !节点) return false;

    const auto* 目标主信息 = 取状态主信息(节点);
    const auto* 场景主信息 = 基础信息_->取主信息<场景节点主信息类>(场景);
    if (!目标主信息 || !场景主信息) return false;

    std::size_t 更新状态数量 = 0;
    for (const auto& 项 : 场景主信息->状态索引) {
        auto* 候选节点 = 私有_解析状态引用(基础信息_, 项);
        const auto* 候选主信息 = 取状态主信息(候选节点);
        if (!候选主信息 || !私有_状态同主体特征(候选主信息, 目标主信息)) continue;
        if (候选节点 == 节点 || (候选节点 && 候选节点->获取主键() == 节点->获取主键())) {
            continue;
        }
        if (私有_状态排序晚于(候选节点, 候选主信息, 节点, 目标主信息)) {
            ++更新状态数量;
            if (更新状态数量 >= 私有_同口径活动状态保留数量) {
                return true;
            }
        }
    }
    return false;
}

std::size_t 状态类::清理已处理状态索引(
    场景节点类* 场景,
    基础信息节点类* 主体,
    特征节点类* 特征,
    std::size_t 最大清理数量)
{
    auto* 场景主信息 = 基础信息_ ? 基础信息_->取主信息<场景节点主信息类>(场景) : nullptr;
    if (!场景主信息) return 0;

    auto& 索引 = 场景主信息->状态索引;
    std::vector<std::string> 待清理主键;
    std::size_t 清理数量 = 0;
    for (const auto& 项 : 索引) {
        if (最大清理数量 != 0 && 清理数量 >= 最大清理数量) {
            break;
        }
        auto* 状态节点 = 私有_解析状态引用(基础信息_, 项);
        const auto* 主信息 = 取状态主信息(状态节点);
        if (!状态节点 || !主信息) {
            待清理主键.push_back(项.主键);
            ++清理数量;
            continue;
        }
        if (!私有_状态在范围内(主信息, 主体, 特征)) {
            continue;
        }
        if (!可从活动状态链清理(场景, 状态节点)) {
            continue;
        }
        待清理主键.push_back(状态节点->获取主键());
        ++清理数量;
    }

    if (待清理主键.empty()) {
        return 0;
    }

    索引.erase(
        std::remove_if(
            索引.begin(),
            索引.end(),
            [&](const auto& 项) {
                auto* 状态节点 = 私有_解析状态引用(基础信息_, 项);
                const auto 键 = 状态节点 ? 状态节点->获取主键() : 项.主键;
                return std::find(待清理主键.begin(), 待清理主键.end(), 键) != 待清理主键.end();
            }),
        索引.end());
    return 待清理主键.size();
}

std::size_t 状态类::清理超过时间窗口的状态索引(
    场景节点类* 场景,
    时间戳 最早保留时间,
    std::size_t 最大清理数量)
{
    auto* 场景主信息 = 基础信息_ ? 基础信息_->取主信息<场景节点主信息类>(场景) : nullptr;
    if (!场景主信息 || 最早保留时间 == 0) return 0;

    std::vector<std::string> 待清理主键;
    std::size_t 清理数量 = 0;
    for (const auto& 项 : 场景主信息->状态索引) {
        if (最大清理数量 != 0 && 清理数量 >= 最大清理数量) {
            break;
        }
        auto* 状态节点 = 私有_解析状态引用(基础信息_, 项);
        const auto* 主信息 = 取状态主信息(状态节点);
        if (!状态节点 || !主信息) {
            待清理主键.push_back(项.主键);
            ++清理数量;
            continue;
        }
        if (私有_状态排序时间(*主信息) >= 最早保留时间) {
            continue;
        }
        if (!可从活动状态链清理(场景, 状态节点)) {
            continue;
        }
        待清理主键.push_back(状态节点->获取主键());
        ++清理数量;
    }

    if (待清理主键.empty()) {
        return 0;
    }

    auto& 索引 = 场景主信息->状态索引;
    索引.erase(
        std::remove_if(
            索引.begin(),
            索引.end(),
            [&](const auto& 项) {
                auto* 状态节点 = 私有_解析状态引用(基础信息_, 项);
                const auto 键 = 状态节点 ? 状态节点->获取主键() : 项.主键;
                return std::find(待清理主键.begin(), 待清理主键.end(), 键) != 待清理主键.end();
            }),
        索引.end());
    return 待清理主键.size();
}

std::size_t 状态类::按活动状态限量清理(
    场景节点类* 场景,
    std::size_t 活动状态上限,
    std::size_t 最大清理数量)
{
    auto* 场景主信息 = 基础信息_ ? 基础信息_->取主信息<场景节点主信息类>(场景) : nullptr;
    if (!场景主信息 || 活动状态上限 == 0) return 0;

    const auto 当前数量 = 场景主信息->状态索引.size();
    if (当前数量 <= 活动状态上限) {
        return 0;
    }

    auto 目标清理数量 = 当前数量 - 活动状态上限;
    if (最大清理数量 != 0 && 最大清理数量 < 目标清理数量) {
        目标清理数量 = 最大清理数量;
    }
    const auto 已清理 = 清理已处理状态索引(场景, nullptr, nullptr, 目标清理数量);
    if (已清理 == 0) {
        std::size_t 被引用状态数量 = 0;
        for (const auto& 项 : 场景主信息->状态索引) {
            auto* 状态节点 = 私有_解析状态引用(基础信息_, 项);
            if (状态节点 && 统计状态引用数量(状态节点) > 0) {
                ++被引用状态数量;
            }
        }

        std::ostringstream 摘要;
        摘要 << "状态活动索引超限但未找到可清理状态"
            << " | 场景=" << (场景 ? 场景->获取主键() : std::string("空场景"))
            << " | 当前=" << 当前数量
            << " | 上限=" << 活动状态上限
            << " | 目标清理=" << 目标清理数量
            << " | 被引用状态=" << 被引用状态数量;

        结构_状态清理问题快照 快照{};
        快照.需要自检需求 = true;
        快照.场景指针 = reinterpret_cast<std::uintptr_t>(场景);
        快照.场景主键 = 场景 ? 场景->获取主键() : std::string{};
        快照.活动状态数量 = 当前数量;
        快照.活动状态上限 = 活动状态上限;
        快照.目标清理数量 = 目标清理数量;
        快照.已清理数量 = 已清理;
        快照.被引用状态数量 = 被引用状态数量;
        快照.记录时间 = 结构体_时间戳::当前_微秒();
        快照.摘要 = 摘要.str();
        {
            std::lock_guard<std::mutex> 锁(清理问题锁_);
            最近清理问题_ = std::move(快照);
        }
        项目运行警告日志(摘要.str());
    }
    return 已清理;
}

