bool 状态类::删除状态(状态节点类* 节点)
{
    if (!基础信息_ || !节点) return false;

    auto* 父节点 = static_cast<基础信息节点类*>(节点->父);
    auto* 场景主信息 = 基础信息_->取主信息<场景节点主信息类>(父节点);
    if (场景主信息) {
        场景主信息->状态索引.erase(
            std::remove_if(
                场景主信息->状态索引.begin(),
                场景主信息->状态索引.end(),
                [&](const auto& 项) { return 项.指针 == 节点 || 项.主键 == 节点->获取主键(); }),
            场景主信息->状态索引.end());
    }

    return 基础信息_->删除节点(节点);
}

std::vector<状态节点类*> 状态类::枚举全部状态() const
{
    std::vector<状态节点类*> out;
    if (!基础信息_) return out;

    for (auto* 节点 : 基础信息_->枚举节点_按类型<状态节点主信息类>()) {
        out.push_back(static_cast<状态节点类*>(节点));
    }
    return out;
}

std::vector<状态节点类*> 状态类::获取场景状态(const 场景节点类* 场景) const
{
    std::vector<状态节点类*> out;
    const auto* 场景主信息 = 基础信息_ ? 基础信息_->取主信息<场景节点主信息类>(场景) : nullptr;
    if (!场景主信息) return out;

    for (const auto& 项 : 场景主信息->状态索引) {
        if (项.指针) out.push_back(项.指针);
    }
    std::stable_sort(out.begin(), out.end(), [this](const auto* 左, const auto* 右) {
        const auto* 左主信息 = 取状态主信息(左);
        const auto* 右主信息 = 取状态主信息(右);
        const auto 左时间 = 左主信息 ? 私有_取状态排序时间(*左主信息) : 0;
        const auto 右时间 = 右主信息 ? 私有_取状态排序时间(*右主信息) : 0;
        if (左时间 != 右时间) return 左时间 < 右时间;
        if (!左 || !右) return 左 < 右;
        return 左->获取主键() < 右->获取主键();
    });
    return out;
}

结构_最近两次I64状态方向结果 状态类::比较最近两次I64状态方向(
    场景节点类* 场景,
    枚举_状态域 状态域,
    基础信息节点类* 状态主体,
    特征节点类* 状态特征) const
{
    结构_最近两次I64状态方向结果 out{};
    if (!状态主体 || !状态特征) return out;

    const auto 状态列表 = 获取场景状态(场景);
    const 状态节点主信息类* 最新 = nullptr;
    const 状态节点主信息类* 上次 = nullptr;
    for (auto it = 状态列表.rbegin(); it != 状态列表.rend(); ++it) {
        const auto* 主信息 = 取状态主信息(*it);
        if (!主信息 || 主信息->状态域 != 状态域) continue;
        if (!私有_节点匹配(主信息->状态主体, 状态主体)) continue;
        if (!私有_节点匹配(主信息->状态特征, 状态特征)) continue;
        if (!std::holds_alternative<I64>(主信息->状态值)) continue;
        if (!最新) {
            最新 = 主信息;
            continue;
        }
        上次 = 主信息;
        break;
    }
    if (!最新 || !上次) return out;

    out.可比较 = true;
    out.方向 = 获取状态二次特征(*上次, *最新);
    out.最新值 = std::get<I64>(最新->状态值);
    out.上次值 = std::get<I64>(上次->状态值);
    out.最新时间 = 私有_取状态排序时间(*最新);
    out.上次时间 = 私有_取状态排序时间(*上次);
    return out;
}

bool 状态类::绑定主体特征(状态节点类* 节点, 基础信息节点类* 主体, 特征节点类* 特征)
{
    auto* 主信息 = 取状态主信息(节点);
    if (!主信息) return false;

    主信息->状态主体 = 私有_生成引用(主体);
    主信息->状态特征 = 私有_生成引用(特征);
    主信息->状态域 = 私有_推断状态域(主体);
    if (!主信息->对应信息节点.有效()) {
        主信息->对应信息节点 = 特征 ? 私有_生成引用(特征) : 私有_生成引用(主体);
    }
    return true;
}

bool 状态类::写入状态值(状态节点类* 节点, const 特征值& 值, 时间戳 发生时间, 时间戳 收到时间)
{
    auto* 主信息 = 取状态主信息(节点);
    if (!主信息) return false;

    主信息->状态值 = 值;
    if (发生时间 != 0) 主信息->发生时间 = 发生时间;
    if (收到时间 != 0) 主信息->收到时间 = 收到时间;
    主信息->统计.记录观测(收到时间 != 0 ? 收到时间 : 结构体_时间戳::当前_微秒());
    return true;
}

bool 状态类::标记变化(
    状态节点类* 节点,
    bool 是否变化,
    const std::string& 原因类别,
    const std::string& 原因说明)
{
    auto* 主信息 = 取状态主信息(节点);
    if (!主信息) return false;

    主信息->是否变化 = 是否变化;
    主信息->变化原因类别 = 原因类别;
    主信息->变化原因说明 = 原因说明;
    return true;
}

枚举_状态域 状态类::私有_推断状态域(const 基础信息节点类* 主体) noexcept
{
    if (!主体 || !主体->主信息) return 枚举_状态域::未定义;

    if (dynamic_cast<存在节点主信息类*>(主体->主信息)
        || dynamic_cast<场景节点主信息类*>(主体->主信息)
        || dynamic_cast<特征节点主信息类*>(主体->主信息)) {
        return 枚举_状态域::世界状态;
    }
    return 枚举_状态域::内部状态;
}

时间戳 状态类::私有_取状态排序时间(const 状态节点主信息类& 主信息) noexcept
{
    return 主信息.发生时间 != 0 ? 主信息.发生时间 : 主信息.收到时间;
}

void 状态类::私有_按时间追加状态索引(场景节点主信息类* 场景主信息, 状态节点类* 节点) const
{
    if (!场景主信息 || !节点) return;
    if (!私有_引用已存在(场景主信息->状态索引, 节点)) {
        场景主信息->状态索引.push_back(私有_生成引用(节点));
    }

    std::stable_sort(场景主信息->状态索引.begin(), 场景主信息->状态索引.end(), [this](const auto& 左, const auto& 右) {
        const auto* 左主信息 = 左.指针 ? 取状态主信息(左.指针) : nullptr;
        const auto* 右主信息 = 右.指针 ? 取状态主信息(右.指针) : nullptr;
        const auto 左时间 = 左主信息 ? 私有_取状态排序时间(*左主信息) : 0;
        const auto 右时间 = 右主信息 ? 私有_取状态排序时间(*右主信息) : 0;
        if (左时间 != 右时间) return 左时间 < 右时间;
        return 左.主键 < 右.主键;
    });
}
