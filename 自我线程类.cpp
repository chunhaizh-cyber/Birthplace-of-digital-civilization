void 自我线程类::刷新治理恢复事件镜像_已加锁() noexcept
{
    std::vector<结构_治理恢复事件镜像> 镜像列表{};
    镜像列表.reserve(治理mailbox_.size() + (存在进行中治理事件_ ? 1 : 0));

    auto 追加镜像 = [&](const 结构_治理邮箱事件& 事件, bool 来自进行中) {
        const auto 处理方式 = 私有_治理恢复事件处理方式(事件.事件类型);
        if (处理方式 != 枚举_治理恢复事件处理方式::待回判事实) {
            return;
        }

        结构_治理恢复事件镜像 镜像{};
        镜像.入队时间 = 事件.进入时间;
        镜像.事件类型 = 事件.事件类型;
        镜像.触发来源 = 事件.触发来源;
        镜像.恢复处理方式 = 处理方式;
        镜像.来源最小原语位图 = 事件.来源最小原语位图;
        镜像.来源主观察特征 = 事件.来源主观察特征;
        镜像.事件幂等键 = 事件.事件幂等键;
        镜像.重放代数 = 事件.重放代数;
        镜像.消费次数 = 事件.消费次数;
        镜像.影子验证状态 = 事件.影子验证状态;
        镜像.允许正式资产提交 = 事件.允许正式资产提交;
        镜像.最后消费结果 = 事件.最后消费结果;
        镜像.摘要 = (来自进行中 ? "进行中待回判 | " : "待回判 | ")
            + (事件.摘要.empty() ? std::string(私有_治理事件类型文本(事件.事件类型)) : 事件.摘要);
        镜像列表.push_back(std::move(镜像));
    };

    if (存在进行中治理事件_) {
        追加镜像(进行中治理事件_, true);
    }
    for (const auto& 事件 : 治理mailbox_) {
        追加镜像(事件, false);
    }

    std::ostringstream 摘要;
    摘要 << "待回判事件数=" << 镜像列表.size();
    if (!镜像列表.empty()) {
        const auto& 首项 = 镜像列表.front();
        摘要 << " | 首项=" << 私有_治理事件类型文本(首项.事件类型)
            << " | 处理=" << 私有_治理恢复事件处理方式文本(首项.恢复处理方式)
            << " | 键=" << (首项.事件幂等键.empty() ? std::string("空") : 首项.事件幂等键)
            << " | 重放=" << 首项.重放代数;
    }

    (void)任务管理任务模块::设置治理恢复事件镜像(
        镜像列表,
        摘要.str(),
        最近循环结果_.最近消费治理事件幂等键,
        最近循环结果_.最近消费治理事件重放代数,
        最近循环结果_.最近消费治理事件结果);
}

void 自我线程类::主循环_()
{
    {
        std::scoped_lock 锁(状态锁_);
        生命周期_ = 枚举_线程生命周期状态::运行中;
        健康运行_ = true;
        运行中_.store(true, std::memory_order_release);
        记录阶段事件_已加锁(
            结构体_时间戳::当前_微秒(),
            枚举_自我线程运行阶段::运行态建立,
            "自我线程已进入运行态");
        刷新快照_已加锁(
            结构体_时间戳::当前_微秒(),
            枚举_自我线程运行阶段::运行态建立,
            "自我线程已进入运行态");
    }

    while (!停止请求_.load(std::memory_order_acquire)) {
        const auto now = 结构体_时间戳::当前_微秒();

        try {
            const auto 结果 = 执行主循环一轮_(now);
            const auto 摘要 = 私有_运行摘要(结果);

            std::scoped_lock 锁(状态锁_);
            ++Tick计数_;
            最近Tick时间_ = now;
            首轮运行已完成_ = true;
            健康运行_ = true;
            最近循环结果_ = 结果;
            记录阶段事件_已加锁(now, 结果.当前运行阶段, 摘要);
            刷新快照_已加锁(now, 结果.当前运行阶段, 摘要);
        }
        catch (const std::exception& 异常) {
            std::scoped_lock 锁(状态锁_);
            停止请求_.store(true, std::memory_order_release);
            故障收口_已加锁(now, 异常.what());
        }
        catch (...) {
            std::scoped_lock 锁(状态锁_);
            停止请求_.store(true, std::memory_order_release);
            故障收口_已加锁(now, "unknown");
        }

        if (!停止请求_.load(std::memory_order_acquire)) {
            std::this_thread::sleep_for(配置_.Tick间隔);
        }
    }

    std::scoped_lock 锁(状态锁_);
    运行中_.store(false, std::memory_order_release);
    if (生命周期_ != 枚举_线程生命周期状态::故障) {
        生命周期_ = 枚举_线程生命周期状态::收尾中;
        记录阶段事件_已加锁(
            结构体_时间戳::当前_微秒(),
            枚举_自我线程运行阶段::线程收尾,
            "自我线程进入收尾阶段");
        刷新快照_已加锁(
            结构体_时间戳::当前_微秒(),
            枚举_自我线程运行阶段::线程收尾,
            "自我线程正在收尾");
        生命周期_ = 枚举_线程生命周期状态::已停止;
    }
    健康运行_ = false;
    刷新快照_已加锁(
        结构体_时间戳::当前_微秒(),
        枚举_自我线程运行阶段::线程停止,
        生命周期_ == 枚举_线程生命周期状态::故障 ? "自我线程异常停止" : "自我线程已停止");
}

