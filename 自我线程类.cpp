    枚举_自我线程最终去向 私有_裁决主循环最终去向(
        const 自我类& 自我对象,
        枚举_任务管理根层重判结果 根层重判结果,
        枚举_任务管理执行前门控结果 执行前门控结果,
        const 结构_治理邮箱事件& 已消费事件,
        const 结构_延迟事实摘要& 延迟事实,
        const 结构_任务管理结果* 治理结果,
        const 自我线程类::结构_循环结果& 当前结果) noexcept
    {
        if (根层重判结果 == 枚举_任务管理根层重判结果::停止退出边界
            || 执行前门控结果 == 枚举_任务管理执行前门控结果::禁止继续
            || 自我对象.是否已消亡()) {
            return 枚举_自我线程最终去向::停止;
        }

        if (根层重判结果 == 枚举_任务管理根层重判结果::收束优先
            || 执行前门控结果 == 枚举_任务管理执行前门控结果::仅允许收束
            || (治理结果
                && 治理结果->当前下一步去向 == 枚举_任务管理下一步去向::进入收束)
            || (已消费事件.事件类型 == 枚举_任务管理触发事件类型::外部反馈
                && (根层重判结果 == 枚举_任务管理根层重判结果::收束优先
                    || 执行前门控结果 == 枚举_任务管理执行前门控结果::仅允许收束))) {
            return 枚举_自我线程最终去向::进入收束;
        }

        if (治理结果
            && 治理结果->当前下一步去向 == 枚举_任务管理下一步去向::回到重筹办) {
            return 枚举_自我线程最终去向::回到重筹办;
        }

        if (根层重判结果 == 枚举_任务管理根层重判结果::待机保持
            || 执行前门控结果 == 枚举_任务管理执行前门控结果::仅允许等待
            || 自我对象.是否待机状态()
            || 自我对象.获取服务值() <= 0
            || (治理结果
                && 治理结果->当前下一步去向 == 枚举_任务管理下一步去向::保持等待)
            || 延迟事实.存在等待正式提交放行) {
            return 枚举_自我线程最终去向::待机;
        }

        if (当前结果.学习调度触发
            || 当前结果.学习执行触发
            || 当前结果.学习任务执行中数 > 0
            || 当前结果.学习任务就绪数 > 0
            || 已消费事件.事件类型 == 枚举_任务管理触发事件类型::学习回流
            || 延迟事实.存在学习待外层裁决
            || 延迟事实.存在学习回流待再判) {
            return 枚举_自我线程最终去向::转入学习;
        }

        return 枚举_自我线程最终去向::继续提交;
    }

    枚举_任务管理根层重判结果 私有_计算根层重判结果(const 自我类& 自我对象) noexcept
    {
        if (自我对象.是否已消亡()) {
            return 枚举_任务管理根层重判结果::停止退出边界;
        }
        if (自我对象.获取服务值() <= 0 || 自我对象.是否待机状态()) {
            return 枚举_任务管理根层重判结果::待机保持;
        }

        const auto* 当前主任务 = 自我对象.获取当前主任务();
        if (当前主任务) {
            switch (当前主任务->主信息.状态) {
            case 枚举_任务状态::完成:
            case 枚举_任务状态::失败:
            case 枚举_任务状态::取消:
                return 枚举_任务管理根层重判结果::收束优先;
            default:
                break;
            }
        }

        return 枚举_任务管理根层重判结果::允许普通推进;
    }

