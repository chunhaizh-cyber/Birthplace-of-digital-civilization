void 自我线程类::记录阶段事件_已加锁(
    时间戳 now,
    const std::string& 阶段键,
    const std::string& 摘要)
{
    auto& 事件列表 = 最近快照_.运行阶段事件;
    事件列表.push_back(结构_自我运行阶段事件{
        now,
        阶段键,
        摘要,
        reinterpret_cast<std::uintptr_t>(自我.获取自我存在()),
        reinterpret_cast<std::uintptr_t>(自我.获取当前主需求()),
        reinterpret_cast<std::uintptr_t>(自我.获取当前主任务()),
        reinterpret_cast<std::uintptr_t>(自我.获取当前主方法()),
        reinterpret_cast<std::uintptr_t>(自我.获取任务管理任务()),
    });

    if (配置_.运行阶段事件保留上限 == 0) {
        事件列表.clear();
        return;
    }

    while (事件列表.size() > 配置_.运行阶段事件保留上限) {
        事件列表.erase(事件列表.begin());
    }
}

