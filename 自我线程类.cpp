    struct 结构_解封前验收结果 {
        bool 通过 = false;
        bool 重外部输入保持封闭 = true;
        std::string 摘要{};
    };

    结构_解封前验收结果 私有_构造解封前验收结果(
        const 自我类& 自我对象,
        const 自我线程类::结构_循环结果& 结果,
        枚举_任务管理根层重判结果 根层重判结果,
        枚举_任务管理执行前门控结果 执行前门控结果,
        const 结构_治理实例堆快照& 治理实例堆,
        std::uint64_t 累计恢复次数,
        const std::string& 最近恢复摘要) noexcept
    {
        const bool 基础主链稳定 =
            自我对象.已初始化()
            && 自我对象.获取自我存在()
            && 自我对象.获取当前主需求()
            && 自我对象.获取当前主任务();
        const bool 治理中轴稳定 =
            根层重判结果 != 枚举_任务管理根层重判结果::停止退出边界
            && 执行前门控结果 != 枚举_任务管理执行前门控结果::禁止继续;
        const bool 学习链可追溯 =
            结果.学习任务总数 > 0
            || !结果.学习调度摘要.empty()
            || !结果.学习最近摘要.empty();
        const bool 治理记忆可解释 =
            治理实例堆.总数 > 0
            || !治理实例堆.最近摘要.empty();
        const bool 恢复快照可解释 =
            累计恢复次数 == 0
            || !最近恢复摘要.empty();
        const bool 已形成回流闭环 =
            结果.学习回流触发
            || !结果.学习最近回流.empty()
            || !结果.学习最近回放.empty();

        结构_解封前验收结果 输出{};
        输出.通过 =
            基础主链稳定
            && 治理中轴稳定
            && 学习链可追溯
            && 治理记忆可解释
            && 恢复快照可解释
            && 已形成回流闭环;

        if (输出.通过) {
            输出.摘要 =
                "解封前验收=通过 | 当前仍保持重外部输入封闭 | 后续仅允许按 宿主消息->候选需求->外层重判 顺序解封";
            return 输出;
        }

        std::vector<std::string> 缺口{};
        if (!基础主链稳定) {
            缺口.emplace_back("主链仍未稳定");
        }
        if (!治理中轴稳定) {
            缺口.emplace_back("根层仍处于禁止继续边界");
        }
        if (!学习链可追溯) {
            缺口.emplace_back("学习链尚不可追溯");
        }
        if (!治理记忆可解释) {
            缺口.emplace_back("治理实例堆仍为空");
        }
        if (!恢复快照可解释) {
            缺口.emplace_back("恢复快照摘要缺失");
        }
        if (!已形成回流闭环) {
            缺口.emplace_back("执行-回流-回放闭环未形成");
        }

        输出.摘要 =
            "解封前验收=未通过 | 保持重外部输入封闭 | 缺口="
            + (缺口.empty() ? std::string("待补充") : 私有_拼接片段(缺口));
        return 输出;
    }

    void 私有_刷新主循环归并(
        自我线程类::结构_循环结果& 结果,
        const 结构_任务管理结果* 治理结果,
        const 结构_学习执行结果* 学习结果) noexcept
    {
        std::vector<std::string> 来源{};
        std::vector<std::string> 片段{};

        if (治理结果 && 结果.任务管理治理触发) {
            结果.已归并任务治理结果 = true;
            来源.emplace_back("任务治理结果");

            std::ostringstream 输出;
            输出 << "治理=";
            if (!治理结果->最近反馈摘要.empty()) {
                输出 << 治理结果->最近反馈摘要;
            }
            else if (!治理结果->摘要.empty()) {
                输出 << 治理结果->摘要;
            }
            else {
                输出 << "本轮治理已消费";
            }
            if (!结果.任务管理当前缺口.empty()) {
                输出 << " | 缺口=" << 结果.任务管理当前缺口;
            }
            if (!结果.任务管理当前去向.empty()) {
                输出 << " | 去向=" << 结果.任务管理当前去向;
            }
            片段.push_back(输出.str());
        }

        if (学习结果 && 学习结果->已命中任务) {
            结果.已归并学习结果 = true;
            来源.emplace_back("学习执行结果");

            std::ostringstream 输出;
            输出 << "学习=";
            if (!学习结果->摘要.empty()) {
                输出 << 学习结果->摘要;
            }
            else {
                输出 << "本轮学习结果已消费";
            }
            if (!结果.学习当前阶段.empty()) {
                输出 << " | 阶段=" << 结果.学习当前阶段;
            }
            if (!结果.学习当前状态.empty()) {
                输出 << " | 状态=" << 结果.学习当前状态;
            }
            if (!结果.学习当前影响面.empty()) {
                输出 << " | 影响面=" << 结果.学习当前影响面;
            }
            片段.push_back(输出.str());
        }

        if (学习结果 && 学习结果->已产出实践结果消息 && !学习结果->实践结果消息.摘要.empty()) {
            来源.emplace_back("学习实践结果消息");
            片段.push_back("实践结果消息=" + 学习结果->实践结果消息.摘要);
        }

        if (学习结果 && 学习结果->已回流 && !学习结果->回流摘要.empty()) {
            结果.已归并学习回流 = true;
            来源.emplace_back("学习回流结果");
            片段.push_back("学习回流=" + 学习结果->回流摘要);
        }

        if (结果.已产出动作实践结果消息 && !结果.动作实践结果消息.摘要.empty()) {
            来源.emplace_back("执行实践结果消息");
            片段.push_back("动作结果=" + 结果.动作实践结果消息.摘要);
        }

        if (!结果.学习最近回放.empty()) {
            来源.emplace_back("学习回放页");
            片段.push_back("学习回放=" + 结果.学习最近回放);
        }

        if (!结果.解封前验收摘要.empty()) {
            来源.emplace_back("解封前验收");
            片段.push_back(结果.解封前验收摘要);
        }

        if (来源.empty()) {
            结果.主循环归并来源.clear();
            结果.主循环归并摘要.clear();
            return;
        }

        结果.主循环归并触发 = true;
        结果.主循环归并来源 = 私有_拼接来源(来源);
        std::ostringstream 输出;
        输出 << "来源=" << 结果.主循环归并来源;
        const auto 归并片段 = 私有_拼接片段(片段);
        if (!归并片段.empty()) {
            输出 << " | " << 归并片段;
        }
        结果.主循环归并摘要 = 输出.str();
    }

