{
    try {
        停止请求_.store(true, std::memory_order_release);
        if (工作线程_.joinable()) {
            if (工作线程_.get_id() != std::this_thread::get_id()) {
                工作线程_.join();
            }
            else {
                工作线程_.detach();
            }
        }
    }
    catch (...) {
    }
}

bool 自我线程类::自我初始化(const std::string& 调用点)
{
    const auto now = 结构体_时间戳::当前_微秒();

    try {
        自我初始化模块::结构_自我初始化结果 初始化结果{};
        (void)自我初始化模块::执行自我初始化(自我, 调用点 + "/自我", &初始化结果);

        std::scoped_lock 锁(状态锁_);
        刷新初始化标记_已加锁();
        健康运行_ = 初始化完成_;
        if (初始化完成_) {
            最近故障摘要_.clear();
            记录阶段事件_已加锁(now, "自我初始化", 初始化结果.摘要);
        }
        刷新快照_已加锁(
            now,
            "自我初始化",
            初始化结果.摘要.empty()
                ? (初始化完成_ ? "自我运行环境已就绪" : "自我运行环境初始化未完成")
                : 初始化结果.摘要);
        return 初始化完成_;
    }
    catch (const std::exception& 异常) {
        std::scoped_lock 锁(状态锁_);
        故障收口_已加锁(now, 异常.what());
        return false;
    }
    catch (...) {
        std::scoped_lock 锁(状态锁_);
        故障收口_已加锁(now, "unknown");
        return false;
    }
}

bool 自我线程类::启动(const std::string& 调用点)
{
    if (运行中_.load(std::memory_order_acquire)) {
        return true;
    }

    if (工作线程_.joinable()) {
        工作线程_.join();
    }

    if (!自我初始化(调用点 + "/初始化")) {
        return false;
    }

    停止请求_.store(false, std::memory_order_release);
    {
        std::scoped_lock 锁(状态锁_);
        生命周期_ = 枚举_线程生命周期状态::启动中;
        记录阶段事件_已加锁(结构体_时间戳::当前_微秒(), "线程启动", "自我线程进入启动中");
        刷新快照_已加锁(结构体_时间戳::当前_微秒(), "线程启动", "自我线程正在启动");
    }

    工作线程_ = std::thread([this] {
        主循环_();
    });
    return true;
}

void 自我线程类::请求停止(const std::string& 调用点)
{
    停止请求_.store(true, std::memory_order_release);

    std::scoped_lock 锁(状态锁_);
    if (生命周期_ == 枚举_线程生命周期状态::运行中 || 生命周期_ == 枚举_线程生命周期状态::启动中) {
        生命周期_ = 枚举_线程生命周期状态::停止请求中;
        记录阶段事件_已加锁(结构体_时间戳::当前_微秒(), "停止请求", 调用点);
        刷新快照_已加锁(结构体_时间戳::当前_微秒(), "停止请求中", "已收到自我线程停止请求");
    }
}

void 自我线程类::等待停止(const std::string& 调用点)
{
    if (工作线程_.joinable()) {
        if (工作线程_.get_id() != std::this_thread::get_id()) {
            工作线程_.join();
        }
        else {
            工作线程_.detach();
        }
    }

    std::scoped_lock 锁(状态锁_);
    运行中_.store(false, std::memory_order_release);
    if (生命周期_ != 枚举_线程生命周期状态::故障) {
        生命周期_ = 枚举_线程生命周期状态::已停止;
    }
    健康运行_ = false;
    记录阶段事件_已加锁(结构体_时间戳::当前_微秒(), "线程停止", 调用点);
    刷新快照_已加锁(结构体_时间戳::当前_微秒(), "线程停止", "自我线程已停止");
}

void 自我线程类::停止(const std::string& 调用点)
{
    请求停止(调用点 + "/请求");
    等待停止(调用点 + "/等待");
}

