    结构_动作执行回执 私有_构造动作执行回执(
        const 自我线程类::结构_循环结果& 结果,
        时间戳 now) noexcept
    {
        结构_动作执行回执 回执{};
        if (结果.服务衰减步长重算触发) {
            私有_追加动作执行记录(
                回执.动作名称列表,
                &回执.动作分类,
                枚举_本能方法ID::自我_按双值重算服务衰减步长);
        }
        if (结果.安全增加步长重算触发) {
            私有_追加动作执行记录(
                回执.动作名称列表,
                &回执.动作分类,
                枚举_本能方法ID::自我_按服务值重算安全增加步长);
        }
        if (结果.安全减少步长重算触发) {
            私有_追加动作执行记录(
                回执.动作名称列表,
                &回执.动作分类,
                枚举_本能方法ID::自我_按服务值重算安全减少步长);
        }
        if (结果.安全低位回升触发) {
            私有_追加动作执行记录(
                回执.动作名称列表,
                &回执.动作分类,
                枚举_本能方法ID::自我_安全低位回升);
        }
        if (结果.安全高位回落触发) {
            私有_追加动作执行记录(
                回执.动作名称列表,
                &回执.动作分类,
                枚举_本能方法ID::自我_安全高位回落);
        }
        if (结果.服务归零待机保护触发) {
            私有_追加动作执行记录(
                回执.动作名称列表,
                &回执.动作分类,
                枚举_本能方法ID::自我_服务归零待机保护);
        }
        if (结果.服务衰减触发) {
            私有_追加动作执行记录(
                回执.动作名称列表,
                &回执.动作分类,
                枚举_本能方法ID::自我_定时衰减服务值);
        }
        if (结果.风险安全回归触发) {
            私有_追加动作执行记录(
                回执.动作名称列表,
                &回执.动作分类,
                枚举_本能方法ID::自我_风险安全回归);
        }

        if (回执.动作名称列表.empty()) {
            return 回执;
        }

        回执.成功 = true;
        回执.错误码 = "无";
        回执.开始时间 = now;
        回执.结束时间 = now;
        回执.诊断 = "线程被动动作已生成最小正式回执";
        回执.摘要 = 构造动作执行回执摘要(回执);
        return 回执;
    }

