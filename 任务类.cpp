    bool 私有_任务节点引用已存在(
        const std::vector<可解析引用<任务节点类>>& 列表,
        const 任务节点* 节点) noexcept
    {
        if (!节点) return false;

        const auto 节点主键 = 节点->获取主键();
        return std::any_of(列表.begin(), 列表.end(), [&](const auto& 项) {
            return 项.指针 == reinterpret_cast<const 任务节点类*>(节点)
                || (!项.主键.empty() && 项.主键 == 节点主键);
        });
    }

    状态节点类* 私有_取首个有效状态(
        const std::vector<可解析引用<状态节点类>>& 列表) noexcept
    {
        for (const auto& 引用 : 列表) {
            if (引用.指针) {
                return 引用.指针;
            }
            if (!引用.主键.empty()) {
                if (auto* 基础节点 = 世界树.基础信息().查找主键(引用.主键)) {
                    return static_cast<状态节点类*>(基础节点);
                }
            }
        }
        return nullptr;
    }

    任务节点* 私有_取独立树根(任务节点* 节点) noexcept
    {
        while (节点 && 节点->父) {
            节点 = static_cast<任务节点*>(节点->父);
        }
        return 节点;
    }

    任务节点* 私有_归一到任务头结点(任务节点* 节点) noexcept
    {
        auto* 当前 = 节点;
        while (当前) {
            if (当前->主信息.节点种类 == 枚举_任务节点种类::头结点) {
                return 当前;
            }
            当前 = static_cast<任务节点*>(当前->父);
        }
        return nullptr;
    }

    任务节点* 私有_独立树添加子节点(任务节点* 父节点, const 任务主信息类& 主信息)
    {
        if (!父节点) return nullptr;

        auto* 独立根 = 私有_取独立树根(父节点);
        if (!独立根) return nullptr;

        任务类 临时树{};
        auto* 原临时根 = 临时树.根指针;
        临时树.根指针 = 独立根;

        auto* 新节点 = 临时树.添加子节点(父节点, 主信息);

        临时树.根指针 = 原临时根;
        return static_cast<任务节点*>(新节点);
    }

    void 私有_独立树删除节点(任务节点* 节点) noexcept
    {
        if (!节点) {
            return;
        }

        auto* 独立根 = 私有_取独立树根(节点);
        if (!独立根) {
            return;
        }

        任务类 临时树{};
        auto* 原临时根 = 临时树.根指针;
        临时树.根指针 = 独立根;
        临时树.删除节点(节点);
        临时树.根指针 = 原临时根;
    }

