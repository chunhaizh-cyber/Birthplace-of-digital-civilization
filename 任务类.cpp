    方法节点类* 私有_查找方法直接子节点(
        方法节点类* 方法头,
        枚举_方法节点种类 节点种类) noexcept
    {
        auto* 方法头节点 = 私有_取方法节点(方法头);
        if (!方法头节点 || !方法头节点->子) {
            return nullptr;
        }

        auto* 起点 = static_cast<方法节点*>(方法头节点->子);
        auto* 当前 = 起点;
        do {
            if (当前->主信息.节点种类 == 节点种类
                && !当前->主信息.来源任务.有效()) {
                return reinterpret_cast<方法节点类*>(当前);
            }
            当前 = static_cast<方法节点*>(当前->下);
        } while (当前 && 当前 != 起点);

        当前 = 起点;
        do {
            if (当前->主信息.节点种类 == 节点种类) {
                return reinterpret_cast<方法节点类*>(当前);
            }
            当前 = static_cast<方法节点*>(当前->下);
        } while (当前 && 当前 != 起点);

        return nullptr;
    }

