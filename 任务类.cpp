    枚举_任务阶段 私有_推导任务阶段(const 任务主信息类& 主信息) noexcept
    {
        if (主信息.当前阶段 != 枚举_任务阶段::未定义) {
            return 主信息.当前阶段;
        }
        switch (主信息.状态) {
        case 枚举_任务状态::完成:
            return 枚举_任务阶段::已完成;
        case 枚举_任务状态::执行中:
        case 枚举_任务状态::运行中:
        case 枚举_任务状态::就绪:
        case 枚举_任务状态::排队中:
            return 枚举_任务阶段::执行;
        case 枚举_任务状态::挂起:
        case 枚举_任务状态::失败:
        case 枚举_任务状态::取消:
        case 枚举_任务状态::超时:
        case 枚举_任务状态::等待中:
        case 枚举_任务状态::无法执行:
            return 枚举_任务阶段::阻塞;
        case 枚举_任务状态::未启动:
        case 枚举_任务状态::筹办中:
        case 枚举_任务状态::待重筹办:
        case 枚举_任务状态::未定义:
        default:
            return 枚举_任务阶段::筹办;
        }
    }

