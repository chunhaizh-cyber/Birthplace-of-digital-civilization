    任务节点* 私有_查找步骤结果节点(任务节点* 步骤节点) noexcept
    {
        if (!步骤节点) {
            return nullptr;
        }

        auto* 后续 = reinterpret_cast<任务节点*>(步骤节点->主信息.后续任务.指针);
        if (后续 && 后续->主信息.节点种类 == 枚举_任务节点种类::结果节点) {
            return 后续;
        }

        if (!步骤节点->子) {
            return nullptr;
        }

        auto* 起点 = static_cast<任务节点*>(步骤节点->子);
        auto* 当前 = 起点;
        do {
            if (当前->主信息.节点种类 == 枚举_任务节点种类::结果节点) {
                return 当前;
            }
            当前 = static_cast<任务节点*>(当前->下);
        } while (当前 && 当前 != 起点);

        return nullptr;
    }

    std::int32_t 私有_推导下一步骤序号(
        任务节点* 所属任务头结点,
        任务节点* 挂接父节点) noexcept
    {
        if (!挂接父节点) {
            return 1;
        }

        if (挂接父节点->主信息.节点种类 == 枚举_任务节点种类::结果节点) {
            auto* 来源步骤 = reinterpret_cast<任务节点*>(挂接父节点->主信息.来源步骤节点.指针);
            if (来源步骤 && 来源步骤->主信息.步骤序号 > 0) {
                return 来源步骤->主信息.步骤序号 + 1;
            }
        }

        if (挂接父节点->主信息.节点种类 == 枚举_任务节点种类::步骤节点
            && 挂接父节点->主信息.步骤序号 > 0) {
            return 挂接父节点->主信息.步骤序号 + 1;
        }

        if (所属任务头结点) {
            auto* 当前步骤 = reinterpret_cast<任务节点*>(所属任务头结点->主信息.当前步骤节点.指针);
            if (当前步骤 && 当前步骤->主信息.步骤序号 > 0) {
                return 当前步骤->主信息.步骤序号 + 1;
            }
        }

        return 1;
    }

