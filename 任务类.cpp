bool 任务类::任务状态已终结(枚举_任务状态 状态) noexcept
{
    return 状态 == 枚举_任务状态::完成
        || 状态 == 枚举_任务状态::失败
        || 状态 == 枚举_任务状态::取消
        || 状态 == 枚举_任务状态::超时;
}

bool 任务类::任务允许派发(const 节点类* 任务节点) noexcept
{
    if (!任务节点 || 任务状态已终结(任务节点->主信息.状态)) {
        return false;
    }
    switch (任务节点->主信息.状态) {
    case 枚举_任务状态::等待中:
    case 枚举_任务状态::挂起:
    case 枚举_任务状态::无法执行:
        return false;
    default:
        return true;
    }
}

