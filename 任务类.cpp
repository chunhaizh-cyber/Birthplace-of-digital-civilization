    const 词性节点类* 私有_任务方法实例场景名称词(
        const 任务节点* 所属任务头结点,
        const 词性节点类* 基础名称,
        const char* 后缀,
        时间戳 now) noexcept
    {
        auto 标题 = 私有_安全词(基础名称);
        if (标题.empty() && 所属任务头结点) {
            标题 = 私有_安全词(所属任务头结点->主信息.名称);
        }
        if (标题.empty() && 所属任务头结点 && !所属任务头结点->获取主键().empty()) {
            标题 = 所属任务头结点->获取主键();
        }
        if (标题.empty()) {
            标题 = "任务方法实例";
        }

        标题 += "_";
        标题 += (后缀 && *后缀) ? 后缀 : "场景";
        if (now != 0) {
            标题 += "_";
            标题 += std::to_string(now);
        }
        return 语素集.添加词性词(标题, "专有名词");
    }

    void 私有_按模板场景初始化任务实例场景(
        场景节点类* 实例场景,
        场景节点类* 模板场景) noexcept
    {
        if (!实例场景 || !模板场景) {
            return;
        }

        const auto* 模板主信息 = 世界树.场景().取场景主信息(模板场景);
        if (!模板主信息) {
            return;
        }

        for (const auto& 二次特征引用 : 模板主信息->关系索引) {
            auto* 节点 = 二次特征引用.指针;
            if (!节点 && !二次特征引用.主键.empty()) {
                节点 = static_cast<二次特征节点类*>(世界树.基础信息().查找主键(二次特征引用.主键));
            }
            if (节点) {
                (void)世界树.场景().追加关系(实例场景, 节点);
            }
        }

        for (const auto& 二次特征引用 : 模板主信息->二次特征索引) {
            auto* 节点 = 二次特征引用.指针;
            if (!节点 && !二次特征引用.主键.empty()) {
                节点 = static_cast<二次特征节点类*>(世界树.基础信息().查找主键(二次特征引用.主键));
            }
            if (节点) {
                (void)世界树.场景().追加二次特征(实例场景, 节点);
            }
        }
    }

    场景节点类* 私有_创建任务方法实例场景(
        任务节点* 所属任务头结点,
        const 词性节点类* 基础名称,
        场景节点类* 模板场景,
        const char* 后缀,
        时间戳 now,
        const std::string& 调用点) noexcept
    {
        if (!所属任务头结点) {
            return nullptr;
        }
        if (now == 0) {
            now = 结构体_时间戳::当前_微秒();
        }

        auto* 承载场景 = 所属任务头结点->主信息.场景.指针
            ? 所属任务头结点->主信息.场景.指针
            : 私有_推导任务场景(所属任务头结点->主信息.来源需求.指针, 所属任务头结点);
        auto* 任务虚拟存在 = 私有_创建并初始化任务虚拟存在(
            所属任务头结点,
            承载场景,
            调用点 + "/任务虚拟存在");
        auto* 承载世界 = 任务虚拟存在
            ? 世界树.取或创建存在内部世界(
                任务虚拟存在,
                私有_任务虚拟存在内部世界名称词(所属任务头结点))
            : nullptr;
        auto* 父锚点 = 承载世界
            ? reinterpret_cast<基础信息节点类*>(承载世界)
            : reinterpret_cast<基础信息节点类*>(承载场景);
        if (!父锚点) {
            return nullptr;
        }

        auto* 场景 = 世界树.取或创建子场景_按名称(
            父锚点,
            私有_任务方法实例场景名称词(所属任务头结点, 基础名称, 后缀, now),
            枚举_世界类型::内部世界);
        if (场景 && 任务虚拟存在) {
            (void)世界树.场景().绑定宿主(场景, 任务虚拟存在);
        }
        私有_按模板场景初始化任务实例场景(场景, 模板场景);
        return 场景;
    }

    场景节点类* 私有_推导任务场景(
        需求节点类* 来源需求,
        任务节点* 上级任务节点) noexcept
    {
        if (上级任务节点 && 上级任务节点->主信息.场景.指针) {
            return 上级任务节点->主信息.场景.指针;
        }
        if (来源需求 && 来源需求->主信息.需求场景.指针) {
            return 来源需求->主信息.需求场景.指针;
        }
        if (!世界树.自我所在场景 && !世界树.虚拟世界) {
            世界树.初始化默认世界();
        }
        if (世界树.自我所在场景) {
            return 世界树.自我所在场景;
        }
        return 世界树.虚拟世界;
    }

    I64 私有_推导基准优先级(需求节点类* 来源需求, 任务节点* 父任务头结点) noexcept
    {
        if (来源需求) {
            if (来源需求->主信息.服务权重 != 0) {
                return 来源需求->主信息.服务权重;
            }
            if (来源需求->主信息.安全权重 != 0) {
                return 来源需求->主信息.安全权重;
            }
        }
        return 父任务头结点 ? 父任务头结点->主信息.基准优先级 : 0;
    }

    方法节点类* 私有_查找方法直接子节点(
        方法节点类* 方法头,
        枚举_方法节点种类 节点种类) noexcept
    {
        auto* 方法头节点 = 私有_取方法节点(方法头);
        if (!方法头节点 || !方法头节点->子) {
            return nullptr;
        }

        auto* 起点 = static_cast<方法节点*>(方法头节点->子);
        auto* 当前 = 起点;
        do {
            if (当前->主信息.节点种类 == 节点种类
                && !当前->主信息.来源任务.有效()) {
                return reinterpret_cast<方法节点类*>(当前);
            }
            当前 = static_cast<方法节点*>(当前->下);
        } while (当前 && 当前 != 起点);

        当前 = 起点;
        do {
            if (当前->主信息.节点种类 == 节点种类) {
                return reinterpret_cast<方法节点类*>(当前);
            }
            当前 = static_cast<方法节点*>(当前->下);
        } while (当前 && 当前 != 起点);

        return nullptr;
    }

