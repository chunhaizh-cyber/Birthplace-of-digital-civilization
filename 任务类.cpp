    任务节点* 私有_查找步骤结果节点(任务节点* 步骤节点) noexcept
    {
        if (!步骤节点) {
            return nullptr;
        }

        auto* 后续 = reinterpret_cast<任务节点*>(步骤节点->主信息.后续任务.指针);
        if (后续 && 后续->主信息.节点种类 == 枚举_任务节点种类::结果节点) {
            return 后续;
        }

        任务节点* 最新实际结果 = nullptr;
        时间戳 最新实际时间 = 0;
        任务节点* 最新预测结果 = nullptr;
        时间戳 最新预测时间 = 0;
        任务节点* 首个结果 = nullptr;

        if (!步骤节点->子) {
            return nullptr;
        }

        auto* 起点 = static_cast<任务节点*>(步骤节点->子);
        auto* 当前 = 起点;
        do {
            if (当前->主信息.节点种类 != 枚举_任务节点种类::结果节点) {
                当前 = static_cast<任务节点*>(当前->下);
                continue;
            }

            if (!首个结果) {
                首个结果 = 当前;
            }

            const auto 当前时间 = 私有_任务节点时间戳(当前);
            if (当前->主信息.结果角色 == 枚举_任务结果角色::实际结果) {
                if (!最新实际结果 || 当前时间 >= 最新实际时间) {
                    最新实际结果 = 当前;
                    最新实际时间 = 当前时间;
                }
            }
            else {
                if (!最新预测结果 || 当前时间 >= 最新预测时间) {
                    最新预测结果 = 当前;
                    最新预测时间 = 当前时间;
                }
            }
            当前 = static_cast<任务节点*>(当前->下);
        } while (当前 && 当前 != 起点);

        if (最新实际结果) {
            return 最新实际结果;
        }
        if (最新预测结果) {
            return 最新预测结果;
        }
        return 首个结果;
    }

    任务节点* 私有_查找步骤预测结果节点(
        任务节点* 步骤节点,
        const 方法节点* 来源方法 = nullptr) noexcept
    {
        if (!步骤节点 || !步骤节点->子) {
            return nullptr;
        }

        任务节点* 最新预测结果 = nullptr;
        时间戳 最新预测时间 = 0;
        const auto 目标方法主键 = 来源方法 ? 来源方法->获取主键() : std::string{};

        auto* 起点 = static_cast<任务节点*>(步骤节点->子);
        auto* 当前 = 起点;
        do {
            if (当前->主信息.节点种类 != 枚举_任务节点种类::结果节点
                || 当前->主信息.结果角色 != 枚举_任务结果角色::预测结果) {
                当前 = static_cast<任务节点*>(当前->下);
                continue;
            }

            if (!目标方法主键.empty()) {
                const auto 当前来源方法主键 = 当前->主信息.来源方法首节点.主键;
                if (!当前来源方法主键.empty() && 当前来源方法主键 != 目标方法主键) {
                    当前 = static_cast<任务节点*>(当前->下);
                    continue;
                }
            }

            const auto 当前时间 = 私有_任务节点时间戳(当前);
            if (!最新预测结果 || 当前时间 >= 最新预测时间) {
                最新预测结果 = 当前;
                最新预测时间 = 当前时间;
            }
            当前 = static_cast<任务节点*>(当前->下);
        } while (当前 && 当前 != 起点);

        return 最新预测结果;
    }

