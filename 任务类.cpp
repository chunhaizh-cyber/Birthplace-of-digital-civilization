    std::int32_t 私有_推导下一步骤序号(
        任务节点* 所属任务头结点,
        任务节点* 挂接父节点) noexcept
    {
        if (!挂接父节点) {
            return 1;
        }

        if (挂接父节点->主信息.节点种类 == 枚举_任务节点种类::结果节点) {
            auto* 来源步骤 = reinterpret_cast<任务节点*>(挂接父节点->主信息.来源步骤节点.指针);
            if (来源步骤 && 来源步骤->主信息.步骤序号 > 0) {
                return 来源步骤->主信息.步骤序号 + 1;
            }
        }

        if (挂接父节点->主信息.节点种类 == 枚举_任务节点种类::步骤节点
            && 挂接父节点->主信息.步骤序号 > 0) {
            return 挂接父节点->主信息.步骤序号 + 1;
        }

        if (所属任务头结点) {
            auto* 当前步骤 = reinterpret_cast<任务节点*>(所属任务头结点->主信息.当前步骤节点.指针);
            if (当前步骤 && 当前步骤->主信息.步骤序号 > 0) {
                return 当前步骤->主信息.步骤序号 + 1;
            }
        }

        return 1;
    }

