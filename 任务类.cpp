    任务节点* 私有_查找最新步骤节点(任务节点* 任务头结点) noexcept
    {
        任务头结点 = 私有_归一到任务头结点(任务头结点);
        if (!任务头结点) {
            return nullptr;
        }

        std::vector<任务节点*> 待查{ 任务头结点 };
        任务节点* 最新步骤 = nullptr;
        时间戳 最新时间 = 0;
        while (!待查.empty()) {
            auto* 当前 = 待查.back();
            待查.pop_back();
            if (!当前) {
                continue;
            }
            if (当前 != 任务头结点 && 当前->主信息.节点种类 == 枚举_任务节点种类::步骤节点) {
                const auto 当前时间 = 私有_任务节点时间戳(当前);
                if (!最新步骤 || 当前时间 >= 最新时间) {
                    最新步骤 = 当前;
                    最新时间 = 当前时间;
                }
            }
            if (!当前->子) {
                continue;
            }
            auto* 首子 = static_cast<任务节点*>(当前->子);
            auto* 子 = 首子;
            do {
                if (子) {
                    待查.push_back(子);
                }
                子 = 子 ? static_cast<任务节点*>(子->下) : nullptr;
            } while (子 && 子 != 首子);
        }
        return 最新步骤;
    }

    任务节点* 私有_查找步骤结果节点(任务节点* 步骤节点) noexcept
    {
        if (!步骤节点) {
            return nullptr;
        }

        任务节点* 最新结果 = nullptr;
        时间戳 最新时间 = 0;
        任务节点* 首个结果 = nullptr;

        if (!步骤节点->子) {
            return nullptr;
        }

        auto* 起点 = static_cast<任务节点*>(步骤节点->子);
        auto* 当前 = 起点;
        do {
            if (当前->主信息.节点种类 != 枚举_任务节点种类::结果节点) {
                当前 = static_cast<任务节点*>(当前->下);
                continue;
            }

            if (!首个结果) {
                首个结果 = 当前;
            }

            const auto 当前时间 = 私有_任务节点时间戳(当前);
            if (!最新结果 || 当前时间 >= 最新时间) {
                最新结果 = 当前;
                最新时间 = 当前时间;
            }
            当前 = static_cast<任务节点*>(当前->下);
        } while (当前 && 当前 != 起点);

        if (最新结果) {
            return 最新结果;
        }
        return 首个结果;
    }

    任务节点* 私有_查找步骤预测结果节点(
        任务节点* 步骤节点,
        const 方法节点* 来源方法 = nullptr) noexcept
    {
        if (!步骤节点 || !步骤节点->子) {
            return nullptr;
        }

        任务节点* 最新预测结果 = nullptr;
        时间戳 最新预测时间 = 0;
        const auto 目标方法主键 = 来源方法 ? 来源方法->获取主键() : std::string{};

        auto* 起点 = static_cast<任务节点*>(步骤节点->子);
        auto* 当前 = 起点;
        do {
            if (当前->主信息.节点种类 != 枚举_任务节点种类::结果节点) {
                当前 = static_cast<任务节点*>(当前->下);
                continue;
            }

            if (!目标方法主键.empty()) {
                const auto* 当前来源方法 = reinterpret_cast<const 方法节点*>(
                    任务类::读取任务执行方法(当前));
                const auto 当前来源方法主键 = 当前来源方法 ? 当前来源方法->获取主键() : std::string{};
                if (!当前来源方法主键.empty() && 当前来源方法主键 != 目标方法主键) {
                    当前 = static_cast<任务节点*>(当前->下);
                    continue;
                }
            }

            const auto 当前时间 = 私有_任务节点时间戳(当前);
            if (!最新预测结果 || 当前时间 >= 最新预测时间) {
                最新预测结果 = 当前;
                最新预测时间 = 当前时间;
            }
            当前 = static_cast<任务节点*>(当前->下);
        } while (当前 && 当前 != 起点);

        return 最新预测结果;
    }

