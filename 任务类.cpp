        私有_写入引用(主信息.结果状态信息, 实际结果状态);
        if (当前方法) {
            主信息.动作句柄 = 当前方法->主信息.动作句柄;
        }

        私有_写入任务节点引用(主信息.所属任务头结点, 所属任务头结点);
        私有_写入任务节点引用(主信息.来源步骤节点, 步骤节点);
