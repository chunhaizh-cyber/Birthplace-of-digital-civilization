任务类::节点类* 任务类::创建任务头结点(
    需求节点类* 来源需求,
    节点类* 上级任务节点,
    时间戳 now,
    const std::string& 调用点)
{
    if (!来源需求) {
        return nullptr;
    }
    if (now == 0) {
        now = 结构体_时间戳::当前_微秒();
    }

    auto* 父任务头结点 = 私有_归一到任务头结点(上级任务节点);
    auto* 场景 = 私有_推导任务场景(来源需求, 上级任务节点);

    任务主信息类 主信息{};
    主信息.名称 = 私有_默认任务名称词(来源需求);
    主信息.类型 = 私有_默认任务类型词(来源需求);
    主信息.节点种类 = 枚举_任务节点种类::头结点;
    主信息.状态 = 枚举_任务状态::未启动;
    主信息.创建时间 = now;
    const auto 基准优先级 = 私有_推导基准优先级(来源需求, 父任务头结点);
    私有_写入引用(主信息.对应需求, 来源需求);
    私有_写入引用(主信息.目标状态, 来源需求->主信息.需求状态.指针);
    私有_写入引用(主信息.结果状态信息, 来源需求->主信息.被需求当前状态.指针);
    if (场景) {
        私有_写入引用(主信息.场景, 场景);
    }

    if (父任务头结点) {
    }

    auto* 新节点 = 上级任务节点
        ? 私有_独立树添加子节点(上级任务节点, 主信息)
        : static_cast<节点类*>(添加子节点(this->根指针, 主信息));
    if (!新节点) {
        return nullptr;
    }
    (void)任务类::写入任务当前阶段(新节点, 枚举_任务阶段::筹办, 场景, now);
    (void)任务类::写入任务基准优先级(新节点, 基准优先级, 场景, now);
    (void)任务类::写入任务调度优先级(新节点, 基准优先级, 场景, now);

    if (来源需求 && !私有_任务节点引用已存在(来源需求->主信息.任务列表, 新节点)) {
        来源需求->主信息.任务列表.push_back(私有_生成任务节点引用(新节点));
    }
    if (来源需求 && !来源需求->主信息.对应任务.有效()) {
        私有_写入任务节点引用(来源需求->主信息.对应任务, 新节点);
    }

    (void)初始化任务虚拟存在信息(
        新节点,
        场景,
        now,
        调用点 + "/任务虚拟存在");
    return 新节点;
}

任务类::节点类* 任务类::创建任务步骤节点(
    节点类* 所属任务头结点,
    节点类* 上级任务节点,
    方法节点类* 当前方法首节点,
    状态节点类* 步骤目标状态,
    时间戳 now,
    const std::string& 调用点)
{
    (void)调用点;
    auto* 任务头结点 = 私有_归一到任务头结点(所属任务头结点);
    if (!任务头结点) {
        return nullptr;
    }
    if (now == 0) {
        now = 结构体_时间戳::当前_微秒();
    }

    auto* 挂接父节点 = 上级任务节点;
    if (!挂接父节点) {
        auto* 当前步骤 = 私有_查找最新步骤节点(任务头结点);
        if (当前步骤) {
            挂接父节点 = 私有_查找步骤结果节点(当前步骤);
        }
        if (!挂接父节点) {
            挂接父节点 = 任务头结点;
        }
    }

    if (挂接父节点->主信息.节点种类 == 枚举_任务节点种类::步骤节点) {
        auto* 步骤场景 = 任务头结点->主信息.场景.指针
            ? 任务头结点->主信息.场景.指针
            : 私有_推导任务场景(任务头结点->主信息.对应需求.指针, 任务头结点);
        auto* 结果节点 = 私有_创建初始化结果节点(
            任务头结点,
            挂接父节点,
            步骤场景,
            当前方法首节点,
            私有_推导步骤目标状态(任务头结点, 当前方法首节点, 步骤目标状态),
            私有_推导当前状态(任务头结点, 挂接父节点),
            now);
        挂接父节点 = 结果节点 ? 结果节点 : 挂接父节点;
    }

    if (挂接父节点->主信息.节点种类 != 枚举_任务节点种类::头结点
        && 挂接父节点->主信息.节点种类 != 枚举_任务节点种类::结果节点) {
        return nullptr;
    }

    auto* 场景 = 任务头结点->主信息.场景.指针
        ? 任务头结点->主信息.场景.指针
        : 私有_推导任务场景(任务头结点->主信息.对应需求.指针, 任务头结点);
    auto* 当前状态 = 私有_推导当前状态(任务头结点, 挂接父节点);
    auto* 目标状态 = 私有_推导步骤目标状态(任务头结点, 当前方法首节点, 步骤目标状态);
    auto* 当前方法 = 私有_取方法节点(当前方法首节点);
    const auto 步骤序号 = 私有_推导下一步骤序号(任务头结点, 挂接父节点);

    任务主信息类 主信息{};
    主信息.名称 = 私有_默认步骤名称词(任务头结点, 当前方法首节点, 步骤序号);
    主信息.类型 = 私有_默认步骤类型词();
    主信息.节点种类 = 枚举_任务节点种类::步骤节点;
    主信息.状态 = 当前方法首节点 ? 枚举_任务状态::就绪 : 枚举_任务状态::筹办中;
    主信息.创建时间 = now;
    私有_写入引用(主信息.对应需求, 任务头结点->主信息.对应需求.指针);
    私有_写入引用(主信息.场景, 场景);
    私有_写入方法节点引用(主信息.执行方法, 当前方法);
    私有_写入引用(主信息.目标状态, 目标状态);
    私有_写入引用(主信息.结果状态信息, 当前状态);
    auto* 步骤节点 = 挂接父节点 == this->根指针
        ? static_cast<节点类*>(添加子节点(this->根指针, 主信息))
        : 私有_独立树添加子节点(挂接父节点, 主信息);
    if (!步骤节点) {
        return nullptr;
    }
    (void)任务类::写入任务执行方法(
        步骤节点,
        reinterpret_cast<方法节点类*>(当前方法),
        场景,
        now);

    auto* 初始化结果节点 = 私有_创建初始化结果节点(
        任务头结点,
        步骤节点,
        场景,
        当前方法首节点,
        目标状态,
        当前状态,
        now);
    if (!初始化结果节点) {
        私有_独立树删除节点(步骤节点);
        return nullptr;
    }

    return 步骤节点;
}

任务类::节点类* 任务类::创建任务结果节点(
    节点类* 所属任务头结点,
    节点类* 来源步骤节点,
    方法节点类* 当前方法首节点,
    状态节点类* 结果状态,
    时间戳 now,
    const std::string& 调用点)
{
    auto* 任务头结点 = 私有_归一到任务头结点(所属任务头结点);
    auto* 步骤节点 = 来源步骤节点;
    if (!任务头结点 || !步骤节点
        || 步骤节点->主信息.节点种类 != 枚举_任务节点种类::步骤节点) {
        return nullptr;
    }
    if (now == 0) {
        now = 结构体_时间戳::当前_微秒();
    }

    auto* 场景 = 任务头结点->主信息.场景.指针
        ? 任务头结点->主信息.场景.指针
        : 私有_推导任务场景(任务头结点->主信息.对应需求.指针, 任务头结点);
    auto* 当前状态 = 私有_推导当前状态(任务头结点, 步骤节点);
    return 私有_创建结果节点(
        任务头结点,
        步骤节点,
        场景,
        当前方法首节点,
        结果状态,
        当前状态,
        now,
        调用点);
}
