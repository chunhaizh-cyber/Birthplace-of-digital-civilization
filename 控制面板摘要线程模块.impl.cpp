    struct 结构_方法直接结构计数 {
        std::size_t 条件数 = 0;
        std::size_t 结果数 = 0;
    };

    结构_方法直接结构计数 私有_统计方法直接结构(const 方法节点* 方法头) noexcept
    {
        结构_方法直接结构计数 计数{};
        if (!方法头 || !方法头->子) {
            return 计数;
        }

        auto* 首节点 = reinterpret_cast<const 方法节点*>(方法头->子);
        auto* 当前 = 首节点;
        std::size_t 保护 = 0;
        do {
            if (当前->主信息.节点种类 == 枚举_方法节点种类::方法条件节点) {
                ++计数.条件数;
            } else if (当前->主信息.节点种类 == 枚举_方法节点种类::方法结果节点) {
                ++计数.结果数;
            }
            当前 = 当前 ? reinterpret_cast<const 方法节点*>(当前->下) : nullptr;
            ++保护;
        } while (当前 && 当前 != 首节点 && 保护 < 摘要扫描上限);
        return 计数;
    }

    void 私有_采集需求树摘要(结构_控制面板摘要快照& 快照) noexcept
    {
        auto* 自我存在 = 自我.获取自我存在();
        auto* 需求根 = 自我存在 ? 世界树.存在().获取需求根节点(自我存在) : nullptr;

        std::size_t 总数 = 0;
        std::size_t 等待数 = 0;
        std::size_t 满足数 = 0;

        私有_遍历全链<需求节点>(需求根, [&](需求节点* 需求) noexcept {
            if (!需求) {
                return;
            }
            ++总数;
            auto* 任务 = reinterpret_cast<任务节点*>(需求->主信息.对应任务.指针);
            const bool 任务完成 =
                任务 && (任务->主信息.当前阶段 == 枚举_任务阶段::已完成
                    || 任务->主信息.状态 == 枚举_任务状态::完成);
            const bool 结算满足 =
                需求->主信息.结算锚已初始化
                && 需求->主信息.目标状态值 == 需求->主信息.已结算状态值;
            if (任务完成 || 结算满足) {
                ++满足数;
            }
            if ((任务 && 任务->主信息.当前阶段 == 枚举_任务阶段::阻塞)
                || 需求->子 != nullptr) {
                ++等待数;
            }
        });

        快照.已满足需求数 = 满足数;
        快照.等待子需求数 = 等待数;
        快照.活跃需求数 = 总数 >= 满足数 ? 总数 - 满足数 : 0;

        std::ostringstream oss;
        oss << "需求数=" << 总数
            << " | 活跃=" << 快照.活跃需求数
            << " | 等待子需求=" << 快照.等待子需求数
            << " | 已满足=" << 快照.已满足需求数;
        if (!需求根) {
            oss << " | 根未就绪";
        }
        快照.需求树摘要 = oss.str();
    }

    void 私有_采集任务树摘要(结构_控制面板摘要快照& 快照) noexcept
    {
        auto* 自我存在 = 自我.获取自我存在();
        auto* 任务根 = 自我存在 ? 世界树.存在().获取任务根节点(自我存在) : nullptr;

        std::size_t 总数 = 0;
        std::size_t 筹办数 = 0;
        std::size_t 执行数 = 0;
        std::size_t 等待数 = 0;

        私有_遍历全链<任务节点>(任务根, [&](任务节点* 任务) noexcept {
            if (!任务) {
                return;
            }
            if (!私有_任务节点属于需求任务链(任务)) {
                return;
            }
            ++总数;
            switch (任务->主信息.当前阶段) {
            case 枚举_任务阶段::筹办:
                ++筹办数;
                break;
            case 枚举_任务阶段::执行:
                ++执行数;
                break;
            case 枚举_任务阶段::阻塞:
                ++等待数;
                break;
            default:
                if (任务->主信息.状态 == 枚举_任务状态::等待中
                    || 任务->主信息.状态 == 枚举_任务状态::挂起) {
                    ++等待数;
                }
                break;
            }
        });

        快照.筹办中任务数 = 筹办数;
        快照.执行中任务数 = 执行数;
        快照.等待任务数 = 等待数;

        std::ostringstream oss;
        oss << "任务数=" << 总数
            << " | 筹办=" << 快照.筹办中任务数
            << " | 执行=" << 快照.执行中任务数
            << " | 等待=" << 快照.等待任务数;
        if (!任务根) {
            oss << " | 根未就绪";
        }
        快照.任务树摘要 = oss.str();
    }

    void 私有_采集方法树摘要(结构_控制面板摘要快照& 快照) noexcept
    {
        auto* 自我存在 = 自我.获取自我存在();
        auto* 方法根 = 自我存在 ? 世界树.存在().获取方法根节点(自我存在) : nullptr;

        std::size_t 首节点数 = 0;
        std::size_t 结构缺口数 = 0;
        std::size_t 能力包缺失数 = 0;
        std::size_t 条件结果对缺失数 = 0;

        私有_遍历全链<方法节点>(方法根, [&](方法节点* 方法) noexcept {
            if (!方法 || 方法->主信息.节点种类 != 枚举_方法节点种类::方法首节点) {
                return;
            }
            ++首节点数;
            const auto 直接结构 = 私有_统计方法直接结构(方法);
            const bool 虚拟存在缺失 = !方法->主信息.方法虚拟存在.有效();
            const bool 条件结果缺失 = 直接结构.条件数 == 0 || 直接结构.结果数 == 0;
            if (!方法->主信息.有动作骨架() || 虚拟存在缺失 || 条件结果缺失) {
                ++结构缺口数;
            }
            if (虚拟存在缺失) {
                ++能力包缺失数;
            }
            if (条件结果缺失) {
                ++条件结果对缺失数;
            }
        });

        快照.方法头节点数 = 首节点数;
        快照.方法结构缺口数 = 结构缺口数;
        快照.能力包缺失数 = 能力包缺失数;
        快照.条件结果对缺失数 = 条件结果对缺失数;

        std::ostringstream oss;
        oss << "方法首节点=" << 快照.方法头节点数
            << " | 结构缺口=" << 快照.方法结构缺口数
            << " | 能力包缺失=" << 快照.能力包缺失数
            << " | 条件结果对缺失=" << 快照.条件结果对缺失数;
        if (!方法根) {
            oss << " | 根未就绪";
        }
        快照.方法树摘要 = oss.str();
    }

