    template<class 节点类型, class 回调类型>
    void 私有_遍历全链(节点类型* 根节点, 回调类型&& 回调) noexcept
    {
        if (!根节点) {
            return;
        }

        std::size_t 保护 = 0;
        for (auto* 当前 = reinterpret_cast<节点类型*>(根节点->链下);
             当前 && 当前 != 根节点 && 保护 < 摘要扫描上限;
             当前 = reinterpret_cast<节点类型*>(当前->链下), ++保护) {
            回调(当前);
        }
    }

    std::uint64_t 私有_混合世界版本(std::uint64_t 累积, std::uint64_t 值) noexcept
    {
        累积 ^= 值 + 0x9e3779b97f4a7c15ull + (累积 << 6) + (累积 >> 2);
        return 累积 == 0 ? 1 : 累积;
    }

    template<class 节点类型>
    std::size_t 私有_统计全链(节点类型* 根节点) noexcept
    {
        std::size_t 数量 = 0;
        私有_遍历全链<节点类型>(根节点, [&](节点类型*) noexcept {
            ++数量;
        });
        return 数量;
    }

    bool 私有_任务头绑定需求(const 任务节点* 节点) noexcept
    {
        return 节点
            && 节点->主信息.节点种类 == 枚举_任务节点种类::头结点
            && (节点->主信息.对应需求.指针
                || !节点->主信息.对应需求.主键.empty());
    }

    bool 私有_任务节点属于需求任务链(const 任务节点* 节点) noexcept
    {
        std::size_t 保护 = 0;
        for (auto* 当前 = 节点;
             当前 && 保护 < 128;
             当前 = reinterpret_cast<const 任务节点*>(当前->父), ++保护) {
            if (当前->主信息.节点种类 == 枚举_任务节点种类::头结点) {
                return 私有_任务头绑定需求(当前);
            }
            if (当前 == reinterpret_cast<const 任务节点*>(当前->父)) {
                break;
            }
        }
        return false;
    }

