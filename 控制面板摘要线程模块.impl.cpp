bool 控制面板摘要线程类::启动()
{
    {
        std::lock_guard<std::mutex> lock(状态锁_);
        if (生命周期_ == 枚举_控制面板摘要线程生命周期状态::运行中
            || 生命周期_ == 枚举_控制面板摘要线程生命周期状态::启动中) {
            return true;
        }
        生命周期_ = 枚举_控制面板摘要线程生命周期状态::启动中;
        健康运行_ = false;
        停止请求_.store(false);
        刷新请求_.store(true);
    }

    try {
        工作线程_ = std::thread(&控制面板摘要线程类::主循环_, this);
    } catch (...) {
        std::lock_guard<std::mutex> lock(状态锁_);
        生命周期_ = 枚举_控制面板摘要线程生命周期状态::故障;
        健康运行_ = false;
        return false;
    }

    return true;
}

void 控制面板摘要线程类::请求停止()
{
    {
        std::lock_guard<std::mutex> lock(状态锁_);
        if (生命周期_ == 枚举_控制面板摘要线程生命周期状态::未启动
            || 生命周期_ == 枚举_控制面板摘要线程生命周期状态::已停止) {
            停止请求_.store(true);
        } else {
            生命周期_ = 枚举_控制面板摘要线程生命周期状态::停止请求中;
            健康运行_ = false;
            停止请求_.store(true);
        }
    }
    唤醒条件_.notify_all();
}

void 控制面板摘要线程类::等待停止()
{
    if (工作线程_.joinable()) {
        工作线程_.join();
    }
}

void 控制面板摘要线程类::请求刷新()
{
    刷新请求_.store(true);
    唤醒条件_.notify_all();
}

