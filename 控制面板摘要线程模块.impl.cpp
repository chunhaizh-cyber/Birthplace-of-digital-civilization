void 控制面板摘要线程类::主循环_()
{
    {
        std::lock_guard<std::mutex> lock(状态锁_);
        生命周期_ = 枚举_控制面板摘要线程生命周期状态::运行中;
        健康运行_ = true;
    }

    while (!停止请求_.load()) {
        try {
            if (刷新请求_.exchange(false)) {
                写入快照_(采集快照_());
            }
        } catch (...) {
            std::lock_guard<std::mutex> lock(状态锁_);
            生命周期_ = 枚举_控制面板摘要线程生命周期状态::故障;
            健康运行_ = false;
        }

        std::unique_lock<std::mutex> lock(状态锁_);
        唤醒条件_.wait_for(lock, 配置_.Tick间隔, [&]() {
            return 停止请求_.load() || 刷新请求_.load();
        });
        if (!停止请求_.load() && !刷新请求_.load()) {
            刷新请求_.store(true);
        }
    }

    std::lock_guard<std::mutex> lock(状态锁_);
    生命周期_ = 枚举_控制面板摘要线程生命周期状态::已停止;
    健康运行_ = false;
}

