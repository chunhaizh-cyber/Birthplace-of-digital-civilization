inline 结构_治理消息 构造治理消息(
    const 枚举_消息子类 子类,
    const 时间戳 产生时间 = 0) noexcept
{
    结构_治理消息 消息{};
    消息.头.子类 = 子类;
    消息.头.大类 = 从消息子类映射消息大类(子类);
    消息.头.优先级 = 默认消息优先级(子类);
    消息.头.产生时间 = 产生时间;
    消息.头.可抢占 = 默认可抢占(子类);
    消息.头.仅局部有效 = 默认仅局部有效(子类);
    return 消息;
}

inline 枚举_任务管理触发事件类型 映射到任务管理触发事件类型(
    const 结构_治理消息& 消息) noexcept
{
    switch (消息.头.大类) {
    case 枚举_消息大类::结果回流消息:
        return 枚举_任务管理触发事件类型::执行回流;
    case 枚举_消息大类::学习与审计消息:
        return 枚举_任务管理触发事件类型::学习反馈;
    case 枚举_消息大类::控制消息:
        return 消息.头.子类 == 枚举_消息子类::恢复重建
            ? 枚举_任务管理触发事件类型::恢复重建
            : 枚举_任务管理触发事件类型::普通推进;
    case 枚举_消息大类::全局环境消息:
    case 枚举_消息大类::工作线程状态消息:
    case 枚举_消息大类::任务推进关键事件消息:
    case 枚举_消息大类::派生需求消息:
        return 枚举_任务管理触发事件类型::外部反馈;
    case 枚举_消息大类::未定义:
    default:
        return 枚举_任务管理触发事件类型::未定义;
    }
}

inline 枚举_任务管理触发来源 映射到任务管理触发来源(
    const 结构_治理消息&) noexcept
{
    return 枚举_任务管理触发来源::自然运行态;
}

inline 结构_任务管理显式事件控制 构造任务管理显式事件控制(
    const 结构_治理消息& 消息) noexcept
{
    结构_任务管理显式事件控制 显式事件{};
    显式事件.启用 = true;
    显式事件.事件类型 = 映射到任务管理触发事件类型(消息);
    显式事件.触发来源 = 映射到任务管理触发来源(消息);
    显式事件.来源最小原语位图 = 消息.来源最小原语位图;
    显式事件.来源主观察特征 = 消息.来源主观察特征;
    显式事件.影子验证状态 = 消息.影子验证状态;
    显式事件.允许正式资产提交 = 消息.允许正式资产提交;
    显式事件.事件摘要 = 消息.摘要;
    return 显式事件;
}

inline bool 是否启用治理恢复重建控制(const 结构_治理消息& 消息) noexcept
{
    return 消息.带恢复快照 && 消息.头.子类 == 枚举_消息子类::恢复重建;
}

inline 枚举_治理恢复事件处理方式 计算恢复事件处理方式(
    const 结构_治理消息& 消息) noexcept
{
    switch (消息.头.大类) {
    case 枚举_消息大类::结果回流消息:
    case 枚举_消息大类::全局环境消息:
    case 枚举_消息大类::学习与审计消息:
    case 枚举_消息大类::任务推进关键事件消息:
    case 枚举_消息大类::派生需求消息:
        return 枚举_治理恢复事件处理方式::待回判事实;
    case 枚举_消息大类::控制消息:
        if (消息.头.子类 == 枚举_消息子类::恢复重建) {
            return 枚举_治理恢复事件处理方式::由恢复重建承接;
        }
        return 枚举_治理恢复事件处理方式::仅内存态;
    case 枚举_消息大类::未定义:
    default:
        return 枚举_治理恢复事件处理方式::仅内存态;
    }
}

inline 结构_治理消息 从任务推进事件桥接治理消息(const 结构_任务推进事件& 事件) noexcept
{
    auto 消息 = 构造治理消息(枚举_消息子类::未定义);
    消息.头.消息主键 = 事件.任务根ID;
    消息.头.来源任务主键 = 事件.任务根ID;
    消息.头.目标任务主键 = 事件.任务根ID;
    消息.头.追踪签名 = static_cast<std::uint64_t>(事件.当前步骤指针);
    消息.摘要 = 事件.摘要;

    switch (事件.事件类型) {
    case 枚举_任务推进事件类型::当前步骤可执行:
        消息 = 构造治理消息(枚举_消息子类::当前步骤可执行);
        break;
    case 枚举_任务推进事件类型::当前步骤阻塞:
        消息 = 构造治理消息(枚举_消息子类::当前步骤阻塞);
        消息.摘要 = 事件.等待原因.empty() ? 事件.摘要 : 事件.等待原因;
        break;
    case 枚举_任务推进事件类型::派生需求上报:
        消息 = 构造治理消息(枚举_消息子类::依赖请求);
        消息.摘要 = 事件.派生需求摘要.empty() ? 事件.摘要 : 事件.派生需求摘要;
        消息.头.仅局部有效 = false;
        break;
    case 枚举_任务推进事件类型::局部结果返回:
        消息 = 构造治理消息(枚举_消息子类::局部结果);
        break;
    case 枚举_任务推进事件类型::最终结果返回:
        消息 = 构造治理消息(枚举_消息子类::最终结果);
        消息.头.仅局部有效 = false;
        break;
    case 枚举_任务推进事件类型::异常上报:
        消息 = 构造治理消息(枚举_消息子类::异常结果);
        消息.头.仅局部有效 = false;
        break;
    case 枚举_任务推进事件类型::未定义:
    default:
        消息 = 构造治理消息(枚举_消息子类::未定义);
        break;
    }

    消息.头.消息主键 = 事件.任务根ID;
    消息.头.来源任务主键 = 事件.任务根ID;
    消息.头.目标任务主键 = 事件.任务根ID;
    消息.头.追踪签名 = static_cast<std::uint64_t>(事件.当前步骤指针);
    if (消息.摘要.empty()) {
        消息.摘要 = 事件.摘要;
    }
    return 消息;
}

