    case 枚举_消息子类::关键对象状态突变:
    case 枚举_消息子类::阈值变化:
    case 枚举_消息子类::外部高优先级事件:
    case 枚举_消息子类::工作线程超时:
    case 枚举_消息子类::工作线程故障:
