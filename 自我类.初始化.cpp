    bool 私有_方法虚拟存在有子特征(
        存在节点类* 方法虚拟存在,
        const 语素入口节点类* 特征类型) noexcept
    {
        return 方法虚拟存在
            && 特征类型
            && 世界树.特征().查找子特征_按类型(方法虚拟存在, 特征类型);
    }

    结构_执行前方法自检结果 私有_自检目标方法结构_执行前(
        方法节点* 目标方法,
        存在节点类* 自我存在,
        场景节点类* 输出结果场景,
        时间戳 now) noexcept
    {
        结构_执行前方法自检结果 结果{};
        if (!目标方法) {
            结果.阻断执行 = true;
            结果.有补齐问题 = true;
            return 结果;
        }

        (void)方法虚拟存在服务类::初始化方法虚拟存在信息(
            目标方法,
            自我存在,
            now);
        结果.方法虚拟存在 = 方法虚拟存在服务类::取或创建方法虚拟存在(
            目标方法,
            自我存在,
            now);
        结果.自我能力值 = 私有_方法自我能力值(目标方法);
        结果.有自我能力值 = 结果.自我能力值 != 0;
        结果.有执行入口 = 目标方法->主信息.首节点信息().动作句柄.有效();

        if (结果.方法虚拟存在) {
            结果.有方法能力包 = 私有_方法虚拟存在有子特征(
                结果.方法虚拟存在,
                私有_特征_方法能力包());
            结果.有形参包 = 私有_方法虚拟存在有子特征(
                结果.方法虚拟存在,
                私有_特征_形参包());
            结果.有结果包 = 私有_方法虚拟存在有子特征(
                结果.方法虚拟存在,
                私有_特征_结果包());
            结果.有条件结果对 = 私有_方法虚拟存在有子特征(
                结果.方法虚拟存在,
                私有_特征_条件结果对());
            结果.有试运行限制包 = 私有_方法虚拟存在有子特征(
                结果.方法虚拟存在,
                私有_特征_试运行限制包());
        }

        结果.有补齐问题 = !结果.方法虚拟存在
            || !结果.有自我能力值
            || !结果.有执行入口
            || !结果.有方法能力包
            || !结果.有形参包
            || !结果.有结果包
            || !结果.有条件结果对
            || !结果.有试运行限制包;
        结果.阻断执行 = !结果.方法虚拟存在 || !结果.有执行入口;

        auto* 自检宿主 = 结果.方法虚拟存在
            ? static_cast<基础信息节点类*>(结果.方法虚拟存在)
            : static_cast<基础信息节点类*>(输出结果场景);
        if (自检宿主) {
            结果.方法自检结果 = 世界树.确保特征(
                自检宿主,
                私有_特征_方法自检结果());
            结果.方法补齐问题 = 结果.方法自检结果
                ? 世界树.确保特征(结果.方法自检结果, 私有_特征_方法补齐问题())
                : nullptr;
        }

        if (结果.方法虚拟存在) {
            (void)私有_写入方法补齐I64(
                结果.方法虚拟存在,
                私有_特征_方法补齐状态(),
                static_cast<I64>(结果.阻断执行
                    ? 枚举_方法补齐尝试状态::缺少执行入口
                    : 枚举_方法补齐尝试状态::可试运行),
                now);
            (void)私有_写入方法补齐I64(
                结果.方法虚拟存在,
                私有_特征_方法结构完整度(),
                static_cast<I64>(结果.有补齐问题
                    ? 枚举_方法结构完整度::候选完整
                    : 枚举_方法结构完整度::可试运行),
                now);
        }

        if (结果.方法补齐问题) {
            (void)私有_写入方法补齐I64(
                结果.方法补齐问题,
                私有_特征_缺口_自我能力值缺失(),
                结果.有自我能力值 ? 0 : 1,
                now);
            (void)私有_写入方法补齐I64(
                结果.方法补齐问题,
                私有_特征_缺口_执行入口缺失(),
                结果.有执行入口 ? 0 : 1,
                now);
            (void)私有_写入方法补齐I64(
                结果.方法补齐问题,
                私有_特征_缺口_方法能力包缺失(),
                结果.有方法能力包 ? 0 : 1,
                now);
            (void)私有_写入方法补齐I64(
                结果.方法补齐问题,
                私有_特征_缺口_形参包缺失(),
                结果.有形参包 ? 0 : 1,
                now);
            (void)私有_写入方法补齐I64(
                结果.方法补齐问题,
                私有_特征_缺口_结果包缺失(),
                结果.有结果包 ? 0 : 1,
                now);
            (void)私有_写入方法补齐I64(
                结果.方法补齐问题,
                私有_特征_缺口_条件结果对缺失(),
                结果.有条件结果对 ? 0 : 1,
                now);
            (void)私有_写入方法补齐I64(
                结果.方法补齐问题,
                私有_特征_缺口_试运行限制包缺失(),
                结果.有试运行限制包 ? 0 : 1,
                now);
        }

        if (输出结果场景) {
            auto* 输出宿主 = 世界树.确保特征(
                输出结果场景,
                私有_特征_方法自检结果());
            if (输出宿主) {
                (void)私有_写入方法补齐I64(
                    输出宿主,
                    私有_特征_方法补齐状态(),
                    static_cast<I64>(结果.阻断执行
                        ? 枚举_方法补齐尝试状态::缺少执行入口
                        : 枚举_方法补齐尝试状态::可试运行),
                    now);
                (void)私有_写入方法补齐指针(
                    输出宿主,
                    私有_特征_目标方法头节点(),
                    目标方法,
                    now);
                (void)私有_写入方法补齐指针(
                    输出宿主,
                    私有_特征_目标方法虚拟存在(),
                    结果.方法虚拟存在,
                    now);
                (void)私有_写入方法补齐I64(
                    输出宿主,
                    私有_特征_目标自我能力值(),
                    结果.自我能力值,
                    now);
                (void)私有_写入方法补齐I64(
                    输出宿主,
                    私有_特征_方法结构完整度(),
                    static_cast<I64>(结果.有补齐问题
                        ? 枚举_方法结构完整度::候选完整
                        : 枚举_方法结构完整度::可试运行),
                    now);
            }
        }

        return 结果;
    }

