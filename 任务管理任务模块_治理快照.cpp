    int 私有_恢复快照学习投影优先级(const 结构_学习任务实体& 实体) noexcept
    {
        if (实体.当前等待原因 == 枚举_学习等待原因::等待正式提交放行) {
            return 500;
        }
        if (实体.待外层裁决) {
            return 450;
        }
        if (实体.当前状态 == 枚举_学习状态::执行中) {
            return 400;
        }
        if (实体.当前状态 == 枚举_学习状态::挂起) {
            return 300;
        }
        if (实体.当前状态 == 枚举_学习状态::可调度) {
            return 250;
        }
        if (!实体.学习反馈摘要.empty() || !实体.最近回流摘要.empty()) {
            return 150;
        }
        return 0;
    }

    void 私有_刷新治理恢复学习投影(结构_治理恢复快照* 快照) noexcept
    {
        if (!快照) {
            return;
        }

        快照->最近学习承接摘要.clear();
        快照->最近学习等待摘要.clear();
        快照->最近学习反馈摘要.clear();
        快照->最近学习回流摘要.clear();
        快照->存在待消费学习反馈 = false;
        快照->存在待消费学习回流 = false;

        const auto 任务列表 = 学习任务模块::枚举学习任务实体();
        const 结构_学习任务实体* 最佳实体 = nullptr;
        int 最佳优先级 = -1;
        for (const auto& 实体 : 任务列表) {
            const int 当前优先级 = 私有_恢复快照学习投影优先级(实体);
            if (当前优先级 <= 0) {
                continue;
            }
            if (当前优先级 > 最佳优先级) {
                最佳优先级 = 当前优先级;
                最佳实体 = &实体;
            }
            if (实体.待外层裁决
                || 实体.当前等待原因 == 枚举_学习等待原因::等待正式提交放行
                || 实体.当前状态 == 枚举_学习状态::执行中
                || 实体.当前状态 == 枚举_学习状态::挂起
                || 实体.当前状态 == 枚举_学习状态::可调度) {
                快照->存在待消费学习反馈 = true;
                快照->存在待消费学习回流 = true;
            }
        }

        if (!最佳实体) {
            return;
        }

        快照->最近学习承接摘要 = 最佳实体->学习承接摘要;
        快照->最近来源正式原语摘要 = 最佳实体->来源正式原语摘要;
        if (快照->最近学习承接摘要.empty()) {
            std::ostringstream 承接摘要;
            承接摘要 << "通道="
                << (最佳实体->来源本能能力缺口类型 == 枚举_任务管理本能能力缺口类型::需学习补齐
                    ? "补方法"
                    : "补原语/治理本能")
                << " | 触发来源=" << static_cast<int>(最佳实体->当前触发来源)
                << " | 需求位=" << static_cast<int>(最佳实体->来源方法需求位);
            快照->最近学习承接摘要 = 承接摘要.str();
        }

        std::ostringstream 等待摘要;
        等待摘要 << "子任务=" << (最佳实体->学习子任务标题.empty() ? "空" : 最佳实体->学习子任务标题)
            << " | 状态=" << 私有_学习状态文本(最佳实体->当前状态)
            << " | 等待=" << 私有_学习等待原因文本(最佳实体->当前等待原因)
            << " | 结果=" << 学习任务模块::学习结果状态文本(最佳实体->当前结果状态);
        if (!最佳实体->当前影子验证状态.empty()) {
            等待摘要 << " | 影子验证=" << 最佳实体->当前影子验证状态;
        }
        快照->最近学习等待摘要 = 等待摘要.str();
        快照->最近学习反馈摘要 =
            !最佳实体->学习反馈摘要.empty()
            ? 最佳实体->学习反馈摘要
            : 最佳实体->最近反馈摘要;
        快照->最近学习回流摘要 =
            !最佳实体->最近回流摘要.empty()
            ? 最佳实体->最近回流摘要
            : 最佳实体->回流目标摘要;
    }

