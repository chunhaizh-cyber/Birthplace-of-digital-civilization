    bool 私有_恢复快照_高阶排序结果有效(const 结构_高阶排序结果& 结果) noexcept
    {
        return !结果.需求轴键.empty()
            || !结果.候选主键列表.empty()
            || !结果.候选摘要.empty()
            || !结果.排序关系.empty()
            || !结果.胜出候选主键.empty()
            || 结果.是否等效
            || 结果.是否过头
            || 结果.置信度_Q10000 != 0
            || !结果.原因说明.empty();
    }

    bool 私有_恢复快照_高阶排序请求有效(const 结构_高阶排序请求& 请求) noexcept
    {
        return !请求.需求轴键.empty()
            || !请求.候选主键列表.empty()
            || !请求.候选摘要.empty()
            || !请求.首候选主键.empty()
            || !请求.次候选主键.empty()
            || !请求.比较特征键.empty()
            || 请求.比较基准类型 != 枚举_原语候选比较基准类型::未定义
            || !请求.比较基准摘要.empty()
            || 请求.存在可比对
            || !请求.未决原因.empty();
    }

    std::string 私有_恢复快照_高阶请求摘要(const 结构_高阶排序请求& 请求) noexcept
    {
        if (!私有_恢复快照_高阶排序请求有效(请求)) {
            return "未触发";
        }

        std::ostringstream 输出;
        输出 << "轴=" << (请求.需求轴键.empty() ? "空" : 请求.需求轴键)
            << ",候选=";
        if (请求.候选主键列表.empty()) {
            输出 << "空";
        }
        else {
            bool 首个 = true;
            for (const auto& 主键 : 请求.候选主键列表) {
                if (!首个) {
                    输出 << "/";
                }
                首个 = false;
                输出 << (主键.empty() ? "空" : 主键);
            }
        }
        if (!请求.首候选主键.empty() || !请求.次候选主键.empty()) {
            输出 << ",候选对="
                << (请求.首候选主键.empty() ? "空" : 请求.首候选主键)
                << " vs "
                << (请求.次候选主键.empty() ? "空" : 请求.次候选主键);
        }
        if (!请求.比较基准摘要.empty()) {
            输出 << ",基准=" << 请求.比较基准摘要;
        }
        输出 << ",可比=" << (请求.存在可比对 ? "是" : "否");
        if (!请求.未决原因.empty()) {
            输出 << ",未决=" << 请求.未决原因;
        }
        return 输出.str();
    }

