enum class 枚举_任务节点种类 : std::uint8_t {
    未定义 = 0,
    头结点 = 1,
    步骤节点 = 2,
    结果节点 = 3,
};

enum class 枚举_任务状态 : std::uint8_t {
    未定义 = 0,
    未启动 = 1,
    运行中 = 2,
    挂起 = 3,
    完成 = 4,
    失败 = 5,
    取消 = 6,
    超时 = 7,
    就绪 = 8,
    执行中 = 9,
    筹办中 = 10,
    排队中 = 11,
    等待中 = 12,
    无法执行 = 13,
    待重筹办 = 14,
};

enum class 枚举_任务树类型 : std::uint8_t {
    未定义 = 0,
    编排任务 = 1,
    叶子任务 = 2,
};

enum class 枚举_分支选择动作 : std::uint8_t {
    未定义 = 0,
    继续下一步 = 1,
    回跳重试 = 2,
    生成补条件子任务 = 3,
    转入尝试学习 = 4,
    挂起等待 = 5,
};

enum class 枚举_任务结果角色 : std::uint8_t {
    未定义 = 0,
    预测结果 = 1,
    实际结果 = 2,
};

// ======= 任务节点主信息基类（实例运行信息）=======
class 任务信息基类 {
public:
    // 基本标识
    const 词性节点类* 名称 = nullptr;
    const 自然句节点类* 描述信息 = nullptr;
    const 词性节点类* 类型 = nullptr;
    枚举_任务节点种类 节点种类 = 枚举_任务节点种类::未定义;

    // 执行元信息
    // - 状态字段仅作为运行期镜像/缓存；权威状态以任务虚拟存在中的特征为准
    枚举_任务状态 状态 = 枚举_任务状态::未定义;
    时间戳 创建时间 = 0;

    // 兼容当前工程已有热路径统计
    结构_统计 统计{};

    // 优先级规则：
    // - 基准优先级：来自主任务需求，是整棵任务树共享的基础值
    // - 局部优先级偏移：步骤命中分、并列候选分等局部调度修正
    // - 调度优先级：运行期实际排队值，由执行器按当前上下文刷新
    std::int64_t 基准优先级 = 0;
    std::int64_t 局部优先级偏移 = 0;
    std::int64_t 调度优先级 = 0;

    // 场景锚点（当前工程保持为可解析引用）
    可解析引用<场景节点类> 场景{};

    // 任务虚拟存在锚点：运行状态与统计写在这里
    可解析引用<存在节点类> 任务虚拟存在{};

    枚举_任务树类型 任务树类型 = 枚举_任务树类型::未定义;

    // 执行流水账镜像：兼容规范里的最小运行面
    std::int64_t 运行次数 = 0;
    std::int64_t 成功次数 = 0;
    std::int64_t 失败次数 = 0;
    std::int64_t 连续失败次数 = 0;
    std::int64_t 转入学习次数 = 0;
    std::int64_t 最近错误码 = 0;
    时间戳 最近调度时间 = 0;

    // 当前工程已经依赖的通用任务字段，继续保留在公共基类。
    // - 需求统一走 `来源需求`，不再和头结点层重复保留一份 `需求`
    // - 当前执行方法统一走 `执行方法`，不再平行保留 `当前/最近方法首节点`
    // - 启停时间统一走 `启动时间/完成时间`，不再平行保留 `最近开始/最近结束时间`
    可解析引用<需求节点类> 来源需求{};
    可解析引用<任务节点类> 父任务{};
    可解析引用<任务节点类> 后续任务{};
    可解析引用<方法节点类> 执行方法{};
    可解析引用<状态节点类> 目标状态{};
    可解析引用<状态节点类> 结果状态信息{};
    结构体_动作句柄 动作句柄{};
    时间戳 启动时间 = 0;
    时间戳 完成时间 = 0;

    virtual ~任务信息基类() = default;
};

