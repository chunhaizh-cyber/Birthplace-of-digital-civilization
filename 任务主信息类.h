// 兼容当前工程：现有链表节点仍以 任务主信息类 为主信息类型。
// 这里让它继承头结点信息，既保留旧用法，也把头/步骤/结果三类正式定义收进同一头文件。
// 现阶段物理节点仍由 `任务主信息类` 统一承载；步骤/结果两类更像“正式字段合同”。
// 后续若要让物理节点真正按三类主信息分载，需要连同 `任务节点类` 的模板承载一起调整。
class 任务主信息类 : public 任务头结点信息 {
public:
    // 现阶段物理节点仍统一承载在 任务主信息类 上；
    // 这里把步骤/结果的“专属且不与公共层重名”的字段直接补到物理载体上。
    // 像 当前子任务头结点 这类已在头结点层存在的字段，步骤节点继续复用同一物理字段。
    可解析引用<任务节点类> 所属任务头结点{};
    可解析引用<方法节点类> 可用方法首节点{};
    可解析引用<场景节点类> 方法条件节点{};
    std::vector<可解析引用<任务节点类>> 并发子任务头结点列表{};
    std::int32_t 步骤序号 = 0;
    std::int32_t 已重试次数 = 0;
    std::int32_t 允许重试次数 = 0;
    时间戳 超时截止时间 = 0;
    bool 允许切换方法 = true;

    枚举_任务结果角色 结果角色 = 枚举_任务结果角色::未定义;
    可解析引用<任务节点类> 来源步骤节点{};
    std::vector<可解析引用<任务节点类>> 派生子任务头结点列表{};
    可解析引用<方法节点类> 来源方法首节点{};
    可解析引用<场景节点类> 方法结果节点{};
    可解析引用<方法节点类> 对应方法结果节点{};
    可解析引用<二次特征节点类> 结果方向二次特征{};
    可解析引用<任务节点类> 命中预测结果节点{};
    枚举_分支选择动作 命中后动作 = 枚举_分支选择动作::未定义;
    bool 是否预测外结果 = false;
    std::int64_t 置信度_Q10000 = 0;
    std::int64_t 错误码 = 0;
    可解析引用<存在节点类> 评估摘要存在{};

    std::int64_t 比较(const 任务主信息类* 对象, 枚举_比较字段 字段) const {
        return 对象 ? 比较(*对象, 字段) : -1;
    }

    std::int64_t 比较(const 任务主信息类& 对象, 枚举_比较字段 字段) const {
        switch (字段) {
        case 枚举_比较字段::名称:
            return 名称 == 对象.名称 ? 1 : 0;
        case 枚举_比较字段::类型:
            return 类型 == 对象.类型 ? 1 : 0;
        case 枚举_比较字段::值:
            return 状态 == 对象.状态 ? 1 : 0;
        default:
            return -1;
        }
    }
};
