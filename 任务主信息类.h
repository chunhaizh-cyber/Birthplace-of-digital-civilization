class 任务节点类;
class 方法节点类;

enum class 枚举_任务状态 : std::uint8_t {
    未定义 = 0, // 尚未设置状态。
    未启动 = 1, // 任务已存在，但尚未进入筹办或执行。
    运行中 = 2, // 任务处于治理或执行推进中。
    挂起 = 3, // 任务主动暂停，等待外部条件变化。
    完成 = 4, // 任务目标已经达成。
    失败 = 5, // 任务执行失败，且本轮不能自动恢复。
    取消 = 6, // 任务被调度或上层需求取消。
    超时 = 7, // 任务超过允许时限。
    就绪 = 8, // 任务条件已具备，等待调度执行。
    执行中 = 9, // 任务正在调用方法执行。
    筹办中 = 10, // 任务正在寻找方法、补齐条件或拆分子需求。
    排队中 = 11, // 任务已可执行，正在等待调度队列。
    等待中 = 12, // 任务等待子需求、学习或外部结果。
    无法执行 = 13, // 当前没有可用方法或必要条件，不能推进。
    待重筹办 = 14, // 依赖条件变化后，需要重新筹办。
};

enum class 枚举_任务阶段 : std::uint8_t {
    未定义 = 0, // 尚未进入两阶段任务壳。
    筹办 = 1, // 读取需求条件包并查找可用自我能力。
    执行 = 2, // 按选中自我能力值配对参数并调用本能函数。
    已完成 = 3, // 对应需求已满足，本轮任务壳完成。
    阻塞 = 4, // 当前缺口已生长为子需求，等待子需求回传。
};

// 兼容占位：旧步骤/结果任务树正在拆除，保留枚举名只为让旧模块先通过编译。
enum class 枚举_任务节点种类 : std::uint8_t {
    未定义 = 0,
    头结点 = 1,
    步骤节点 = 2,
    结果节点 = 3,
};

// 兼容占位：新逻辑由需求树组织任务，不再以任务树类型驱动调度。
enum class 枚举_任务树类型 : std::uint8_t {
    未定义 = 0,
    编排任务 = 1,
    叶子任务 = 2,
};

// 兼容占位：结果节点已废弃，任务结果应落到状态和执行记录。
enum class 枚举_任务结果角色 : std::uint8_t {
    未定义 = 0,
    预测结果 = 1,
    实际结果 = 2,
};

// 兼容占位：旧结果分支选择动作，后续会被任务执行结果/需求状态替换。
enum class 枚举_分支选择动作 : std::uint8_t {
    未定义 = 0,
    继续下一步 = 1,
    回跳重试 = 2,
    生成补条件子任务 = 3,
    转入尝试学习 = 4,
    挂起等待 = 5,
};

enum class 枚举_任务角色 : std::uint8_t {
    未定义 = 0, // 尚未判断任务职责。
    需求管理任务 = 1, // 挂在非叶子需求上，负责组织、等待、汇总子需求。
    需求执行任务 = 2, // 挂在叶子需求上，负责调用方法改变目标状态。
    学习任务 = 3, // 兼容占位；新逻辑中学习是知识缺口需求节点的普通任务。
    任务管理任务 = 4, // 兼容占位；新逻辑中管理由需求树生长和回传完成。
};

enum class 枚举_任务完成口径 : std::uint8_t {
    未定义 = 0, // 尚未设置完成判断方式。
    需求状态达成 = 1, // 通过比较需求当前状态与目标状态判断完成。
    子需求全部完成 = 2, // 非叶子需求的全部子需求完成后判断完成。
    方法已完善 = 3, // 学习任务对应的方法结构已经补齐。
    治理状态完成 = 4, // 任务管理任务的治理对象已经达到完成状态。
};

struct 结构_任务执行记录 {
    时间戳 开始时间 = 0; // 本次执行开始时间。
    时间戳 结束时间 = 0; // 本次执行结束时间；0 表示尚未结束。
    枚举_任务状态 执行前状态 = 枚举_任务状态::未定义; // 执行开始前任务状态。
    枚举_任务状态 执行后状态 = 枚举_任务状态::未定义; // 执行结束后任务状态。
    可解析引用<方法节点类> 执行方法{}; // 本次执行调用的方法。
    可解析引用<状态节点类> 输入状态{}; // 方法执行前观察到的状态。
    可解析引用<状态节点类> 输出状态{}; // 方法执行后得到的状态。
    std::int64_t 错误码 = 0; // 方法或任务执行返回的错误码；0 表示无错误。
};

// ============================================================================
// 任务主信息
// - 任务不再负责组织树结构；需求树才是组织结构。
// - 每个任务挂在一个需求节点上，任务目标就是达成该需求。
// - 任务只有筹办和执行两个核心阶段；缺口通过需求树生长为子需求。
// - 任务结果不是节点，统一落在结果状态和执行记录中。
// ============================================================================
class 任务主信息类 {
public:
    const 词性节点类* 名称 = nullptr; // 任务名称词，用于显示和查找。
    const 自然句节点类* 描述信息 = nullptr; // 任务的人类可读说明，可为空。
    const 词性节点类* 类型 = nullptr; // 任务类型词，用于兼容既有类型判断。
    枚举_任务角色 任务角色 = 枚举_任务角色::未定义; // 该任务在需求树上的职责。

    枚举_任务状态 状态 = 枚举_任务状态::未定义; // 任务当前运行状态。
    枚举_任务阶段 当前阶段 = 枚举_任务阶段::未定义; // 两阶段任务壳当前阶段；旧状态仅作兼容镜像。
    枚举_任务完成口径 完成口径 = 枚举_任务完成口径::未定义; // 判断任务完成的规则。
    时间戳 创建时间 = 0; // 任务创建时间。
    时间戳 启动时间 = 0; // 最近一次进入执行的时间。
    时间戳 完成时间 = 0; // 任务最终完成时间；0 表示尚未完成。
    时间戳 最近调度时间 = 0; // 最近一次被调度器选中的时间。
    结构_统计 统计{}; // 任务观察、执行、调度等统计信息。

    可解析引用<需求节点类> 对应需求{}; // 该任务服务的需求；任务父子关系从需求树推导。
    可解析引用<存在节点类> 任务虚拟存在{}; // 任务自身的运行特征承载位置。
    可解析引用<场景节点类> 场景{}; // 任务运行时所在或适用的场景。
    可解析引用<状态节点类> 目标状态{}; // 任务希望达成的目标状态，通常来自对应需求。
    可解析引用<状态节点类> 结果状态信息{}; // 任务最近一次执行或治理后得到的状态。

    可解析引用<方法节点类> 执行方法{}; // 当前选定或最近执行的方法。
    可解析引用<方法节点类> 候选方法首节点{}; // 可用于该任务的候选方法链首节点。
    可解析引用<方法节点类> 等待学习方法首节点{}; // 因缺方法而等待学习补齐的方法链首节点。
    std::int64_t 选中自我能力值 = 0; // 任务长期保存的能力主键；方法首节点引用只作为运行期缓存。
    可解析引用<存在节点类> 方法配对结果{}; // 缓存镜像；权威语义写入任务虚拟存在特征槽。
    可解析引用<存在节点类> 参数配对结果{}; // 缓存镜像；权威语义写入任务虚拟存在特征槽。
    可解析引用<场景节点类> 可执行输入参数场景{}; // 缓存镜像；权威语义写入任务虚拟存在特征槽。
    可解析引用<存在节点类> 最近运行虚拟存在{}; // 缓存镜像；权威语义写入任务虚拟存在特征槽。
    bool 等待学习唤醒 = false; // 学习完成后是否需要唤醒该任务重新筹办。

    std::int64_t 基准优先级 = 0; // 来自需求权重的基础优先级。
    std::int64_t 局部优先级偏移 = 0; // 运行期临时提高或降低优先级的偏移。
    std::int64_t 调度优先级 = 0; // 调度器实际比较使用的优先级。
    std::int64_t 运行次数 = 0; // 任务累计进入执行的次数。
    std::int64_t 成功次数 = 0; // 任务执行成功次数。
    std::int64_t 失败次数 = 0; // 任务执行失败次数。
    std::int64_t 连续失败次数 = 0; // 最近连续失败次数，用于触发学习或降级。
    std::int64_t 转入学习次数 = 0; // 因缺方法或失败转入学习的次数。
    std::int64_t 最近错误码 = 0; // 最近一次失败或异常的错误码。

    std::vector<结构_任务执行记录> 执行记录列表{}; // 任务历史执行记录。
    结构_任务执行记录 最近执行记录{}; // 最近一次执行记录的快捷镜像。

    可解析引用<任务节点类> 管理对象任务{}; // 任务管理任务当前治理的目标任务。

    // 兼容占位字段：以下字段只为旧模块过渡编译保留，新的任务组织关系以需求树为准。
    枚举_任务节点种类 节点种类 = 枚举_任务节点种类::头结点; // 旧任务树节点类型占位。
    枚举_任务树类型 任务树类型 = 枚举_任务树类型::未定义; // 旧任务树类型占位。
    枚举_任务结果角色 结果角色 = 枚举_任务结果角色::未定义; // 旧结果节点角色占位。
    bool 是否真根任务 = false; // 旧根链判定占位。
    bool 是否常驻任务 = false; // 旧常驻任务标记占位。
    bool 是否预测外结果 = false; // 旧预测结果标记占位。
    bool 允许切换方法 = true; // 旧步骤重筹办控制占位。
    std::int32_t 步骤序号 = 0; // 旧步骤序号占位。
    std::int32_t 已重试次数 = 0; // 旧步骤重试计数占位。
    std::int32_t 允许重试次数 = 0; // 旧步骤重试上限占位。
    时间戳 超时截止时间 = 0; // 旧步骤超时占位。
    时间戳 最近执行时间 = 0; // 旧方法/步骤最近执行时间占位。
    时间戳 最近完成时间 = 0; // 旧方法/步骤最近完成时间占位。
    时间戳 时间起 = 0; // 旧条件时间窗开始占位。
    时间戳 时间止 = 0; // 旧条件时间窗结束占位。
    std::int64_t 错误码 = 0; // 旧结果错误码占位。
    std::int64_t 置信度_Q10000 = 0; // 旧结果置信度占位。
    std::int64_t 安全权重 = 0; // 旧任务局部安全权重占位。
    std::int64_t 服务权重 = 0; // 旧任务局部服务权重占位。
    const 词性节点类* 动作名 = nullptr; // 旧动作名占位。
    结构体_动作句柄 动作句柄{}; // 旧动作句柄占位。
    const 词性节点类* 主结果特征类型 = nullptr; // 旧方法结果特征占位。
    std::string 条件主键{}; // 旧条件归桶键占位。
    std::string 条件场景模板主键{}; // 旧条件场景模板键占位。
    std::string 结果主键{}; // 旧结果归桶键占位。
    std::string 来源方法主键{}; // 旧派生来源方法键占位。
    可解析引用<需求节点类> 来源需求{}; // 旧来源需求占位；新逻辑使用 对应需求。
    可解析引用<任务节点类> 父任务{}; // 旧任务树父节点占位。
    可解析引用<任务节点类> 父任务头结点{}; // 旧父任务头占位。
    可解析引用<任务节点类> 所属任务头结点{}; // 旧步骤所属任务头占位。
    可解析引用<任务节点类> 来源任务{}; // 旧来源任务占位。
    可解析引用<任务节点类> 来源任务头结点{}; // 旧来源任务头占位。
    可解析引用<任务节点类> 来源父步骤节点{}; // 旧来源父步骤占位。
    可解析引用<任务节点类> 来源父结果节点{}; // 旧来源父结果占位。
    可解析引用<任务节点类> 来源步骤节点{}; // 旧来源步骤占位。
    可解析引用<任务节点类> 下一跳步骤节点{}; // 旧结果后续步骤占位。
    可解析引用<任务节点类> 后续任务{}; // 旧链后续任务占位。
    可解析引用<任务节点类> 当前步骤节点{}; // 旧任务头当前步骤占位。
    可解析引用<任务节点类> 当前子任务头结点{}; // 旧当前子任务占位。
    可解析引用<任务节点类> 最近实际结果节点{}; // 旧最近结果占位。
    可解析引用<任务节点类> 当前前置任务头结点{}; // 旧前置任务占位。
    可解析引用<任务节点类> 命中预测结果节点{}; // 旧预测结果占位。
    可解析引用<任务节点类> 相关任务{}; // 旧单任务挂接占位。
    std::vector<可解析引用<任务节点类>> 直属子任务头结点列表{}; // 旧直属子任务列表占位。
    std::vector<可解析引用<任务节点类>> 并发前置任务头结点列表{}; // 旧并发前置列表占位。
    std::vector<可解析引用<任务节点类>> 派生子任务头结点列表{}; // 旧派生子任务列表占位。
    可解析引用<方法节点类> 可用方法首节点{}; // 旧可用方法占位；新逻辑使用 候选方法首节点。
    可解析引用<方法节点类> 来源方法首节点{}; // 旧来源方法占位。
    可解析引用<场景节点类> 条件场景{}; // 旧步骤条件场景占位。
    可解析引用<场景节点类> 结果场景{}; // 旧结果场景占位。
    可解析引用<场景节点类> 需求场景{}; // 旧需求场景镜像占位。
    可解析引用<场景节点类> 方法条件节点{}; // 旧方法条件实例场景占位。
    可解析引用<场景节点类> 方法结果节点{}; // 旧方法结果实例场景占位。
    可解析引用<方法节点类> 对应方法结果节点{}; // 旧方法结果节点占位。
    可解析引用<二次特征节点类> 结果方向二次特征{}; // 旧结果方向占位。
    枚举_分支选择动作 命中后动作 = 枚举_分支选择动作::未定义; // 旧预测命中动作占位。
    std::vector<可解析引用<状态节点类>> 结果初始状态{}; // 旧结果初始状态占位。
    std::vector<可解析引用<二次特征节点类>> 条件判定索引{}; // 旧条件索引占位。

    std::int64_t 比较(const 任务主信息类* 对象, 枚举_比较字段 字段) const {
        return 对象 ? 比较(*对象, 字段) : -1;
    }

    std::int64_t 比较(const 任务主信息类& 对象, 枚举_比较字段 字段) const {
        switch (字段) {
        case 枚举_比较字段::名称:
            return 名称 == 对象.名称 ? 1 : 0;
        case 枚举_比较字段::类型:
            return 类型 == 对象.类型 ? 1 : 0;
        case 枚举_比较字段::值:
            return 状态 == 对象.状态 ? 1 : 0;
        default:
            return -1;
        }
    }
};
