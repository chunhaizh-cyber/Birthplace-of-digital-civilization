#pragma once

#include <cstdint>
#include <string>
#include <vector>

#include "语言节点类型.h"
#include "语素节点类型.h"
#include "基础信息节点类型.h"
#include "双向链表模板核心.h"

class 任务节点类;
class 方法节点类;

enum class 枚举_方法节点种类 : std::uint8_t {
    未定义 = 0,
    方法首节点 = 1,
    方法条件节点 = 2,
    方法结果节点 = 3,
};

enum class 枚举_动作句柄类型 : std::uint8_t {
    未设置 = 0,
    本能函数ID = 1,
    外部实现主键 = 2,
    动作序列主键 = 3,
};

enum class 枚举_方法类型 : std::uint8_t {
    未定义 = 0,
    内部方法 = 1,
    外部方法 = 2,
};

enum class 枚举_方法来源 : std::uint8_t {
    未定义 = 0,
    本能 = 1,
    外部导入 = 2,
    因果抽象 = 3,
    观察抽象 = 4,
    路径提炼 = 5,
    运行时学习沉淀 = 6,
};

enum class 枚举_方法作用域 : std::uint8_t {
    未定义 = 0,
    任务局部 = 1,
    主体局部 = 2,
    通用复用 = 3,
    跨域复用 = 4,
};

enum class 枚举_方法作用对象 : std::uint8_t {
    任意 = 0,
    执行者 = 1,
    目标 = 2,
    环境 = 3,
    第三方 = 4,
};

struct 结构体_动作句柄 {
    枚举_动作句柄类型 类型 = 枚举_动作句柄类型::未设置;

    I64 本能ID = 0;
    std::string 外部实现主键{};
    std::string 动作序列主键{};

    bool 有效() const noexcept {
        switch (类型) {
        case 枚举_动作句柄类型::本能函数ID: return 本能ID != 0;
        case 枚举_动作句柄类型::外部实现主键: return !外部实现主键.empty();
        case 枚举_动作句柄类型::动作序列主键: return !动作序列主键.empty();
        default: return false;
        }
    }
};

class 方法信息基类 {
public:
    const 自然句节点类* 描述信息 = nullptr;
    枚举_方法节点种类 节点种类 = 枚举_方法节点种类::未定义;

    // 当前工程热路径仍在使用的方法运行锚点与镜像。
    结构_统计 统计{};
    可解析引用<存在节点类> 方法虚拟存在{};
    可解析引用<场景节点类> 条件场景{};
    可解析引用<场景节点类> 结果场景{};

    // 动作面：方法描述“做什么”。
    const 词性节点类* 动作名 = nullptr;
    结构体_动作句柄 动作句柄{};
    枚举_方法来源 来源 = 枚举_方法来源::未定义;
    枚举_方法作用域 作用域 = 枚举_方法作用域::未定义;

    // 挂接面：方法与任务、方法树、目标状态的关联。
    可解析引用<任务节点类> 来源任务{};
    可解析引用<方法节点类> 父方法{};
    可解析引用<方法节点类> 前置方法{};
    可解析引用<方法节点类> 后续方法{};
    std::vector<可解析引用<状态节点类>> 结果初始状态{};
    时间戳 最近执行时间 = 0;
    时间戳 最近完成时间 = 0;

    // 条件面：方法“在什么条件下”成立。
    std::string 条件主键{};
    std::vector<可解析引用<二次特征节点类>> 条件判定索引{};
    时间戳 时间起 = 0;
    时间戳 时间止 = 0;
    std::string 条件场景模板主键{};

    // 结果面：方法“会生成什么结果”。
    const 词性节点类* 主结果特征类型 = nullptr;
    std::string 结果主键{};
    std::vector<可解析引用<二次特征节点类>> 结果方向索引{};

    方法信息基类() = default;
    explicit 方法信息基类(const 自然句节点类* 描述) : 描述信息(描述) {}
    virtual ~方法信息基类() = default;

    bool 有动作骨架() const noexcept {
        return 动作名 || 动作句柄.有效();
    }

    bool 有最小出生锚点() const noexcept {
        return 有动作骨架() || 主结果特征类型;
    }

    bool 是仅结果特征需求位() const noexcept {
        return 主结果特征类型 && !有动作骨架();
    }

    std::int64_t 比较(const 方法信息基类* 对象, 枚举_比较字段 字段) const {
        return 对象 ? 比较(*对象, 字段) : -1;
    }

    virtual std::int64_t 比较(const 方法信息基类& 对象, 枚举_比较字段 字段) const {
        switch (字段) {
        case 枚举_比较字段::名称:
            return 动作名 == 对象.动作名 ? 1 : 0;
        case 枚举_比较字段::类型:
            return 节点种类 == 对象.节点种类 ? 1 : 0;
        case 枚举_比较字段::值:
            return 私有_动作句柄相等(动作句柄, 对象.动作句柄) ? 1 : 0;
        default:
            return 0;
        }
    }

protected:
    static bool 私有_动作句柄相等(const 结构体_动作句柄& a, const 结构体_动作句柄& b) noexcept {
        return a.类型 == b.类型
            && a.本能ID == b.本能ID
            && a.外部实现主键 == b.外部实现主键
            && a.动作序列主键 == b.动作序列主键;
    }
};

class 方法首节点主信息类 : public 方法信息基类 {
public:
    方法首节点主信息类() { 节点种类 = 枚举_方法节点种类::方法首节点; }
    方法首节点主信息类(const 词性节点类* 动作, 结构体_动作句柄 动作句柄_) {
        节点种类 = 枚举_方法节点种类::方法首节点;
        动作名 = 动作;
        动作句柄 = 动作句柄_;
    }
};

class 方法条件节点主信息类 : public 方法信息基类 {
public:
    方法条件节点主信息类() { 节点种类 = 枚举_方法节点种类::方法条件节点; }
};

class 方法结果节点主信息类 : public 方法信息基类 {
public:
    方法结果节点主信息类() { 节点种类 = 枚举_方法节点种类::方法结果节点; }
};
