#include "任务管理最小原语模块.h"

namespace {
    bool 私有_执行任务管理最小原语(
        const 任务管理最小原语模块::结构_任务管理最小原语执行请求& 请求) noexcept
    {
        const auto 原语ID = 请求.原语ID != 枚举_任务管理最小原语ID::未定义
            ? 请求.原语ID
            : 任务管理最小原语模块::查询对应最小原语ID(请求.本能ID);
        const auto 本能ID = 请求.本能ID != 枚举_本能方法ID::未定义
            ? 请求.本能ID
            : 任务管理最小原语模块::查询对应本能方法ID(原语ID);

        if (原语ID == 枚举_任务管理最小原语ID::未定义
            || 本能ID == 枚举_本能方法ID::未定义
            || !请求.执行体) {
            return false;
        }

        if (任务管理最小原语模块::查询对应本能方法ID(原语ID) != 本能ID) {
            return false;
        }

        if (!本能方法类::查询元信息(本能ID)) {
            return false;
        }

        (void)请求.调用点;
        return 请求.执行体(请求.执行上下文);
    }
}

枚举_本能方法ID 任务管理最小原语模块::查询对应本能方法ID(
    枚举_任务管理最小原语ID 原语ID) noexcept
{
    switch (原语ID) {
    case 枚举_任务管理最小原语ID::确保单承载存在:
        return 枚举_本能方法ID::管理_确保单承载存在;
    case 枚举_任务管理最小原语ID::读取单特征当前值:
        return 枚举_本能方法ID::管理_读取单特征当前值;
    case 枚举_任务管理最小原语ID::写入单特征当前值:
        return 枚举_本能方法ID::管理_写入单特征当前值;
    case 枚举_任务管理最小原语ID::写入单挂点指针:
        return 枚举_本能方法ID::管理_写入单挂点指针;
    case 枚举_任务管理最小原语ID::记录动作开始:
        return 枚举_本能方法ID::管理_记录动作开始;
    case 枚举_任务管理最小原语ID::记录动作结束:
        return 枚举_本能方法ID::管理_记录动作结束;
    default:
        return 枚举_本能方法ID::未定义;
    }
}

枚举_任务管理最小原语ID 任务管理最小原语模块::查询对应最小原语ID(
    枚举_本能方法ID 本能ID) noexcept
{
    switch (本能ID) {
    case 枚举_本能方法ID::管理_确保单承载存在:
        return 枚举_任务管理最小原语ID::确保单承载存在;
    case 枚举_本能方法ID::管理_读取单特征当前值:
        return 枚举_任务管理最小原语ID::读取单特征当前值;
    case 枚举_本能方法ID::管理_写入单特征当前值:
        return 枚举_任务管理最小原语ID::写入单特征当前值;
    case 枚举_本能方法ID::管理_写入单挂点指针:
        return 枚举_任务管理最小原语ID::写入单挂点指针;
    case 枚举_本能方法ID::管理_记录动作开始:
        return 枚举_任务管理最小原语ID::记录动作开始;
    case 枚举_本能方法ID::管理_记录动作结束:
        return 枚举_任务管理最小原语ID::记录动作结束;
    default:
        return 枚举_任务管理最小原语ID::未定义;
    }
}

