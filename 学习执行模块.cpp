    bool 私有_方法具备条件侧(const 方法节点* 方法头) noexcept
    {
        return 方法头
            && (方法头->主信息.条件场景.指针
                || !方法头->主信息.条件判定索引.empty());
    }

    bool 私有_方法具备结果侧(const 方法节点* 方法头) noexcept
    {
        return 方法头
            && (方法头->主信息.主结果特征类型
                || !方法头->主信息.结果方向索引.empty());
    }

    bool 私有_学习方法有动作骨架(const 方法节点* 方法头) noexcept
    {
        return 方法头 && 方法头->主信息.动作句柄.有效();
    }

    I64 私有_学习方法条件数量(const 方法节点* 方法头) noexcept
    {
        if (!方法头) {
            return 0;
        }
        if (!方法头->主信息.条件判定索引.empty()) {
            return static_cast<I64>(方法头->主信息.条件判定索引.size());
        }
        return 私有_方法具备条件侧(方法头) ? 1 : 0;
    }

    I64 私有_学习方法结果数量(const 方法节点* 方法头) noexcept
    {
        if (!方法头) {
            return 0;
        }
        if (!方法头->主信息.结果方向索引.empty()) {
            return static_cast<I64>(方法头->主信息.结果方向索引.size());
        }
        return 方法头->主信息.主结果特征类型 ? 1 : 0;
    }

    bool 私有_学习方法可直接进入学习(const 方法节点* 方法头) noexcept
    {
        return 方法头
            && 私有_学习方法有动作骨架(方法头)
            && 私有_学习方法条件数量(方法头) > 0;
    }

    bool 私有_学习方法已具备可用骨架(const 方法节点* 方法头) noexcept
    {
        return 方法头
            && 私有_学习方法可直接进入学习(方法头)
            && 私有_学习方法结果数量(方法头) > 0;
    }

    方法节点* 私有_选择学习结构参考方法(
        const 结构_学习任务实体& 实体,
        任务节点* 当前步骤,
        方法节点* 目标方法) noexcept
    {
        const 方法节点* 候选表[] = {
            私有_取步骤执行方法(当前步骤),
            实体.当前选中学习方法,
            实体.学习任务兜底方法,
            实体.来源方法,
            实体.学习方法,
        };
        for (const auto* 候选 : 候选表) {
            if (!候选 || 候选 == 目标方法) {
                continue;
            }
            return const_cast<方法节点*>(候选);
        }
        return nullptr;
    }

    结构_学习步骤结构写回结果 私有_按学习步骤写回方法结构(
        const 结构_学习任务实体& 实体,
        枚举_学习动作类型 动作类型,
        时间戳 当前时间,
        const std::string& 调用点) noexcept
    {
        结构_学习步骤结构写回结果 输出{};
        auto* 当前步骤 = reinterpret_cast<任务节点*>(实体.来源步骤);
        auto* 当前结果 = reinterpret_cast<任务节点*>(实体.来源结果);
        auto* 学习子任务 = reinterpret_cast<任务节点*>(实体.学习子任务);
        auto* 目标方法 = 私有_取步骤语义目标方法(当前步骤);
        if (!目标方法) {
            目标方法 = 实体.来源方法
                ? 实体.来源方法
                : (实体.学习方法 ? 实体.学习方法 : 实体.当前选中学习方法);
        }
        输出.当前学习方法节点 = 目标方法;
        if (!目标方法) {
            return 输出;
        }

        auto* 参考方法 = 私有_选择学习结构参考方法(实体, 当前步骤, 目标方法);
        const bool 初始有动作骨架 = 目标方法->主信息.动作句柄.有效();
        const bool 初始有条件侧 = 私有_方法具备条件侧(目标方法);
        const bool 初始有结果侧 = 私有_方法具备结果侧(目标方法);

        if (动作类型 == 枚举_学习动作类型::候选动作生成 && 参考方法) {
            if (!目标方法->主信息.动作名 && 参考方法->主信息.动作名) {
                目标方法->主信息.动作名 = 参考方法->主信息.动作名;
            }
            if (!目标方法->主信息.动作句柄.有效() && 参考方法->主信息.动作句柄.有效()) {
                目标方法->主信息.动作句柄 = 参考方法->主信息.动作句柄;
            }
            if (目标方法->主信息.来源 == 枚举_方法来源::未定义
                && 目标方法->主信息.动作句柄.有效()) {
                目标方法->主信息.来源 = 枚举_方法来源::运行时学习沉淀;
            }
            if (目标方法->主信息.作用域 == 枚举_方法作用域::未定义
                && 参考方法->主信息.作用域 != 枚举_方法作用域::未定义) {
                目标方法->主信息.作用域 = 参考方法->主信息.作用域;
            }
            输出.已补动作骨架 = !初始有动作骨架 && 目标方法->主信息.动作句柄.有效();
        }

        if (动作类型 == 枚举_学习动作类型::共同条件提取) {
            if (参考方法) {
                if (!目标方法->主信息.条件场景.指针 && 参考方法->主信息.条件场景.指针) {
                    目标方法->主信息.条件场景 = 参考方法->主信息.条件场景;
                }
                if (目标方法->主信息.条件主键.empty()) {
                    目标方法->主信息.条件主键 = 参考方法->主信息.条件主键;
                }
                if (目标方法->主信息.条件场景模板主键.empty()) {
                    目标方法->主信息.条件场景模板主键 = 参考方法->主信息.条件场景模板主键;
                }
                (void)私有_吸收二次特征引用列表(
                    目标方法->主信息.条件判定索引,
                    参考方法->主信息.条件判定索引);
            }
            if (!目标方法->主信息.条件场景.指针) {
                if (auto* 条件场景 = 方法类::取或创建_方法条件场景(
                    目标方法,
                    nullptr,
                    当前时间,
                    调用点 + "/条件场景")) {
                    私有_写入场景引用(目标方法->主信息.条件场景, 条件场景);
                    if (目标方法->主信息.条件主键.empty()) {
                        目标方法->主信息.条件主键 = 条件场景->获取主键();
                    }
                    if (目标方法->主信息.条件场景模板主键.empty()) {
                        目标方法->主信息.条件场景模板主键 = 条件场景->获取主键();
                    }
                }
            }
            if (auto* 条件节点 = 方法类::创建条件节点(
                目标方法,
                目标方法->主信息.条件场景.指针,
                目标方法->主信息.条件判定索引,
                目标方法->主信息.条件场景模板主键,
                目标方法->主信息.时间起,
                目标方法->主信息.时间止,
                调用点 + "/条件节点")) {
                if (学习子任务) {
                    私有_写入任务引用(条件节点->主信息.来源任务, 学习子任务);
                }
            }
            输出.已补条件节点 = !初始有条件侧 && 私有_方法具备条件侧(目标方法);
        }

        if (动作类型 == 枚举_学习动作类型::结果预测) {
            if (参考方法) {
                if (!目标方法->主信息.主结果特征类型 && 参考方法->主信息.主结果特征类型) {
                    目标方法->主信息.主结果特征类型 = 参考方法->主信息.主结果特征类型;
                }
                if (!目标方法->主信息.结果场景.指针 && 参考方法->主信息.结果场景.指针) {
                    目标方法->主信息.结果场景 = 参考方法->主信息.结果场景;
                }
                if (目标方法->主信息.结果主键.empty()) {
                    目标方法->主信息.结果主键 = 参考方法->主信息.结果主键;
                }
                (void)私有_吸收二次特征引用列表(
                    目标方法->主信息.结果方向索引,
                    参考方法->主信息.结果方向索引);
                (void)私有_吸收状态引用列表(
                    目标方法->主信息.结果初始状态,
                    参考方法->主信息.结果初始状态);
            }
            if (!目标方法->主信息.结果场景.指针) {
                if (auto* 结果场景 = 方法类::取或创建_方法结果场景(
                    目标方法,
                    nullptr,
                    当前时间,
                    调用点 + "/结果场景")) {
                    私有_写入场景引用(目标方法->主信息.结果场景, 结果场景);
                    if (目标方法->主信息.结果主键.empty()) {
                        目标方法->主信息.结果主键 = 结果场景->获取主键();
                    }
                }
            }
            输出.当前方法结果节点 = 方法类::创建结果节点(
                目标方法,
                目标方法->主信息.结果场景.指针,
                目标方法->主信息.结果方向索引,
                调用点 + "/结果节点");
            if (输出.当前方法结果节点 && 学习子任务) {
                私有_写入任务引用(输出.当前方法结果节点->主信息.来源任务, 学习子任务);
            }
            输出.已补结果节点 = !初始有结果侧 && 私有_方法具备结果侧(目标方法);
        }

        输出.已写回学习方法结构 =
            输出.已补动作骨架 || 输出.已补条件节点 || 输出.已补结果节点;

        if (!输出.当前方法结果节点 && 当前结果) {
            输出.当前方法结果节点 =
                reinterpret_cast<方法节点*>(当前结果->主信息.对应方法结果节点.指针);
        }

        任务节点* 写回结果节点 = 当前结果;
        if (输出.已写回学习方法结构 && 当前步骤 && 学习子任务) {
            if (!写回结果节点
                || 写回结果节点->主信息.结果角色 != 枚举_任务结果角色::实际结果) {
                任务类 临时树{};
                auto* 实际结果节点 = 临时树.创建任务结果节点(
                    学习子任务,
                    当前步骤,
                    reinterpret_cast<方法节点类*>(输出.当前学习方法节点),
                    枚举_任务结果角色::实际结果,
                    当前结果 ? 当前结果->主信息.结果状态信息.指针 : nullptr,
                    当前时间,
                    调用点 + "/学习实际结果");
                if (实际结果节点) {
                    写回结果节点 = 实际结果节点;
                }
            }
        }

        if (写回结果节点 && 输出.当前学习方法节点) {
            私有_写入方法引用(写回结果节点->主信息.来源方法首节点, 输出.当前学习方法节点);
            if (输出.当前方法结果节点) {
                私有_写入方法引用(写回结果节点->主信息.对应方法结果节点, 输出.当前方法结果节点);
            }
            if (输出.已写回学习方法结构) {
                写回结果节点->主信息.错误码 = 0;
                写回结果节点->主信息.置信度_Q10000 =
                    (输出.已补动作骨架 && 输出.已补条件节点 && 输出.已补结果节点) ? 9500 : 8200;
            }
        }
        return 输出;
    }

