    结构_学习执行结果 私有_进入正式提交闸门等待(
        结构_学习任务实体& 实体,
        结构_学习任务更新输入& 更新,
        const std::string& 执行摘要,
        bool 来自提交阶段) noexcept
    {
        更新.覆写阶段 = true;
        更新.当前阶段 = 枚举_学习阶段::提交;
        更新.覆写状态 = true;
        更新.当前状态 = 枚举_学习状态::挂起;
        更新.覆写等待原因 = true;
        更新.当前等待原因 = 枚举_学习等待原因::等待正式提交放行;
        更新.覆写待外层裁决 = true;
        更新.待外层裁决 = true;
        更新.覆写当前外层裁决 = true;
        更新.当前外层裁决 = 枚举_学习外层裁决::待裁决;
        更新.覆写最近执行摘要 = true;
        更新.最近执行摘要 = 来自提交阶段
            ? "正式提交门未放行，继续等待外层显式允许"
            : "学习结果已通过验证门，等待正式提交放行";
        更新.覆写最近外层裁决摘要 = true;
        更新.最近外层裁决摘要 = "正式提交门未放行：等待外层显式允许正式资产提交";
        学习任务模块::更新学习任务实体(更新, &实体);
        auto 输出 = 私有_构造结果(
            实体,
            执行摘要 + (来自提交阶段
                ? " | 正式提交门未放行，保持等待"
                : " | 学习结果已通过验证门，等待正式提交放行") + 私有_评判摘要(实体),
            {},
            "正式提交门未放行：仅保留学习实体与等待态，不进入正式资产写回",
            true,
            false);
        私有_写入学习反馈语义(
            输出,
            "正式提交门未放行：仅保留学习实体与等待态，不进入正式资产写回",
            "已形成学习反馈，等待外层放行后决定消费",
            枚举_任务管理学习反馈消费策略::挂起观察);
        输出.已推进阶段 = true;
        if (!来自提交阶段) {
            输出.已验证 = true;
        }
        return 输出;
    }

