    结构_实践结果消息 私有_构造实践结果消息(
        const 结构_学习任务实体& 实体,
        bool 是否叶子完成)
    {
        结构_实践结果消息 消息{};
        消息.来源承接类型 = 枚举_实践结果来源承接类型::学习;
        消息.来源宿主任务指针 = reinterpret_cast<std::uintptr_t>(实体.来源宿主任务);
        消息.来源当前步骤指针 = reinterpret_cast<std::uintptr_t>(实体.来源步骤);
        消息.来源当前方法首节点指针 = reinterpret_cast<std::uintptr_t>(实体.学习方法);
        消息.最近结果节点指针或镜像 = reinterpret_cast<std::uintptr_t>(实体.来源结果);
        消息.实际结果特征列表 = {
            { "学习综合分", 实体.当前综合分 },
            { "学习结果状态", static_cast<std::int64_t>(实体.当前结果状态) },
            { "学习影响面", static_cast<std::int64_t>(实体.当前影响面) },
            { "学习外层裁决", static_cast<std::int64_t>(实体.当前外层裁决) },
            { "学习审计次数", 实体.审计次数 },
            { "学习重试次数", 实体.重试次数 },
        };
        if (实体.当前执行策略 == 枚举_学习执行策略::受控微试探) {
            消息.实际结果特征列表.push_back({ "受控微试探放行", 实体.受控微试探放行 ? 1 : 0 });
        }
        消息.当前步骤后状态建议 = 私有_步骤后状态建议(实体);
        消息.当前下一步去向建议 = 私有_下一步去向建议(实体);
        消息.是否叶子完成 = 是否叶子完成;
        if (!实体.来源宿主任务标题.empty()) {
            消息.命中需求回挂提示列表.push_back("宿主=" + 实体.来源宿主任务标题);
        }
        if (!实体.学习子任务标题.empty()) {
            消息.命中需求回挂提示列表.push_back("学习子任务=" + 实体.学习子任务标题);
        }
        消息.摘要.clear();
        return 消息;
    }

